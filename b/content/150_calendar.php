   <p:Head2>NOTES ON THE TABLES OF <text:line-break/>OCCURRENCE AND CONCURRENCE</p>
   <p:BodyEIndent>1. A feast of our Lord of the 1st or 2nd class, occurring on a Sunday, takes the place of that Sunday with all rights and privileges: hence no commemoration is made of the Sunday.</p>
   <p:Spacer/>
   <p:BodyEIndent>2. If two feasts of the same divine Person or two feasts of the same saint or blessed occur on the same day, the Office is of the first which holds a higher place in the table of precedence, and the other is omitted.</p>
   <p:Spacer/>
   <p:BodyEIndent>3. If a feast of our Lord of the 1st or 2nd class concurs with any Sunday, or vice versa, Vespers are ordered according to the table of concurrence, but a commemoration is never made of a concurring Sunday in Vespers of the feast of our Lord, nor vice versa.</p>
   <p:BodySm/>
   <p:Head2>When to Make the Sign of the Cross, Stand, Kneel, and Sit in the Recitation of the Divine Office</p>
   <p:BodyEIndent><s:T10>261. </s>What is said here about the sign of the cross and the position of the body in the recitation of the Divine Office holds for the recitation in choir or in common. It is fitting, however, that those who recite the Divine Office alone conform to what is said about the sign of the cross.</p>
   <p:BodyEIndent><s:T10>262. </s>The special rules for the hebdomadary and the chanters are found in the books of ceremonies. Therefore only those things which concern the &quot;choir members&quot; in general are indicated here.</p>
   <p:BodyEIndent><s:T10>263. </s>All make the sign of the cross from the forehead to the breast and from the left shoulder to the right:</p>
   <p:BodyEIndent>a) at the beginning of all the Hours, when the <s:T4>Deus, in adjutorium </s>(&quot;O God, come to my assistance&quot;) is said;</p>
   <p:BodyEIndent>b) at the verse <s:T4>Adjutorium nostrum </s>(&quot;Our help&quot;);</p>
   <p:BodyEIndent>c) at the absolution <s:T4>Indulgentiam </s>(&quot;May the almighty and merciful Lord&quot;) after the Confiteor at Compline;</p>
   <p:BodyEIndent>d) at the beginning of the canticles <s:T4>Benedictus</s>, <s:T4>Magnificat</s>, and <s:T4>Nunc Dimittis</s>;</p>
   <p:BodyEIndent>e) at the blessing at the end of Prime and Compline;</p>
   <p:BodyEIndent>f) at the verse <s:T4>Divinum auxilium </s>(&quot;May the divine assistance&quot;) at the end of the Divine Office.</p>
   <p:BodyEIndent><s:T10>264. </s>They make the sign of the cross on their mouth at the beginning of Matins, at the words <s:T4>Domine, labia mea aperies </s>(&quot;O Lord, open thou my lips.&quot;).</p>
   <p:BodyEIndent><s:T10>265. </s>They make the sign of the cross on their breast at the words <s:T4>Converte nos </s>(&quot;Convert us&quot;) at Compline.</p>
   <p:BodyEIndent><s:T10>266. </s>All stand:</p>
   <p:BodyEIndent>a) at the beginning of each Hour, until the first verse of the psalm has been begun;</p>
   <p:BodyEIndent>b) while the hymns and gospel canticles are said;</p>
   <p:BodyEIndent>c) at Matins also at the invitatory with its psalm and from the end of the last antiphon of each nocturn until the first blessing before the lessons, inclusive; and while the text of the gospel is read before the homily;</p>
   <p:BodyEIndent>d) at Lauds and at Vespers also from the repetition of the antiphon after the last psalm to the end, unless they are to kneel at the preces or at the collect, according to the rubrics;</p>
   <p:BodyEIndent>e) at Prime, from the repetition of the antiphon to the end, except at the reading of the Martyrology, unless they are to kneel at the collects;</p>
   <p:BodyEIndent>f) at Terce, Sext and None, from the repetition of the antiphon to the end, unless they are to kneel at the collect;</p>
   <p:BodyEIndent>g) at Compline, from the repetition of the antiphon after the psalms to the end, unless they are to kneel at the collect;</p>
   <p:BodyEIndent>h) at the intonation of the antiphons in sung Matins, Lauds and Vespers, according to custom;</p>
   <p:BodyEIndent>i) at the final antiphon of the Blessed Virgin Mary, after Compline, on Saturday and Sunday, even if the Office is not of the Sunday, and throughout Paschaltide.</p>
   <p:BodyEIndent><s:T10>267. </s>All kneel:</p>
   <p:BodyEIndent>a) at the words <s:T4>venite, adoremus et procidamus</s>, etc. (&quot;Come, let us adore and fall down,&quot; etc.) in the psalm <s:T4>Venite, exultemus</s> at the beginning of Matins.</p>
   <p:BodyEIndent>b) at the verse <s:T4>Te ergo quǽsumus</s> (&quot;We beseech the, therefore&quot;) in the hymn <s:T4>Te Deum</s>;</p>
   <p:BodyEIndent>c) at the preces when they are to be said;</p>
   <p:BodyEIndent>d) in the ferial Office of Advent, Lent and passiontide and also of the Ember Days in September, and of vigils of the 2nd and 3rd class, except the vigil of the Ascension, in all Hours at the collect and any commemorations that may follow; the hebdomadary, however, stands;</p>
   <p:BodyEIndent>e) at the final antiphon of the Blessed Virgin Mary, after Compline, except on Saturday and Sunday and throughout Paschaltide; the hebdomadary stands, however, while he says the prayer;</p>
   <p:BodyEIndent>f) at certain other special times, noted in their respective places.</p>
   <p:BodyEIndent><s:T2>268. </s>All sit:</p>
   <p:BodyEIndent>a) at every Hour, when the first verse of the first psalm has been begun, until the antiphon of the last psalm has been repeated;</p>
   <p:BodyEIndent>b) at the lessons with their responsories at Matins, except while the text of the gospel is being read before the homily;</p>
   <p:BodyEIndent>c) while the Martyrology is being read at Prime, unless another posture is prescribed.</p>
