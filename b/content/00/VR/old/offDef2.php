Qui Lázarum resuscitásti a monuménto fœtidum,
tu eis, Dómine, dona réquiem, et locum indulgéntiæ.
Qui ventúrus es judicáre vivos et mórtuos, et sæculum per ignem.
Thou who didst raise Lazarus fetid from the grave,
thou, O Lord, give them rest, and a place of pardon.
Who art to come to judge the living and the dead, and the world by fire.
