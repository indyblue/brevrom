Líbera me, Dómine, de morte ætérna in die illa treménda,
quando cæli movéndi sunt et terra, * Dum véneris judicáre sæculum per ignem.
Tremens factus sum ego et tímeo, dum discússio vénerit atque ventúra ira.
Deliver me, O Lord, from eternal death, in that dreadful day:
when the heavens and the earth shall be moved, * when thou shalt come to judge the world by fire.
I tremble and do fear, when the examination is to be, and thy wrath to come.
