Dómine, secúndum actum meum noli me judicáre: nihil dignum in conspéctu tuo egi; ídeo déprecor majestátem tuam,
ut tu, Deus, deleas iniquitátem meam.
Amplius lava me, Dómine, ab injustítia mea, et a delícto meo munda me.
O Lord, judge me not according to my works, for I have done nothing worthy in thy sight; therefore I beseech thy Majesty,
that thou, O God, wouldst blot out my iniquity.
Wash me, O Lord, yet more from mine injustice, and cleanse me from my sin.
