Cant. Salomónis (Sap. 12)
O quam bonus et suávis est, Dómine, * spíritus tuus in ómnibus!
Ideóque eos qui exérrant, pártibus córripis: † et de quibus peccant admónes et allóqueris: * ut relícta malítia credant in te, Dómine.
Quis dicet tibi, quid fecísti? * aut quis stabit contra judícium tuum?
Aut quis in conspéctu tuo véniet vindex iniquórum hóminum? * aut quis tibi imputábit, si períerint natiónes quas tu fecísti?
Non enim est álius deus quam tu, cui cura est de ómnibus; * ut osténdas quóniam non injúste júdicas judícium.
Neque rex, neque tyránnus * in conspéctu tuo inquírent de his quos perdidísti.
Cum ergo sis justus, juste ómnia dispónis; † ipsum quoque qui non debet puníri, condemnáre, * éxterum ǽstimas a tua virtúte.
Virtus enim tua justítiæ inítium est, * et ob hoc quod Dóminus es, ómnibus te párcere facis.
Virtútem enim osténdis tu, qui non créderis esse in virtúte consummátus, * et horum qui te nésciunt audáciam tradúcis.
Tu autem dominátor virtútis, cum tranquillitáte júdicas, † et cum magna reveréntia dispónis nos: * subest enim tibi, cum volúeris posse.
