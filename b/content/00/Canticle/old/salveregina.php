      <p:BodyEDrop>HAIL, holy Queen, mother of mercy! Hail, our life, our sweetness, and our hope! To thee do we cry, poor banished children of Eve; to thee do we send up our sighs, mourning and weeping in this vale of tears. Turn then, most gracious Advocate, thine eyes of mercy toward us. And after this, our exile, show unto us the blessed fruit of Thy womb, Jesus. O clement, O loving, O sweet Virgin Mary.</p>
