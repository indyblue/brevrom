   <p:Head3>Symbolum Athanasianum</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>QUICÚMQUE vult salvus esse, * ante ómnia opus est, ut téneat cathólicam fidem:</p>
     </td>
     <td:B1>
      <p:BodyEDrop>WHOSOEVER willeth to be saved, * before all things it is necessary that he hold the Catholic faith.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quam nisi quisque íntegram inviolatámque serváverit, * absque dúbio in ætérnum períbit.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Which faith except every one do keep whole and undefiled, * without doubt he shall perish eternally.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Fides autem cathólica hæc est: * ut unum Deum in Trinitáte, et Trinitátem in unitáte venerémur.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Now the Catholic faith is this, * that we worship one God in Trinity, and Trinity in Unity.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Neque confundéntes persónas, * neque substántiam separántes.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Neither confounding the Persons, * nor dividing the substance.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Alia est enim persóna Patris, ália Fílii, * ália Spíritus Sancti:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For there is one Person of the Father, another of the Son, * and another of the Holy Ghost.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Sed Patris, et Fílii, et Spíritus Sancti una est divínitas, * æquális glória, coætérna majéstas.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>But the Godhead of the Father, of the Son, and of the Holy Ghost is one, * the Glory Equal, the Majesty Co-Eternal.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Qualis Pater, talis Fílius, * talis Spíritus Sanctus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Such as the Father is, such is the Son, * and such is the Holy Ghost.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Increátus Pater, increátus Fílius, * increátus Spíritus Sanctus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The Father Uncreated, the Son Uncreated, * and the Holy Ghost Uncreated.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Imménsus Pater, imménsus Fílius, * imménsus Spíritus Sanctus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The Father Infinite, the Son Infinite, * and the Holy Ghost Infinite.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ætérnus Pater, ætérnus Fílius, * ætérnus Spíritus Sanctus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The Father Eternal, the Son Eternal, * and the Holy Ghost Eternal.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et tamen non tres ætérni, * sed unus ætérnus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And yet they are not three Eternals, * but one Eternal.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Sicut non tres increáti, nec tres imménsi, * sed unus increátus, et unus imménsus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>As also they are not three Uncreated, nor three Infinites, * but One Uncreated, and One Infinite.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Simíliter omnípotens Pater, omnípotens Fílius, * omnípotens Spíritus Sanctus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>So likewise the Father is Almighty, the Son Almighty, * and the Holy Ghost Almighty.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et tamen non tres omnipoténtes, * sed unus omnípotens.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And yet they are not three Almighties, * but One Almighty.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ita Deus Pater, Deus Fílius, * Deus Spíritus Sanctus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>So the Father is God, the Son God, * and the Holy Ghost God.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ut tamen non tres Dii, * sed unus est Deus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And yet they are not three Gods, * but One God.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ita Dóminus Pater, Dóminus Fílius, * Dóminus Spíritus Sanctus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>So the Father is Lord, the Son Lord, * and the Holy Ghost Lord.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et tamen non tres Dómini, * sed unus est Dóminus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And yet they are not three Lords, * but One Lord.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quia, sicut singillátim unamquámque persónam Deum ac Dóminum confitéri christiána veritáte compéllimur: * ita tres Deos aut Dóminos dícere cathólica religióne prohibémur.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For, like as we are compelled by Christian truth to acknowledge every Person by Himself to be God and Lord, * so are we forbidden by the Catholic Religion to say, there be three Gods or three Lords.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Pater a nullo est factus: * nec creátus, nec génitus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The Father is made of none, * neither created, nor begotten.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Fílius a Patre solo est: * non factus, nec creátus, sed génitus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The Son is of the Father alone: * not made, nor created, but begotten.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Spíritus Sanctus a Patre et Fílio: * non factus, nec creátus, nec génitus, sed procédens.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The Holy Ghost is of the Father, and the Son: * not made, nor created, nor begotten, but proceeding.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Unus ergo Pater, non tres Patres: unus Fílius, non tres Fílii: * unus Spíritus Sanctus, non tres Spíritus Sancti.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>So there is One Father, not three Fathers; one Son, not three Sons; * one Holy Ghost, not three Holy Ghosts.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et in hac Trinitáte nihil prius aut postérius, nihil majus aut minus: * sed totæ tres persónæ coætérnæ sibi sunt et coæquáles.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And in this Trinity is nothing afore or after, nothing is greater or less; * but the whole three Persons are Co-Eternal together, and Co-Equal.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ita ut per ómnia, sicut jam supra dictum est, * et únitas in Trinitáte, et Trínitas in unitáte veneránda sit.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>So that in all things, as is aforesaid, * the Unity in Trinity, and the Trinity in Unity is to be worshipped.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Qui vult ergo salvus esse, * ita de Trinitáte séntiat.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He therefore that willeth to be safe, * let him thus think of the Trinity.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Sed necessárium est ad ætérnam salútem, * ut Incarnatiónem quoque Dómini nostri Jesu Christi fidéliter credat.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>But it is necessary to eternal salvation, * that he also believe faithfully the Incarnation of our Lord Jesus Christ.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Est ergo fides recta ut credámus et confiteámur, * quia Dóminus noster Jesus Christus, Dei Fílius, Deus et homo est.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The right Faith therefore is, that we believe and confess, * that our Lord Jesus Christ, the Son of God, is God and man.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Deus est ex substántia Patris ante sæcula génitus: * et homo est ex substántia matris in sæculo natus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>God, of the Substance of the Father, Begotten before the worlds: * and Man, of the substance of His mother, born in the world.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Perféctus Deus, perféctus homo: * ex ánima rationáli et humána carne subsístens.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Perfect God, Perfect Man, * of a reasoning soul and human flesh subsisting.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Æquális Patri secúndum divinitátem: * minor Patre secúndum humanitátem.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Equal to the Father as touching His Godhead, * inferior to the Father as touching His Manhood.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Qui licet Deus sit et homo, * non duo tamen, sed unus est Christus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Who, although He be God and Man, * yet He is not two, but One Christ.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Unus autem non conversióne divinitátis in carnem, * sed assumptióne humanitátis in Deum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>One, however, not by conversion of the Godhead into Flesh, * but by taking of the Manhood into God.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Unus omníno, non confusióne substántiæ, * sed unitáte persónæ.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>One altogether, not by confusion of Substance, * but by Unity of Person.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Nam sicut ánima rationális et caro unus est homo: * ita Deus et homo unus est Christus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For as the reasoning soul and flesh is one man, * so God and man is One Christ.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Qui passus est pro salúte nostra: descéndit ad ínferos: * tértia die resurréxit a mórtuis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Who suffered for our salvation, descended into hell, * rose again the third day from the dead.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ascéndit ad cælos, sedet ad déxteram Dei Patris omnipoténtis: * inde ventúrus est judicáre vivos et mórtuos.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He ascended into heaven, He sitteth on the right hand of the Father, God Almighty, * from whence He shall come to judge the quick and the dead.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ad cujus advéntum omnes hómines resúrgere habent cum corpóribus suis; * et redditúri sunt de factis própriis ratiónem.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>At whose coming all men shall rise again with their bodies, * and shall give account for their own works.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et qui bona egérunt, ibunt in vitam ætérnam: * qui vero mala, in ignem ætérnum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And they that have done good shall go into life eternal, * but they that have done evil into eternal fire.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Hæc est fides cathólica, * quam nisi quisque fidéliter firmitérque credíderit, salvus esse non póterit.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>This is the Catholic Faith, * which except a man believe faithfully and firmly, he cannot be safe.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Glória Patri.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Glory Be.</p>
     </td>
    </tr>
   </table>
