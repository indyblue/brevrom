     </td>
     <td:B1>
      <p:BodyEIndent>O Adonai, Lord, great art thou, and glorious in thy power, * and no one can overcome thee.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Tibi sérviat omnis creatúra tua: * quia dixísti, et facta sunt:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Let all thy creatures serve thee: * because thou hast spoken, and they were made:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Misísti spíritum tuum, et creáta sunt, * et non est qui resístat voci tuæ.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou didst send forth thy spirit, and they were created, * and there is no one that can resist thy voice.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Montes a fundaméntis movebúntur cum aquis: * petræ, sicut cera, liquéscent ante fáciem tuam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The mountains shall be moved from the foundations with the waters: * the rocks shall melt as wax before thy face.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Qui autem timent te, * magni erunt apud te per ómnia.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>But they that fear thee, * shall be great with thee in all things.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Væ genti insurgénti super genus meum: Dóminus enim omnípotens vindicábit in eis, * in die judícii visitábit illos.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Woe be to the nation that riseth up against my people: for the Lord almighty will take revenge on them, * in the day of judgment he will visit them.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
