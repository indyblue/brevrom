      <p:BodyLDrop>HYMNUM cantémus Dómino, * hymnum novum cantémus Deo nostro.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>LET us sing a hymn to the Lord, * let us sing a new hymn to our God.</p>
     </td>
    </tr>
    <tr>
