     </td>
     <td:B1>
      <p:BodyEDrop>I WILL give thanks to thee, O Lord, for thou wast angry with me: * thy wrath is turned away, and thou hast comforted me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ecce Deus salvátor meus, * fiduciáliter agam, et non timébo:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Behold, God is my saviour, * I will deal confidently, and will not fear:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quia fortitúdo mea, et laus mea Dóminus, * et factus est mihi in salútem.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Because the Lord is my strength, and my praise, * and he is become my salvation.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Hauriétis aquas in gáudio de fóntibus Salvatóris: * et dicétis in die illa: Confitémini Dómino, et invocáte nomen ejus:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>You shall draw waters with joy out of the saviour’s fountains: * And you shall say in that day: Praise ye the Lord, and call upon his name:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Notas fácite in pópulis adinventiónes ejus: * mementóte quóniam excélsum est nomen ejus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Make his works known among the people: * remember that his name is high.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Cantáte Dómino quóniam magnífice fecit: * annuntiáte hoc in univérsa terra.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Sing ye to the Lord, for he hath done great things: * shew this forth in all the earth.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Exsúlta, et lauda, habitátio Sion: * quia magnus in médio tui Sanctus Israël.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Rejoice, and praise, O thou habitation of Sion: * for great is he that is in the midst of thee, the Holy One of Israel.</p>
     </td>
    </tr>
