Cant. Salomónis (Sap. 11)
Ómnia in mensúra, et número, * et póndere disposuísti;
Multum enim válere, tibi soli supérerat semper: * et virtúti bráchii tui quis resístet?
Quóniam tamquam moméntum statéræ, sic est ante te orbis terrárum; * et tamquam gutta roris antelucáni quæ descéndit in terram.
Sed miseréris ómnium, quia ómnia potes; * et dissímulas peccáta hóminum propter pœniténtiam.
Díligis enim ómnia quæ sunt, et nihil odísti eórum quæ fecísti; * nec enim ódiens áliquid constituísti aut fecísti.
Quómodo autem posset áliquid permanére, nisi tu voluísses? * aut quod a te vocátum non esset conservarétur?
Parcis autem ómnibus, * quóniam tua sunt, Dómine, qui amas ánimas.
