DEUS, qui inter cétera poténtiæ tuæ mirácula étiam in sexu frágili victóriam martýrii contulísti: concéde propítius; ut qui beátæ 
 Vírginis et Mártyris tuæ natalícia cólimus, per ejus ad te exémpla gradiámur. Per Dóminum.
O GOD, who, among the other wondrous works of thy power, hast bestowed the victory of martyrdom even on the weaker sex; mercifully grant that we who celebrate the heavenly birthday of blessed 
 thy Virgin and Martyr, may, by following her example, advance in the way that leadeth unto thee. Through our Lord.
