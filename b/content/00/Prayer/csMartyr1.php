PRÆSTA, quæsumus, omnípotens Deus: ut, qui beáti 
 Mártyris tui natalícia cólimus, intercessióne ejus, in tui nóminis amóre roborémur. Per Dóminum.
GRANT, we beseech thee, O almighty God, that as we celebrate the festival of blessed 
 thy Martyr, so by his intercession we may be strengthened in the love of thy name. Through our Lord.
