PRÆSTA, quæsumus, omnípotens Deus: ut, intercedénte beáto 
 Mártyre tuo, et a cunctis adversitátibus liberémur in córpore, et a pravis cogitatiónibus mundémur in mente. Per Dóminum.
GRANT, we beseech thee, O almighty God, that, by the intercession of blessed 
 thy Martyr, our bodies may be protected from all adversity, and our hearts made pure from every evil thought. Through our Lord.
