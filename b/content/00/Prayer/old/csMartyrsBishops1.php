
Beatórum Mártyrum paritérque Pontíficum <sr>P.</s> nos, quæsumus, Dómine, festa tueántur: et eórum comméndet orátio veneránda. Per Dóminum.
We beseech thee, O Lord, that the festive commemoration of blessed <sr>P.</s> thy Martyrs and Bishops may assist us, and their venerable prayers obtain for us thy gracious favour. Through our Lord.
