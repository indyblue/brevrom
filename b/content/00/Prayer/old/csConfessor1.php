
Deus, qui nos beáti <sr>N.</s> Confessóris tui ánnua solemnitáte lætíficas: concéde propítius; ut, cujus natalícia cólimus, étiam actiónes imitémur. Per Dóminum.
O God, who dost gladden us by the yearly solemnity of blessed <sr>N.</s>, thy Confessor; mercifully grant that, as we celebrate his festival, we may also follow the example of his holy life. Through our Lord.
