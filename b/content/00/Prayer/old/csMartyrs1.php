
Deus, qui nos concédis sanctórum Mártyrum tuórum <sr>P.</s> natalícia cólere: da nobis in ætérna beatitúdine de eórum societáte gaudére. Per Dóminum.
O God, who givest unto us to celebrate the festival of thy holy Martyrs <sr>P.</s>, grant to us to rejoice in their company in eternal happiness. Through our Lord.
