SANCTÓRUM Mártyrum tuórum nos, Dómine, Sérgii, Bacchi, Marcélli et Apuléii beáta mérita prosequántur: et tuo semper fáciant amóre fervéntes. Per Dóminum.
O LORD, may the blessed merits of thy Holy Martyrs Sergius, Bacchus, Marcellus and Apuleius accompany us: and make us ever fervent in thy love. Through our Lord.
