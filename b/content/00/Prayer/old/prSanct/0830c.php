
MAJESTÁTEM tuam, Dómine, súpplices exorámus: ut, sicut nos júgiter Sanctórum tuórum commemoratióne lætíficas; ita semper supplicatióne deféndas. Per Dóminum.
WE suppliantly beseech thy majesty, O Lord, that as thou dost ever gladden us by the commemoration of thy Saints, so thou wouldst always defend us by their prayers. Through our Lord.
