
ADÉSTO, Dómine, pópulo tuo: ut, beáti Nicomédis Mártyris tui mérita præclára suscípiens, ad impetrándam misericórdiam tuam semper ejus patrocíniis adjuvétur. Per Dóminum.
GIVE ear, O Lord, unto the prayer of thy people, that, having recourse to the glorious merits of blessed Nicomedes thy Martyr, they may ever be aided by his patronage in obtaining thy mercy. Through our Lord.
