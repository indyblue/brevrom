  Laverty
PRÆSTA, quæsumus, omnípotens Deus: ut, beátæ Felicitátis Mártyris tuæ solémnia recenséntes, méritis ipsíus protegámur et précibus. Per Dóminum.
GRANT, we beseech thee, almighty God, that we who are keeping the feast of blessed Felicitas thy Martyr, may be shielded by her merits and prayers. Through our Lord.
