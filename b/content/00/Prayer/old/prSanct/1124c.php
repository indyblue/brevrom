
ADÉSTO, Dómine, supplicatiónibus nostris: ut, qui ex iniquitáte nostra reos nos esse cognóscimus, beáti Chrysógoni Mártyris tui intercessióne liberémur. Per Dóminum.
GIVE ear, O Lord, to our supplications; that we who know ourselves to be guilty by reason of our sins, may be delivered by the intercession of thy blessed Martyr Chrysogonus. Through our Lord.
