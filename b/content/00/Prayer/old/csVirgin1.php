
Exáudi nos, Deus, salutáris noster: ut, sicut de beátæ <sr>N.</s> Vírginis tuæ festivitáte gaudémus; ita piæ devotiónis erudiámur afféctu. Per Dóminum.
Graciously hear us, O God our salvation, that as we rejoice in the festival of blessed <sr>N.</s> thy Virgin, so we may be strengthened in the love of true piety. Through our Lord.
