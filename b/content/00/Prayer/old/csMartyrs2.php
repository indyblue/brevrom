
Deus, qui nos ánnua sanctórum Mártyrum tuórum <sr>P.</s> solemnitáte lætíficas: concéde propítius; ut, quorum gaudémus méritis, accendámur exémplis. Per Dóminum.
O God, who dost gladden us with the yearly solemnity of thy blessed Martyrs <sr>P.</s>, grant, we beseech thee, that, as we rejoice in their merits, we may be animated to fervor by their example. Through our Lord.
