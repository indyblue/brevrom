DEUS, qui nos beáti 
 Mártyris tui atque Pontíficis ánnua solemnitáte lætíficas: concéde propítius; ut, cujus natalícia cólimus, de ejúsdem étiam protectióne gaudeámus. Per Dóminum.
O GOD, who dost gladden us by the yearly solemnity of blessed 
 thy Martyr and Bishop; mercifully grant that, as we celebrate his festival, we may also rejoice in his protection. Through our Lord.
