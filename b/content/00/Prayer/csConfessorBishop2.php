EXÁUDI, quæsumus, Dómine, preces nostras, quas in beáti 
 Confessóris tui atque Pontíficis solemnitáte deférimus: et, qui tibi digne méruit famulári, ejus intercedéntibus méritis, ab ómnibus nos absólve peccátis. Per Dóminum.
GRACIOUSLY hear the prayers, we beseech thee, O Lord, which we offer unto thee on the solemnity of blessed 
 thy Confessor and Bishop, and as he deserved worthily to serve thee, so, by his merits, do thou absolve us from all our sins. Through our Lord.
