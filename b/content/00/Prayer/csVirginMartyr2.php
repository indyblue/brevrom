INDULGÉNTIAM nobis, quæsumus, Dómine, beáta 
 Virgo et Martyr implóret: quæ tibi grata semper éxstitit, et mérito castitátis, et tuæ professióne virtútis. Per Dóminum.
MAY BLESSED 
 thy Virgin and Martyr, we beseech thee, O Lord, implore for us forgiveness; inasmuch as she was ever pleasing unto thee, both by the merit of her chastity and the confession of thy power. Through our Lord.
