BEATÓRUM Mártyrum paritérque Pontíficum 
 nos, quæsumus, Dómine, festa tueántur: et eórum comméndet orátio veneránda. Per Dóminum.
WE BESEECH thee, O Lord, that the festive commemoration of blessed 
 thy Martyrs and Bishops may assist us, and their venerable prayers obtain for us thy gracious favour. Through our Lord.
