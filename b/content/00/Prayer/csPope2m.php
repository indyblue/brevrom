GREGEM tuum, Pastor ætérne, placátus inténde: et per beátos 
 Mártyres tuos atque Summos Pontífices perpétua protectióne custódi; quos totíus Ecclésiæ præstitísti esse pastóres. Per Dóminum.
LOOK forgivingly on thy flock, Eternal Shepherd, and keep it in thy constant protection, by the intercession of blessed 
 thy Martyrs and Sovereign Pontiffs, whom thou didst constitute Shepherds of the whole Church. Through our Lord.
