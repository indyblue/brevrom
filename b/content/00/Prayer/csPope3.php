DEUS, qui Ecclésiam tuam in apostólicæ petræ soliditáte fundátam, ab infernárum éruis terróre portárum: præsta, quæsumus; ut, intercedénte beáto 
 Summo Pontífice, in tua veritáte persístens, contínua securitáte muniátur. Per Dóminum.
O GOD, who didst solidly found thy Church on the apostolic rock and dost shield her from the dreaded gates of hell, grant, we beseech thee, through the intercession of blessed 
 thy Sovereign Pontiff, that under thy sure protection she may persevere in thy truth. Through our Lord.
