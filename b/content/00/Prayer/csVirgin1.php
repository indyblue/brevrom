EXÁUDI nos, Deus, salutáris noster: ut, sicut de beátæ 
 Vírginis tuæ festivitáte gaudémus; ita piæ devotiónis erudiámur afféctu. Per Dóminum.
GRACIOUSLY hear us, O God our salvation, that as we rejoice in the festival of blessed 
 thy Virgin, so we may be strengthened in the love of true piety. Through our Lord.
