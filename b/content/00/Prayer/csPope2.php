GREGEM tuum, Pastor ætérne, placátus inténde: et per beátos 
 Summos Pontífices perpétua protectióne custódi; quos totíus Ecclésiæ præstitísti esse pastóres. Per Dóminum.
LOOK forgivingly on thy flock, Eternal Shepherd, and keep it in thy constant protection, by the intercession of blessed 
 thy Sovereign Pontiffs, whom thou didst constitute Shepherds of the whole Church. Through our Lord.
