GREGEM tuum, Pastor ætérne, placátus inténde: et per beátum 
 Mártyrem tuum atque Summum Pontíficem perpétua protectióne custódi; quem totíus Ecclésiæ præstitísti esse pastórem. Per Dóminum.
LOOK forgivingly on thy flock, Eternal Shepherd, and keep it in thy constant protection, by the intercession of blessed 
 thy Martyr and Sovereign Pontiff, whom thou didst constitute Shepherd of the whole Church. Through our Lord.
