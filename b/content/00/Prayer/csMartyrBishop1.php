INFIRMITÁTEM nostram réspice, omnípotens Deus: et, quia pondus própriæ actiónis gravat, beáti 
 Mártyris tui atque Pontíficis intercéssio gloriósa nos prótegat. Per Dóminum.
LOOK down, O almighty God, upon our infirmity, and because the weight of our own deeds oppresses us, let the glorious intercession of blessed 
 thy Martyr and Bishop protect us. Through our Lord.
