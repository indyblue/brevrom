INTERCÉSSIO nos, quæsumus, Dómine, beáti 
 Abbátis comméndet: ut quod nostris méritis non valémus, ejus patrocínio assequámur. Per Dóminum.
MAY THE intercession, O Lord, we beseech thee, of the blessed Abbot 
 commend us unto thee, that what by our own merits we are not worthy to receive, we may obtain by his patronage. Through our Lord.
