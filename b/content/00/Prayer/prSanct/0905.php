
DA, QUÆSUMUS, omnípotens Deus: ut beáti Lauréntii Confessóris tui atque Pontíficis veneránda solémnitas, et devotiónem nobis augeat, et salútem. Per Dóminum.
GRANT, we beseech thee, O almighty God, that the venerable Lawrence thy Confessor and Bishop may avail to increase our devotion and to further our salvation. Through our Lord.
