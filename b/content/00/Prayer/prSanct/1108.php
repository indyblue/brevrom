
PRÆSTA, quæsumus, omnípotens Deus: ut, qui gloriósos Mártyres fortes in sua confessióne cognóvimus, pios apud te in nostra intercessióne sentiámus. Per Dóminum.
GRANT, we beseech thee, O almighty God, that we who acknowledge thy glorious Martyrs to have been valiant in their confession of thy faith, may now experience their compassion by their intercession for us with thee. Through our Lord.
