
SANCTI Joánnis Baptístæ Præcursóris et Mártyris tui, quæsumus, Dómine, veneránda festívitas: salutáris auxílii nobis præstet efféctum: Qui vivis.
WE beseech thee, O Lord, that the holy festival of St. John the Baptist, thy Forerunner and Martyr, may give us the fruit of thy saving help. Who livest.
