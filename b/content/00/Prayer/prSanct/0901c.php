
FRATÉRNA nos, Dómine, Mártyrum tuórum coróna lætíficet: quæ et fídei nostræ præbeat increménta virtútum, et multíplici nos suffrágio consolétur. Per Dóminum.
WE BESEECH thee, O Lord, that the victory of the holy Martyr Brother may gladden us, and that our faith may be strengthened and our hearts comforted by the prayers of so many intercessors. Through our Lord.
