
INTERVÉNIAT pro nobis, quæsumus, Dómine, sanctus tuus Lucas Evangelísta: qui crucis mortificatiónem júgiter in suo córpore, pro tui nóminis honóre, portávit. Per Dóminum.
LET THY holy Evangelist Luke intercede for us, we beseech thee, O Lord; who, for the honour of thy name, ever bore in his body the mortification of the cross. Through our Lord.
