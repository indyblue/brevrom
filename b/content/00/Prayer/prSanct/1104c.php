
PRÆSTA, quæsumus, omnípotens Deus: ut, qui sanctórum Mártyrum tuórum Vitális et Agrícolæ solémnia cólimus, eórum apud te intercessiónibus adjuvémur. Per Dóminum.
GRANT, we beseech thee, almighty God, that we who celebrate the festival of thy holy Martyrs Vitalis and Agricola, may be aided by their intercession with thee. Through our Lord.
