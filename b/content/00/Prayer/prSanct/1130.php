
MAJESTÁTEM tuam, Dómine, supplíciter exorámus: ut, sicut Ecclésiæ tuæ beátus Andreas Apóstolus éxstitit prædicátor et rector; ita apud te sit pro nobis perpétuus intercéssor. Per Dóminum.
WE HUMBLY entreat thy Majesty, O Lord, that as the blessed Apostle Andrew was eminent as a preacher and ruler in thy Church, so he may unceasingly intercede for us with thee. Through our Lord.
