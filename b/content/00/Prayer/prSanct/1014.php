
DEUS, qui nos cónspicis ex nostra infirmitáte defícere: ad amórem tuum nos misericórditer per Sanctórum tuórum exémpla restáura. Per Dóminum.
O GOD, wo seest that we fail by reason of our own weakness: mercifully restore us, we beseech thee, to thy love, by the example of thy Saints. Through our Lord.
