
BEATÓRUM Mártyrum paritérque Pontíficum Cornélii et Cypriáni nos, quæsumus, Dómine, festa tueántur: et eórum comméndet orátio veneránda. Per Dóminum.
WE BESEECH thee, O Lord, that the festive commemoration of blessed Cornelius and Cyprian thy Martyrs and Pontiffs may assist us, and their venerable prayers obtain for us thy gracious favour. Through our Lord.
