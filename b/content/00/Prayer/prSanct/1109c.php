
DEUS, qui nos beáti Theodóri Mártyris tui confessióne gloriósa circúmdas et prótegis: præsta nobis ex ejus imitatióne profícere, et oratióne fulcíri. Per Dóminum.
O GOD, who dost surround and protect us by the glorious confession of blessed Theodore; grant that we may profit by his example, and be strengthened by his intercession. Through our Lord.
