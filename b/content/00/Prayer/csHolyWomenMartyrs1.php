DA NOBIS, quæsumus, Dómine, Deus noster, sanctárum Mártyrum tuárum 
 palmas incesábili devotióne venerári: ut, quas digna mente non póssumus celebráre; humílibus saltem frequentémus obséquiis. Per Dóminum.
GRANT unto us, we beseech thee, O Lord our God, to venerate with unceasing devotion the glorious triumphs of 
 thy holy Martyrs; that, whereas we cannot duly celebrate their praises, we may at least honour them with humble service. Through our Lord.
