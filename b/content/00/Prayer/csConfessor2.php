ADÉSTO, Dómine, supplicatiónibus nostris, quas in beáti 
 Confessóris tui solemnitáte deférimus: ut, qui nostræ justítiæ fidúciam non habémus, ejus, qui tibi plácuit précibus adjuvémur. Per Dóminum.
GIVE ear, O Lord, unto our supplications, which we offer to thee on the solemnity of blessed 
 thy Confessor, that, as we put no trust in our own merits, we may be aided by the prayers of him who was well-pleasing unto thee. Through our Lord.
