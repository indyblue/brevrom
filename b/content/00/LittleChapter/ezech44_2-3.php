Ezech. 44:2-3
Porta hæc clausa erit, non aperiétur, et vir non intrábit per eam, quóniam Dóminus Deus Israël ingréssus est per eam: erítque clausa príncipi. Princeps ipse sedébit in ea.
This gate shall be shut, it shall not be opened, and no man shall pass through it, because the Lord the God of Israel hath entered by it: it shall be shut for the Prince. The Prince himself shall sit in it.
