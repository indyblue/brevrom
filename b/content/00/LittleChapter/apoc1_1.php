Apoc. 1:1-2
Significávit Deus, quæ opórtet fíeri cito, loquens per Ángelum suum servo suo Joánni, qui testimónium perhíbuit verbo Dei, et testimónium Jesu Christi, quæcúmque vidit.
God, speaking by his Angel, signified unto his servant John the things which must shortly come to pass: who hath given testimony to the word of God, and the testimony of Jesus Christ, what things soever he hath seen.
