Ecclus. 24:9-11
In omni terra steti et in omni pópulo, et in omni gente primátum hábui, et ómnium excelléntium et humílium corda virtúte calcávi.
And I have stood in all the earth: and in every people, and in every nation I have had the chief rule, and by my power I have trodden under my feet the hearts of all the high and low.

