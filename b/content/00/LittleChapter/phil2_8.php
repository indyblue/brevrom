Phil. 2:8-9
Humiliávit semetípsum factus obédiens usque ad mortem, mortem autem Crucis. Propter quod et Deus exaltávit illum, et donávit illi nomen, quod est super omne nomen.
He humbled himself, becoming obedient unto death, even to the death of the Cross. For which cause God also hath exalted him, and given him a name which is above all names.
