Hebr. 1:11-12
Ipsi períbunt, tu autem permanébis; et omnes sicut vestiméntum veteráscent: et velut amíctum mutábis eos, et mutabúntur: tu autem idem ipse es, et anni tui non defícient.
They shall perish, but thou shalt continue: and they shall all grow old as a garment. And as a vesture shalt thou change them, and they shall be changed: but thou art the selfsame, and thy years shall not fail.
