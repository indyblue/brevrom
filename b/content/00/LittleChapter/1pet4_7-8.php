I Peter 4:7-8
Caríssimi: Estóte prudéntes, et vigiláte in oratiónibus. Ante ómnia autem mútuam in vobismetípsis caritátem contínuam habéntes, quia cáritas óperit multitúdinem peccatórum.
Dearly beloved: Be prudent, and watch in prayers. But before all things have a constant mutual charity among yourselves, for charity covereth a multitude of sins.
