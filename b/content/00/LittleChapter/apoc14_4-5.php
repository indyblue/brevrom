Apoc. 14:4-5
Hi empti sunt ex homínibus primítiæ Deo et Agno, et in ore ipsórum non est invéntum mendácium: sine mácula enim sunt ante thronum Dei.
These were purchased from among men, the firstfruits to God and to the Lamb, and in their mouth there was found no lie: for they are without spot before the throne of God.
