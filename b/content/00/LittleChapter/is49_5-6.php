Is. 49:5-6
Et nunc, hæc dicit Dóminus formans me ex útero servum sibi: Dedi te in lucem Géntium, ut sis salus mea usque ad extrémum terræ.
And now saith the Lord, that formed me from the womb to be his servant: I have given thee to be the light of the Gentiles, that thou mayst be my salvation even to the farthest part of the earth.
