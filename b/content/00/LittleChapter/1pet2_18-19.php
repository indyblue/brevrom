I Peter 2:18-19
Servi, súbditi estóte in omni timóre dóminis, non tantum bonis et modéstis, sed étiam dýscolis. Hæc est enim grátia in Christo Jesu Dómino nostro.
Servants, be subject to your masters with all fear, not only to the good and gentle, but also to the froward. For this is thankworthy in Christ Jesus our Lord.
