Phil. 2:8-9
In nómine Jesu omne genu flectátur cæléstium, terréstrium, et infernórum: et omnis lingua confiteátur, quia Dóminus Jesus Christus in glória est Dei Patris.
At the name of Jesus let every knee bow, of things in heaven, and things in earth, and things under the earth: and let every tongue confess that our Lord Jesus Christ is in the glory of God the Father.
