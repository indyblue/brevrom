Exod. 23:20-21
Ecce ego mittam Ángelum meum, qui præcédat te et custódiat in via et introdúcat in locum quem parávi. Obsérva eum et audi vocem ejus.
Behold I will send my Angel before thee, to keep thee in the way, and to bring thee into the place which I have prepared. Take notice of him, and hear his voice.
