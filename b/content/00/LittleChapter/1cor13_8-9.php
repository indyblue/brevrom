I Cor. 13:8-9
Cáritas numquam éxcidit: sive prophetíæ evacuabúntur, sive linguæ cessábunt, sive sciéntia destruétur. Ex parte enim cognóvimus, et ex parte prophetámus.
Charity never falleth away: whether prophecies shall be made void, or tongues shall cease, or knowledge shall be destroyed. For we know in part, and we prophesy in part.
