Apoc. 21:10-11,23
Sústulit me in spíritu in montem magnum et altum, et osténdit mihi civitátem sanctam Jerúsalem, descendéntem de cælo a Deo, habéntem claritátem Dei; et lucérna ejus est Agnus.
He took me up in spirit to a great and high mountain: and he shewed me the holy city Jerusalem coming down out of heaven from God, having the glory of God; and the Lamb is the lamp thereof.
