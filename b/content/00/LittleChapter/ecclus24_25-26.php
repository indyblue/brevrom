Ecclus. 24:25-26
In me grátia omnis viæ et veritátis, in me omnis spes vitæ et virtútis. Transíte ad me, omnes qui concupíscitis me, et a generatiónibus meis implémini. 
In me is all grace of the way and of the truth; in me is all hope of life and virtue. Come over to me, all ye that desire me, and be filled with my fruits.
