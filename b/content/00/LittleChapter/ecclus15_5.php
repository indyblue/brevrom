Ecclus. (Sir.) 15:5
In médio Ecclésiæ apéruit os ejus, et implévit illum Dóminus spíritu sapiéntiæ et intelléctus, et stolam glóriæ índuit eum.
In the midst of the Church he opened his mouth, and the Lord hath filled him with the spirit of wisdom and understanding, and hath clothed him with a robe of glory.
