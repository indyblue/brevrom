Phil. 2:10-11
In nómine Jesu omne genu flectátur cæléstium, terréstrium, et infernórum: et omnis lingua confiteátur, quia Dóminus Jesus Christus in glória est Dei Patris.
At the name of Jesus let every knee bow, of those that are in heaven, on earth, and under the earth: and let every tongue confess that the Lord Jesus Christ is in the glory of God the Father.
