Eph. 5:5
Hoc enim scitóte intelligéntes, quod omnis fornicátor, aut immúndus, aut avárus, quod est idolórum servítus, non habet hereditátem in regno Christi et Dei.
For know ye this and understand, that no fornicator, or unclean, or covetous person (which is a serving of idols) hath inheritance in the kingdom of Christ and of God.
