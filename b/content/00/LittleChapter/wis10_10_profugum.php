Wis. 10:10
Prófugum justum dedúxit Sapiéntia per vias rectas, et osténdit illi regnum Dei, et dedit illi sciéntiam sanctórum; honestávit illum in labóribus, et complévit labóres illíus.
When the just fled, Wisdom conducted him through the right ways, and showed him the kingdom of God, and gave him the knowledge of the holy things; made him honourable in his labours, and accomplished his labours.
