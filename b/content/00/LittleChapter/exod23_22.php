Exod. 23:22-23
Quod, si audíeris vocem ejus et féceris ómnia quæ loquor, inimícus ero inimícis tuis et afflígam affligéntes te. Præcedétque te Ángelus meus.
But if thou wilt hear his voice, and do all that I speak, I will be an enemy to thy enemies, and will afflict them that afflict thee. And my Angel shall go before thee.
