Rom. 1:5-6
Per quem accépimus grátiam, et apostolátum ad obediéndum fídei in ómnibus géntibus pro nómine ejus, in quibus estis et vos vocáti Jesu Christi Dómini nostri.
By whom we have received grace and apostleship for obedience to the faith in all nations for his name, among whom are you also the called of Jesus Christ.
