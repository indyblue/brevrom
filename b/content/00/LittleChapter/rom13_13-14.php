Rom. 13:13-14
Sicut in die honéste ambulémus, non in comessatiónibus et ebrietátibus, non in cubílibus et impudicítiis, non in contentióne et æmulatióne; sed induímini Dóminum Jesum Christum.
Let us walk honestly, as in the day, not in rioting and drunkenness, not in chambering and impurities, not in contention and envy: but put ye on the Lord Jesus Christ.
