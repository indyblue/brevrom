Wis 10:17
RÉDDIDIT Deus mercédem labórum sanctórum suórum, et dedúxit illos in via mirábili; et fuit illis in velaménto diéi, et in luce stellárum nocte.
GOD HAS rendered to the just the wages of their labours, and conducted them in a wonderful way: and he was to them for a covert by day, and for the light of stars by night.
