Malach 3:1-2
Ecce véniet, dicit Dóminus exercítuum: et quis póterit cogitáre diem advéntus ejus, et quis stabit ad vidéndum eum? Ipse enim quasi ignis conflans, et quasi herba fullónum.
Behold he cometh, saith the Lord of hosts: and who shall be able to think of the day of his coming? and who shall stand to see him? For he is like a refining fire, and like the fuller’s herb.
