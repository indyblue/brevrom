Rom. 10:10-11
Fratres: Corde enim créditur ad justítiam, ore autem conféssio fit ad salútem. Dicit enim Scriptúra: Omnis, qui credit in illum, non confundétur.
Brethren: With the heart we believe unto justice; but with the mouth confession is made unto salvation. For the Scripture saith: Whosoever believeth in him shall not be confounded.
