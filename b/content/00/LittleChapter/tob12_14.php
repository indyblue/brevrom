Tob. 12:14-15
Et nunc misit me Dóminus ut curárem te, et Saram, uxórem fílii tui, a dæmónio liberárem. Ego enim sum Ráphaël Ángelus: unus ex septem, qui astámus ante Dóminum.
And now the Lord hath sent me to heal thee, and to deliver Sara thy son’s wife from the devil. For I am the angel Raphael, one of the seven, who stand before the Lord.

