Jer. 11:20
Tu autem, Dómine Sábaoth, qui júdicas juste, et probas renes et corda, vídeam ultiónem tuam ex eis: tibi enim revelávi causam meam, Dómine, Deus meus.
But thou, O Lord of Sabaoth, who judgest justly, and triest the reins and the hearts, let me see thy revenge on them: for to thee I have revealed my cause, O Lord, my God.
