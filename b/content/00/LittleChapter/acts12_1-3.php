Acts 12:1-3
Misit Heródes rex manus ut afflígeret quosdam de Ecclésia. Occídit autem Jacóbum fratrem Joánnis gládio. Videns autem quia placéret Judǽis, appósuit ut apprehénderet et Petrum.
Herod the king stretched forth his hands, to afflict some of the church. And he killed James, the brother of John, with the sword. And seeing that it pleased the Jews, he proceeded to take up Peter also.

