Apoc. 5:11
AUDÍVI vocem Angelórum multórum in circúitu throni, et animálium et seniórum; et erat númerus eórum míllia míllium, voce magna dicéntium: Salus Deo nostro.
I HEARD the voice of many Angels round about the throne, and the living creatures, and the ancients; and the number of them was thousands of thousands, saying with a loud voice: Salvation to our God.
