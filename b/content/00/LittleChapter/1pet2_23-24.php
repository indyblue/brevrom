I Peter 2:23-24
Tradébat autem judicánti se injúste: qui peccáta nostra ipse pértulit in córpore suo super lignum: ut peccátis mórtui, justítiæ vivámus: cujus livóre sanáti sumus.
But he delivered himself to him that judged him unjustly: who his own self bore our sins in his body upon the tree: that we, being dead to sins, should live to justice: by whose stripes you were healed.
