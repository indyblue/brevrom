Titus 3:4
Appáruit benígnitas, et humánitas Salvatóris nostri Dei: non ex opéribus justítiæ, quæ fécimus nos, sed secúndum suam misericórdiam salvos nos fecit.
The goodness, and kindness of God our Saviour hath appeared: not by the works of justice, which we have done, but according to his mercy he saved us.
