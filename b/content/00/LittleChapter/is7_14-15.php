Is. 7:14-15
Ecce virgo concípiet, et páriet fílium, et vocábitur nomen ejus Emmánuel. Butyrum et mel cómedet, ut sciat reprobáre malum, et elígere bonum.
Behold a virgin shall conceive, and bear a son, and his name shall be called Emmanuel. He shall eat butter and honey, that he may know to refuse the evil and choose the good.
