Ecclus. 24:17-18
Quasi cedrus exaltáta sum in Líbano, et quasi cypréssus in monte Sion: quasi palma exaltáta sum in Cades, et quasi plantátio rosæ in Jéricho.
I was exalted like a cedar in Libanus, and as a cypress tree on mount Sion; I was exalted like a palm tree in Cades, and as a rose plant in Jericho.
