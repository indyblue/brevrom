I Peter 4:11
Si quis lóquitur, quasi sermónes Dei: si quis minístrat, tamquam ex virtúte, quam adminístrat Deus: ut in ómnibus honorificétur Deus per Jesum Christum Dóminum nostrum.
If any man speak, let him speak as the words of God: if any man minister, let him do it as of the power which God administereth: that in all things God may be honoured through Jesus Christ our Lord.
