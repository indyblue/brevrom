James 1:17
Caríssimi: Omne datum óptimum, et omne donum perféctum desúrsum est, descéndens a Patre lúminum, apud quem non est transmutátio, nec vicissitúdinis obumbrátio.
Dearly beloved: Every best gift and every perfect gift is from above, coming down from the Father of lights, with whom there is no change nor shadow of alteration.
