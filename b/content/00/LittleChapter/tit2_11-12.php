Titus 2:11-12
Appáruit grátia Dei Salvatóris nostri ómnibus homínibus, erúdiens nos, ut, abnegántes impietátem et sæculária desidéria, sóbrie et juste et pie vivámus in hoc sǽculo.
The grace of God our Saviour hath appeared to all men, instructing us, that, denying ungodliness and worldly desires, we should live soberly and justly and godly in this world.
