I John 5:5-6
Quis est autem qui vincit mundum, nisi qui credit, quóniam Jesus est Fílius Dei? hic est, qui venit per aquam et sánguinem, Jesus Christus: non in aqua solum, sed in aqua et sánguine.
Who is he that overcometh the world, but he that believeth that Jesus is the Son of God? This is he that came by water and blood, Jesus Christ: not by water only, but by water and blood.
