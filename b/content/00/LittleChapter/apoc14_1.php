Apoc. 14:1
Vidi supra montem Sion Agnum stantem, et cum eo centum quadragínta quátuor míllia habéntes nomen ejus, et nomen Patris ejus scriptum in fróntibus suis.
I beheld the Lamb standing on mount Sion, and with him a hundred forty-four thousand, having his name and the name of his Father written on their foreheads.
