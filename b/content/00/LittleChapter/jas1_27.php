James 1:27
Relígio munda et immaculáta apud Deum et Patrem, hæc est: Visitáre pupíllos et víduas in tribulatióne eórum, et immaculátum se custodíre ab hoc sǽculo.
Religion clean and undefiled before God and the Father, is this: to visit the fatherless and widows in their tribulation, and to keep oneself unspotted from this world.
