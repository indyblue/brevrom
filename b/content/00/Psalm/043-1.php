   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>DEUS, áuribus nostris audívimus: * patres nostri annuntiavérunt nobis.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>WE HAVE heard, O God, with our ears: * our fathers have declared to us,</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Opus, quod operátus es in diébus eórum, * et in diébus antíquis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The work thou hast wrought in their days, * and in the days of old.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Manus tua gentes dispérdidit, et plantásti eos: * afflixísti pópulos, et expulísti eos.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thy hand destroyed the Gentiles, and thou plantedst them: * thou didst afflict the people and cast them out.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Nec enim in gládio suo possedérunt terram, * et bráchium eórum non salvávit eos:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For they got not the possession of the land by their own sword: * neither did their own arm save them.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Sed déxtera tua, et bráchium tuum, et illuminátio vultus tui: * quóniam complacuísti in eis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>But thy right hand and thy arm, and the light of thy countenance: * because thou wast pleased with them.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Tu es ipse Rex meus et Deus meus: * qui mandas salútes Jacob.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou art thyself my king and my God, * who commandest the saving of Jacob.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>In te inimícos nostros ventilábimus cornu: * et in nómine tuo spernémus insurgéntes in nobis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Through thee we will push down our enemies with the horn: * and through thy name we will despise them that rise up against us.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Non enim in arcu meo sperábo: * et gládius meus non salvábit me.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For I will not trust in my bow: * neither shall my sword save me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Salvásti enim nos de affligéntibus nos: * et odiéntes nos confudísti.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>But thou hast saved us from them that afflict us: * and hast put them to shame that hate us.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>In Deo laudábimur tota die: * et in nómine tuo confitébimur in sæculum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>In God shall we glory all the day long: * and in thy name we will give praise for ever.</p>
     </td>
    </tr>
