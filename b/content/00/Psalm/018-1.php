   <p:Head2Ps>Psalmus 18 i. Cæli enárrant</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>CÆLI enárrant glóriam Dei: * et ópera mánuum ejus annúntiat firmaméntum.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>THE heavens shew forth the glory of God, * and the firmament declareth the work of his hands.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Dies diéi erúctat verbum, * et nox nocti índicat sciéntiam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Day to day uttereth speech, * and night to night sheweth knowledge.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Non sunt loquélæ, neque sermónes, * quorum non audiántur voces eórum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>There are no speeches nor languages, * where their voices are not heard.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>In omnem terram exívit sonus eórum: * et in fines orbis terræ verba eórum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Their sound hath gone forth into all the earth: * and their words unto the ends of the world.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>In sole pósuit tabernáculum suum: * et ipse tamquam sponsus procédens de thálamo suo:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He hath set his tabernacle in the sun: * and he, as a bridegroom coming out of his bride chamber,</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Exsultávit ut gigas ad curréndam viam, * a summo cælo egréssio ejus:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Hath rejoiced as a giant to run the way: * his going out is from the end of heaven,</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et occúrsus ejus usque ad summum ejus: * nec est qui se abscóndat a calóre ejus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And his circuit even to the end thereof: * and there is no one that can hide himself from his heat.</p>
     </td>
    </tr>
   </table>
