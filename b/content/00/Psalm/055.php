   <p:Head2Ps>Psalmus 55. Miserére mei, Deus</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>MISERÉRE mei, Deus, quóniam conculcávit me homo: * tota die impúgnans tribulávit me.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>HAVE mercy on me, O God, for man hath trodden me under foot; * all the day long he hath afflicted me fighting against me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Conculcavérunt me inimíci mei tota die: * quóniam multi bellántes advérsum me.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>My enemies have trodden on me all the day long; * for they are many that make war against me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ab altitúdine diéi timébo: * ego vero in te sperábo.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>From the height of the day I shall fear: * but I will trust in thee.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>In Deo laudábo sermónes meos, in Deo sperávi: * non timébo quid fáciat mihi caro.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>In God I will praise my words, in God I have put my trust: * I will not fear what flesh can do against me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Tota die verba mea exsecrabántur: * advérsum me omnes cogitatiónes eórum in malum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>All the day long they detested my words: * all their thoughts were against me unto evil.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Inhabitábunt et abscóndent: * ipsi calcáneum meum observábunt.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>They will dwell and hide themselves: * they will watch my heel.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Sicut sustinuérunt ánimam meam, pro níhilo salvos fácies illos: * in ira pópulos confrínges.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>As they have waited for my soul, for nothing shalt thou save them: * in thy anger thou shalt break the people in pieces.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Deus, vitam meam annuntiávi tibi: * posuísti lácrimas meas in conspéctu tuo,</p>
     </td>
     <td:B1>
      <p:BodyEIndent>O God, I have declared to thee my life: * thou hast set my tears in thy sight,</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Sicut et in promissióne tua: * tunc converténtur inimíci mei retrórsum:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>As also in thy promise. * Then shall my enemies be turned back.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>In quacúmque die invocávero te: * ecce, cognóvi, quóniam Deus meus es.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>In what day soever I shall call upon thee, * behold I know thou art my God.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>In Deo laudábo verbum, in Dómino laudábo sermónem: * in Deo sperávi, non timébo quid fáciat mihi homo.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>In God will I praise the word, in the Lord will I praise his speech. * In God have I hoped, I will not fear what man can do to me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>In me sunt, Deus, vota tua, * quæ reddam, laudatiónes tibi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>In me, O God, are vows to thee, * which I will pay, praises to thee:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quóniam eripuísti ánimam meam de morte, et pedes meos de lapsu: * ut pláceam coram Deo in lúmine vivéntium.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Because thou hast delivered my soul from death, my feet from falling: * that I may please in the sight of God, in the light of the living.</p>
     </td>
    </tr>
   </table>
