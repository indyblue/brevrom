   <p:Head2Ps>Psalmus 91. Bonum est confitéri</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>BONUM est confitéri Dómino: * et psállere nómini tuo, Altíssime.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>IT IS good to give praise to the Lord: * and to sing to thy name, O most High.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ad annuntiándum mane misericórdiam tuam: * et veritátem tuam per noctem.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>To shew forth thy mercy in the morning, * and thy truth in the night:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>In decachórdo, psaltério: * cum cántico, in cíthara.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Upon an instrument of ten strings, upon the psaltery: * with a canticle upon the harp.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quia delectásti me, Dómine, in factúra tua: * et in opéribus mánuum tuárum exsultábo.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For thou hast given me, O Lord, a delight in thy doings: * and in the works of thy hands I shall rejoice.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quam magnificáta sunt ópera tua, Dómine! * nimis profúndæ factæ sunt cogitatiónes tuæ.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>O Lord, how great are thy works! * thy thoughts are exceeding deep.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Vir insípiens non cognóscet: * et stultus non intélliget hæc.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The senseless man shall not know: * nor will the fool understand these things.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Cum exórti fúerint peccatóres sicut fœnum: * et apparúerint omnes, qui operántur iniquitátem:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>When the wicked shall spring up as grass: * and all the workers of iniquity shall appear:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ut intéreant in sæculum sæculi: * tu autem Altíssimus in ætérnum, Dómine.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>That they may perish for ever and ever: * but thou, O Lord, art most high for evermore.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quóniam ecce inimíci tui, Dómine, quóniam ecce inimíci tui períbunt: * et dispergéntur omnes, qui operántur iniquitátem.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For behold thy enemies, O Lord, for behold thy enemies shall perish: * and all the workers of iniquity shall be scattered.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et exaltábitur sicut unicórnis cornu meum: * et senéctus mea in misericórdia úberi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>But my horn shall be exalted like that of the unicorn: * and my old age in plentiful mercy.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et despéxit óculus meus inimícos meos: * et in insurgéntibus in me malignántibus áudiet auris mea.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>My eye also hath looked down upon my enemies: * and my ear shall hear of the downfall of the malignant that rise up against me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Justus, ut palma florébit: * sicut cedrus Líbani multiplicábitur.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The just shall flourish like the palm tree: * he shall grow up like the cedar of Libanus.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Plantáti in domo Dómini, * in átriis domus Dei nostri florébunt.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>They that are planted in the house of the Lord * shall flourish in the courts of the house of our God.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Adhuc multiplicabúntur in senécta úberi: * et bene patiéntes erunt, ut annúntient:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>They shall still increase in a fruitful old age: * and shall be well treated, that they may shew,</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quóniam rectus Dóminus, Deus noster: * et non est iníquitas in eo.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>That the Lord our God is righteous, * and there is no iniquity in him.</p>
     </td>
    </tr>
   </table>
