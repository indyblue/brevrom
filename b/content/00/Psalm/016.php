   <p:Head2Ps>Psalmus 16. Exáudi, Dómine</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>EXÁUDI, Dómine, justítiam meam: * inténde deprecatiónem meam.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>HEAR, O Lord, my justice: * attend to my supplication.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Áuribus pércipe oratiónem meam, * non in lábiis dolósis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Give ear unto my prayer, * which proceedeth not from deceitful lips.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>De vultu tuo judícium meum pródeat: * óculi tui vídeant æquitátes.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Let my judgment come forth from thy countenance: * let thy eyes behold the things that are equitable.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Probásti cor meum, et visitásti nocte: * igne me examinásti, et non est invénta in me iníquitas.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou hast proved my heart, and visited it by night, * thou hast tried me by fire: and iniquity hath not been found in me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ut non loquátur os meum ópera hóminum: * propter verba labiórum tuórum ego custodívi vias duras.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>That my mouth may not speak the works of men: * for the sake of the words of thy lips, I have kept hard ways.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Pérfice gressus meos in sémitis tuis: * ut non moveántur vestígia mea.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Perfect thou my goings in thy paths: * that my footsteps be not moved.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ego clamávi, quóniam exaudísti me, Deus: * inclína aurem tuam mihi, et exáudi verba mea.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I have cried to thee, for thou, O God, hast heard me: * O incline thy ear unto me, and hear my words.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Mirífica misericórdias tuas, * qui salvos facis sperántes in te.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Shew forth thy wonderful mercies; * thou who savest them that trust in thee.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>A resisténtibus déxteræ tuæ custódi me, * ut pupíllam óculi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>From them that resist thy right hand keep me, * as the apple of thy eye.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Sub umbra alárum tuárum prótege me: * a fácie impiórum qui me afflixérunt.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Protect me under the shadow of thy wings. * from the face of the wicked who have afflicted me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Inimíci mei ánimam meam circumdedérunt, ádipem suum conclusérunt: * os eórum locútum est supérbiam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>My enemies have surrounded my soul: They have shut up their fat: * their mouth hath spoken proudly.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Projiciéntes me nunc circumdedérunt me: * óculos suos statuérunt declináre in terram.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>They have cast me forth, and now they have surrounded me: * they have set their eyes bowing down to the earth.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Suscepérunt me sicut leo parátus ad prædam: * et sicut cátulus leónis hábitans in ábditis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>They have taken me, as a lion prepared for the prey; * and as a young lion dwelling in secret places.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Exsúrge, Dómine, prævéni eum, et supplánta eum: * éripe ánimam meam ab ímpio, frámeam tuam ab inimícis manus tuæ.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Arise, O Lord, disappoint him and supplant him; * deliver my soul from the wicked one; thy sword from the enemies of thy hand.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Dómine, a paucis de terra dívide eos in vita eórum: * de abscónditis tuis adimplétus est venter eórum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent><text:s/>O Lord, divide them from the few of the earth in their life: * their belly is filled from thy hidden stores.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Saturáti sunt fíliis: * et dimisérunt relíquias suas párvulis suis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>They are full of children: * and they have left to their little ones the rest of their substance.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ego autem in justítia apparébo conspéctui tuo: * satiábor cum apparúerit glória tua.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>But as for me, I will appear before thy sight in justice: * I shall be satisfied when thy glory shall appear.</p>
     </td>
    </tr>
   </table>
