   <p:Head2Ps>Psalmus 149. Cantáte Dómino</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>CANTÁTE Dómino cánticum novum: * laus ejus in ecclésia sanctórum.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>SING ye to the Lord a new canticle: * let his praise be in the church of the saints.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Lætétur Israël in eo, qui fecit eum: * et fílii Sion exsúltent in rege suo.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Let Israel rejoice in him that made him: * and let the children of Sion be joyful in their king.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Laudent nomen ejus in choro: * in týmpano, et psaltério psallant ei:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Let them praise his name in choir: * let them sing to him with the timbrel and the psaltery.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quia beneplácitum est Dómino in pópulo suo: * et exaltábit mansuétos in salútem.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For the Lord is well pleased with his people: * and he will exalt the meek unto salvation.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Exsultábunt sancti in glória: * lætabúntur in cubílibus suis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The saints shall rejoice in glory: * they shall be joyful in their beds.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Exaltatiónes Dei in gútture eórum: * et gládii ancípites in mánibus eórum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The high praises of God shall be in their mouth: * and two-edged swords in their hands:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ad faciéndam vindíctam in natiónibus: * increpatiónes in pópulis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>To execute vengeance upon the nations, * chastisements among the people:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ad alligándos reges eórum in compédibus: * et nóbiles eórum in mánicis férreis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>To bind their kings with fetters, * and their nobles with manacles of iron.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ut fáciant in eis judícium conscríptum: * glória hæc est ómnibus sanctis ejus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>To execute upon them the judgment that is written: * this glory is to all his saints.</p>
     </td>
    </tr>
   </table>
