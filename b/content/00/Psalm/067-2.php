      <p:BodyLIndent>Ut intingátur pes tuus in sánguine: * lingua canum tuórum ex inimícis, ab ipso.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>That thy foot may be dipped in the blood of thy enemies; * the tongue of thy dogs be red with the same.</p>
     </td>
    </tr>
