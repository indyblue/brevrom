     </td>
     <td:B1>
      <p:BodyEIndent>The king of powers is of the beloved, * of the beloved; and the beauty of the house shall divide spoils.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Si dormiátis inter médios cleros, pennæ colúmbæ deargentátæ, * et posterióra dorsi ejus in pallóre auri.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>If you sleep among the midst of lots, you shall be as the wings of a dove covered with silver, * and the hinder parts of her back with the paleness of gold.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Dum discérnit cæléstis reges super eam, nive dealbabúntur in Selmon: * mons Dei, mons pinguis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>When he that is in heaven appointeth kings over her, * they shall be whited with snow in Selmon.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Mons coagulátus, mons pinguis: * ut quid suspicámini montes coagulátos?</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The mountain of God is a fat mountain, a curdled mountain, a fat mountain; * Why suspect, ye curdled mountains?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Mons, in quo beneplácitum est Deo habitáre in eo: * étenim Dóminus habitábit in finem.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>A mountain in which God is well pleased to dwell: * for there the Lord shall dwell unto the end.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Currus Dei decem míllibus múltiplex, míllia lætántium: * Dóminus in eis in Sina in sancto.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The chariot of God is attended by ten thousands; thousands of them that rejoice: * the Lord is among them in Sinai, in the holy place.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ascendísti in altum, cepísti captivitátem: * accepísti dona in homínibus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou hast ascended on high, thou hast led captivity captive; * thou hast received gifts in men.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Étenim non credéntes, * inhabitáre Dóminum Deum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Yea for those also that do not believe, * the dwelling of the Lord God.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Benedíctus Dóminus die quotídie: * prósperum iter fáciet nobis Deus salutárium nostrórum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Blessed be the Lord day by day: * the God of our salvation will make our journey prosperous to us.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Deus noster, Deus salvos faciéndi: * et Dómini Dómini éxitus mortis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Our God is the God of salvation: * and of the Lord, of the Lord are the issues from death.</p>
     </td>
    </tr>
    <tr>
