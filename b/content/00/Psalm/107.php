   <p:Head2Ps>Psalmus 107. Parátum cor meum</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>PARÁTUM cor meum, Deus, parátum cor meum: * cantábo, et psallam in glória mea.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>MY HEART is ready, O God, my heart is ready: * I will sing, and will give praise, with my glory.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Exsúrge, glória mea, exsúrge, psaltérium et cíthara: * exsúrgam dilúculo.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Arise, my glory; arise, psaltery and harp: * I will arise in the morning early.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Confitébor tibi in pópulis, Dómine: * et psallam tibi in natiónibus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I will praise thee, O Lord, among the people: * and I will sing unto thee among the nations.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quia magna est super cælos misericórdia tua: * et usque ad nubes véritas tua:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For thy mercy is great above the heavens: * and thy truth even unto the clouds.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Exaltáre super cælos, Deus, et super omnem terram glória tua: * ut liberéntur dilécti tui.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Be thou exalted, O God, above the heavens, and thy glory over all the earth: * that thy beloved may be delivered.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Salvum fac déxtera tua, et exáudi me: * Deus locútus est in sancto suo:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Save with thy right hand and hear me. * God hath spoken in his holiness.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Exsultábo, et dívidam Síchimam, * et convállem tabernaculórum dimétiar.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I will rejoice, and I will divide Sichem * and I will mete out the vale of tabernacles.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Meus est Gálaad, et meus est Manásses: * et Éphraim suscéptio cápitis mei.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Galaad is mine: and Manasses is mine: * and Ephraim the protection of my head.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Juda rex meus: * Moab lebes spei meæ.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Juda is my king: * Moab the pot of my hope.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>In Idumæam exténdam calceaméntum meum: * mihi alienígenæ amíci facti sunt.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Over Edom I will stretch out my shoe: * the aliens are become my friends.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quis dedúcet me in civitátem munítam? * quis dedúcet me usque in Idumæam?</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Who will bring me into the strong city? * who will lead me into Edom?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Nonne tu, Deus, qui repulísti nos, * et non exíbis, Deus, in virtútibus nostris?</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Wilt not thou, O God, who hast cast us off? * and wilt not thou, O God, go forth with our armies?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Da nobis auxílium de tribulatióne: * quia vana salus hóminis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>O grant us help from trouble: * for vain is the help of man.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>In Deo faciémus virtútem: * et ipse ad níhilum dedúcet inimícos nostros.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Through God we shall do mightily: * and he will bring our enemies to nothing.</p>
     </td>
    </tr>
   </table>
