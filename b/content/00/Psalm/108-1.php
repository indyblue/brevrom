   <p:Head2Ps>Psalmus 108 i. Deus, laudem</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>DEUS, laudem meam ne tacúeris: * quia os peccatóris, et os dolósi super me apértum est.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>O GOD, be not thou silent in my praise: * for the mouth of the wicked and the mouth of the deceitful man is opened against me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Locúti sunt advérsum me lingua dolósa, et sermónibus ódii circumdedérunt me: * et expugnavérunt me gratis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>They have spoken against me with deceitful tongues; and they have compassed me about with words of hatred; * and have fought against me without cause.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Pro eo ut me dilígerent, detrahébant mihi: * ego autem orábam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Instead of making me a return of love, they detracted me: * but I gave myself to prayer.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et posuérunt advérsum me mala pro bonis: * et ódium pro dilectióne mea.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And they repaid me evil for good: * and hatred for my love.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Constítue super eum peccatórem: * et diábolus stet a dextris ejus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Set thou the sinner over him: * and may the devil stand at his right hand.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Cum judicátur, éxeat condemnátus: * et orátio ejus fiat in peccátum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>When he is judged, may he go out condemned; * and may his prayer be turned to sin.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Fiant dies ejus pauci: * et episcopátum ejus accípiat alter.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>May his days be few: * and his bishopric let another take.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Fiant fílii ejus órphani: * et uxor ejus vídua.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>May his children be fatherless, * and his wife a widow.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Nutántes transferántur fílii ejus, et mendícent: * et ejiciántur de habitatiónibus suis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Let his children be carried about vagabonds, and beg; * and let them be cast out of their dwellings.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Scrutétur fœnerátor omnem substántiam ejus: * et dirípiant aliéni labóres ejus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>May the usurer search all his substance: * and let strangers plunder his labours.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Non sit illi adjútor: * nec sit qui misereátur pupíllis ejus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>May there be none to help him: * nor none to pity his fatherless offspring.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Fiant nati ejus in intéritum: * in generatióne una deleátur nomen ejus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>May his posterity be cut off; * in one generation may his name be blotted out.</p>
     </td>
    </tr>
   </table>
