	<p:Head2Ps>Psalmus 40. Beátus qui intélligit</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>BEÁTUS qui intélligit super egénum, et páuperem: * in die mala liberábit eum Dóminus.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>BLESSED is he that understandeth concerning the needy and the poor: * the Lord will deliver him in the evil day.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Dóminus consérvet eum, et vivíficet eum, et beátum fáciat eum in terra: * et non tradat eum in ánimam inimicórum ejus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The Lord preserve him and give him life, and make him blessed upon the earth: * and deliver him not up to the will of his enemies.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Dóminus opem ferat illi super lectum dolóris ejus: * univérsum stratum ejus versásti in infirmitáte ejus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The Lord help him on his bed of sorrow: * thou hast turned all his couch in his sickness.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ego dixi: Dómine, miserére mei: * sana ánimam meam, quia peccávi tibi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I said: O Lord, be thou merciful to me: * heal my soul, for I have sinned against thee.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Inimíci mei dixérunt mala mihi: * Quando moriétur, et períbit nomen ejus?</p>
     </td>
     <td:B1>
      <p:BodyEIndent>My enemies have spoken evils against me: * when shall he die and his name perish?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et si ingrediebátur ut vidéret, vana loquebátur: * cor ejus congregávit iniquitátem sibi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And if he came in to see me, he spoke vain things: * his heart gathered together iniquity to itself.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Egrediebátur foras, * et loquebátur in idípsum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He went out * and spoke to the same purpose.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Advérsum me susurrábant omnes inimíci mei: * advérsum me cogitábant mala mihi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>All my enemies whispered together against me: * they devised evils to me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Verbum iníquum constituérunt advérsum me: * Numquid qui dormit non adjíciet ut resúrgat?</p>
     </td>
     <td:B1>
      <p:BodyEIndent>They determined against me an unjust word: * shall he that sleepeth rise again no more?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Étenim homo pacis meæ, in quo sperávi: * qui edébat panes meos, magnificávit super me supplantatiónem.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For even the man of my peace, in whom I trusted, * who ate my bread, hath greatly supplanted me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Tu autem, Dómine, miserére mei, et resúscita me: * et retríbuam eis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>But thou, O Lord, have mercy on me, and raise me up again: * and I will requite them.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>In hoc cognóvi quóniam voluísti me: * quóniam non gaudébit inimícus meus super me.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>By this I know, that thou hast had a good will for me: * because my enemy shall not rejoice over me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Me autem propter innocéntiam suscepísti: * et confirmásti me in conspéctu tuo in ætérnum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>But thou hast upheld me by reason of my innocence: * and hast established me in thy sight for ever.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Benedíctus Dóminus, Deus Israël, a sæculo et usque in sæculum: * fiat, fiat.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Blessed be the Lord the God of Israel from eternity to eternity. * So be it. So be it.</p>
     </td>
    </tr>
