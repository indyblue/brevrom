   <p:Head2Ps>Psalmus 9 ii. Psállite Dómino</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>PSÁLLITE Dómino, qui hábitat in Sion: * annuntiáte inter Gentes stúdia ejus:</p>
     </td>
     <td:B1>
      <p:BodyEDrop>SING ye to the Lord, who dwelleth in Sion: * declare his ways among the Gentiles.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quóniam requírens sánguinem eórum recordátus est: * non est oblítus clamórem páuperum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For requiring their blood he hath remembered them: * he hath not forgotten the cry of the poor.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Miserére mei, Dómine: * vide humilitátem meam de inimícis meis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Have mercy on me, O Lord: * see my humiliation which I suffer from my enemies.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Qui exáltas me de portis mortis, * ut annúntiem omnes laudatiónes tuas in portis fíliæ Sion.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou that liftest me up from the gates of death, * that I may declare all thy praises in the gates of the daughter of Sion.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Exsultábo in salutári tuo: * infíxæ sunt Gentes in intéritu, quem fecérunt.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I will rejoice in thy salvation: * the Gentiles have stuck fast in the destruction which they have prepared.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>In láqueo isto, quem abscondérunt, * comprehénsus est pes eórum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Their foot hath been taken * in the very snare which they hid.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Cognoscétur Dóminus judícia fáciens: * in opéribus mánuum suárum comprehénsus est peccátor.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The Lord shall be known when he executeth judgments: * the sinner hath been caught in the works of his own hands.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Convertántur peccatóres in inférnum, * omnes Gentes quæ obliviscúntur Deum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The wicked shall be turned into hell, * all the nations that forget God.</p>
     </td>
    </tr>
   </table>
