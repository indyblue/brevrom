   <p:Head2Ps>Psalmus 90. Qui hábitat</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>QUI hábitat in adjutório Altíssimi, * in protectióne Dei cæli commorábitur.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>HE THAT dwelleth in the aid of the most High, * shall abide under the protection of the God of Jacob.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Dicet Dómino: Suscéptor meus es tu, et refúgium meum: * Deus meus sperábo in eum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He shall say to the Lord: Thou art my protector, and my refuge: * my God, in him will I trust.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quóniam ipse liberávit me de láqueo venántium, * et a verbo áspero.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For he hath delivered me from the snare of the hunters: * and from the sharp word.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Scápulis suis obumbrábit tibi: * et sub pennis ejus sperábis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He will overshadow thee with his shoulders: * and under his wings thou shalt trust.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Scuto circúmdabit te véritas ejus: * non timébis a timóre noctúrno,</p>
     </td>
     <td:B1>
      <p:BodyEIndent>His truth shall compass thee with a shield: * thou shalt not be afraid of the terror of the night.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>A sagítta volánte in die, a negótio perambulánte in ténebris: * ab incúrsu, et dæmónio meridiáno.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Of the arrow that flieth in the day, of the business that walketh about in the dark: * of invasion, or of the noonday devil.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Cadent a látere tuo mille, et decem míllia a dextris tuis: * ad te autem non appropinquábit.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>A thousand shall fall at thy side, and ten thousand at thy right hand: * but it shall not come nigh thee.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Verúmtamen óculis tuis considerábis: * et retributiónem peccatórum vidébis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>But thou shalt consider with thy eyes: * and shalt see the reward of the wicked.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quóniam tu es, Dómine, spes mea: * Altíssimum posuísti refúgium tuum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Because thou, O Lord, art my hope: * thou hast made the most High thy refuge.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Non accédet ad te malum: * et flagéllum non appropinquábit tabernáculo tuo.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>There shall no evil come to thee: * nor shall the scourge come near thy dwelling.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quóniam Ángelis suis mandávit de te: * ut custódiant te in ómnibus viis tuis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For he hath given his angels charge over thee; * to keep thee in all thy ways.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>In mánibus portábunt te: * ne forte offéndas ad lápidem pedem tuum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>In their hands they shall bear thee up: * lest thou dash thy foot against a stone.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Super áspidem, et basilíscum ambulábis: * et conculcábis leónem et dracónem.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou shalt walk upon the asp and the basilisk: * and thou shalt trample under foot the lion and the dragon.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quóniam in me sperávit, liberábo eum: * prótegam eum, quóniam cognóvit nomen meum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Because he hoped in me I will deliver him: * I will protect him because he hath known my name.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Clamábit ad me, et ego exáudiam eum: * cum ipso sum in tribulatióne: erípiam eum et glorificábo eum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He shall cry to me, and I will hear him: * I am with him in tribulation, I will deliver him, and I will glorify him.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Longitúdine diérum replébo eum: * et osténdam illi salutáre meum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I will fill him with length of days; * and I will shew him my salvation.</p>
     </td>
    </tr>
   </table>
