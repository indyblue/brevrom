   <p:Head2Ps>Psalmus 113. In éxitu Israël</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>IN ÉXITU Israël de Ægýpto, * domus Jacob de pópulo bárbaro:</p>
     </td>
     <td:B1>
      <p:BodyEDrop>WHEN Israel went out of Egypt, * the house of Jacob from a barbarous people:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Facta est Judæa sanctificátio ejus, * Israël potéstas ejus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Judea was made his sanctuary, * Israel his dominion.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Mare vidit, et fugit: * Jordánis convérsus est retrórsum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The sea saw and fled: * Jordan was turned back.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Montes exsultavérunt ut aríetes, * et colles sicut agni óvium.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The mountains skipped like rams, * and the hills like the lambs of the flock.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quid est tibi, mare, quod fugísti: * et tu, Jordánis, quia convérsus es retrórsum?</p>
     </td>
     <td:B1>
      <p:BodyEIndent>What ailed thee, O thou sea, that thou didst flee: * and thou, O Jordan, that thou wast turned back?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Montes, exsultástis sicut aríetes, * et colles, sicut agni óvium.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Ye mountains, that ye skipped like rams, * and ye hills, like lambs of the flock?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>A fácie Dómini mota est terra, * a fácie Dei Jacob.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>At the presence of the Lord the earth was moved, * at the presence of the God of Jacob:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Qui convértit petram in stagna aquárum, * et rupem in fontes aquárum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Who turned the rock into pools of water, * and the stony hill into fountains of waters.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Non nobis, Dómine, non nobis: * sed nómini tuo da glóriam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Not to us, O Lord, not to us; * but to thy name give glory.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Super misericórdia tua, et veritáte tua: * nequándo dicant gentes: Ubi est Deus eórum?</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For thy mercy, and for thy truth’s sake: * lest the Gentiles should say: Where is their God?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Deus autem noster in cælo: * ómnia quæcúmque vóluit, fecit.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>But our God is in heaven: * he hath done all things whatsoever he would.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Simulácra géntium argéntum, et aurum, * ópera mánuum hóminum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The idols of the Gentiles are silver and gold, * the works of the hands of men.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Os habent, et non loquéntur: * óculos habent, et non vidébunt.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>They have mouths and speak not: * they have eyes and see not.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Aures habent, et non áudient: * nares habent, et non odorábunt.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>They have ears and hear not: * they have noses and smell not.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Manus habent, et non palpábunt: pedes habent, et non ambulábunt: * non clamábunt in gútture suo.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>They have hands and feel not: they have feet and walk not: * neither shall they cry out through their throat.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Símiles illis fiant qui fáciunt ea: * et omnes qui confídunt in eis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Let them that make them become like unto them: * and all such as trust in them.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Domus Israël sperávit in Dómino: * adjútor eórum et protéctor eórum est,</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The house of Israel hath hoped in the Lord: * he is their helper and their protector.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Domus Aaron sperávit in Dómino: * adjútor eórum et protéctor eórum est,</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The house of Aaron hath hoped in the Lord: * he is their helper and their protector.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Qui timent Dóminum, speravérunt in Dómino: * adjútor eórum et protéctor eórum est.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>They that fear the Lord have hoped in the Lord: * he is their helper and their protector.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Dóminus memor fuit nostri: * et benedíxit nobis:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The Lord hath been mindful of us, * and hath blessed us.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Benedíxit dómui Israël: * benedíxit dómui Aaron.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He hath blessed the house of Israel: * he hath blessed the house of Aaron.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Benedíxit ómnibus, qui timent Dóminum, * pusíllis cum majóribus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He hath blessed all that fear the Lord, * both little and great.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Adjíciat Dóminus super vos: * super vos, et super fílios vestros.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>May the Lord add blessings upon you: * upon you, and upon your children.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Benedícti vos a Dómino, * qui fecit cælum, et terram.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Blessed be you of the Lord, * who made heaven and earth.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Cælum cæli Dómino: * terram autem dedit fíliis hóminum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The heaven of heaven is the Lord’s: * but the earth he has given to the children of men.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Non mórtui laudábunt te, Dómine: * neque omnes, qui descéndunt in inférnum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The dead shall not praise thee, O Lord: * nor any of them that go down to hell.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Sed nos qui vívimus, benedícimus Dómino, * ex hoc nunc et usque in sæculum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>But we that live bless the Lord: * from this time now and for ever.</p>
     </td>
    </tr>
   </table>
