     </td>
     <td:B1>
      <p:BodyEIndent>Because he struck the rock, and the waters gushed out, * and the streams overflowed.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Numquid et panem potest dare, * aut paráre mensam pópulo suo?</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Can he also give bread, * or provide a table for his people?</p>
     </td>
    </tr>
    <tr>
