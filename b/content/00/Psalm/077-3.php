      <p:BodyLIndent>Et pluit illis manna ad manducándum: * et panem cæli dedit eis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And had rained down manna upon them to eat, * and had given them the bread of heaven.</p>
     </td>
    </tr>
