      <p:BodyLIndent>Ipsi dispergéntur ad manducándum: * si vero non fúerint saturáti, et murmurábunt.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>They shall be scattered abroad to eat, * and shall murmur if they be not filled.</p>
     </td>
    </tr>
