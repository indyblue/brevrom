   <p:Head2Ps>Psalmus 137. Confitébor tibi</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>CONFITÉBOR tibi, Dómine, in toto corde meo: * quóniam audísti verba oris mei.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>I WILL praise thee, O Lord, with my whole heart: * for thou hast heard the words of my mouth.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>In conspéctu Angelórum psallam tibi: * adorábo ad templum sanctum tuum, et confitébor nómini tuo.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I will sing praise to thee in the sight of the Angels: * I will worship towards thy holy temple, and I will give glory to thy name.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Super misericórdia tua, et veritáte tua: * quóniam magnificásti super omne, nomen sanctum tuum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For thy mercy, and for thy truth: * for thou hast magnified thy holy name above all.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>In quacúmque die invocávero te, exáudi me: * multiplicábis in ánima mea virtútem.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>In what day soever I shall call upon thee, hear me: * thou shalt multiply strength in my soul.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Confiteántur tibi, Dómine, omnes reges terræ: * quia audiérunt ómnia verba oris tui:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>May all the kings of the earth give glory to thee: * for they have heard all the words of thy mouth.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et cantent in viis Dómini: * quóniam magna est glória Dómini.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And let them sing in the ways of the Lord: * for great is the glory of the Lord.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quóniam excélsus Dóminus, et humília réspicit: * et alta a longe cognóscit.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For the Lord is high, and looketh on the low: * and the high he knoweth afar off.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Si ambulávero in médio tribulatiónis, vivificábis me: * et super iram inimicórum meórum extendísti manum tuam, et salvum me fecit déxtera tua.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>If I shall walk in the midst of tribulation, thou wilt quicken me: * and thou hast stretched forth thy hand against the wrath of my enemies: and thy right hand hath saved me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Dóminus retríbuet pro me: * Dómine, misericórdia tua in sæculum: ópera mánuum tuárum ne despícias.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The Lord will repay for me: * thy mercy, O Lord, endureth for ever: O despise not the works of thy hands.</p>
     </td>
    </tr>
   </table>
