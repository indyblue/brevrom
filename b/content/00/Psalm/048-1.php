   <p:Head2Ps>Psalmus 48 i. Audíte hæc</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>AUDÍTE hæc, omnes Gentes: * áuribus percípite omnes, qui habitátis orbem:</p>
     </td>
     <td:B1>
      <p:BodyEDrop>HEAR these things, all ye nations: * give ear, all ye inhabitants of the world.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quique terrígenæ, et fílii hóminum: * simul in unum dives et pauper.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>All you that are earthborn, and you sons of men: * both rich and poor together.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Os meum loquétur sapiéntiam: * et meditátio cordis mei prudéntiam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>My mouth shall speak wisdom: * and the meditation of my heart understanding.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Inclinábo in parábolam aurem meam: * apériam in psaltério propositiónem meam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I will incline my ear to a parable; * I will open my proposition on the psaltery.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Cur timébo in die mala? * iníquitas calcánei mei circúmdabit me:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Why shall I fear in the evil day? * the iniquity of my heel shall encompass me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Qui confídunt in virtúte sua: * et in multitúdine divitiárum suárum gloriántur.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>They that trust in their own strength, * and glory in the multitude of their riches,</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Frater non rédimit, rédimet homo: * non dabit Deo placatiónem suam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>No brother can redeem, nor shall man redeem: * he shall not give to God his ransom,</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et prétium redemptiónis ánimæ suæ: * et laborábit in ætérnum, et vivet adhuc in finem.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Nor the price of the redemption of his soul: * and shall labour for ever, and shall still live unto the end.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Non vidébit intéritum, cum víderit sapiéntes moriéntes: * simul insípiens, et stultus períbunt.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He shall not see destruction, when he shall see the wise dying: * the senseless and the fool shall perish together:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et relínquent aliénis divítias suas: * et sepúlcra eórum domus illórum in ætérnum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And they shall leave their riches to strangers: * and their sepulchres shall be their houses for ever.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Tabernácula eórum in progénie et progénie: * vocavérunt nómina sua in terris suis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Their dwelling places to all generations: * they have called their lands by their names.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et homo, cum in honóre esset, non intelléxit: * comparátus est juméntis insipiéntibus, et símilis factus est illis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And man when he was in honour did not understand; * he is compared to senseless beasts, and is become like to them.</p>
     </td>
    </tr>
   </table>
