     </td>
     <td:B1>
      <p:BodyEIndent>He hath distributed, he hath given to the poor: his justice remaineth for ever and ever: * his horn shall be exalted in glory.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Peccátor vidébit, et irascétur, déntibus suis fremet et tabéscet: * desidérium peccatórum períbit.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The wicked shall see, and shall be angry, he shall gnash with his teeth and pine away: * the desire of the wicked shall perish.</p>
     </td>
