     </td>
     <td:B1>
      <p:BodyEIndent>Glory and wealth shall be in his house: * and his justice remaineth for ever and ever.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Exórtum est in ténebris lumen rectis: * miséricors, et miserátor, et justus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>To the righteous a light is risen up in darkness: * he is merciful, and compassionate and just.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Jucúndus homo qui miserétur et cómmodat, dispónet sermónes suos in judício: * quia in ætérnum non commovébitur.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Acceptable is the man that sheweth mercy and lendeth: he shall order his words with judgment: * because he shall not be moved for ever.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
