     </td>
     <td:B1>
      <p:BodyEIndent>The just shall be in everlasting remembrance: * he shall not fear the evil hearing.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Parátum cor ejus speráre in Dómino, confirmátum est cor ejus: * non commovébitur donec despíciat inimícos suos.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>His heart is ready to hope in the Lord: his heart is strengthened, * he shall not be moved until he look over his enemies.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
