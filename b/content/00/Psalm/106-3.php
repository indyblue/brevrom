      <p:BodyLDrop>CONFITEÁNTUR Dómino misericórdiæ ejus: * et mirabília ejus fíliis hóminum.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>LET THE mercies of the Lord give glory to him, * and his wonderful works to the children of men.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et exáltent eum in ecclésia plebis: * et in cáthedra seniórum laudent eum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And let them exalt him in the church of the people: * and praise him in the chair of the ancients.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Pósuit flúmina in desértum: * et éxitus aquárum in sitim.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He hath turned rivers into a wilderness: * and the sources of water into dry ground:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Terram fructíferam in salsúginem: * a malítia inhabitántium in ea.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>A fruitful land into barrenness, * for the wickedness of them that dwell therein.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Pósuit desértum in stagna aquárum: * et terram sine aqua in éxitus aquárum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He hath turned a wilderness into pools of water, * and a dry land into water springs.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et collocávit illic esuriéntes: * et constituérunt civitátem habitatiónis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And hath placed there the hungry; * and they made a city for their habitation.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et seminavérunt agros, et plantavérunt víneas: * et fecérunt fructum nativitátis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And they sowed fields, and planted vineyards: * and they yielded fruit of birth.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et benedíxit eis, et multiplicáti sunt nimis: * et juménta eórum non minorávit.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And he blessed them, and they were multiplied exceedingly: * and their cattle he suffered not to decrease.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et pauci facti sunt: * et vexáti sunt a tribulatióne malórum, et dolóre.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Then they were brought to be few: * and they were afflicted through the trouble of evils and sorrow.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Effúsa est contémptio super príncipes: * et erráre fecit eos in ínvio, et non in via.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Contempt was poured forth upon their princes: *and he caused them to wander where there was no passing, and out of the way.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et adjúvit páuperem de inópia: * et pósuit sicut oves famílias.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And he helped the poor out of poverty: * and made him families like a flock of sheep.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Vidébunt recti, et lætabúntur: * et omnis iníquitas oppilábit os suum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The just shall see, and shall rejoice, * and all iniquity shall stop their mouth.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quis sápiens et custódiet hæc? * et intélliget misericórdias Dómini.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Who is wise, and will keep these things: * and will understand the mercies of the Lord?</p>
     </td>
    </tr>
