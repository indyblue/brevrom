   <p:Head2Ps>Psalmus 48 ii. Hæc via illórum</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>HÆC via illórum scándalum ipsis: * et póstea in ore suo complacébunt.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>THIS way of theirs is a stumblingblock to them: * and afterwards they shall delight in their mouth.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Sicut oves in inférno pósiti sunt: * mors depáscet eos.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>They are laid in hell like sheep: * death shall feed upon them.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et dominabúntur eórum justi in matutíno: * et auxílium eórum veteráscet in inférno a glória eórum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And the just shall have dominion over them in the morning; * and their help shall decay in hell from their glory.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Verúmtamen Deus rédimet ánimam meam de manu ínferi: * cum accéperit me.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>But God will redeem my soul from the hand of hell, * when he shall receive me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ne timúeris, cum dives factus fúerit homo: * et cum multiplicáta fúerit glória domus ejus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Be not thou afraid, when a man shall be made rich, * and when the glory of his house shall be increased.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quóniam cum interíerit, non sumet ómnia: * neque descéndet cum eo glória ejus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For when he shall die he shall take nothing away; * nor shall his glory descend with him.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quia ánima ejus in vita ipsíus benedicétur: * confitébitur tibi cum beneféceris ei.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For in his lifetime his soul will be blessed: * and he will praise thee when thou shalt do well to him.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Introíbit usque in progénies patrum suórum: * et usque in ætérnum non vidébit lumen.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He shall go in to the generations of his fathers: * and he shall never see light.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Homo, cum in honóre esset, non intelléxit: * comparátus est juméntis insipiéntibus, et símilis factus est illis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Man when he was in honour did not understand: * he hath been compared to senseless beasts, and made like to them.</p>
     </td>
    </tr>
   </table>
