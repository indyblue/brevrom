   <p:Head2Ps>Psalmus 81. Deus stetit</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>DEUS stetit in synagóga deórum: * in médio autem deos dijúdicat.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>GOD hath stood in the congregation of gods: * and being in the midst of them he judgeth gods.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Úsquequo judicátis iniquitátem: * et fácies peccatórum súmitis?</p>
     </td>
     <td:B1>
      <p:BodyEIndent>How long will you judge unjustly: * and accept the persons of the wicked?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Judicáte egéno, et pupíllo: * húmilem, et páuperem justificáte.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Judge for the needy and fatherless: * do justice to the humble and the poor.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Erípite páuperem: * et egénum de manu peccatóris liberáte.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Rescue the poor; * and deliver the needy out of the hand of the sinner.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Nesciérunt, neque intellexérunt, in ténebris ámbulant: * movebúntur ómnia fundaménta terræ.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>They have not known nor understood: they walk on in darkness: * all the foundations of the earth shall be moved.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ego dixi: Dii estis, * et fílii Excélsi omnes.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I have said: You are gods * and all of you the sons of the most High.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Vos autem sicut hómines moriémini: * et sicut unus de princípibus cadétis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>But you like men shall die: * and shall fall like one of the princes.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Surge, Deus, júdica terram: * quóniam tu hereditábis in ómnibus géntibus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Arise, O God, judge thou the earth: * for thou shalt inherit among all the nations.</p>
     </td>
    </tr>
   </table>
