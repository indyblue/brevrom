     <td:A1>
      <p:BodyLDrop>QUÓMODO miserétur pater filiórum, misértus est Dóminus timéntibus se: * quóniam ipse cognóvit figméntum nostrum.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>AS A FATHER hath compassion on his children, so hath the Lord compassion on them that fear him: * for he knoweth our frame.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Recordátus est quóniam pulvis sumus: * homo, sicut fœnum dies ejus, tamquam flos agri sic efflorébit.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He remembereth that we are dust: * man’s days are as grass, as the flower of the field so shall he flourish.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quóniam spíritus pertransíbit in illo, et non subsístet: * et non cognóscet ámplius locum suum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For the spirit shall pass in him, and he shall not be: * and he shall know his place no more.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Misericórdia autem Dómini ab ætérno, * et usque in ætérnum super timéntes eum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>But the mercy of the Lord is from eternity * and unto eternity upon them that fear him:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et justítia illíus in fílios filiórum, * his qui servant testaméntum ejus:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And his justice unto children’s children, * to such as keep his covenant,</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et mémores sunt mandatórum ipsíus, * ad faciéndum ea.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And are mindful of his commandments * to do them.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Dóminus in cælo parávit sedem suam: * et regnum ipsíus ómnibus dominábitur.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The Lord hath prepared his throne in heaven: * and his kingdom shall rule over all.</p>
     </td>
    </tr>
