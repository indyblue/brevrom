      <p:BodyLIndent>Benedícite Dómino, ómnia ópera ejus: * in omni loco dominatiónis ejus, bénedic, ánima mea, Dómino.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Bless the Lord, all his works: * in every place of his dominion, O my soul, bless thou the Lord.</p>
