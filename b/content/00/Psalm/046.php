      <p:BodyLIndent>Subjécit pópulos nobis: * et gentes sub pédibus nostris.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He hath subdued the people under us; * and the nations under our feet.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Elégit nobis hereditátem suam: * spéciem Jacob, quam diléxit.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He hath chosen for us his inheritance, * the beauty of Jacob which he hath loved.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ascéndit Deus in júbilo: * et Dóminus in voce tubæ.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>God is ascended with jubilee, * and the Lord with the sound of trumpet.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Psállite Deo nostro, psállite: * psállite Regi nostro, psállite.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Sing praises to our God, sing ye: * sing praises to our king, sing ye.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quóniam Rex omnis terræ Deus: * psállite sapiénter.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For God is the king of all the earth: * sing ye wisely.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Regnábit Deus super gentes: * Deus sedet super sedem sanctam suam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>God shall reign over the nations: * God sitteth on his holy throne.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Príncipes populórum congregáti sunt cum Deo Ábraham: * quóniam dii fortes terræ veheménter eleváti sunt.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The princes of the people are gathered together, with the God of Abraham: * for the strong gods of the earth are exceedingly exalted.</p>
     </td>
    </tr>
