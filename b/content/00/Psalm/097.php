   <p:Head2Ps>Psalmus 97. Cantáte Dómino</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>CANTÁTE Dómino cánticum novum: * quia mirabília fecit.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>SING ye to the Lord a new canticle: * because he hath done wonderful things.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Salvávit sibi déxtera ejus: * et bráchium sanctum ejus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>His right hand hath wrought for him salvation, * and his arm is holy.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Notum fecit Dóminus salutáre suum: * in conspéctu géntium revelávit justítiam suam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The Lord hath made known his salvation: * he hath revealed his justice in the sight of the Gentiles.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Recordátus est misericórdiæ suæ, * et veritátis suæ dómui Israël.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He hath remembered his mercy * and his truth toward the house of Israel.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Vidérunt omnes términi terræ * salutáre Dei nostri.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>All the ends of the earth have seen * the salvation of our God.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Jubiláte Deo, omnis terra: * cantáte, et exsultáte, et psállite.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Sing joyfully to God, all the earth; * make melody, rejoice and sing.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Psállite Dómino in cíthara, in cíthara et voce psalmi: * in tubis ductílibus, et voce tubæ córneæ.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Sing praise to the Lord on the harp, on the harp, and with the voice of a psalm: * with long trumpets, and sound of cornet.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Jubiláte in conspéctu regis Dómini: * moveátur mare, et plenitúdo ejus: orbis terrárum, et qui hábitant in eo.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Make a joyful noise before the Lord our king: * let the sea be moved and the fulness thereof: the world and they that dwell therein.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Flúmina plaudent manu, simul montes exsultábunt a conspéctu Dómini: * quóniam venit judicáre terram.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The rivers shall clap their hands, the mountains shall rejoice together at the presence of the Lord: * because he cometh to judge the earth.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Judicábit orbem terrárum in justítia, * et pópulos in æquitáte.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He shall judge the world with justice, * and the people with equity.</p>
     </td>
    </tr>
   </table>
