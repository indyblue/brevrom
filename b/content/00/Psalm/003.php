   <p:Head2Ps>Psalmus 3. Dómine quid multiplicáti</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>DÓMINE quid multiplicáti sunt qui tríbulant me? * multi insúrgunt advérsum me.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>WHY, O Lord, are they multiplied that afflict me? * many are they who rise up against me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Multi dicunt ánimæ meæ: * Non est salus ipsi in Deo ejus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Many say to my soul: * There is no salvation for him in his God.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Tu autem, Dómine, suscéptor meus es, * glória mea, et exáltans caput meum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>But thou, O Lord art my protector, * my glory, and the lifter up of my head.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Voce mea ad Dóminum clamávi: * et exaudívit me de monte sancto suo.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I have cried to the Lord with my voice: * and he hath heard me from his holy hill.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ego dormívi, et soporátus sum: * et exsurréxi, quia Dóminus suscépit me.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I have slept and taken my rest: * and I have risen up, because the Lord hath protected me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Non timébo míllia pópuli circumdántis me: * exsúrge, Dómine, salvum me fac, Deus meus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I will not fear thousands of the people, surrounding me: * arise, O Lord; save me, O my God.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quóniam tu percussísti omnes adversántes mihi sine causa: * dentes peccatórum contrivísti.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For thou hast struck all them who are my adversaries without cause: * thou hast broken the teeth of sinners.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Dómini est salus: * et super pópulum tuum benedíctio tua.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Salvation is of the Lord: and thy blessing is upon thy people.</p>
     </td>
    </tr>
   </table>
