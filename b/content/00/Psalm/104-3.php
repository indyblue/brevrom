     </td>
     <td:B1>
      <p:BodyEDrop>HE SENT darkness, and made it obscure: * and grieved not his words.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Convértit aquas eórum in sánguinem: * et occídit pisces eórum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He turned their waters into blood, * and destroyed their fish.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Édidit terra eórum ranas: * in penetrálibus regum ipsórum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Their land brought forth frogs, * in the inner chambers of their kings.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Dixit, et venit cœnomyía: * et cínifes in ómnibus fínibus eórum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He spoke, and there came divers sorts of flies, * and sciniphs in all their coasts.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Pósuit plúvias eórum grándinem: * ignem comburéntem in terra ipsórum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He gave them hail for rain, * a burning fire in the land.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et percússit víneas eórum, et ficúlneas eórum: * et contrívit lignum fínium eórum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And he destroyed their vineyards, and their fig trees: * and he broke in pieces the trees of their coasts.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Dixit, et venit locústa, et bruchus, * cujus non erat númerus:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He spoke, and the locust came, and the bruchus, * of which there was no number.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et comédit omne fœnum in terra eórum: * et comédit omnem fructum terræ eórum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And they devoured all the grass in their land, * and consumed all the fruit of their ground.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et percússit omne primogénitum in terra eórum: * primítias omnis labóris eórum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And he slew all the firstborn in their land: * the firstfruits of all their labour.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et edúxit eos cum argénto et auro: * et non erat in tríbubus eórum infírmus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And he brought them out with silver and gold: * and there was not among their tribes one that was feeble.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Lætáta est Ægýptus in profectióne eórum: * quia incúbuit timor eórum super eos.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Egypt was glad when they departed: * for the fear of them lay upon them.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Expándit nubem in protectiónem eórum: * et ignem ut lucéret eis per noctem.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He spread a cloud for their protection, * and fire to give them light in the night.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Petiérunt, et venit cotúrnix: * et panem cæli saturávit eos.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>They asked, and the quail came: * and he filled them with the bread of heaven.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Dirúpit petram et fluxérunt aquæ: * abiérunt in sicco flúmina;</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He opened the rock, and waters flowed: * rivers ran down in the dry land.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quóniam memor fuit verbi sancti sui: * quod hábuit ad Ábraham, púerum suum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Because he remembered his holy word, * which he had spoken to his servant Abraham.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et edúxit pópulum suum in exsultatióne, * et eléctos suos in lætítia.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And he brought forth his people with joy, * and his chosen with gladness.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et dedit illis regiónes Géntium: * et labóres populórum possedérunt:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And he gave them the lands of the Gentiles: * and they possessed the labours of the people:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ut custódiant justificatiónes ejus, * et legem ejus requírant.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>That they might observe his justifications, * and seek after his law.</p>
     </td>
    </tr>
   </table>
