     </td>
     <td:B1>
      <p:BodyEIndent>I will be glad and rejoice in thee: * I will sing to thy name, O thou most high.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>In converténdo inimícum meum retrórsum: * infirmabúntur, et períbunt a fácie tua.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>When my enemy shall be turned back: * they shall be weakened and perish before thy face.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quóniam fecísti judícium meum et causam meam: * sedísti super thronum, qui júdicas justítiam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For thou hast maintained my judgment and my cause: * thou hast sat on the throne, who judgest justice.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Increpásti Gentes, et périit ímpius: * nomen eórum delésti in ætérnum, et in sæculum sæculi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou hast rebuked the Gentiles, and the wicked one hath perished: * thou hast blotted out their name for ever and ever.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Inimíci defecérunt frámeæ in finem: * et civitátes eórum destruxísti.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The swords of the enemy have failed unto the end: * and their cities thou hast destroyed.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Périit memória eórum cum sónitu: * et Dóminus in ætérnum pérmanet.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Their memory hath perished with a noise. * But the Lord remaineth for ever.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Parávit in judício thronum suum: * et ipse judicábit orbem terræ in æquitáte, judicábit pópulos in justítia.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He hath prepared his throne in judgment: * And he shall judge the world in equity, he shall judge the people in justice.</p>
     </td>
    </tr>
    <tr>
