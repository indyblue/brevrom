     </td>
     <td:B1>
      <p:BodyEIndent>And the Lord is become a refuge for the poor: * a helper in due time in tribulation.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et sperent in te qui novérunt nomen tuum: * quóniam non dereliquísti quæréntes te, Dómine.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And let them trust in thee who know thy name: * for thou hast not forsaken them that seek thee, O Lord.</p>
     </td>
