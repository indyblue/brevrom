   <p:Head2Ps>Psalmus 105 i. Confitémini Dómino</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>CONFITÉMINI Dómino, quóniam bonus: * quóniam in sæculum misericórdia ejus.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>GIVE glory to the Lord, for he is good: * for his mercy endureth for ever.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quis loquétur poténtias Dómini, * audítas fáciet omnes laudes ejus?</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Who shall declare the powers of the Lord? * who shall set forth all his praises?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Beáti, qui custódiunt judícium, * et fáciunt justítiam in omni témpore.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Blessed are they that keep judgment, * and do justice at all times.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Meménto nostri, Dómine, in beneplácito pópuli tui: * vísita nos in salutári tuo:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Remember us, O Lord, in the favour of thy people: * visit us with thy salvation.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ad vidéndum in bonitáte electórum tuórum, ad lætándum in lætítia gentis tuæ: * ut laudéris cum hereditáte tua.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>That we may see the good of thy chosen, that we may rejoice in the joy of thy nation: * that thou mayst be praised with thy inheritance.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Peccávimus cum pátribus nostris: * injúste égimus, iniquitátem fécimus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>We have sinned with our fathers: * we have acted unjustly, we have wrought iniquity.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Patres nostri in Ægýpto non intellexérunt mirabília tua: * non fuérunt mémores multitúdinis misericórdiæ tuæ.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Our fathers understood not thy wonders in Egypt: * they remembered not the multitude of thy mercies:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et irritavérunt ascendéntes in mare, * Mare Rubrum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And they provoked to wrath going up to the sea, * even the Red Sea.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et salvávit eos propter nomen suum: * ut notam fáceret poténtiam suam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And he saved them for his own name&apos;s sake: * that he might make his power known.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et incrépuit Mare Rubrum, et exsiccátum est, * et dedúxit eos in abýssis sicut in desérto.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And he rebuked the Red Sea, and it was dried up: * and he led them through the depths, as in a wilderness.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et salvávit eos de manu odiéntium: * et redémit eos de manu inimíci.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And he saved them from the hand of them that hated them: * and he redeemed them from the hand of the enemy.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et opéruit aqua tribulántes eos: * unus ex eis non remánsit.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And the water covered them that afflicted them: * there was not one of them left.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et credidérunt verbis ejus: * et laudavérunt laudem ejus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And they believed his words: * and they sang his praises.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Cito fecérunt, oblíti sunt óperum ejus: * et non sustinuérunt consílium ejus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>They had quickly done, they forgot his works: * and they waited not for his counsels.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et concupiérunt concupiscéntiam in desérto: * et tentavérunt Deum in inaquóso.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And they coveted their desire in the desert: * and they tempted God in the place without water.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et dedit eis petitiónem ipsórum: * et misit saturitátem in ánimas eórum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And he gave them their request: * and sent fulness into their souls.</p>
     </td>
    </tr>
   </table>
