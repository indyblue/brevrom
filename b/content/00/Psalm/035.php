   <p:Head2Ps>Psalmus 35. Dixit injústus</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>DIXIT injústus ut delínquat in semetípso: * non est timor Dei ante óculos ejus.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>THE unjust hath said within himself, that he would sin: * there is no fear of God before his eyes.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quóniam dolóse egit in conspéctu ejus: * ut inveniátur iníquitas ejus ad ódium.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For in his sight he hath done deceitfully, * that his iniquity may be found unto hatred.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Verba oris ejus iníquitas, et dolus: * nóluit intellígere ut bene ágeret.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The words of his mouth are iniquity and guile: * he would not understand that he might do well.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Iniquitátem meditátus est in cubíli suo: * ástitit omni viæ non bonæ, malítiam autem non odívit.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He hath devised iniquity on his bed, * he hath set himself on every way that is not good: but evil he hath not hated.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Dómine, in cælo misericórdia tua: * et véritas tua usque ad nubes.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>O Lord, thy mercy is in heaven, * and thy truth reacheth even to the clouds.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Justítia tua sicut montes Dei: * judícia tua abýssus multa.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thy justice is as the mountains of God, * thy judgments are a great deep.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Hómines, et juménta salvábis, Dómine: * quemádmodum multiplicásti misericórdiam tuam, Deus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Men and beasts thou wilt preserve, O Lord: * O how hast thou multiplied thy mercy, O God!</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Fílii autem hóminum, * in tégmine alárum tuárum sperábunt.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>But the children of men * shall put their trust under the covert of thy wings.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Inebriabúntur ab ubertáte domus tuæ: * et torrénte voluptátis tuæ potábis eos.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>They shall be inebriated with the plenty of thy house; * and thou shalt make them drink of the torrent of thy pleasure.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quóniam apud te est fons vitæ: * et in lúmine tuo vidébimus lumen.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For with thee is the fountain of life; * and in thy light we shall see light.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Præténde misericórdiam tuam sciéntibus te, * et justítiam tuam his, qui recto sunt corde.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Extend thy mercy to them that know thee, * and thy justice to them that are right in heart.</p>
     </td>
    </tr>
    <tr>
