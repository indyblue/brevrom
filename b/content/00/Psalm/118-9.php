   <p:Head2Ps>Psalmus 118 ix. Mirabília</p>
   <p:Head4>Phe</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>MIRABÍLIA testimónia tua: * ídeo scrutáta est ea ánima mea.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>THY testimonies are wonderful: * therefore my soul hath sought them.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Declarátio sermónum tuórum illúminat: * et intelléctum dat párvulis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The declaration of thy words giveth light: * and giveth understanding to little ones.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Os meum apérui, et attráxi spíritum: * quia mandáta tua desiderábam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I opened my mouth, and panted: * because I longed for thy commandments.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Áspice in me, et miserére mei, * secúndum judícium diligéntium nomen tuum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Look thou upon me, and have mercy on me, * according to the judgment of them that love thy name.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Gressus meos dírige secúndum elóquium tuum: * et non dominétur mei omnis injustítia.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Direct my steps according to thy word: * and let no iniquity have dominion over me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Rédime me a calúmniis hóminum: * ut custódiam mandáta tua.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Redeem me from the calumnies of men: * that I may keep thy commandments.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Fáciem tuam illúmina super servum tuum: * et doce me justificatiónes tuas.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Make thy face to shine upon thy servant: * and teach me thy justifications.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Éxitus aquárum deduxérunt óculi mei: * quia non custodiérunt legem tuam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>My eyes have sent forth springs of water: * because they have not kept thy law.</p>
     </td>
    </tr>
   </table>
