   <table>
    <tr>
     <td:A1>
      <p:BodyLIndent>Justus es, Dómine: * et rectum judícium tuum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou art just, O Lord: * and thy judgment is right.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Mandásti justítiam testimónia tua: * et veritátem tuam nimis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou hast commanded justice thy testimonies: * and thy truth exceedingly.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Tabéscere me fecit zelus meus: * quia oblíti sunt verba tua inimíci mei.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>My zeal hath made me pine away: * because my enemies forgot thy words.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ignítum elóquium tuum veheménter: * et servus tuus diléxit illud.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thy word is exceedingly refined: * and thy servant hath loved it.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Adolescéntulus sum ego et contémptus: * justificatiónes tuas non sum oblítus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I am very young and despised; but * I forget not thy justifications.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Justítia tua, justítia in ætérnum: * et lex tua véritas.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thy justice is justice for ever: * and thy law is the truth.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Tribulátio, et angústia invenérunt me: * mandáta tua meditátio mea est.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Trouble and anguish have found me: * thy commandments are my meditation.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Æquitas testimónia tua in ætérnum: * intelléctum da mihi, et vivam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thy testimonies are justice for ever: * give me understanding, and I shall live.</p>
     </td>
    </tr>
   </table>
