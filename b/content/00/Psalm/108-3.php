   <p:Head2Ps>Psalmus 108 iii. Líbera me</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>LÍBERA me quia egénus, et pauper ego sum: * et cor meum conturbátum est intra me.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>DO THOU deliver me, for I am poor and needy, * and my heart is troubled within me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Sicut umbra cum declínat, ablátus sum: * et excússus sum sicut locústæ.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I am taken away like the shadow when it declineth: * and I am shaken off as locusts.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Génua mea infirmáta sunt a jejúnio: * et caro mea immutáta est propter óleum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>My knees are weakened through fasting: * and my flesh is changed for oil.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et ego factus sum oppróbrium illis: * vidérunt me, et movérunt cápita sua.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And I am become a reproach to them: * they saw me and they shaked their heads.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ádjuva me, Dómine, Deus meus: * salvum me fac secúndum misericórdiam tuam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Help me, O Lord my God; * save me according to thy mercy.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et sciant quia manus tua hæc: * et tu, Dómine, fecísti eam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And let them know that this is thy hand: * and that thou, O Lord, hast done it.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Maledícent illi, et tu benedíces: * qui insúrgunt in me, confundántur: servus autem tuus lætábitur.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>They will curse and thou wilt bless: * let them that rise up against me be confounded: but thy servant shall rejoice.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Induántur qui détrahunt mihi, pudóre: * et operiántur sicut diplóide confusióne sua.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Let them that detract me be clothed with shame: * and let them be covered with their confusion as with a double cloak.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Confitébor Dómino nimis in ore meo: * et in médio multórum laudábo eum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I will give great thanks to the Lord with my mouth: * and in the midst of many I will praise him.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quia ástitit a dextris páuperis, * ut salvam fáceret a persequéntibus ánimam meam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Because he hath stood at the right hand of the poor, * to save my soul from persecutors.</p>
     </td>
    </tr>
   </table>
