   <p:Head2Ps>Psalmus 118 iii. Legem pone</p>
   <p:Head4>He</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>LEGEM pone mihi, Dómine, viam justificatiónum tuárum: * et exquíram eam semper.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>SET before me for a law the way of thy justifications, O Lord: * and I will always seek after it.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Da mihi intelléctum, et scrutábor legem tuam: * et custódiam illam in toto corde meo.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Give me understanding, and I will search thy law; * and I will keep it with my whole heart.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Deduc me in sémitam mandatórum tuórum: * quia ipsam vólui.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Lead me into the path of thy commandments; * for this same I have desired.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Inclína cor meum in testimónia tua: * et non in avarítiam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Incline my heart into thy testimonies * and not to covetousness.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Avérte óculos meos ne vídeant vanitátem: * in via tua vivífica me.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Turn away my eyes that they may not behold vanity: * quicken me in thy way.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Státue servo tuo elóquium tuum, * in timóre tuo.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Establish thy word to thy servant, * in thy fear.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ámputa oppróbrium meum quod suspicátus sum: * quia judícia tua jucúnda.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Turn away my reproach, which I have apprehended: * for thy judgments are delightful.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ecce, concupívi mandáta tua: * in æquitáte tua vivífica me.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Behold I have longed after thy precepts: * quicken me in thy justice.</p>
     </td>
    </tr>
   </table>
   <p:Head4>Vau</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et véniat super me misericórdia tua, Dómine: * salutáre tuum secúndum elóquium tuum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Let thy mercy also come upon me, O Lord: * thy salvation according to thy word.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et respondébo exprobrántibus mihi verbum: * quia sperávi in sermónibus tuis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>So shall I answer them that reproach me in any thing; * that I have trusted in thy words.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et ne áuferas de ore meo verbum veritátis usquequáque: * quia in judíciis tuis supersperávi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And take not thou the word of truth utterly out of my mouth: * for in thy words, I have hoped exceedingly.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et custódiam legem tuam semper: * in sæculum et in sæculum sæculi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>So shall I always keep thy law, * for ever and ever.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et ambulábam in latitúdine: * quia mandáta tua exquisívi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And I walked at large: * because I have sought after thy commandments.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et loquébar in testimóniis tuis in conspéctu regum: * et non confundébar.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And I spoke of thy testimonies before kings: * and I was not ashamed.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et meditábar in mandátis tuis, * quæ diléxi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I meditated also on thy commandments, * which I loved.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et levávi manus meas ad mandáta tua, quæ diléxi: * et exercébar in justificatiónibus tuis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And I lifted up my hands to thy commandments, which I loved: * and I was exercised in thy justifications.</p>
     </td>
    </tr>
   </table>
