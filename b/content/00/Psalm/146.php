     </td>
     <td:B1>
      <p:BodyEIndent>The Lord buildeth up Jerusalem: * he will gather together the dispersed of Israel.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Qui sanat contrítos corde: * et álligat contritiónes eórum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Who healeth the broken of heart, * and bindeth up their bruises.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Qui númerat multitúdinem stellárum: * et ómnibus eis nómina vocat.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Who telleth the number of the stars: * and calleth them all by their names.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Magnus Dóminus noster, et magna virtus ejus: * et sapiéntiæ ejus non est númerus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Great is our Lord, and great is his power: * and of his wisdom there is no number.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Suscípiens mansuétos Dóminus: * humílians autem peccatóres usque ad terram.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The Lord lifteth up the meek, * and bringeth the wicked down even to the ground.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Præcínite Dómino in confessióne: * psállite Deo nostro in cíthara.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Sing ye to the Lord with praise: * sing to our God upon the harp.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Qui óperit cælum núbibus: * et parat terræ plúviam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Who covereth the heaven with clouds, * and prepareth rain for the earth.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Qui prodúcit in móntibus fœnum: * et herbam servitúti hóminum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Who maketh grass to grow on the mountains, * and herbs for the service of men.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Qui dat juméntis escam ipsórum: * et pullis corvórum invocántibus eum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Who giveth to beasts their food: * and to the young ravens that call upon him.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Non in fortitúdine equi voluntátem habébit: * nec in tíbiis viri beneplácitum erit ei.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He shall not delight in the strength of the horse: * nor take pleasure in the legs of a man.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Beneplácitum est Dómino super timéntes eum: * et in eis, qui sperant super misericórdia ejus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The Lord taketh pleasure in them that fear him: * and in them that hope in his mercy.</p>
     </td>
    </tr>
