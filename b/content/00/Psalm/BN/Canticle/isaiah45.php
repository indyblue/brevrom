Canticum Isaiæ.      Is. 45. 15.
Vere tu es Deus abscónditus, * Deus Israël, Salvátor.
2  Confúsi sunt, et erubuérunt omnes: * simul abiérunt in confusiónem fabricatóres errórum.
3  Israël salvátus est in Dómino salúte ætérna: * non confundémini, et non erubescétis usque in sæculum sæculi.
4  Quia hæc dicit Dóminus creans cælos, * ipse Deus formans terram, et fáciens eam, ipse plastes ejus.
5  Non in vanum creávit eam, ut habitarétur, formávit eam: * Ego Dóminus, et non est álius.
6  Non in abscóndito locútus sum: * in loco terræ tenebróso :
7  Non dixi sémini Jacob frustra : quærite me: * ego Dóminus loquens justítiam, annúntians recta.
8  Congregámini, et veníte, et accédite simul * qui salváti estis ex Géntibus :
9  Nesciérunt qui levant lignum sculptúræ suæ, * et rogant deum non salvántem.
10  Annuntiáte, et veníte, et consiliámini simul : * Quis audítum fecit hoc ab inítio, ex tunc prædíxit illud?
11  Numquid non ego Dóminus, et non est ultra Deus absque me? *  Deus justus, et salvans non est præter me.
12  Convertímini ad me, et salvi éritis, omnes fines terræ: * quia ego Deus, et non est álius.
13  In memetípso jurávi, egrediétur de ore meo justítiæ verbum, * et non revertétur.
14  Quia mihi curvábitur omne genu, * et jurábit omnis lingua.
15  Ergo in Dómino, dicet, meæ sunt justítiæ et impérium: * ad eum vénient, et confundéntur omnes qui repúgnant ei.
16  In Dómino justificábitur, et laudábitur * omne semen Israël.
