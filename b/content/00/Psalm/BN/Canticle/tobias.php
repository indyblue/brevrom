Canticum Tobiæ.      Tob. 13. 1.
Magnus es, Dómine, in ætérnum: * et in ómnia sæcula regnum tuum:
2  Quóniam tu flagéllas, et salvas: dedúcis ad ínferos, et redúcis: * et non est qui effúgiat manum tuam.
3  Confitémini Dómino, fílii Israël, * et in conspéctu géntium laudáte eum:
4  Quóniam ídeo dispérsit vos inter Gentes, quæ ignórant eum, * ut vos enarrétis mirabília ejus,
5  Et faciátis scire eos, * quia non est álius Deus omnípotens præter eum.
6  Ipse castigávit nos propter iniquitátes nostras: * et ipse salvábit nos propter misericórdiam suam.
7  Aspícite ergo quæ fecit nobíscum, et cum timóre et tremóre confitémini illi: * Regémque sæculórum exaltáte in opéribus vestris.
8  Ego autem in terra captivitátis meæ confitébor illi: * quóniam osténdit majestátem suam in gentem peccatrícem.
9  Convertímini ítaque, peccatóres, et fácite justítiam coram Deo, * credéntes quod fáciat vobíscum misericórdiam suam:
10  Ego autem, et ánima mea * in eo lætábimur.
11  Benedícite Dóminum, omnes elécti ejus: * ágite dies lætítiæ, et confitémini illi.
