   <p:Head2Ps>Psalmus 53. Deus, in nómine</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>DEUS, in nómine tuo salvum me fac: * et in virtúte tua júdica me.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>SAVE me, O God, by thy name, * and judge me in thy strength.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Deus, exáudi oratiónem meam: * áuribus pércipe verba oris mei.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>O God, hear my prayer: * give ear to the words of my mouth.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quóniam aliéni insurrexérunt advérsum me, et fortes quæsiérunt ánimam meam: * et non proposuérunt Deum ante conspéctum suum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For strangers have risen up against me; and the mighty have sought after my soul: * and they have not set God before their eyes.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ecce enim, Deus ádjuvat me: * et Dóminus suscéptor est ánimæ meæ.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For behold God is my helper: * and the Lord is the protector of my soul.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Avérte mala inimícis meis: * et in veritáte tua dispérde illos.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Turn back the evils upon my enemies; * and cut them off in thy truth.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Voluntárie sacrificábo tibi, * et confitébor nómini tuo, Dómine: quóniam bonum est:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I will freely sacrifice to thee, * and will give praise, O God, to thy name: because it is good:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quóniam ex omni tribulatióne eripuísti me: * et super inimícos meos despéxit óculus meus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For thou hast delivered me out of all trouble: * and my eye hath looked down upon my enemies.</p>
     </td>
    </tr>
   </table>
