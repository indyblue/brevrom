     </td>
     <td:B1>
      <p:BodyEIndent>Hearken to the voice of my prayer, * O my King and my God.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quóniam ad te orábo: * Dómine, mane exáudies vocem meam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For to thee will I pray: * O Lord, in the morning thou shalt hear my voice.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Mane astábo tibi et vidébo: * quóniam non Deus volens iniquitátem tu es.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>In the morning I will stand before thee, and will see: * because thou art not a God that willest iniquity.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Neque habitábit juxta te malígnus: * neque permanébunt injústi ante óculos tuos.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Neither shall the wicked dwell near thee: * nor shall the unjust abide before thy eyes.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Odísti omnes, qui operántur iniquitátem: * perdes omnes, qui loquúntur mendácium.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou hatest all the workers of iniquity: * thou wilt destroy all that speak a lie.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Virum sánguinum et dolósum abominábitur Dóminus: * ego autem in multitúdine misericórdiæ tuæ.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The bloody and the deceitful man the Lord will abhor. * But as for me in the multitude of thy mercy,</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Introíbo in domum tuam: * adorábo ad templum sanctum tuum in timóre tuo.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I will come into thy house; * I will worship towards thy holy temple, in thy fear.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Dómine, deduc me in justítia tua: * propter inimícos meos dírige in conspéctu tuo viam meam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Conduct me, O Lord, in thy justice: * because of my enemies, direct my way in thy sight.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quóniam non est in ore eórum véritas: * cor eórum vanum est.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For there is no truth in their mouth: * their heart is vain.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Sepúlcrum patens est guttur eórum, linguis suis dolóse agébant, * júdica illos, Deus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Their throat is an open sepulchre: they dealt deceitfully with their tongues: * judge them, O God.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Décidant a cogitatiónibus suis, secúndum multitúdinem impietátum eórum expélle eos, * quóniam irritavérunt te, Dómine.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Let them fall from their devices: according to the multitude of their wickednesses cast them out: * for they have provoked thee, O Lord.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et læténtur omnes, qui sperant in te, * in ætérnum exsultábunt: et habitábis in eis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>But let all them be glad that hope in thee: * they shall rejoice for ever, and thou shalt dwell in them.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et gloriabúntur in te omnes, qui díligunt nomen tuum, * quóniam tu benedíces justo.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And all they that love thy name shall glory in thee: * For thou wilt bless the just.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Dómine, ut scuto bonæ voluntátis tuæ * coronásti nos.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>O Lord, thou hast crowned us, * as with a shield of thy good will.</p>
     </td>
    </tr>
