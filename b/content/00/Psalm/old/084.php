   <p:Head2Ps>Psalmus 84. Benedixísti</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>BENEDIXÍSTI, Dómine, terram tuam: * avertísti captivitátem Jacob.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>LORD, thou hast blessed thy land: * thou hast turned away the captivity of Jacob.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Remisísti iniquitátem plebis tuæ: * operuísti ómnia peccáta eórum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou hast forgiven the iniquity of thy people: * thou hast covered all their sins.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Mitigásti omnem iram tuam: * avertísti ab ira indignatiónis tuæ.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou hast mitigated all thy anger: * thou hast turned away from the wrath of thy indignation.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Convérte nos, Deus, salutáris noster: * et avérte iram tuam a nobis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Convert us, O God our saviour: * and turn off thy anger from us.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Numquid in ætérnum irascéris nobis? * aut exténdes iram tuam a generatióne in generatiónem?</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Wilt thou be angry with us for ever: * or wilt thou extend thy wrath from generation to generation?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Deus, tu convérsus vivificábis nos: * et plebs tua lætábitur in te.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou wilt turn, O God, and bring us to life: * and thy people shall rejoice in thee.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Osténde nobis, Dómine, misericórdiam tuam: * et salutáre tuum da nobis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Shew us, O Lord, thy mercy; * and grant us thy salvation.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Audiam quid loquátur in me Dóminus Deus: * quóniam loquétur pacem in plebem suam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I will hear what the Lord God will speak in me: * for he will speak peace unto his people:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et super sanctos suos: * et in eos, qui convertúntur ad cor.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And unto his saints: * and unto them that are converted to the heart.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Verúmtamen prope timéntes eum salutáre ipsíus: * ut inhábitet glória in terra nostra.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Surely his salvation is near to them that fear him: * that glory may dwell in our land.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Misericórdia, et véritas obviavérunt sibi: * justítia, et pax osculátæ sunt.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Mercy and truth have met each other: * justice and peace have kissed.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Véritas de terra orta est: * et justítia de cælo prospéxit.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Truth is sprung out of the earth: * and justice hath looked down from heaven.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Étenim Dóminus dabit benignitátem: * et terra nostra dabit fructum suum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For the Lord will give goodness: * and our earth shall yield her fruit.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Justítia ante eum ambulábit: * et ponet in via gressus suos.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Justice shall walk before him: * and shall set his steps in the way.</p>
     </td>
    </tr>
   </table>
