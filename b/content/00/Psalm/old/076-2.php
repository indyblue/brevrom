      <p:BodyLIndent>In mari via tua, et sémitæ tuæ in aquis multis: * et vestígia tua non cognoscéntur.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thy way is in the sea, and thy paths in many waters: * and thy footsteps shall not be known.</p>
     </td>
    </tr>
