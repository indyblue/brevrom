      <p:BodyLIndent>Multitúdo sónitus aquárum: * vocem dedérunt nubes.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Great was the noise of the waters: * the clouds sent out a sound.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Étenim sagíttæ tuæ tránseunt: * vox tonítrui tui in rota.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For thy arrows pass: * the voice of thy thunder in a wheel.</p>
     </td>
    </tr>
    <tr>
