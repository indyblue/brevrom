   <p:Head2Ps>Psalmus 118 vi. Defécit ánima mea</p>
   <p:Head4>Caph</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>DEFÉCIT in salutáre tuum ánima mea: * et in verbum tuum supersperávi.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>MY SOUL hath fainted after thy salvation: * and in thy word I have very much hoped.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Defecérunt óculi mei in elóquium tuum, * dicéntes: Quando consoláberis me?</p>
     </td>
     <td:B1>
      <p:BodyEIndent>My eyes have failed for thy word, * saying: When wilt thou comfort me?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quia factus sum sicut uter in pruína: * justificatiónes tuas non sum oblítus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For I am become like a bottle in the frost: * I have not forgotten thy justifications.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quot sunt dies servi tui? * quando fácies de persequéntibus me judícium?</p>
     </td>
     <td:B1>
      <p:BodyEIndent>How many are the days of thy servant: * when wilt thou execute judgment on them that persecute me?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Narravérunt mihi iníqui fabulatiónes: * sed non ut lex tua.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The wicked have told me fables: * but not as thy law.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Omnia mandáta tua véritas: * iníque persecúti sunt me, ádjuva me.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>All thy statutes are truth: * they have persecuted me unjustly, do thou help me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Paulo minus consummavérunt me in terra: * ego autem non derelíqui mandáta tua.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>They had almost made an end of me upon earth: * but I have not forsaken thy commandments.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Secúndum misericórdiam tuam vivífica me: * et custódiam testimónia oris tui.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Quicken thou me according to thy mercy: * and I shall keep the testimonies of thy mouth.</p>
     </td>
    </tr>
   </table>
