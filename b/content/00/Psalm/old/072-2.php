   <p:Head2Ps>Psalmus 72 ii. Ídeo convertétur</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>IDEO convertétur pópulus meus hic: * et dies pleni inveniéntur in eis.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>THEREFORE will my people return here * and full days shall be found in them.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et dixérunt: Quómodo scit Deus, * et si est sciéntia in excélso?</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And they said: How doth God know? * and is there knowledge in the most High?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ecce, ipsi peccatóres, et abundántes in sæculo, * obtinuérunt divítias.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Behold these are sinners; and yet abounding in the world * they have obtained riches.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et dixi: Ergo sine causa justificávi cor meum, * et lavi inter innocéntes manus meas:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And I said: Then have I in vain justified my heart, * and washed my hands among the innocent.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et fui flagellátus tota die, * et castigátio mea in matutínis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And I have been scourged all the day; * and my chastisement hath been in the mornings.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Si dicébam: Narrábo sic: * ecce, natiónem filiórum tuórum reprobávi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>If I said: I will speak thus; * behold I should condemn the generation of thy children.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Existimábam ut cognóscerem hoc, * labor est ante me:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I studied that I might know this thing, * it is a labour in my sight:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Donec intrem in Sanctuárium Dei: * et intélligam in novíssimis eórum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Until I go into the sanctuary of God, * and understand concerning their last ends.</p>
     </td>
    </tr>
   </table>
