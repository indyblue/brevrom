   <p:Head2Ps>Psalmus 82. Deus, quis símilis</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>DEUS, quis símilis erit tibi? * Ne táceas, neque compescáris, Deus.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>O GOD, who shall be like to thee? * Hold not thy peace, neither be thou still, O God.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quóniam ecce inimíci tui sonuérunt: * et qui odérunt te extulérunt caput.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For lo, thy enemies have made a noise: * and they that hate thee have lifted up the head.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Super pópulum tuum malignavérunt consílium: * et cogitavérunt advérsus sanctos tuos.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>They have taken a malicious counsel against thy people, * and have consulted against thy saints.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Dixérunt: Veníte, et disperdámus eos de gente: * et non memorétur nomen Israël ultra.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>They have said: Come and let us destroy them, * so that they be not a nation: and let the name of Israel be remembered no more.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quóniam cogitavérunt unanímiter: * simul advérsum te testaméntum disposuérunt, tabernácula Idumæórum et Ismahelítæ:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For they have contrived with one consent: * they have made a covenant together against thee; the tabernacles of the Edomites, and the Ismahelites:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Moab, et Agaréni, Gebal, et Ammon, et Ámalec: * alienígenæ cum habitántibus Tyrum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Moab, and the Agarens, Gebal, and Ammon and Amalec: * the Philistines, with the inhabitants of Tyre.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Étenim Assur venit cum illis: * facti sunt in adjutórium fíliis Lot.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Yea, and the Assyrian also is joined with them: * they are come to the aid of the sons of Lot.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Fac illis sicut Mádian, et Sísaræ: * sicut Jabin in torrénte Cisson.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Do to them as thou didst to Madian and to Sisara: * as to Jabin at the brook of Cisson.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Disperiérunt in Endor: * facti sunt ut stercus terræ.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Who perished at Endor: * and became as dung for the earth.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Pone príncipes eórum sicut Oreb, et Zeb, * et Zébee, et Sálmana:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Make their princes like Oreb, and Zeb, * and Zebee, and Salmana.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Omnes príncipes eórum: * qui dixérunt: Hereditáte possideámus Sanctuárium Dei.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>All their princes, * who have said: Let us possess the sanctuary of God for an inheritance.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Deus meus, pone illos ut rotam: * et sicut stípulam ante fáciem venti.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>O my God, make them like a wheel; * and as stubble before the wind.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Sicut ignis, qui combúrit silvam: * et sicut flamma combúrens montes:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>As fire which burneth the wood: * and as a flame burning mountains:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ita persequéris illos in tempestáte tua: * et in ira tua turbábis eos.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>So shalt thou pursue them with thy tempest: * and shalt trouble them in thy wrath.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Imple fácies eórum ignomínia: * et quærent nomen tuum, Dómine.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Fill their faces with shame; * and they shall seek thy name, O Lord.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Erubéscant, et conturbéntur in sæculum sæculi: * et confundántur, et péreant.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Let them be ashamed and troubled for ever and ever: * and let them be confounded and perish.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et cognóscant quia nomen tibi Dóminus: * tu solus Altíssimus in omni terra.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And let them know that the Lord is thy name: * thou alone art the most High over all the earth.</p>
     </td>
    </tr>
   </table>
