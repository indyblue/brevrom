   <p:Head2Ps>Psalmus 42. Júdica me, Deus</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>JÚDICA me, Deus, et discérne causam meam de gente non sancta, * ab hómine iníquo, et dolóso érue me.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>JUDGE me, O God, and distinguish my cause from the nation that is not holy: * deliver me from the unjust and deceitful man.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quia tu es, Deus, fortitúdo mea: * quare me repulísti? et quare tristis incédo, dum afflígit me inimícus?</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For thou art God my strength: * why hast thou cast me off? and why do I go sorrowful whilst the enemy afflicteth me?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Emítte lucem tuam et veritátem tuam: * ipsa me deduxérunt, et adduxérunt in montem sanctum tuum, et in tabernácula tua.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Send forth thy light and thy truth: * they have conducted me, and brought me unto thy holy hill, and into thy tabernacles.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et introíbo ad altáre Dei: * ad Deum, qui lætíficat juventútem meam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And I will go in to the altar of God: * to God who giveth joy to my youth.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Confitébor tibi in cíthara, Deus, Deus meus: * quare tristis es, ánima mea? et quare contúrbas me?</p>
     </td>
     <td:B1>
      <p:BodyEIndent>To thee, O God my God, I will give praise upon the harp: * why art thou sad, O my soul? and why dost thou disquiet me?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Spera in Deo, quóniam adhuc confitébor illi: * salutáre vultus mei, et Deus meus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Hope in God, for I will still give praise to him: * the salvation of my countenance, and my God.</p>
     </td>
    </tr>
   </table>
