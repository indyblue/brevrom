   <p:Head2Ps>Psalmus 73 iii. Memor esto hujus</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>MEMOR esto hujus, inimícus improperávit Dómino: * et pópulus insípiens incitávit nomen tuum.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>REMEMBER this, the enemy hath reproached the Lord: * and a foolish people hath provoked thy name.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ne tradas béstiis ánimas confiténtes tibi, * et ánimas páuperum tuórum ne obliviscáris in finem.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Deliver not up to beasts the souls that confess to thee: * and forget not to the end the souls of thy poor.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Réspice in testaméntum tuum: * quia repléti sunt, qui obscuráti sunt terræ dómibus iniquitátum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Have regard to thy covenant: * for they that are the obscure of the earth have been filled with dwellings of iniquity.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ne avertátur húmilis factus confúsus: * pauper et inops laudábunt nomen tuum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Let not the humble be turned away with confusion: * the poor and needy shall praise thy name.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Exsúrge, Deus, júdica causam tuam: * memor esto improperiórum tuórum, eórum quæ ab insipiénte sunt tota die.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Arise, O God, judge thy own cause: * remember thy reproaches with which the foolish man hath reproached thee all the day.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ne obliviscáris voces inimicórum tuórum: * supérbia eórum, qui te odérunt, ascéndit semper.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Forget not the voices of thy enemies: * the pride of them that hate thee ascendeth continually.</p>
     </td>
    </tr>
   </table>
