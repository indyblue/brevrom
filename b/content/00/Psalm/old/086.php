     </td>
     <td:B1>
      <p:BodyEDrop>THE foundations thereof are in the holy mountains: * The Lord loveth the gates of Sion above all the tabernacles of Jacob.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Gloriósa dicta sunt de te, * cívitas Dei.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Glorious things are said of thee, * O city of God.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Memor ero Rahab, et Babylónis * sciéntium me.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I will be mindful of Rahab and of Babylon * knowing me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ecce, alienígenæ, et Tyrus, et pópulus Æthíopum, * hi fuérunt illic.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Behold the foreigners, and Tyre, and the people of the Ethiopians, * these were there.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Numquid Sion dicet: Homo, et homo natus est in ea: * et ipse fundávit eam Altíssimus?</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Shall not Sion say: This man and that man is born in her? * and the Highest himself hath founded her.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Dóminus narrábit in scriptúris populórum, et príncipum: * horum, qui fuérunt in ea.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The Lord shall tell in his writings of peoples and of princes, * of them that have been in her.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Sicut lætántium ómnium * habitátio est in te.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The dwelling in thee * is as it were of all rejoicing.</p>
     </td>
    </tr>
