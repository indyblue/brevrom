   <p:Head2Ps>Psalmus 61. Nonne Deo</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>NONNE Deo subjécta erit ánima mea? * ab ipso enim salutáre meum.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>SHALL not my soul be subject to God? * for from him is my salvation.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Nam et ipse Deus meus, et salutáris meus: * suscéptor meus, non movébor ámplius.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For he is my God and my saviour: * he is my protector, I shall be moved no more.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quoúsque irrúitis in hóminem? * interfícitis univérsi vos: tamquam paríeti inclináto et macériæ depúlsæ?</p>
     </td>
     <td:B1>
      <p:BodyEIndent>How long do you rush in upon a man? * you all kill, as if you were thrusting down a leaning wall, and a tottering fence.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Verúmtamen prétium meum cogitavérunt repéllere, cucúrri in siti: * ore suo benedicébant, et corde suo maledicébant.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>But they have thought to cast away my price; I ran in thirst: * they blessed with their mouth, but cursed with their heart.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Verúmtamen Deo subjécta esto, ánima mea: * quóniam ab ipso patiéntia mea.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>But be thou, O my soul, * subject to God: for from him is my patience.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quia ipse Deus meus, et salvátor meus: * adjútor meus, non emigrábo.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For he is my God and my saviour: * he is my helper, I shall not be moved.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>In Deo salutáre meum, et glória mea: * Deus auxílii mei, et spes mea in Deo est.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>In God is my salvation and my glory: * he is the God of my help, and my hope is in God.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Speráte in eo omnis congregátio pópuli, effúndite coram illo corda vestra: * Deus adjútor noster in ætérnum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Trust in him, all ye congregation of people: pour out your hearts before him. * God is our helper for ever.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Verúmtamen vani fílii hóminum, mendáces fílii hóminum in statéris: * ut decípiant ipsi de vanitáte in idípsum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>But vain are the sons of men, the sons of men are liars in the balances: * that by vanity they may together deceive.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Nolíte speráre in iniquitáte, et rapínas nolíte concupíscere: * divítiæ si áffluant, nolíte cor appónere.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Trust not in iniquity, and cover not robberies: * if riches abound, set not your heart upon them.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Semel locútus est Deus, duo hæc audívi, quia potéstas Dei est, et tibi, Dómine, misericórdia: * quia tu reddes unicuíque juxta ópera sua.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>God hath spoken once, these two things have I heard, that power belongeth to God, and mercy to thee, O Lord; * for thou wilt render to every man according to his works.</p>
     </td>
    </tr>
