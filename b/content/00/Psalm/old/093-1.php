   <p:Head2Ps>Psalmus 93 i. Deus ultiónum Dóminus</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>DEUS ultiónum Dóminus: * Deus ultiónum líbere egit.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>THE Lord is the God to whom revenge belongeth: * the God of revenge hath acted freely.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Exaltáre, qui júdicas terram: * redde retributiónem supérbis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Lift up thyself, thou that judgest the earth: * render a reward to the proud.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Úsquequo peccatóres, Dómine, * úsquequo peccatóres gloriabúntur:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>How long shall sinners, O Lord: * how long shall sinners glory?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Effabúntur, et loquéntur iniquitátem: * loquéntur omnes, qui operántur injustítiam?</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Shall they utter, and speak iniquity: * shall all speak who work injustice?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Pópulum tuum, Dómine, humiliavérunt: * et hereditátem tuam vexavérunt.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thy people, O Lord, they have brought low: * and they have afflicted thy inheritance.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Víduam, et ádvenam interfecérunt: * et pupíllos occidérunt.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>They have slain the widow and the stranger: * and they have murdered the fatherless.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et dixérunt: Non vidébit Dóminus, * nec intélliget Deus Jacob.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And they have said: The Lord shall not see: * neither shall the God of Jacob understand.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Intellígite, insipiéntes in pópulo: * et stulti, aliquándo sápite.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Understand, ye senseless among the people: * and, you fools, be wise at last.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Qui plantávit aurem, non áudiet? * aut qui finxit óculum, non consíderat?</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He that planted the ear, shall he not hear? * or he that formed the eye, doth he not consider?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Qui córripit gentes, non árguet: * qui docet hóminem sciéntiam?</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He that chastiseth nations, shall he not rebuke: * he that teacheth man knowledge?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Dóminus scit cogitatiónes hóminum, * quóniam vanæ sunt.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The Lord knoweth the thoughts of men, * that they are vain.</p>
     </td>
    </tr>
   </table>
