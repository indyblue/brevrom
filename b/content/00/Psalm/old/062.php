   <p:Head2Ps>Psalmus 62. Deus, Deus meus</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>DEUS, Deus meus, * ad te de luce vígilo.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>O GOD, my God, * to thee do I watch at break of day.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Sitívit in te ánima mea, * quam multiplíciter tibi caro mea.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For thee my soul hath thirsted; * for thee my flesh, O how many ways!</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>In terra desérta, et ínvia, et inaquósa: * sic in sancto appárui tibi, ut vidérem virtútem tuam, et glóriam tuam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>In a desert land, and where there is no way, and no water: * so in the sanctuary have I come before thee, to see thy power and thy glory.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quóniam mélior est misericórdia tua super vitas: * lábia mea laudábunt te.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For thy mercy is better than lives: * thee my lips shall praise.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Sic benedícam te in vita mea: * et in nómine tuo levábo manus meas.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thus will I bless thee all my life long: * and in thy name I will lift up my hands.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Sicut ádipe et pinguédine repleátur ánima mea: * et lábiis exsultatiónis laudábit os meum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Let my soul be filled as with marrow and fatness: * and my mouth shall praise thee with joyful lips.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Si memor fui tui super stratum meum, in matutínis meditábor in te: * quia fuísti adjútor meus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>If I have remembered thee upon my bed, I will meditate on thee in the morning: * because thou hast been my helper.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et in velaménto alárum tuárum exsultábo, adhæsit ánima mea post te: * me suscépit déxtera tua.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And I will rejoice under the covert of thy wings: my soul hath stuck close to thee: * thy right hand hath received me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ipsi vero in vanum quæsiérunt ánimam meam, introíbunt in inferióra terræ: * tradéntur in manus gládii, partes vúlpium erunt.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>But they have sought my soul in vain, they shall go into the lower parts of the earth: * they shall be delivered into the hands of the sword, they shall be the portions of foxes.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Rex vero lætábitur in Deo, laudabúntur omnes qui jurant in eo: * quia obstrúctum est os loquéntium iníqua.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>But the king shall rejoice in God, all they shall be praised that swear by him: * because the mouth is stopped of them that speak wicked things.</p>
     </td>
    </tr>
   </table>
