   <p:Head2Ps>Psalmus 43 iii. Si oblíti sumus</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>SI OBLÍTI sumus nomen Dei nostri, * et si expándimus manus nostras ad deum aliénum:</p>
     </td>
     <td:B1>
      <p:BodyEDrop>IF WE have forgotten the name of our God, * and if we have spread forth our hands to a strange god:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Nonne Deus requíret ista? * ipse enim novit abscóndita cordis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Shall not God search out these things: * for he knoweth the secrets of the heart.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quóniam propter te mortificámur tota die: * æstimáti sumus sicut oves occisiónis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Because for thy sake we are killed all the day long: * we are counted as sheep for the slaughter.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Exsúrge, quare obdórmis, Dómine? * exsúrge, et ne repéllas in finem.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Arise, why sleepest thou, O Lord? * arise, and cast us not off to the end.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quare fáciem tuam avértis, * oblivísceris inópiæ nostræ, et tribulatiónis nostræ?</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Why turnest thou thy face away? * and forgettest our want and our trouble?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quóniam humiliáta est in púlvere ánima nostra: * conglutinátus est in terra venter noster.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For our soul is humbled down to the dust: * our belly cleaveth to the earth.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Exsúrge, Dómine, ádjuva nos: * et rédime nos propter nomen tuum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Arise, O Lord, help us * and redeem us for thy name’s sake.</p>
     </td>
    </tr>
   </table>
