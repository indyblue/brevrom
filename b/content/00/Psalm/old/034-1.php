     </td>
     <td:B1>
      <p:BodyEDrop>JUDGE thou, O Lord, them that wrong me: * overthrow them that fight against me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Apprehénde arma et scutum: * et exsúrge in adjutórium mihi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Take hold of arms and shield: * and rise up to help me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Effúnde frámeam, et conclúde advérsus eos, qui persequúntur me: * dic ánimæ meæ: Salus tua ego sum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Bring out the sword, and shut up the way against them that persecute me: * say to my soul: I am thy salvation.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Confundántur et revereántur, * quæréntes ánimam meam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Let them be confounded and ashamed * that seek after my soul.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Avertántur retrórsum, et confundántur * cogitántes mihi mala.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Let them be turned back and be confounded * that devise against me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Fiant tamquam pulvis ante fáciem venti: * et Ángelus Dómini coárctans eos.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Let them become as dust before the wind: * and let the Angel of the Lord straiten them.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Fiat via illórum ténebræ et lúbricum: * et Ángelus Dómini pérsequens eos.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Let their way become dark and slippery; * and let the Angel of the Lord pursue them.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quóniam gratis abscondérunt mihi intéritum láquei sui: * supervácue exprobravérunt ánimam meam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For without cause they have hidden their net for me unto destruction: * without cause they have upbraided my soul.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Véniat illi láqueus, quem ignórat: et cáptio, quam abscóndit, apprehéndat eum: * et in láqueum cadat in ipsum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Let the snare which he knoweth not come upon him: and let the net which he hath hidden catch him: * and into that very snare let them fall.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ánima autem mea exsultábit in Dómino: * et delectábitur super salutári suo.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>But my soul shall rejoice in the Lord; * and shall be delighted in his salvation.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Omnia ossa mea dicent: * Dómine, quis símilis tibi?</p>
     </td>
     <td:B1>
      <p:BodyEIndent>All my bones shall say: * Lord, who is like to thee?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Erípiens ínopem de manu fortiórum ejus: * egénum et páuperem a diripiéntibus eum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Who deliverest the poor from the hand of them that are stronger than he; * the needy and the poor from them that strip him.</p>
     </td>
    </tr>
