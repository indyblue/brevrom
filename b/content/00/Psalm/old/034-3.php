   <p:Head2Ps>Psalmus 34 iii. Confitébor tibi</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>CONFITÉBOR tibi in ecclésia magna, * in pópulo gravi laudábo te.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>I WILL give thanks to thee in a great church; * I will praise thee in a strong people.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Non supergáudeant mihi qui adversántur mihi iníque: * qui odérunt me gratis et ánnuunt óculis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Let not them that are my enemies wrongfully rejoice over me: * who have hated me without cause, and wink with the eyes.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quóniam mihi quidem pacífice loquebántur: * et in iracúndia terræ loquéntes, dolos cogitábant.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For they spoke indeed peaceably to me; * and speaking in the anger of the earth they devised guile.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et dilatavérunt super me os suum: * dixérunt: Euge, euge, vidérunt óculi nostri.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And they opened their mouth wide against me; * they said: Well done, well done, our eyes have seen it.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Vidísti, Dómine, ne síleas: * Dómine, ne discédas a me.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou hast seen, O Lord, be not thou silent: * O Lord, depart not from me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Exsúrge et inténde judício meo: * Deus meus, et Dóminus meus in causam meam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Arise, and be attentive to my judgment: * to my cause, my God, and my Lord.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Júdica me secúndum justítiam tuam, Dómine, Deus meus, * et non supergáudeant mihi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Judge me, O Lord my God according to thy justice, * and let them not rejoice over me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Non dicant in córdibus suis: Euge, euge, ánimæ nostræ: * nec dicant: Devorávimus eum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Let them not say in their hearts: It is well, it is well, to our mind: * neither let them say: We have swallowed him up.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Erubéscant et revereántur simul, * qui gratulántur malis meis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Let them blush: and be ashamed together, * who rejoice at my evils.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Induántur confusióne et reveréntia * qui magna loquúntur super me.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Let them be clothed with confusion and shame, * who speak great things against me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Exsúltent et læténtur qui volunt justítiam meam: * et dicant semper: Magnificétur Dóminus qui volunt pacem servi ejus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Let them rejoice and be glad, who are well pleased with my justice, * and let them say always: The Lord be magnified, who delights in the peace of his servant.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et lingua mea meditábitur justítiam tuam, * tota die laudem tuam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And my tongue shall meditate thy justice, * thy praise all the day long.</p>
     </td>
    </tr>
   </table>
