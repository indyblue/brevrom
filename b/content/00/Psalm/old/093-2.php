      <p:BodyLDrop>BEÁTUS homo, quem tu erudíeris, Dómine: * et de lege tua docúeris eum,</p>
     </td>
     <td:B1>
      <p:BodyEDrop>BLESSED is the man whom thou shalt instruct, O Lord: * and shalt teach him out of thy law.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ut mítiges ei a diébus malis: * donec fodiátur peccatóri fóvea.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>That thou mayst give him rest from the evil days: * till a pit be dug for the wicked.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quia non repéllet Dóminus plebem suam: * et hereditátem suam non derelínquet.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For the Lord will not cast off his people: * neither will he forsake his own inheritance.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quoadúsque justítia convertátur in judícium: * et qui juxta illam omnes qui recto sunt corde.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Until justice be turned into judgment: * and they that are near it are all the upright in heart.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quis consúrget mihi advérsus malignántes? * aut quis stabit mecum advérsus operántes iniquitátem?</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Who shall rise up for me against the evildoers? * or who shall stand with me against the workers of iniquity?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Nisi quia Dóminus adjúvit me: * paulo minus habitásset in inférno ánima mea.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Unless the Lord had been my helper, * my soul had almost dwelt in hell.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Si dicébam: Motus est pes meus: * misericórdia tua, Dómine, adjuvábat me.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>If I said: My foot is moved: * thy mercy, O Lord, assisted me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Secúndum multitúdinem dolórum meórum in corde meo: * consolatiónes tuæ lætificavérunt ánimam meam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>According to the multitude of my sorrows in my heart, * thy comforts have given joy to my soul.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Numquid adhæret tibi sedes iniquitátis: * qui fingis labórem in præcépto?</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Doth the seat of iniquity stick to thee, * who framest labour in commandment?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Captábunt in ánimam justi: * et sánguinem innocéntem condemnábunt.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>They will hunt after the soul of the just, * and will condemn innocent blood.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et factus est mihi Dóminus in refúgium: * et Deus meus in adjutórium spei meæ.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>But the Lord is my refuge: * and my God the help of my hope.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et reddet illis iniquitátem ipsórum: et in malítia eórum dispérdet eos: * dispérdet illos Dóminus, Deus noster.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And he will render them their iniquity: and in their malice he will destroy them: * the Lord our God will destroy them.</p>
     </td>
    </tr>
