     </td>
     <td:B1>
      <p:BodyEDrop>O LORD, rebuke me not in thy indignation, * nor chastise me in thy wrath.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Miserére mei, Dómine, quóniam infírmus sum: * sana me, Dómine, quóniam conturbáta sunt ossa mea.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Have mercy on me, O Lord, for I am weak: * heal me, O Lord, for my bones are troubled.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et ánima mea turbáta est valde: * sed tu, Dómine, úsquequo?</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And my soul is troubled exceedingly: * but thou, O Lord, how long?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Convértere, Dómine, et éripe ánimam meam: * salvum me fac propter misericórdiam tuam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Turn to me, O Lord, and deliver my soul: * O save me for thy mercy’s sake.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quóniam non est in morte qui memor sit tui: * in inférno autem quis confitébitur tibi?</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For there is no one in death, that is mindful of thee: * and who shall confess to thee in hell?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Laborávi in gémitu meo, lavábo per síngulas noctes lectum meum: * lácrimis meis stratum meum rigábo.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I have laboured in my groanings, every night I will wash my bed: * I will water my couch with my tears.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Turbátus est a furóre óculus meus: * inveterávi inter omnes inimícos meos.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>My eye is troubled through indignation: * I have grown old amongst all my enemies.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Discédite a me, omnes, qui operámini iniquitátem: * quóniam exaudívit Dóminus vocem fletus mei.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Depart from me, all ye workers of iniquity: * for the Lord hath heard the voice of my weeping.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Exaudívit Dóminus deprecatiónem meam, * Dóminus oratiónem meam suscépit.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The Lord hath heard my supplication: * the Lord hath received my prayer.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Erubéscant, et conturbéntur veheménter omnes inimíci mei: * convertántur et erubéscant valde velóciter.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Let all my enemies be ashamed, and be very much troubled: * let them be turned back, and be ashamed very speedily.</p>
     </td>
    </tr>
