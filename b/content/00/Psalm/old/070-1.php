     </td>
     <td:B1>
      <p:BodyEDrop>IN THEE, O Lord, I have hoped, let me never be put to confusion: * deliver me in thy justice, and rescue me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Inclína ad me aurem tuam, * et salva me.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Incline thy ear unto me, * and save me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Esto mihi in Deum protectórem, et in locum munítum: * ut salvum me fácias,</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Be thou unto me a God, a protector, and a place of strength: * that thou mayst make me safe.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quóniam firmaméntum meum, * et refúgium meum es tu.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For thou art my firmament * and my refuge.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Deus meus, éripe me de manu peccatóris, * et de manu contra legem agéntis et iníqui:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Deliver me, O my God, out of the hand of the sinner, * and out of the hand of the transgressor of the law and of the unjust.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quóniam tu es patiéntia mea, Dómine: * Dómine, spes mea a juventúte mea.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For thou art my patience, O Lord: * my hope, O Lord, from my youth.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>In te confirmátus sum ex útero: * de ventre matris meæ tu es protéctor meus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>By thee have I been confirmed from the womb: * from my mother’s womb thou art my protector.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>In te cantátio mea semper: * tamquam prodígium factus sum multis: et tu adjútor fortis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Of thee shall I continually sing: * I am become unto many as a wonder, but thou art a strong helper.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Repleátur os meum laude, ut cantem glóriam tuam: * tota die magnitúdinem tuam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Let my mouth be filled with praise, that I may sing thy glory; * thy greatness all the day long.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ne projícias me in témpore senectútis: * cum defécerit virtus mea, ne derelínquas me.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Cast me not off in the time of old age: * when my strength shall fail, do not thou forsake me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quia dixérunt inimíci mei mihi: * et qui custodiébant ánimam meam, consílium fecérunt in unum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For my enemies have spoken against me; * and they that watched my soul have consulted together,</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Dicéntes: Deus derelíquit eum, persequímini, et comprehéndite eum: * quia non est qui erípiat.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Saying: God hath forsaken him: pursue and take him, * for there is none to deliver him.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Deus, ne elongéris a me: * Deus meus, in auxílium meum réspice.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>O God, be not thou far from me: * O my God, make haste to my help.</p>
     </td>
    </tr>
