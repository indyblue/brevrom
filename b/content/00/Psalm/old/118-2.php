   <p:Head2Ps>Psalmus 118 ii. Retríbue servo tuo</p>
   <p:Head4>Ghimel</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>RETRÍBUE servo tuo, vivífica me: * et custódiam sermónes tuos.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>GIVE bountifully to thy servant, enliven me: * and I shall keep thy words.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Revéla óculos meos: * et considerábo mirabília de lege tua.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Open thou my eyes: * and I will consider the wondrous things of thy law.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Íncola ego sum in terra: * non abscóndas a me mandáta tua.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I am a sojourner on the earth: * hide not thy commandments from me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Concupívit ánima mea desideráre justificatiónes tuas, * in omni témpore.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>My soul hath coveted to long for thy justifications, * at all times.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Increpásti supérbos: * maledícti qui declínant a mandátis tuis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou hast rebuked the proud: * they are cursed who decline from thy commandments.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Aufer a me oppróbrium, et contémptum: * quia testimónia tua exquisívi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Remove from me reproach and contempt: * because I have sought after thy testimonies.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Étenim sedérunt príncipes, et advérsum me loquebántur: * servus autem tuus exercebátur in justificatiónibus tuis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For princes sat, and spoke against me: * but thy servant was employed in thy justifications.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Nam et testimónia tua meditátio mea est: * et consílium meum justificatiónes tuæ.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For thy testimonies are my meditation: * and thy justifications my counsel.</p>
     </td>
    </tr>
   </table>
   <p:Head4>Daleth</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLIndent>Adhæsit paviménto ánima mea: * vivífica me secúndum verbum tuum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>My soul hath cleaved to the pavement: * quicken thou me according to thy word.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Vias meas enuntiávi, et exaudísti me: * doce me justificatiónes tuas.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I have declared my ways, and thou hast heard me: * teach me thy justifications.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Viam justificatiónum tuárum ínstrue me: * et exercébor in mirabílibus tuis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Make me to understand the way of thy justifications: * and I shall be exercised in thy wondrous works.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Dormitávit ánima mea præ tædio: * confírma me in verbis tuis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>My soul hath slumbered through heaviness: * strengthen thou me in thy words.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Viam iniquitátis ámove a me: * et de lege tua miserére mei.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Remove from me the way of iniquity: * and out of thy law have mercy on me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Viam veritátis elégi: * judícia tua non sum oblítus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I have chosen the way of truth: * thy judgments I have not forgotten.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Adhæsi testimóniis tuis, Dómine: * noli me confúndere.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I have stuck to thy testimonies, O Lord: * put me not to shame.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Viam mandatórum tuórum cucúrri, * cum dilatásti cor meum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I have run the way of thy commandments, * when thou didst enlarge my heart.</p>
     </td>
    </tr>
   </table>
