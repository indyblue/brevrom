   <p:Head2Ps>Psalmus 103 iii. Quam magnificáta</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>QUAM magnificáta sunt ópera tua, Dómine! * ómnia in sapiéntia fecísti: impléta est terra possessióne tua.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>HOW great are thy works, O Lord? * thou hast made all things in wisdom: the earth is filled with thy riches.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Hoc mare magnum, et spatiósum mánibus: * illic reptília, quorum non est númerus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>So is this great sea, which stretcheth wide its arms: * there are creeping things without number:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Animália pusílla cum magnis: * illic naves pertransíbunt.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Creatures little and great. * There the ships shall go.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Draco iste, quem formásti ad illudéndum ei: * ómnia a te exspéctant ut des illis escam in témpore.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>This sea dragon which thou hast formed to play therein. * All expect of thee that thou give them food in season.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Dante te illis, cólligent: * aperiénte te manum tuam, ómnia implebúntur bonitáte.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>What thou givest to them they shall gather up: * when thou openest thy hand, they shall all be filled with good.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Averténte autem te fáciem, turbabúntur: * áuferes spíritum eórum, et defícient, et in púlverem suum reverténtur.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>But if thou turnest away thy face, they shall be troubled: * thou shalt take away their breath, and they shall fail, and shall return to their dust.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Emíttes spíritum tuum, et creabúntur: * et renovábis fáciem terræ.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou shalt send forth thy spirit, and they shall be created: * and thou shalt renew the face of the earth.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Sit glória Dómini in sæculum: * lætábitur Dóminus in opéribus suis:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>May the glory of the Lord endure for ever: * the Lord shall rejoice in his works.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Qui réspicit terram, et facit eam trémere: * qui tangit montes, et fúmigant.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He looketh upon the earth, and maketh it tremble: * he toucheth the mountains, and they smoke.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Cantábo Dómino in vita mea: * psallam Deo meo, quámdiu sum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I will sing to the Lord as long as I live: * I will sing praise to my God while I have my being.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Jucúndum sit ei elóquium meum: * ego vero delectábor in Dómino.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Let my speech be acceptable to him: * but I will take delight in the Lord.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Defíciant peccatóres a terra, et iníqui ita ut non sint: * bénedic, ánima mea, Dómino.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Let sinners be consumed out of the earth, and the unjust, so that they be no more: * O my soul, bless thou the Lord.</p>
     </td>
    </tr>
   </table>
