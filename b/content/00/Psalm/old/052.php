   <p:Head2Ps>Psalmus 52. Dixit insípiens</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>DIXIT insípiens in corde suo: * Non est Deus.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>THE fool said in his heart: * There is no God.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Corrúpti sunt, et abominábiles facti sunt in iniquitátibus: * non est qui fáciat bonum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>They are corrupted, and become abominable in iniquities: * there is none that doth good.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Deus de cælo prospéxit super fílios hóminum: * ut vídeat si est intélligens, aut requírens Deum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>God looked down from heaven on the children of men: * to see if there were any that did understand, or did seek God.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Omnes declinavérunt, simul inútiles facti sunt: * non est qui fáciat bonum, non est usque ad unum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>All have gone aside, they are become unprofitable together, * there is none that doth good, no not one.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Nonne scient omnes qui operántur iniquitátem, * qui dévorant plebem meam ut cibum panis?</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Shall not all the workers of iniquity know, * who eat up my people as they eat bread?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Deum non invocavérunt: * illic trepidavérunt timóre, ubi non erat timor.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>They have not called upon God: * there have they trembled for fear, where there was no fear.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quóniam Deus dissipávit ossa eórum qui homínibus placent: * confúsi sunt, quóniam Deus sprevit eos.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For God hath scattered the bones of them that please men: * they have been confounded, because God hath despised them.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quis dabit ex Sion salutáre Israël? * cum convérterit Deus captivitátem plebis suæ, exsultábit Jacob, et lætábitur Israël.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Who will give out of Sion the salvation of Israel? * when God shall bring back the captivity of his people, Jacob shall rejoice, and Israel shall be glad.</p>
     </td>
    </tr>
   </table>
