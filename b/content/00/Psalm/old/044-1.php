   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>ERUCTÁVIT cor meum verbum bonum: * dico ego ópera mea Regi.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>MY HEART hath uttered a good word; * I speak my works to the king;</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Lingua mea cálamus scribæ: * velóciter scribéntis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>My tongue is the pen of a scrivener * that writeth swiftly.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Speciósus forma præ fíliis hóminum, diffúsa est grátia in lábiis tuis: * proptérea benedíxit te Deus in ætérnum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou art beautiful above the sons of men: grace is poured abroad in thy lips; * therefore hath God blessed thee for ever.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Accíngere gládio tuo super femur tuum, * potentíssime.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Gird thy sword upon thy thigh, * O thou most mighty.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Spécie tua et pulchritúdine tua: * inténde, próspere procéde, et regna.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>With thy comeliness and thy beauty * set out, proceed prosperously, and reign.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Propter veritátem, et mansuetúdinem, et justítiam: * et dedúcet te mirabíliter déxtera tua.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Because of truth and meekness and justice: * and thy right hand shall conduct thee wonderfully.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Sagíttæ tuæ acútæ, pópuli sub te cadent: * in corde inimicórum Regis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thy arrows are sharp: under thee shall people fall, * into the hearts of the king&apos;s enemies.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Sedes tua, Deus, in sæculum sæculi: * virga directiónis virga regni tui.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thy throne, O God, is for ever and ever: * the sceptre of thy kingdom is a sceptre of uprightness.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Dilexísti justítiam, et odísti iniquitátem: * proptérea unxit te, Deus, Deus tuus, óleo lætítiæ præ consórtibus tuis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou hast loved justice, and hated iniquity: * therefore God, thy God, hath anointed thee with the oil of gladness above thy fellows.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Myrrha, et gutta, et cásia a vestiméntis tuis, a dómibus ebúrneis: * ex quibus delectavérunt te fíliæ regum in honóre tuo.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Myrrh and stacte and cassia perfume thy garments, from the ivory houses: * out of which the daughters of kings have delighted thee in thy glory.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ástitit regína a dextris tuis in vestítu deauráto: * circúmdata varietáte.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The queen stood on thy right hand, in gilded clothing; * surrounded with variety.</p>
     </td>
    </tr>
