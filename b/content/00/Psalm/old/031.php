   <p:Head2Ps>Psalmus 31. Beáti quorum</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>BEÁTI quorum remíssæ sunt iniquitátes: * et quorum tecta sunt peccáta.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>BLESSED are they whose iniquities are forgiven, * and whose sins are covered.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Beátus vir, cui non imputávit Dóminus peccátum, * nec est in spíritu ejus dolus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Blessed is the man to whom the Lord hath not imputed sin, * and in whose spirit there is no guile.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quóniam tácui, inveteravérunt ossa mea, * dum clamárem tota die.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Because I was silent my bones grew old; * whilst I cried out all the day long.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quóniam die ac nocte graváta est super me manus tua: * convérsus sum in ærúmna mea, dum confígitur spina.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For day and night thy hand was heavy upon me: * I am turned in my anguish, whilst the thorn is fastened.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Delíctum meum cógnitum tibi feci: * et injustítiam meam non abscóndi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I have acknowledged my sin to thee, * and my injustice I have not concealed.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Dixi: Confitébor advérsum me injustítiam meam Dómino: * et tu remisísti impietátem peccáti mei.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I said I will confess against myself my injustice to the Lord: * and thou hast forgiven the wickedness of my sin.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Pro hac orábit ad te omnis sanctus, * in témpore opportúno.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For this shall every one that is holy pray to thee * in a seasonable time.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Verúmtamen in dilúvio aquárum multárum, * ad eum non approximábunt.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And yet in a flood of many waters, * they shall not come nigh unto him.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Tu es refúgium meum a tribulatióne, quæ circúmdedit me: * exsultátio mea, érue me a circumdántibus me.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou art my refuge from the trouble which hath encompassed me: * my joy, deliver me from them that surround me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Intelléctum tibi dabo, et ínstruam te in via hac, qua gradiéris: * firmábo super te óculos meos.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I will give thee understanding, and I will instruct thee in this way, in which thou shalt go: * I will fix my eyes upon thee.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Nolíte fíeri sicut equus et mulus, * quibus non est intelléctus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Do not become like the horse and the mule, * who have no understanding.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>In camo et freno maxíllas eórum constrínge, * qui non appróximant ad te.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>With bit and bridle bind fast their jaws, * who come not near unto thee.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Multa flagélla peccatóris, * sperántem autem in Dómino misericórdia circúmdabit.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Many are the scourges of the sinner, * but mercy shall encompass him that hopeth in the Lord.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Lætámini in Dómino et exsultáte, justi, * et gloriámini, omnes recti corde.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Be glad in the Lord, and rejoice, ye just, * and glory, all ye right of heart.</p>
     </td>
    </tr>
