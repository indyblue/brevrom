   <p:Head2Ps>Psalmus 95. Cantáte Dómino</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>CANTÁTE Dómino cánticum novum: * cantáte Dómino, omnis terra.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>SING ye to the Lord a new canticle: * sing to the Lord, all the earth.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Cantáte Dómino, et benedícite nómini ejus: * annuntiáte de die in diem salutáre ejus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Sing ye to the Lord and bless his name: * shew forth his salvation from day to day.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Annuntiáte inter gentes glóriam ejus, * in ómnibus pópulis mirabília ejus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Declare his glory among the Gentiles: * his wonders among all people.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quóniam magnus Dóminus, et laudábilis nimis: * terríbilis est super omnes deos.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For the Lord is great, and exceedingly to be praised: * he is to be feared above all gods.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quóniam omnes dii géntium dæmónia: * Dóminus autem cælos fecit.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For all the gods of the Gentiles are devils: * but the Lord made the heavens.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Conféssio, et pulchritúdo in conspéctu ejus: * sanctimónia et magnificéntia in sanctificatióne ejus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Praise and beauty are before him: * holiness and majesty in his sanctuary.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Afférte Dómino, pátriæ géntium, afférte Dómino glóriam et honórem: * afférte Dómino glóriam nómini ejus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Bring ye to the Lord, O ye kindreds of the Gentiles, bring ye to the Lord glory and honour: * bring to the Lord glory unto his name.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Tóllite hóstias, et introíte in átria ejus: * adoráte Dóminum in átrio sancto ejus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Bring up sacrifices, and come into his courts: * adore ye the Lord in his holy court.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Commoveátur a fácie ejus univérsa terra: * dícite in géntibus quia Dóminus regnávit.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Let all the earth be moved at his presence. * Say ye among the Gentiles, the Lord hath reigned.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Étenim corréxit orbem terræ qui non commovébitur: * judicábit pópulos in æquitáte.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For he hath corrected the world, which shall not be moved: * he will judge the people with justice.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Læténtur cæli, et exsúltet terra: commoveátur mare, et plenitúdo ejus: * gaudébunt campi, et ómnia quæ in eis sunt.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Let the heavens rejoice, and let the earth be glad, let the sea be moved, and the fulness thereof: * the fields and all things that are in them shall be joyful.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Tunc exsultábunt ómnia ligna silvárum a fácie Dómini, quia venit: * quóniam venit judicáre terram.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Then shall all the trees of the woods rejoice before the face of the Lord, because he cometh: * because he cometh to judge the earth.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Judicábit orbem terræ in æquitáte, * et pópulos in veritáte sua.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He shall judge the world with justice, * and the people with his truth.</p>
     </td>
    </tr>
   </table>
