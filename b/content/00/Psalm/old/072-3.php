   <p:Head2Ps>Psalmus 72 iii. Verúmtamen</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>VERÚMTAMEN propter dolos posuísti eis: * dejecísti eos dum allevaréntur.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>BUT indeed for deceits thou hast put it to them: * when they were lifted up thou hast cast them down.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quómodo facti sunt in desolatiónem, súbito defecérunt: * periérunt propter iniquitátem suam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>How are they brought to desolation? they have suddenly ceased to be: * they have perished by reason of their iniquity.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Velut sómnium surgéntium, Dómine, * in civitáte tua imáginem ipsórum ad níhilum rédiges.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>As the dream of them that awake, O Lord; * so in thy city thou shalt bring their image to nothing.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quia inflammátum est cor meum, et renes mei commutáti sunt: * et ego ad níhilum redáctus sum, et nescívi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For my heart hath been inflamed, and my reins have been changed: * and I am brought to nothing, and I knew not.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ut juméntum factus sum apud te: * et ego semper tecum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I am become as a beast before thee: * and I am always with thee.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Tenuísti manum déxteram meam: et in voluntáte tua deduxísti me, * et cum glória suscepísti me.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou hast held me by my right hand; and by thy will thou hast conducted me, * and with thy glory thou hast received me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quid enim mihi est in cælo? * et a te quid vólui super terram?</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For what have I in heaven? * and besides thee what do I desire upon earth?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Defécit caro mea, et cor meum: * Deus cordis mei, et pars mea Deus in ætérnum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For thee my flesh and my heart hath fainted away: * thou art the God of my heart, and the God that is my portion for ever.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quia ecce, qui elóngant se a te, períbunt: * perdidísti omnes, qui fornicántur abs te.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For behold they that go far from thee shall perish: * thou hast destroyed all them that are disloyal to thee.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Mihi autem adhærére Deo bonum est: * pónere in Dómino Deo spem meam:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>But it is good for me to adhere to my God, * to put my hope in the Lord God:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ut annúntiem omnes prædicatiónes tuas, * in portis fíliæ Sion.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>That I may declare all thy praises, * in the gates of the daughter of Sion.</p>
     </td>
    </tr>
   </table>
