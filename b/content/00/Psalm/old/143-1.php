   <p:Head2Ps>Psalmus 143 i. Benedíctus Dóminus</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>BENEDÍCTUS Dóminus, Deus meus, qui docet manus meas ad prælium, * et dígitos meos ad bellum.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>BLESSED be the Lord my God, who teacheth my hands to fight, * and my fingers to war.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Misericórdia mea, et refúgium meum: * suscéptor meus, et liberátor meus:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>My mercy, and my refuge: * my support, and my deliverer:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Protéctor meus, et in ipso sperávi: * qui subdit pópulum meum sub me.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>My protector, and I have hoped in him: * who subdueth my people under me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Dómine, quid est homo, quia innotuísti ei? * aut fílius hóminis, quia réputas eum?</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Lord, what is man, that thou art made known to him? * or the son of man, that thou makest account of him?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Homo vanitáti símilis factus est: * dies ejus sicut umbra prætéreunt.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Man is like to vanity: * his days pass away like a shadow.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Dómine, inclína cælos tuos, et descénde: * tange montes, et fumigábunt.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Lord, bow down thy heavens and descend: * touch the mountains, and they shall smoke.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Fúlgura coruscatiónem, et dissipábis eos: * emítte sagíttas tuas, et conturbábis eos.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Send forth lightning, and thou shalt scatter them: * shoot out thy arrows, and thou shalt trouble them.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Emítte manum tuam de alto, éripe me, et líbera me de aquis multis: * de manu filiórum alienórum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Put forth thy hand from on high, take me out, and deliver me from many waters: * from the hand of strange children:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quorum os locútum est vanitátem: * et déxtera eórum, déxtera iniquitátis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Whose mouth hath spoken vanity: * and their right hand is the right hand of iniquity.</p>
     </td>
    </tr>
   </table>
