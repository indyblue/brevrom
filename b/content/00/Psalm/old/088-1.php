     </td>
     <td:B1>
      <p:BodyEIndent>I have made a covenant with my elect: I have sworn to David my servant: * Thy seed will I settle for ever.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et ædificábo in generatiónem et generatiónem * sedem tuam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And I will build up thy throne * unto generation and generation.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Confitebúntur cæli mirabília tua, Dómine: * étenim veritátem tuam in ecclésia sanctórum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The heavens shall confess thy wonders, O Lord: * and thy truth in the church of the saints.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quóniam quis in núbibus æquábitur Dómino: * símilis erit Deo in fíliis Dei?</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For who in the clouds can be compared to the Lord: * or who among the sons of God shall be like to God?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Deus, qui glorificátur in consílio sanctórum: * magnus et terríbilis super omnes qui in circúitu ejus sunt.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>God, who is glorified in the assembly of the saints: * great and terrible above all them that are about him.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Dómine, Deus virtútum, quis símilis tibi? * potens es, Dómine, et véritas tua in circúitu tuo.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>O Lord God of hosts, who is like to thee? * thou art mighty, O Lord, and thy truth is round about thee.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Tu domináris potestáti maris: * motum autem flúctuum ejus tu mítigas.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou rulest the power of the sea: * and appeasest the motion of the waves thereof.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Tu humiliásti sicut vulnerátum, supérbum: * in bráchio virtútis tuæ dispersísti inimícos tuos.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou hast humbled the proud one, as one that is slain: * with the arm of thy strength thou hast scattered thy enemies.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Tui sunt cæli, et tua est terra, orbem terræ et plenitúdinem ejus tu fundásti: * aquilónem, et mare tu creásti.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thine are the heavens, and thine is the earth: the world and the fulness thereof thou hast founded: * the north and the sea thou hast created.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Thabor et Hermon in nómine tuo exsultábunt: * tuum bráchium cum poténtia.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thabor and Hermon shall rejoice in thy name: * thy arm is with might.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Firmétur manus tua, et exaltétur déxtera tua: * justítia et judícium præparátio sedis tuæ.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Let thy hand be strengthened, and thy right hand exalted: * justice and judgment are the preparation of thy throne.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Misericórdia et véritas præcédent fáciem tuam: * beátus pópulus, qui scit jubilatiónem.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Mercy and truth shall go before thy face: * blessed is the people that knoweth jubilation.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Dómine, in lúmine vultus tui ambulábunt, et in nómine tuo exsultábunt tota die: * et in justítia tua exaltabúntur.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>They shall walk, O Lord, in the light of thy countenance: and in thy name they shall rejoice all the day, * and in thy justice they shall be exalted.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quóniam glória virtútis eórum tu es: * et in beneplácito tuo exaltábitur cornu nostrum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For thou art the glory of their strength: * and in thy good pleasure shall our horn be exalted.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quia Dómini est assúmptio nostra, * et Sancti Israël, regis nostri.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For our protection is of the Lord, * and of our king the holy one of Israel.</p>
     </td>
    </tr>
