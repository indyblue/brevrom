   <p:Head2Ps>Psalmus 150. Laudáte Dóminum</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>LAUDÁTE Dóminum in sanctis ejus: * laudáte eum in firmaménto virtútis ejus.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>PRAISE ye the Lord in his holy places: * praise ye him in the firmament of his power.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Laudáte eum in virtútibus ejus: * laudáte eum secúndum multitúdinem magnitúdinis ejus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Praise ye him for his mighty acts: * praise ye him according to the multitude of his greatness.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Laudáte eum in sono tubæ: * laudáte eum in psaltério, et cíthara.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Praise him with sound of trumpet: * praise him with psaltery and harp.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Laudáte eum in týmpano, et choro: * laudáte eum in chordis, et órgano.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Praise him with timbrel and choir: * praise him with strings and organs.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Laudáte eum in cýmbalis benesonántibus: laudáte eum in cýmbalis jubilatiónis: * omnis spíritus laudet Dóminum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Praise him on high sounding cymbals: praise him on cymbals of joy: * let every spirit praise the Lord.</p>
     </td>
    </tr>
   </table>
