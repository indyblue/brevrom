     </td>
     <td:B1>
      <p:BodyEDrop>I WILL bless the Lord at all times, * his praise shall be always in my mouth.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>In Dómino laudábitur ánima mea: * áudiant mansuéti, et læténtur.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>In the Lord shall my soul be praised: * let the meek hear and rejoice.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Magnificáte Dóminum mecum: * et exaltémus nomen ejus in idípsum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>O magnify the Lord with me; * and let us extol his name together.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Exquisívi Dóminum, et exaudívit me: * et ex ómnibus tribulatiónibus meis erípuit me.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I sought the Lord, and he heard me; * and he delivered me from all my troubles.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Accédite ad eum, et illuminámini: * et fácies vestræ non confundéntur.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Come ye to him and be enlightened: * and your faces shall not be confounded.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Iste pauper clamávit, et Dóminus exaudívit eum: * et de ómnibus tribulatiónibus ejus salvávit eum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>This poor man cried, and the Lord heard him: * and saved him out of all his troubles.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Immíttet Ángelus Dómini in circúitu timéntium eum: * et erípiet eos.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The Angel of the Lord shall encamp round about them that fear him: * and shall deliver them.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Gustáte, et vidéte quóniam suávis est Dóminus: * beátus vir, qui sperat in eo.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>O taste, and see that the Lord is sweet: * blessed is the man that hopeth in him.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Timéte Dóminum, omnes sancti ejus: * quóniam non est inópia timéntibus eum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Fear the Lord, all ye his saints: * for there is no want to them that fear him.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Dívites eguérunt et esuriérunt: * inquiréntes autem Dóminum non minuéntur omni bono.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The rich have wanted, and have suffered hunger: * but they that seek the Lord shall not be deprived of any good.</p>
     </td>
    </tr>
