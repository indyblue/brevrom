   <p:Head2Ps>Psalmus 12. Úsquequo, Dómine</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>USQUEQUO, Dómine, obliviscéris me in finem? * Úsquequo avértis fáciem tuam a me?</p>
     </td>
     <td:B1>
      <p:BodyEDrop>HOW long, O Lord, wilt thou forget me unto the end? * how long dost thou turn away thy face from me?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quámdiu ponam consília in ánima mea, * dolórem in corde meo per diem?</p>
     </td>
     <td:B1>
      <p:BodyEIndent>How long shall I take counsels in my soul, * sorrow in my heart all the day?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Úsquequo exaltábitur inimícus meus super me? * réspice, et exáudi me, Dómine, Deus meus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>How long shall my enemy be exalted over me? * Consider, and hear me, O Lord my God.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Illúmina óculos meos ne umquam obdórmiam in morte: * nequándo dicat inimícus meus: Præválui advérsus eum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Enlighten my eyes that I never sleep in death: * lest at any time my enemy say: I have prevailed against him.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Qui tríbulant me, exsultábunt si motus fúero: * ego autem in misericórdia tua sperávi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>They that trouble me will rejoice when I am moved: * but I have trusted in thy mercy.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Exsultábit cor meum in salutári tuo: cantábo Dómino qui bona tríbuit mihi: * et psallam nómini Dómini altíssimi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>My heart shall rejoice in thy salvation: I will sing to the Lord, who giveth me good things: * yea I will sing to the name of the Lord the most High.</p>
     </td>
    </tr>
   </table>
