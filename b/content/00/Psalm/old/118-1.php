   <p:Head2Ps>Psalmus 118 i. Beáti immaculáti</p>
   <p:Head4>Aleph</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>BEÁTI immaculáti in via: * qui ámbulant in lege Dómini.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>BLESSED are the undefiled in the way, * who walk in the law of the Lord.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Beáti, qui scrutántur testimónia ejus: * in toto corde exquírunt eum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Blessed are they that search his testimonies: * that seek him with their whole heart.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Non enim qui operántur iniquitátem, * in viis ejus ambulavérunt.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For they that work iniquity, * have not walked in his ways.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Tu mandásti * mandáta tua custodíri nimis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou hast commanded * thy commandments to be kept most diligently.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Útinam dirigántur viæ meæ, * ad custodiéndas justificatiónes tuas!</p>
     </td>
     <td:B1>
      <p:BodyEIndent>O! that my ways may be directed * to keep thy justifications.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Tunc non confúndar, * cum perspéxero in ómnibus mandátis tuis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Then shall I not be confounded, * when I shall look into all thy commandments.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Confitébor tibi in directióne cordis: * in eo quod dídici judícia justítiæ tuæ.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I will praise thee with uprightness of heart, * when I shall have learned the judgments of thy justice.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Justificatiónes tuas custódiam: * non me derelínquas usquequáque.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I will keep thy justifications: * O! do not thou utterly forsake me.</p>
     </td>
    </tr>
   </table>
   <p:Head4>Béth</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLIndent>In quo córrigit adolescéntior viam suam? * In custodiéndo sermónes tuos.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>By what doth a young man correct his way? * By observing thy words.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>In toto corde meo exquisívi te: * ne repéllas me a mandátis tuis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>With my whole heart have I sought after thee: * let me not stray from thy commandments.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>In corde meo abscóndi elóquia tua: * ut non peccem tibi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thy words have I hidden in my heart, * that I may not sin against thee.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Benedíctus es, Dómine: * doce me justificatiónes tuas.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Blessed art thou, O Lord: * teach me thy justifications.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>In lábiis meis, * pronuntiávi ómnia judícia oris tui.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>With my lips * I have pronounced all the judgments of thy mouth.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>In via testimoniórum tuórum delectátus sum, * sicut in ómnibus divítiis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I have been delighted in the way of thy testimonies, * as in all riches.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>In mandátis tuis exercébor: * et considerábo vias tuas.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I will meditate on thy commandments: * and I will consider thy ways.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>In justificatiónibus tuis meditábor: * non oblivíscar sermónes tuos.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I will think of thy justifications: * I will not forget thy words.</p>
     </td>
    </tr>
   </table>
