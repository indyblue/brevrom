     </td>
     <td:B1>
      <p:BodyEDrop>O LORD our Lord, * how admirable is thy name in the whole earth!</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quóniam eleváta est magnificéntia tua, * super cælos.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For thy magnificence is elevated * above the heavens.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ex ore infántium et lacténtium perfecísti laudem propter inimícos tuos, * ut déstruas inimícum et ultórem.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Out of the mouth of infants and of sucklings thou hast perfected praise, because of thy enemies, * that thou mayst destroy the enemy and the avenger.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quóniam vidébo cælos tuos, ópera digitórum tuórum: * lunam et stellas, quæ tu fundásti.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For I will behold thy heavens, the works of thy fingers: * the moon and the stars which thou hast founded.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quid est homo quod memor es ejus? * aut fílius hóminis, quóniam vísitas eum?</p>
     </td>
     <td:B1>
      <p:BodyEIndent>What is man that thou art mindful of him? * or the son of man that thou visitest him?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Minuísti eum paulo minus ab Ángelis, glória et honóre coronásti eum: * et constituísti eum super ópera mánuum tuárum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou hast made him a little less than the angels, thou hast crowned him with glory and honour: * and hast set him over the works of thy hands.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Omnia subjecísti sub pédibus ejus, * oves et boves univérsas: ínsuper et pécora campi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou hast subjected all things under his feet, * all sheep and oxen: moreover the beasts also of the fields.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Vólucres cæli, et pisces maris, * qui perámbulant sémitas maris.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The birds of the air, and the fishes of the sea, * that pass through the paths of the sea.</p>
     </td>
    </tr>
