     <td:A1>
      <p:BodyLIndent>Dómine, Dóminus noster, * quam admirábile est nomen tuum in univérsa terra!</p>
     </td>
     <td:B1>
      <p:BodyEIndent>O Lord our Lord, * how admirable is thy name in all the earth!</p>
     </td>
    </tr>
