      <p:BodyLIndent>Lavábo inter innocéntes manus meas: * et circúmdabo altáre tuum, Dómine:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I will wash my hands among the innocent; * and will compass thy altar, O Lord:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ut áudiam vocem laudis, * et enárrem univérsa mirabília tua.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>That I may hear the voice of thy praise: * and tell of all thy wondrous works.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Dómine, diléxi decórem domus tuæ, * et locum habitatiónis glóriæ tuæ.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I have loved, O Lord, the beauty of thy house; * and the place where thy glory dwelleth.</p>
     </td>
    </tr>
    <tr>
