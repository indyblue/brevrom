      <p:BodyEIndent>For thy mercy is before my eyes; * and I am well pleased with thy truth.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Non sedi cum concílio vanitátis: * et cum iníqua geréntibus non introíbo.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I have not sat with the council of vanity: * neither will I go in with the doers of unjust things.</p>
     </td>
    </tr>
    <tr>
