      <p:BodyLIndent>Odívi ecclésiam malignántium: * et cum ímpiis non sedébo.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I have hated the assembly of the malignant; * and with the wicked I will not sit.</p>
     </td>
    </tr>
    <tr>
