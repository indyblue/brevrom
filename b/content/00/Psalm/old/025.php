      <p:BodyLIndent>Ne perdas cum ímpiis, Deus, ánimam meam, * et cum viris sánguinum vitam meam:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Take not away my soul, O God, with the wicked: * nor my life with bloody men:</p>
     </td>
    </tr>
    <tr>
