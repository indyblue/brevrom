   <p:Head2Ps>Psalmus 74. Confitébimur tibi</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>CONFITÉBIMUR tibi, Deus: * confitébimur, et invocábimus nomen tuum.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>WE WILL praise thee, O God: * we will praise, and we will call upon thy name.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Narrábimus mirabília tua: * cum accépero tempus, ego justítias judicábo.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>We will relate thy wondrous works: * when I shall take a time, I will judge justices.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Liquefácta est terra, et omnes qui hábitant in ea: * ego confirmávi colúmnas ejus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The earth is melted, and all that dwell therein: * I have established the pillars thereof.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Dixi iníquis: Nolíte iníque ágere: * et delinquéntibus: Nolíte exaltáre cornu:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I said to the wicked: Do not act wickedly: * and to the sinners: Lift not up the horn.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Nolíte extóllere in altum cornu vestrum: * nolíte loqui advérsus Deum iniquitátem.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Lift not up your horn on high: * speak not iniquity against God.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quia neque ab Oriénte, neque ab Occidénte, neque a desértis móntibus: * quóniam Deus judex est.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For neither from the east, nor from the west, nor from the desert hills: * for God is the judge.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Hunc humíliat, et hunc exáltat: * quia calix in manu Dómini vini meri plenus misto.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>One he putteth down, and another he lifteth up: * for in the hand of the Lord there is a cup of strong wine full of mixture.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et inclinávit ex hoc in hoc: verúmtamen fæx ejus non est exinaníta: * bibent omnes peccatóres terræ.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And he hath poured it out from this to that: but the dregs thereof are not emptied: * all the sinners of the earth shall drink.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ego autem annuntiábo in sæculum: * cantábo Deo Jacob.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>But I will declare for ever: * I will sing to the God of Jacob.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et ómnia córnua peccatórum confríngam: * et exaltabúntur córnua justi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And I will break all the horns of sinners: * but the horns of the just shall be exalted.</p>
     </td>
    </tr>
   </table>
