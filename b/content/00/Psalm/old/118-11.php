   <p:Head2Ps>Psalmus 118 xi. Príncipes persecúti sunt</p>
   <p:Head4>Sin</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>PRÍNCIPES persecúti sunt me gratis: * et a verbis tuis formidávit cor meum.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>PRINCES have persecuted me without cause: * and my heart hath been in awe of thy words.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Lætábor ego super elóquia tua: * sicut qui invénit spólia multa.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I will rejoice at thy words, * as one that hath found great spoil.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Iniquitátem ódio hábui, et abominátus sum: * legem autem tuam diléxi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I have hated and abhorred iniquity; * but I have loved thy law.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Sépties in die laudem dixi tibi, * super judícia justítiæ tuæ.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Seven times a day I have given praise to thee, * for the judgments of thy justice.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Pax multa diligéntibus legem tuam: * et non est illis scándalum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Much peace have they that love thy law, * and to them there is no stumbling-block.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Exspectábam salutáre tuum, Dómine: * et mandáta tua diléxi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I looked for thy salvation, O Lord: * and I loved thy commandments.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Custodívit ánima mea testimónia tua: * et diléxit ea veheménter.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>My soul hath kept thy testimonies: * and hath loved them exceedingly.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Servávi mandáta tua, et testimónia tua: * quia omnes viæ meæ in conspéctu tuo.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I have kept thy commandments and thy testimonies: * because all my ways are in thy sight.</p>
     </td>
    </tr>
   </table>
   <p:Head4>Tau</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLIndent>Appropínquet deprecátio mea in conspéctu tuo, Dómine: * juxta elóquium tuum da mihi intelléctum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Let my supplication, O Lord, come near in thy sight: * give me understanding according to thy word.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Intret postulátio mea in conspéctu tuo: * secúndum elóquium tuum éripe me.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Let my request come in before thee; * deliver thou me according to thy word.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Eructábunt lábia mea hymnum, * cum docúeris me justificatiónes tuas.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>My lips shall utter a hymn, * when thou shalt teach me thy justifications.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Pronuntiábit lingua mea elóquium tuum: * quia ómnia mandáta tua æquitas.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>My tongue shall pronounce thy word: * because all thy commandments are justice.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Fiat manus tua ut salvet me: * quóniam mandáta tua elégi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Let thy hand be with me to save me; * for I have chosen thy precepts.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Concupívi salutáre tuum, Dómine: * et lex tua meditátio mea est.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I have longed for thy salvation, O Lord; * and thy law is my meditation.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Vivet ánima mea, et laudábit te: * et judícia tua adjuvábunt me.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>My soul shall live and shall praise thee: * and thy judgments shall help me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Errávi, sicut ovis, quæ périit: * quære servum tuum, quia mandáta tua non sum oblítus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I have gone astray like a sheep that is lost: * seek thy servant, because I have not forgotten thy commandments.</p>
     </td>
    </tr>
   </table>
