   <p:Head2Ps>Psalmus 36 ii. Mélius est módicus</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>MÉLIUS est módicum justo, * super divítias peccatórum multas.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>BETTER is a little to the just, * than the great riches of the wicked.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quóniam bráchia peccatórum conteréntur: * confírmat autem justos Dóminus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For the arms of the wicked shall be broken in pieces; * but the Lord strengtheneth the just.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Novit Dóminus dies immaculatórum: * et heréditas eórum in ætérnum erit.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The Lord knoweth the days of undefiled; * and their inheritance shall be for ever.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Non confundéntur in témpore malo, et in diébus famis saturabúntur: * quia peccatóres períbunt.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>They shall not be confounded in the evil time; and in the days of famine they shall be filled, * because the wicked shall perish.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Inimíci vero Dómini mox ut honorificáti fúerint et exaltáti: * deficiéntes, quemádmodum fumus defícient.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And the enemies of the Lord, presently after they shall be honoured and exalted, * shall come to nothing and vanish like smoke.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Mutuábitur peccátor, et non solvet: * justus autem miserétur et tríbuet.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The sinner shall borrow, and not pay again; * but the just sheweth mercy and shall give.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quia benedicéntes ei hereditábunt terram: * maledicéntes autem ei disperíbunt.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For such as bless him shall inherit the land: * but such as curse him shall perish.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Apud Dóminum gressus hóminis dirigéntur: * et viam ejus volet.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>With the Lord shall the steps of a man be directed, * and he shall like well his way.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Cum cecíderit non collidétur: * quia Dóminus suppónit manum suam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>When he shall fall he shall not be bruised, * for the Lord putteth his hand under him.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Júnior fui, étenim sénui: * et non vidi justum derelíctum, nec semen ejus quærens panem.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I have been young, and now am old; * and I have not seen the just forsaken, nor his seed seeking bread.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Tota die miserétur et cómmodat: * et semen illíus in benedictióne erit.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He sheweth mercy, and lendeth all the day long; * and his seed shall be in blessing.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Declína a malo, et fac bonum: * et inhábita in sæculum sæculi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Decline from evil and do good, * and dwell for ever and ever.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quia Dóminus amat judícium, et non derelínquet sanctos suos: * in ætérnum conservabúntur.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For the Lord loveth judgment, and will not forsake his saints: * they shall be preserved for ever.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Injústi puniéntur: * et semen impiórum períbit.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The unjust shall be punished, * and the seed of the wicked shall perish.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Justi autem hereditábunt terram: * et inhabitábunt in sæculum sæculi super eam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>But the just shall inherit the land, * and shall dwell therein for evermore.</p>
     </td>
    </tr>
