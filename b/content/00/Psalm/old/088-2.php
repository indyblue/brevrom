      <p:BodyLIndent>Invéni David, servum meum: * óleo sancto meo unxi eum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I have found David my servant: * with my holy oil I have anointed him.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Manus enim mea auxiliábitur ei: * et bráchium meum confortábit eum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For my hand shall help him: * and my arm shall strengthen him.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Nihil profíciet inimícus in eo, * et fílius iniquitátis non appónet nocére ei.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The enemy shall have no advantage over him: * nor the son of iniquity have power to hurt him.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et concídam a fácie ipsíus inimícos ejus: * et odiéntes eum in fugam convértam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And I will cut down his enemies before his face; * and them that hate him I will put to flight.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et véritas mea, et misericórdia mea cum ipso: * et in nómine meo exaltábitur cornu ejus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And my truth and my mercy shall be with him: * and in my name shall his horn be exalted.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et ponam in mari manum ejus: * et in flumínibus déxteram ejus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And I will set his hand in the sea; * and his right hand in the rivers.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ipse invocábit me: Pater meus es tu: * Deus meus, et suscéptor salútis meæ.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He shall cry out to me: Thou art my father: * my God, and the support of my salvation.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et ego primogénitum ponam illum * excélsum præ régibus terræ.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And I will make him my firstborn, * high above the kings of the earth.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>In ætérnum servábo illi misericórdiam meam: * et testaméntum meum fidéle ipsi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I will keep my mercy for him for ever: * and my covenant faithful to him.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et ponam in sæculum sæculi semen ejus: * et thronum ejus sicut dies cæli.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And I will make his seed to endure for evermore: * and his throne as the days of heaven.</p>
     </td>
    </tr>
    <tr>
