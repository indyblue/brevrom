     </td>
     <td:B1>
      <p:BodyEIndent>And if his children forsake my law, * and walk not in my judgments:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Si justítias meas profanáverint: * et mandáta mea non custodíerint:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>If they profane my justices: * and keep not my commandments:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Visitábo in virga iniquitátes eórum: * et in verbéribus peccáta eórum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I will visit their iniquities with a rod: * and their sins with stripes.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Misericórdiam autem meam non dispérgam ab eo: * neque nocébo in veritáte mea:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>But my mercy I will not take away from him: * nor will I suffer my truth to fail.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Neque profanábo testaméntum meum: * et quæ procédunt de lábiis meis, non fáciam írrita.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Neither will I profane my covenant: * and the words that proceed from my mouth I will not make void.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Semel jurávi in sancto meo: Si David méntiar: * semen ejus in ætérnum manébit.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Once have I sworn by my holiness: I will not lie unto David: * his seed shall endure for ever.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et thronus ejus sicut sol in conspéctu meo, * et sicut luna perfécta in ætérnum: et testis in cælo fidélis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And his throne as the sun before me: * and as the moon perfect for ever, and a faithful witness in heaven.</p>
     </td>
    </tr>
