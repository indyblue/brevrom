   <p:Head2Ps>Psalmus 118 iv. Memor esto verbi tui</p>
   <p:Head4>Zain</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>MEMOR esto verbi tui servo tuo, * in quo mihi spem dedísti.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>BE THOU mindful of thy word to thy servant, * in which thou hast given me hope.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Hæc me consoláta est in humilitáte mea: * quia elóquium tuum vivificávit me.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>This hath comforted me in my humiliation: * because thy word hath enlivened me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Supérbi iníque agébant usquequáque: * a lege autem tua non declinávi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The proud did iniquitously altogether: * but I declined not from thy law.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Memor fui judiciórum tuórum a sæculo, Dómine: * et consolátus sum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I remembered, O Lord, thy judgments of old: * and I was comforted.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Deféctio ténuit me, * pro peccatóribus derelinquéntibus legem tuam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>A fainting hath taken hold of me, * because of the wicked that forsake thy law.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Cantábiles mihi erant justificatiónes tuæ, * in loco peregrinatiónis meæ.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thy justifications were the subject of my song, * in the place of my pilgrimage.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Memor fui nocte nóminis tui, Dómine: * et custodívi legem tuam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>In the night I have remembered thy name, O Lord: * and have kept thy law.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Hæc facta est mihi: * quia justificatiónes tuas exquisívi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>This happened to me: * because I sought after thy justifications.</p>
     </td>
    </tr>
   </table>
