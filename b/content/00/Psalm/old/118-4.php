     </td>
     <td:B1>
      <p:BodyEIndent>O Lord, my portion, * I have said, I would keep thy law.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Deprecátus sum fáciem tuam in toto corde meo: * miserére mei secúndum elóquium tuum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I entreated thy face with all my heart: * have mercy on me according to thy word.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Cogitávi vias meas: * et convérti pedes meos in testimónia tua.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I have thought on my ways: * and turned my feet unto thy testimonies.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Parátus sum, et non sum turbátus: * ut custódiam mandáta tua.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I am ready, and am not troubled: * that I may keep thy commandments.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Funes peccatórum circumpléxi sunt me: * et legem tuam non sum oblítus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The cords of the wicked have encompassed me: * but I have not forgotten thy law.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Média nocte surgébam ad confiténdum tibi, * super judícia justificatiónis tuæ.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I rose at midnight to give praise to thee; * for the judgments of thy justification.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Párticeps ego sum ómnium timéntium te: * et custodiéntium mandáta tua.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I am a partaker with all them that fear thee, * and that keep thy commandments.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Misericórdia tua, Dómine, plena est terra: * justificatiónes tuas doce me.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The earth, O Lord, is full of thy mercy: * teach me thy justifications.</p>
     </td>
    </tr>
