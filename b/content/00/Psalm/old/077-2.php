     </td>
     <td:B1>
      <p:BodyEIndent>They kept not the covenant of God: * and in his law they would not walk.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et oblíti sunt benefactórum ejus: * et mirabílium ejus quæ osténdit eis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And they forgot his benefits, * and his wonders that he had shewn them.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Coram pátribus eórum fecit mirabília in terra Ægýpti: * in campo Táneos.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Wonderful things did he do in the sight of their fathers, in the land of Egypt, * in the field of Tanis.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Interrúpit mare, et perdúxit eos: * et státuit aquas quasi in utre.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He divided the sea and brought them through: * and he made the waters to stand as in a vessel.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et dedúxit eos in nube diéi: * et tota nocte in illuminatióne ignis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And he conducted them with a cloud by day: * and all the night with a light of fire.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Interrúpit petram in erémo: * et adaquávit eos velut in abýsso multa.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He struck the rock in the wilderness: * and gave them to drink, as out of the great deep.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et edúxit aquam de petra: * et dedúxit tamquam flúmina aquas.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He brought forth water out of the rock: * and made streams run down as rivers.</p>
     </td>
    </tr>
