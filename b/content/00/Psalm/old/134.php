   <p:Head2Ps>Psalmus 134. Laudáte nomen Dómini</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>LAUDÁTE nomen Dómini, * laudáte, servi, Dóminum.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>PRAISE ye the name of the Lord: * O you his servants, praise the Lord:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Qui statis in domo Dómini, * in átriis domus Dei nostri.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>You that stand in the house of the Lord, * in the courts of the house of our God.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Laudáte Dóminum, quia bonus Dóminus: * psállite nómini ejus, quóniam suáve.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Praise ye the Lord, for the Lord is good: * sing ye to his name, for it is sweet.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quóniam Jacob elégit sibi Dóminus, * Israël in possessiónem sibi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For the Lord hath chosen Jacob unto himself: * Israel for his own possession.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quia ego cognóvi quod magnus est Dóminus, * et Deus noster præ ómnibus diis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For I have known that the Lord is great, * and our God is above all gods.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Omnia quæcúmque vóluit, Dóminus fecit in cælo, et in terra, * in mari, et in ómnibus abýssis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Whatsoever the Lord pleased he hath done, in heaven, in earth, * in the sea, and in all the deeps.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Edúcens nubes ab extrémo terræ: * fúlgura in plúviam fecit.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He bringeth up clouds from the end of the earth: * he hath made lightnings for the rain.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Qui prodúcit ventos de thesáuris suis: * qui percússit primogénita Ægýpti ab hómine usque ad pecus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He bringeth forth winds out of his stores: * He slew the firstborn of Egypt from man even unto beast.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et misit signa, et prodígia in médio tui, Ægýpte: * in Pharaónem, et in omnes servos ejus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He sent forth signs and wonders in the midst of thee, O Egypt: * upon Pharao, and upon all his servants.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Qui percússit gentes multas: * et occídit reges fortes:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He smote many nations, * and slew mighty kings:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Sehon, regem Amorrhæórum, et Og, regem Basan, * et ómnia regna Chánaan.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Sehon king of the Amorrhites, and Og king of Basan, * and all the kingdoms of Chanaan.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et dedit terram eórum hereditátem, * hereditátem Israël, pópulo suo.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And gave their land for an inheritance, * for an inheritance to his people Israel.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Dómine, nomen tuum in ætérnum: * Dómine, memoriále tuum in generatiónem et generatiónem.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thy name, O Lord, is for ever: * thy memorial, O Lord, unto all generations.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quia judicábit Dóminus pópulum suum: * et in servis suis deprecábitur.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For the Lord will judge his people, * and will be entreated in favour of his servants.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Simulácra géntium argéntum, et aurum, * ópera mánuum hóminum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The idols of the Gentiles are silver and gold, * the works of men’s hands.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Os habent, et non loquéntur: * óculos habent, et non vidébunt.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>They have a mouth, but they speak not: * they have eyes, but they see not.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Aures habent, et non áudient: * neque enim est spíritus in ore ipsórum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>They have ears, but they hear not: * neither is there any breath in their mouths.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Símiles illis fiant qui fáciunt ea: * et omnes qui confídunt in eis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Let them that make them be like to them: * and every one that trusteth in them.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Domus Israël, benedícite Dómino: * domus Aaron, benedícite Dómino.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Bless the Lord, O house of Israel: * bless the Lord, O house of Aaron.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Domus Levi, benedícite Dómino: * qui timétis Dóminum, benedícite Dómino.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Bless the Lord, O house of Levi: * you that fear the Lord, bless the Lord.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Benedíctus Dóminus ex Sion, * qui hábitat in Jerúsalem.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Blessed be the Lord out of Sion, * who dwelleth in Jerusalem.</p>
     </td>
    </tr>
   </table>
