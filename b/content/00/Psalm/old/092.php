     </td>
     <td:B1>
      <p:BodyEDrop>THE Lord hath reigned, he is clothed with beauty: * the Lord is clothed with strength, and hath girded himself.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Étenim firmávit orbem terræ, * qui non commovébitur.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For he hath established the world * which shall not be moved.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Paráta sedes tua ex tunc: * a sæculo tu es.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thy throne is prepared from of old: * thou art from everlasting.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Elevavérunt flúmina, Dómine: * elevavérunt flúmina vocem suam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The floods have lifted up, O Lord: * the floods have lifted up their voice.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Elevavérunt flúmina fluctus suos, * a vócibus aquárum multárum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The floods have lifted up their waves, * with the noise of many waters.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Mirábiles elatiónes maris: * mirábilis in altis Dóminus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Wonderful are the surges of the sea: * wonderful is the Lord on high.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Testimónia tua credibília facta sunt nimis: * domum tuam decet sanctitúdo, Dómine, in longitúdinem diérum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thy testimonies are become exceedingly credible: * holiness becometh thy house, O Lord, unto length of days.</p>
     </td>
    </tr>
