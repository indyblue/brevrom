   <p:Head2Ps>Psalmus 115. Crédidi</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>CRÉDIDI, propter quod locútus sum: * ego autem humiliátus sum nimis.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>I HAVE believed, therefore have I spoken; * but I have been humbled exceedingly.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ego dixi in excéssu meo: * Omnis homo mendax.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I said in my excess: * Every man is a liar.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quid retríbuam Dómino, * pro ómnibus, quæ retríbuit mihi?</p>
     </td>
     <td:B1>
      <p:BodyEIndent>What shall I render to the Lord, * for all the things that he hath rendered to me?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Cálicem salutáris accípiam: * et nomen Dómini invocábo.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I will take the chalice of salvation; * and I will call upon the name of the Lord.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Vota mea Dómino reddam coram omni pópulo ejus: * pretiósa in conspéctu Dómini mors sanctórum ejus:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I will pay my vows to the Lord before all his people: * precious in the sight of the Lord is the death of his saints.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>O Dómine, quia ego servus tuus: * ego servus tuus, et fílius ancíllæ tuæ.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>O Lord, for I am thy servant: * I am thy servant, and the son of thy handmaid.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Dirupísti víncula mea: * tibi sacrificábo hóstiam laudis, et nomen Dómini invocábo.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou hast broken my bonds: * I will sacrifice to thee the sacrifice of praise, and I will call upon the name of the Lord.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Vota mea Dómino reddam in conspéctu omnis pópuli ejus: * in átriis domus Dómini, in médio tui, Jerúsalem.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I will pay my vows to the Lord in the sight of all his people: * in the courts of the house of the Lord, in the midst of thee, O Jerusalem.</p>
     </td>
    </tr>
   </table>
