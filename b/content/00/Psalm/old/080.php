   <p:Head2Ps>Psalmus 80. Exsultáte Deo</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>EXSUTÁTE Deo, adjutóri nostro: * jubiláte Deo Jacob.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>REJOICE O God our helper: sing aloud to the God of Jacob.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Súmite psalmum, et date týmpanum: * psaltérium jucúndum cum cíthara.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Take a psalm, and bring hither the timbrel: * the pleasant psaltery with the harp.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Buccináte in Neoménia tuba, * in insígni die solemnitátis vestræ.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Blow up the trumpet on the new moon, * on the noted day of your solemnity.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quia præcéptum in Israël est: * et judícium Dei Jacob.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For it is a commandment in Israel, * and a judgment to the God of Jacob.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Testimónium in Joseph pósuit illud, cum exíret de terra Ægýpti: * linguam, quam non nóverat, audívit.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He ordained it for a testimony in Joseph, when he came out of the land of Egypt: * he heard a tongue which he knew not.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Divértit ab onéribus dorsum ejus: * manus ejus in cóphino serviérunt.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He removed his back from the burdens: * his hands had served in baskets.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>In tribulatióne invocásti me, et liberávi te: * exaudívi te in abscóndito tempestátis: probávi te apud aquam contradictiónis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou calledst upon me in affliction, and I delivered thee: * I heard thee in the secret place of tempest: I proved thee at the waters of contradiction.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Audi, pópulus meus, et contestábor te: * Israël, si audíeris me, non erit in te deus recens, neque adorábis deum aliénum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Hear, O my people, and I will testify to thee: * O Israel, if thou wilt hearken to me; there shall be no new god in thee: neither shalt thou adore a strange god.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ego enim sum Dóminus Deus tuus, qui edúxi te de terra Ægýpti: * diláta os tuum, et implébo illud.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For I am the Lord thy God, who brought thee out of the land of Egypt: * open thy mouth wide, and I will fill it.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et non audívit pópulus meus vocem meam: * et Israël non inténdit mihi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>But my people heard not my voice: * and Israel hearkened not to me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et dimísi eos secúndum desidéria cordis eórum: * ibunt in adinventiónibus suis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>So I let them go according to the desires of their heart: * they shall walk in their own inventions.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Si pópulus meus audísset me: * Israël si in viis meis ambulásset:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>If my people had heard me: * if Israel had walked in my ways:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Pro níhilo fórsitan inimícos eórum humiliássem: * et super tribulántes eos misíssem manum meam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I should soon have humbled their enemies, * and laid my hand on them that troubled them.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Inimíci Dómini mentíti sunt ei: * et erit tempus eórum in sæcula.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The enemies of the Lord have lied to him: * and their time shall be for ever.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et cibávit eos ex ádipe fruménti: * et de petra, melle saturávit eos.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And he fed them with the fat of wheat, * and filled them with honey out of the rock.</p>
     </td>
    </tr>
