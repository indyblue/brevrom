   <p:Head2Ps>Psalmus 142. Dómine, exáudi</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>DÓMINE, exáudi oratiónem meam: áuribus pércipe obsecratiónem meam in veritáte tua: * exáudi me in tua justítia.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>HEAR, O Lord, my prayer: give ear to my supplication in thy truth: * hear me in thy justice.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et non intres in judícium cum servo tuo: * quia non justificábitur in conspéctu tuo omnis vivens.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And enter not into judgment with thy servant: * for in thy sight no man living shall be justified.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quia persecútus est inimícus ánimam meam: * humiliávit in terra vitam meam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For the enemy hath persecuted my soul: * he hath brought down my life to the earth.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Collocávit me in obscúris sicut mórtuos sæculi: * et anxiátus est super me spíritus meus, in me turbátum est cor meum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He hath made me to dwell in darkness as those that have been dead of old: * and my spirit is in anguish within me: my heart within me is troubled.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Memor fui diérum antiquórum, meditátus sum in ómnibus opéribus tuis: * in factis mánuum tuárum meditábar.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I remembered the days of old, I meditated on all thy works: * I meditated upon the works of thy hands.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Expándi manus meas ad te: * ánima mea sicut terra sine aqua tibi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I stretched forth my hands to thee: * my soul is as earth without water unto thee.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Velóciter exáudi me, Dómine: * defécit spíritus meus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Hear me speedily, O Lord: * my spirit hath fainted away.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Non avértas fáciem tuam a me: * et símilis ero descendéntibus in lacum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Turn not away thy face from me, * lest I be like unto them that go down into the pit.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Audítam fac mihi mane misericórdiam tuam: * quia in te sperávi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Cause me to hear thy mercy in the morning; * for in thee have I hoped.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Notam fac mihi viam, in qua ámbulem: * quia ad te levávi ánimam meam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Make the way known to me, wherein I should walk: * for I have lifted up my soul to thee.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Éripe me de inimícis meis, Dómine, ad te confúgi: * doce me fácere voluntátem tuam, quia Deus meus es tu.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Deliver me from my enemies, O Lord, to thee have I fled: * teach me to do thy will, for thou art my God.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Spíritus tuus bonus dedúcet me in terram rectam: * propter nomen tuum, Dómine, vivificábis me, in æquitáte tua.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thy good spirit shall lead me into the right land: * for thy name’s sake, O Lord, thou wilt quicken me in thy justice.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Edúces de tribulatióne ánimam meam: * et in misericórdia tua dispérdes inimícos meos.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou wilt bring my soul out of trouble: * and in thy mercy thou wilt destroy my enemies.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et perdes omnes, qui tríbulant ánimam meam: * quóniam ego servus tuus sum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And thou wilt cut off all them that afflict my soul: * for I am thy servant.</p>
     </td>
    </tr>
   </table>
