   <p:Head2Ps>Psalmus 128. Sæpe expugnavérunt me</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>SÆPE expugnavérunt me a juventúte mea, * dicat nunc Israël:</p>
     </td>
     <td:B1>
      <p:BodyEDrop>OFTEN have they fought against me from my youth, * let Israel now say.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Sæpe expugnavérunt me a juventúte mea: * étenim non potuérunt mihi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Often have they fought against me from my youth: * but they could not prevail over me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Supra dorsum meum fabricavérunt peccatóres: * prolongavérunt iniquitátem suam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The wicked have wrought upon my back: * they have lengthened their iniquity.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Dóminus justus concídit cervíces peccatórum: * confundántur et convertántur retrórsum omnes, qui odérunt Sion.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The Lord who is just will cut the necks of sinners: * let them all be confounded and turned back that hate Sion.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Fiant sicut fœnum tectórum: * quod priúsquam evellátur, exáruit:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Let them be as grass upon the tops of houses: * which withereth before it be plucked up:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>De quo non implévit manum suam qui metit, * et sinum suum qui manípulos cólligit.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Wherewith the mower filleth not his hand: * nor he that gathereth sheaves his bosom.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et non dixérunt qui præteríbant: Benedíctio Dómini super vos: * benedíximus vobis in nómine Dómini.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And they that passed by have not said: The blessing of the Lord be upon you: * we have blessed you in the name of the Lord.</p>
     </td>
    </tr>
