     </td>
     <td:B1>
      <p:BodyEIndent>In my trouble I called upon the Lord: * and the Lord heard me, and enlarged me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Dóminus mihi adjútor: * non timébo quid fáciat mihi homo.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The Lord is my helper: * I will not fear what man can do unto me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Dóminus mihi adjútor: * et ego despíciam inimícos meos.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The Lord is my helper: * and I will look over my enemies.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Bonum est confídere in Dómino, * quam confídere in hómine:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>It is good to confide in the Lord, * rather than to have confidence in man.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Bonum est speráre in Dómino, * quam speráre in princípibus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>It is good to trust in the Lord, * rather than to trust in princes.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Omnes gentes circuiérunt me: * et in nómine Dómini quia ultus sum in eos.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>All nations compassed me about; * and in the name of the Lord I have been revenged on them.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Circumdántes circumdedérunt me: * et in nómine Dómini quia ultus sum in eos.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Surrounding me they compassed me about: * and in the name of the Lord I have been revenged on them.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Circumdedérunt me sicut apes, et exarsérunt sicut ignis in spinis: * et in nómine Dómini quia ultus sum in eos.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>They surrounded me like bees, and they burned like fire among thorns: * and in the name of the Lord I was revenged on them.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Impúlsus evérsus sum ut cáderem: * et Dóminus suscépit me.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Being pushed I was overturned that I might fall: * but the Lord supported me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Fortitúdo mea, et laus mea Dóminus: * et factus est mihi in salútem.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The Lord is my strength and my praise: * and he is become my salvation.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Vox exsultatiónis, et salútis * in tabernáculis justórum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The voice of rejoicing and of salvation * is in the tabernacles of the just.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Déxtera Dómini fecit virtútem: déxtera Dómini exaltávit me, * déxtera Dómini fecit virtútem.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The right hand of the Lord hath wrought strength: the right hand of the Lord hath exalted me: * the right hand of the Lord hath wrought strength.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Non móriar, sed vivam: * et narrábo ópera Dómini.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I shall not die, but live: * and shall declare the works of the Lord.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Castígans castigávit me Dóminus: * et morti non trádidit me.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The Lord chastising hath chastised me: * but he hath not delivered me over to death.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Aperíte mihi portas justítiæ, ingréssus in eas confitébor Dómino: * hæc porta Dómini, justi intrábunt in eam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Open ye to me the gates of justice: I will go in to them, and give praise to the Lord. * This is the gate of the Lord, the just shall enter into it.</p>
     </td>
    </tr>
