      <p:BodyLIndent>Dicat nunc domus Aaron: * quóniam in sæculum misericórdia ejus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Let the house of Aaron now say, * that his mercy endureth for ever.</p>
     </td>
    </tr>
    <tr>
