      <p:BodyLIndent>Dicant nunc qui timent Dóminum: * quóniam in sæculum misericórdia ejus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Let them that fear the Lord now say, * that his mercy endureth for ever.</p>
     </td>
    </tr>
