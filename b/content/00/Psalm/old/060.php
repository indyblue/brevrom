   <p:Head2Ps>Psalmus 60. Exáudi, Deus</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>EXÁUDI, Deus, deprecatiónem meam: * inténde oratióni meæ.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>HEAR, O God, my supplication: * be attentive to my prayer.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>A fínibus terræ ad te clamávi: * dum anxiarétur cor meum, in petra exaltásti me.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>To thee have I cried from the ends of the earth: * when my heart was in anguish, thou hast exalted me on a rock.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Deduxísti me, quia factus es spes mea: * turris fortitúdinis a fácie inimíci.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou hast conducted me; for thou hast been my hope: * a tower of strength against the face of the enemy.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Inhabitábo in tabernáculo tuo in sæcula: * prótegar in velaménto alárum tuárum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>In thy tabernacle I shall dwell for ever: * I shall be protected under the covert of thy wings.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quóniam tu, Deus meus, exaudísti oratiónem meam: * dedísti hereditátem timéntibus nomen tuum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For thou, my God, hast heard my prayer: * thou hast given an inheritance to them that fear thy name.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Dies super dies regis adjícies: * annos ejus usque in diem generatiónis et generatiónis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou wilt add days to the days of the king: * his years even to generation and generation.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Pérmanet in ætérnum in conspéctu Dei: * misericórdiam et veritátem ejus quis requíret?</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He abideth for ever in the sight of God: * his mercy and truth who shall search?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Sic psalmum dicam nómini tuo in sæculum sæculi: * ut reddam vota mea de die in diem.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>So will I sing a psalm to thy name for ever and ever: * that I may pay my vows from day to day.</p>
     </td>
    </tr>
   </table>
