     </td>
     <td:B1>
      <p:BodyEIndent>I have done judgment and justice: * give me not up to them that slander me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Súscipe servum tuum in bonum: * non calumniéntur me supérbi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Uphold thy servant unto good: * let not the proud calumniate me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Óculi mei defecérunt in salutáre tuum: * et in elóquium justítiæ tuæ.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>My eyes have fainted after thy salvation: * and for the word of thy justice.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Fac cum servo tuo secúndum misericórdiam tuam: * et justificatiónes tuas doce me.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Deal with thy servant according to thy mercy: * and teach me thy justifications.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Servus tuus sum ego: * da mihi intelléctum, ut sciam testimónia tua.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I am thy servant: * give me understanding that I may know thy testimonies.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Tempus faciéndi, Dómine: * dissipavérunt legem tuam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>It is time, O Lord, to do: * they have dissipated thy law.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ídeo diléxi mandáta tua, * super aurum et topázion.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Therefore have I loved thy commandments * above gold and the topaz.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Proptérea ad ómnia mandáta tua dirigébar: * omnem viam iníquam ódio hábui.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Therefore was I directed to all thy commandments: * I have hated all wicked ways.</p>
     </td>
    </tr>
