   <p:Head2Ps>Psalmus 118 viii. Iníquos ódio hábui</p>
   <p:Head4>Samech</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>INÍQUOS ódio hábui: * et legem tuam diléxi.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>I HAVE hated the unjust: * and have loved thy law.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Adjútor et suscéptor meus es tu: * et in verbum tuum supersperávi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou art my helper and my protector: * and in thy word I have greatly hoped.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Declináte a me, malígni: * et scrutábor mandáta Dei mei.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Depart from me, ye malignant: * and I will search the commandments of my God.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Súscipe me secúndum elóquium tuum, et vivam: * et non confúndas me ab exspectatióne mea.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Uphold me according to thy word, and I shall live: * and let me not be confounded in my expectation.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ádjuva me, et salvus ero: * et meditábor in justificatiónibus tuis semper.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Help me, and I shall be saved: * and I will meditate always on thy justifications.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Sprevísti omnes discedéntes a judíciis tuis: * quia injústa cogitátio eórum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou hast despised all them that fall off from thy judgments; * for their thought is unjust.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Prævaricántes reputávi omnes peccatóres terræ: * ídeo diléxi testimónia tua.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I have accounted all the sinners of the earth prevaricators: * therefore have I loved thy testimonies.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Confíge timóre tuo carnes meas: * a judíciis enim tuis tímui.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Pierce thou my flesh with thy fear: * for I am afraid of thy judgments.</p>
     </td>
    </tr>
   </table>
