   <p:Head2Ps>Psalmus 69. Deus, in adjutórium</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>DEUS, in adjutórium meum inténde: * Dómine, ad adjuvándum me festína.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>O GOD, come to my assistance; * O Lord, make haste to help me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Confundántur et revereántur, * qui quærunt ánimam meam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Let them be confounded and ashamed * that seek my soul:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Avertántur retrórsum, et erubéscant, * qui volunt mihi mala.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Let them be turned backward, and blush for shame * that desire evils to me:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Avertántur statim erubescéntes, * qui dicunt mihi: Euge, euge.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Let them be presently turned away blushing for shame * that say to me: ’T is well, ’t is well.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Exsúltent et læténtur in te omnes qui quærunt te, * et dicant semper: Magnificétur Dóminus: qui díligunt salutáre tuum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Let all that seek thee rejoice and be glad in thee; * and let such as love thy salvation say always: The Lord be magnified.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ego vero egénus, et pauper sum: * Deus, ádjuva me.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>But I am needy and poor; * O God, help me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Adjútor meus, et liberátor meus es tu: * Dómine, ne moréris.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou art my helper and my deliverer: * O Lord, make no delay.</p>
     </td>
    </tr>
   </table>
