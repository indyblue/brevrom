   <p:Head2Ps>Psalmus 73 i. Ut quid Deus</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>UT QUID, Deus, repulísti in finem: * irátus est furor tuus super oves páscuæ tuæ?</p>
     </td>
     <td:B1>
      <p:BodyEDrop>O GOD, why hast thou cast us off unto the end: * why is thy wrath enkindled against the sheep of thy pasture?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Memor esto congregatiónis tuæ, * quam possedísti ab inítio.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Remember thy congregation, * which thou hast possessed from the beginning.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Redemísti virgam hereditátis tuæ: * mons Sion, in quo habitásti in eo.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The sceptre of thy inheritance which thou hast redeemed: * mount Sion in which thou hast dwelt.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Leva manus tuas in supérbias eórum in finem: * quanta malignátus est inimícus in sancto!</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Lift up thy hands against their pride unto the end; * see what things the enemy hath done wickedly in the sanctuary.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et gloriáti sunt qui odérunt te: * in médio solemnitátis tuæ.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And they that hate thee have made their boasts, * in the midst of thy solemnity.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Posuérunt signa sua, signa: * et non cognovérunt sicut in éxitu super summum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>They have set up their ensigns for signs, * and they knew not both in the going out and on the highest top.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quasi in silva lignórum secúribus excidérunt jánuas ejus in idípsum: * in secúri et áscia dejecérunt eam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>As with axes in a wood of trees, they have cut down at once the gates thereof, * with axe and hatchet they have brought it down.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Incendérunt igni Sanctuárium tuum: * in terra polluérunt tabernáculum nóminis tui.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>They have set fire to thy sanctuary: * they have defiled the dwelling place of thy name on the earth.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Dixérunt in corde suo cognátio eórum simul: * Quiéscere faciámus omnes dies festos Dei a terra.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>They said in their heart, the whole kindred of them together: * Let us abolish all the festival days of God from the land.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Signa nostra non vídimus, jam non est prophéta: * et nos non cognóscet ámplius.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Our signs we have not seen, there is now no prophet: * and he will know us no more.</p>
     </td>
    </tr>
   </table>
