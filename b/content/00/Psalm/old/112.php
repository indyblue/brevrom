     </td>
     <td:B1>
      <p:BodyEDrop>PRAISE the Lord, ye children: * praise ye the name of the Lord.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Sit nomen Dómini benedíctum, * ex hoc nunc, ut usque in sæculum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Blessed be the name of the Lord, * from henceforth now and for ever.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>A solis ortu usque ad occásum, * laudábile nomen Dómini.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>From the rising of the sun unto the going down of the same, * the name of the Lord is worthy of praise.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Excélsus super omnes gentes Dóminus, * et super cælos glória ejus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The Lord is high above all nations; * and his glory above the heavens.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quis sicut Dóminus, Deus noster, qui in altis hábitat, * et humília réspicit in cælo et in terra?</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Who is as the Lord our God, who dwelleth on high: * and looketh down on the low things in heaven and in earth?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Súscitans a terra ínopem, * et de stércore érigens páuperem:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Raising up the needy from the earth, * and lifting up the poor out of the dunghill:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ut cóllocet eum cum princípibus, * cum princípibus pópuli sui.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>That he may place him with princes, * with the princes of his people.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Qui habitáre facit stérilem in domo, * matrem filiórum lætántem.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Who maketh a barren woman to dwell in a house, * the joyful mother of children.</p>
     </td>
    </tr>
   </table>
