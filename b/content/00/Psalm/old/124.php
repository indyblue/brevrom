   <p:Head2Ps>Psalmus 124. Qui confídunt</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>QUI confídunt in Dómino, sicut mons Sion: * non commovébitur in ætérnum, qui hábitat in Jerúsalem.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>THEY that trust in the Lord shall be as mount Sion: * he shall not be moved for ever that dwelleth in Jerusalem.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Montes in circúitu ejus: * et Dóminus in circúitu pópuli sui, ex hoc nunc et usque in sæculum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Mountains are round about it: * so the Lord is round about his people from henceforth now and for ever.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quia non relínquet Dóminus virgam peccatórum super sortem justórum: * ut non exténdant justi ad iniquitátem manus suas.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For the Lord will not leave the rod of sinners upon the lot of the just: * that the just may not stretch forth their hands to iniquity.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Bénefac, Dómine, bonis, * et rectis corde.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Do good, O Lord, to those that are good, * and to the upright of heart.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Declinántes autem in obligatiónes addúcet Dóminus cum operántibus iniquitátem: * pax super Israël.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>But such as turn aside into bonds, the Lord shall lead out with the workers of iniquity: * peace upon Israel.</p>
     </td>
    </tr>
