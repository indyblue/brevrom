   <p:Head2Ps>Psalmus 118 v. Bonitátem fecísti</p>
   <p:Head4>Teth</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>BONITÁTEM fecísti cum servo tuo, Dómine, * secúndum verbum tuum.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>THOU hast done well with thy servant, O Lord, * according to thy word.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Bonitátem, et disciplínam, et sciéntiam doce me: * quia mandátis tuis crédidi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Teach me goodness and discipline and knowledge; * for I have believed thy commandments.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Priúsquam humiliárer ego delíqui: * proptérea elóquium tuum custodívi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Before I was humbled I offended; * therefore have I kept thy word.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Bonus es tu: * et in bonitáte tua doce me justificatiónes tuas.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou art good; * and in thy goodness teach me thy justifications.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Multiplicáta est super me iníquitas superbórum: * ego autem in toto corde meo scrutábor mandáta tua.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The iniquity of the proud hath been multiplied over me: * but I will seek thy commandments with my whole heart.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Coagulátum est sicut lac cor eórum: * ego vero legem tuam meditátus sum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Their heart is curdled like milk: * but I have meditated on thy law.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Bonum mihi quia humiliásti me: * ut discam justificatiónes tuas.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>It is good for me that thou hast humbled me, * that I may learn thy justifications.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Bonum mihi lex oris tui, * super míllia auri et argénti.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The law of thy mouth is good to me, * above thousands of gold and silver.</p>
     </td>
    </tr>
   </table>
   <p:Head4>Jod</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLIndent>Manus tuæ fecérunt me, et plasmavérunt me: * da mihi intelléctum, et discam mandáta tua.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thy hands have made me and formed me: * give me understanding, and I will learn thy commandments.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Qui timent te vidébunt me, et lætabúntur: * quia in verba tua supersperávi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>They that fear thee shall see me, and shall be glad: * because I have greatly hoped in thy words.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Cognóvi, Dómine, quia æquitas judícia tua: * et in veritáte tua humiliásti me.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I know, O Lord, that thy judgments are equity: * and in thy truth thou hast humbled me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Fiat misericórdia tua ut consolétur me, * secúndum elóquium tuum servo tuo.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>O! let thy mercy be for my comfort, * according to thy word unto thy servant.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Véniant mihi miseratiónes tuæ, et vivam: * quia lex tua meditátio mea est.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Let thy tender mercies come unto me, and I shall live: * for thy law is my meditation.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Confundántur supérbi, quia injúste iniquitátem fecérunt in me: * ego autem exercébor in mandátis tuis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Let the proud be ashamed, because they have done unjustly towards me: * but I will be employed in thy commandments.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Convertántur mihi timéntes te: * et qui novérunt testimónia tua.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Let them that fear thee turn to me: * and they that know thy testimonies.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Fiat cor meum immaculátum in justificatiónibus tuis, * ut non confúndar.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Let my heart be undefiled in thy justifications, * that I may not be confounded.</p>
     </td>
    </tr>
   </table>
