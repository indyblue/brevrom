     </td>
     <td:B1>
      <p:BodyEDrop>BUT now thou hast cast us off, and put us to shame: * and thou, O God, wilt not go out with our armies.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Avertísti nos retrórsum post inimícos nostros: * et qui odérunt nos, diripiébant sibi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou hast made us turn our back to our enemies: * and they that hated us plundered for themselves.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Dedísti nos tamquam oves escárum: * et in géntibus dispersísti nos.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou hast given us up like sheep to be eaten: * thou hast scattered us among the nations.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Vendidísti pópulum tuum sine prétio: * et non fuit multitúdo in commutatiónibus eórum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou hast sold thy people for no price: * and there was no reckoning in the exchange of them.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Posuísti nos oppróbrium vicínis nostris, * subsannatiónem et derísum his, qui sunt in circúitu nostro.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou hast made us a reproach to our neighbours, * a scoff and derision to them that are round about us.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Posuísti nos in similitúdinem géntibus: * commotiónem cápitis in pópulis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou hast made us a byword among the Gentiles: * a shaking of the head among the people.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Tota die verecúndia mea contra me est, * et confúsio faciéi meæ coopéruit me.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>All the day long my shame is before me: * and the confusion of my face hath covered me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>A voce exprobrántis, et obloquéntis: * a fácie inimíci, et persequéntis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>At the voice of him that reproacheth and detracteth me: * at the face of the enemy and persecutor.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Hæc ómnia venérunt super nos, nec oblíti sumus te: * et iníque non égimus in testaménto tuo.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>All these things have come upon us, yet we have not forgotten thee: * and we have not done wickedly in thy covenant.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et non recéssit retro cor nostrum: * et declinásti sémitas nostras a via tua:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And our heart hath not turned back: * neither hast thou turned aside our steps from thy way.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quóniam humiliásti nos in loco afflictiónis, * et coopéruit nos umbra mortis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For thou hast humbled us in the place of affliction: * and the shadow of death hath covered us.</p>
     </td>
    </tr>
