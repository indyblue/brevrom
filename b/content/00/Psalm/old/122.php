   <p:Head2Ps>Psalmus 122. Ad te levávi</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>AD TE levávi óculos meos, * qui hábitas in cælis.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>TO THEE have I lifted up my eyes, * who dwellest in heaven.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ecce, sicut óculi servórum * in mánibus dominórum suórum,</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Behold as the eyes of servants * are on the hands of their masters,</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Sicut óculi ancíllæ in mánibus dóminæ suæ: * ita óculi nostri ad Dóminum, Deum nostrum, donec misereátur nostri.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>As the eyes of the handmaid are on the hands of her mistress: * so are our eyes unto the Lord our God, until he have mercy on us.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Miserére nostri, Dómine, miserére nostri: * quia multum repléti sumus despectióne:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Have mercy on us, O Lord, have mercy on us: * for we are greatly filled with contempt.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quia multum repléta est ánima nostra: * oppróbrium abundántibus, et despéctio supérbis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For our soul is greatly filled: * we are a reproach to the rich, and contempt to the proud.</p>
     </td>
    </tr>
   </table>
