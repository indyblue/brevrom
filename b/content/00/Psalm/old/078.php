   <p:Head2Ps>Psalmus 78. Deus, venérunt</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>DEUS, venérunt Gentes in hereditátem tuam, polluérunt templum sanctum tuum: * posuérunt Jerúsalem in pomórum custódiam.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>O GOD, the heathens are come into thy inheritance, they have defiled thy holy temple: * they have made Jerusalem as a place to keep fruit.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Posuérunt morticína servórum tuórum, escas volatílibus cæli: * carnes sanctórum tuórum béstiis terræ.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>They have given the dead bodies of thy servants to be meat for the fowls of the air: * the flesh of thy saints for the beasts of the earth.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Effudérunt sánguinem eórum tamquam aquam in circúitu Jerúsalem: * et non erat qui sepelíret.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>They have poured out their blood as water, round about Jerusalem, * and there was none to bury them.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Facti sumus oppróbrium vicínis nostris: * subsannátio et illúsio his, qui in circúitu nostro sunt.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>We are become a reproach to our neighbours: * a scorn and derision to them that are round about us.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Úsquequo, Dómine, irascéris in finem: * accendétur velut ignis zelus tuus?</p>
     </td>
     <td:B1>
      <p:BodyEIndent>How long, O Lord, wilt thou be angry for ever: * shall thy zeal be kindled like a fire?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Effúnde iram tuam in Gentes, quæ te non novérunt: * et in regna quæ nomen tuum non invocavérunt:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Pour out thy wrath upon the nations that have not known thee: * and upon the kingdoms that have not called upon thy name.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quia comedérunt Jacob: * et locum ejus desolavérunt.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Because they have devoured Jacob; * and have laid waste his place.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ne memíneris iniquitátum nostrárum antiquárum, cito antícipent nos misericórdiæ tuæ: * quia páuperes facti sumus nimis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Remember not our former iniquities: let thy mercies speedily prevent us, * for we are become exceeding poor.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ádjuva nos, Deus, salutáris noster: et propter glóriam nóminis tui, Dómine, líbera nos: * et propítius esto peccátis nostris, propter nomen tuum:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Help us, O God, our saviour: and for the glory of thy name, O Lord, deliver us: * and forgive us our sins for thy name&apos;s sake:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ne forte dicant in Géntibus: Ubi est Deus eórum? * et innotéscat in natiónibus coram óculis nostris.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Lest they should say among the Gentiles: Where is their God? * And let him be made known among the nations before our eyes,</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ultio sánguinis servórum tuórum, qui effúsus est: * intróeat in conspéctu tuo gémitus compeditórum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>By the revenging the blood of thy servants, which hath been shed: * let the sighing of the prisoners come in before thee.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Secúndum magnitúdinem bráchii tui, * pósside fílios mortificatórum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>According to the greatness of thy arm, take possession of the children of them that have been put to death.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et redde vicínis nostris séptuplum in sinu eórum: * impropérium ipsórum, quod exprobravérunt tibi, Dómine.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And render to our neighbours sevenfold in their bosom: the reproach wherewith they have reproached thee, O Lord.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Nos autem pópulus tuus, et oves páscuæ tuæ, * confitébimur tibi in sæculum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>But we thy people, and the sheep of thy pasture, * will give thanks to thee for ever.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>In generatiónem et generatiónem * annuntiábimus laudem tuam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>We will shew forth thy praise, * unto generation and generation.</p>
     </td>
    </tr>
