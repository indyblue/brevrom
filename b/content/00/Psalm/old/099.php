   <p:Head2Ps>Psalmus 99. Jubiláte</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>JUBILÁTE Deo, omnis terra: * servíte Dómino in lætítia.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>SING joyfully to God, all the earth: * serve ye the Lord with gladness.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Introíte in conspéctu ejus, * in exsultatióne.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Come in before his presence * with exceeding great joy.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Scitóte quóniam Dóminus ipse est Deus: * ipse fecit nos, et non ipsi nos.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Know ye that the Lord he is God: * he made us, and not we ourselves.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Pópulus ejus, et oves páscuæ ejus: * introíte portas ejus in confessióne, átria ejus in hymnis: confitémini illi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>We are his people and the sheep of his pasture. * Go ye into his gates with praise, into his courts with hymns: and give glory to him.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Laudáte nomen ejus: quóniam suávis est Dóminus, in ætérnum misericórdia ejus, * et usque in generatiónem et generatiónem véritas ejus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Praise ye his name: for the Lord is sweet, his mercy endureth for ever, * and his truth to generation and generation.</p>
     </td>
    </tr>
