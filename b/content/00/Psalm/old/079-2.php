   <p:Head2Ps>Psalmus 79 ii. Víneam de Ægýpto</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>VÍNEAM de Ægýpto transtulísti: * ejecísti gentes, et plantásti eam.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>THOU hast brought a vineyard out of Egypt: * thou hast cast out the Gentiles and planted it.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Dux itíneris fuísti in conspéctu ejus: * plantásti radíces ejus, et implévit terram.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou wast the guide of its journey in its sight: * thou plantedst the roots thereof, and it filled the land.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Opéruit montes umbra ejus: * et arbústa ejus cedros Dei.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The shadow of it covered the hills: * and the branches thereof the cedars of God.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Exténdit pálmites suos usque ad mare: * et usque ad flumen propágines ejus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>It stretched forth its branches unto the sea, * and its boughs unto the river.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ut quid destruxísti macériam ejus: * et vindémiant eam omnes, qui prætergrediúntur viam?</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Why hast thou broken down the hedge thereof, * so that all they who pass by the way do pluck it?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Exterminávit eam aper de silva: * et singuláris ferus depástus est eam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The boar out of the wood hath laid it waste: * and a singular wild beast hath devoured it.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Deus virtútum, convértere: * réspice de cælo, et vide, et vísita víneam istam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Turn again, O God of hosts, * look down from heaven, and see, and visit this vineyard:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et pérfice eam, quam plantávit déxtera tua: * et super fílium hóminis, quem confirmásti tibi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And perfect the same which thy right hand hath planted: * and upon the son of man whom thou hast confirmed for thyself.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Incénsa igni, et suffóssa * ab increpatióne vultus tui períbunt.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Things set on fire and dug down * shall perish at the rebuke of thy countenance.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Fiat manus tua super virum déxteræ tuæ: * et super fílium hóminis, quem confirmásti tibi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Let thy hand be upon the man of thy right hand: * and upon the son of man whom thou hast confirmed for thyself.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et non discédimus a te, vivificábis nos: * et nomen tuum invocábimus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And we depart not from thee, thou shalt quicken us: * and we will call upon thy name.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Dómine, Deus virtútum, convérte nos: * et osténde fáciem tuam, et salvi érimus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>O Lord God of hosts, convert us: * and shew thy face, and we shall be saved.</p>
     </td>
    </tr>
   </table>
