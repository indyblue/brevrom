     </td>
     <td:B1>
      <p:BodyEDrop>IN THEE, O Lord, have I hoped, let me never be confounded: * deliver me in thy justice.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Inclína ad me aurem tuam, * accélera ut éruas me.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Bow down thy ear to me: * make haste to deliver me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Esto mihi in Deum protectórem, et in domum refúgii: * ut salvum me fácias.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Be thou unto me a God, a protector, and a house of refuge, * to save me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quóniam fortitúdo mea, et refúgium meum es tu: * et propter nomen tuum dedúces me, et enútries me.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For thou art my strength and my refuge; * and for thy name’s sake thou wilt lead me, and nourish me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Edúces me de láqueo hoc, quem abscondérunt mihi: * quóniam tu es protéctor meus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou wilt bring me out of this snare, which they have hidden for me: * for thou art my protector.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>In manus tuas comméndo spíritum meum: * redemísti me, Dómine, Deus veritátis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Into thy hands I commend my spirit: * thou hast redeemed me, O Lord, the God of truth.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Odísti observántes vanitátes, * supervácue.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou hast hated them that regard vanities, * to no purpose.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ego autem in Dómino sperávi: * exsultábo, et lætábor in misericórdia tua.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>But I have hoped in the Lord: * I will be glad and rejoice in thy mercy.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quóniam respexísti humilitátem meam, * salvásti de necessitátibus ánimam meam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For thou hast regarded my humility, * thou hast saved my soul out of distresses.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Nec conclusísti me in mánibus inimíci: * statuísti in loco spatióso pedes meos.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And thou hast not shut me up in the hands of the enemy: * thou hast set my feet in a spacious place.</p>
     </td>
    </tr>
