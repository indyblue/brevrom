   <p:Head2Ps>Psalmus 14. Dómine, quis habitábit</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>DÓMINE, quis habitábit in tabernáculo tuo? * aut quis requiéscet in monte sancto tuo?</p>
     </td>
     <td:B1>
      <p:BodyEDrop>LORD, who shall dwell in thy tabernacle? * or who shall rest in thy holy hill?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Qui ingréditur sine mácula, * et operátur justítiam:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He that walketh without blemish, * and worketh justice:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Qui lóquitur veritátem in corde suo, * qui non egit dolum in lingua sua:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He that speaketh truth in his heart, * who hath not used deceit in his tongue:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Nec fecit próximo suo malum, * et oppróbrium non accépit advérsus próximos suos.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Nor hath done evil to his neighbour: * nor taken up a reproach against his neighbours.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ad níhilum dedúctus est in conspéctu ejus malígnus: * timéntes autem Dóminum gloríficat:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>In his sight the malignant is brought to nothing: * but he glorifieth them that fear the Lord:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Qui jurat próximo suo, et non décipit, * qui pecúniam suam non dedit ad usúram, et múnera super innocéntem non accépit.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He that sweareth to his neighbour, and deceiveth not; * He that hath not put out his money to usury, nor taken bribes against the innocent:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Qui facit hæc: * non movébitur in ætérnum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He that doth these things, * shall not be moved for ever.</p>
     </td>
    </tr>
   </table>
