      <p:BodyLDrop>MEMÉNTO, Dómine, David, * et omnis mansuetúdinis ejus:</p>
     </td>
     <td:B1>
      <p:BodyEDrop>O LORD, remember David, * and all his meekness.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Sicut jurávit Dómino, * votum vovit Deo Jacob:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>How he swore to the Lord, * he vowed a vow to the God of Jacob:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Si introíero in tabernáculum domus meæ, * si ascéndero in lectum strati mei:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>If I shall enter into the tabernacle of my house: * if I shall go up into the bed wherein I lie:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Si dédero somnum óculis meis, * et pálpebris meis dormitatiónem:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>If I shall give sleep to my eyes, * or slumber to my eyelids,</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et réquiem tempóribus meis: donec invéniam locum Dómino, * tabernáculum Deo Jacob.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Or rest to my temples: until I find out a place for the Lord, * a tabernacle for the God of Jacob.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ecce, audívimus eam in Éphrata: * invénimus eam in campis silvæ.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Behold we have heard of it in Ephrata: * we have found it in the fields of the wood.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Introíbimus in tabernáculum ejus: * adorábimus in loco, ubi stetérunt pedes ejus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>We will go into his tabernacle: * we will adore in the place where his feet stood.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Surge, Dómine, in réquiem tuam, * tu et arca sanctificatiónis tuæ.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Arise, O Lord, into thy resting place: * thou and the ark, which thou hast sanctified.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Sacerdótes tui induántur justítiam: * et sancti tui exsúltent.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Let thy priests be clothed with justice: * and let thy saints rejoice.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Propter David, servum tuum, * non avértas fáciem Christi tui.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For thy servant David’s sake, * turn not away the face of thy anointed.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Jurávit Dóminus David veritátem, et non frustrábitur eam: * De fructu ventris tui ponam super sedem tuam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The Lord hath sworn truth to David, and he will not make it void: * of the fruit of thy womb I will set upon thy throne.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Si custodíerint fílii tui testaméntum meum, * et testimónia mea hæc, quæ docébo eos:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>If thy children will keep my covenant, * and these my testimonies which I shall teach them:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et fílii eórum usque in sæculum, * sedébunt super sedem tuam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Their children also for evermore * shall sit upon thy throne.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quóniam elégit Dóminus Sion: * elégit eam in habitatiónem sibi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For the Lord hath chosen Sion: * he hath chosen it for his dwelling.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Hæc réquies mea in sæculum sæculi: * hic habitábo quóniam elégi eam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>This is my rest for ever and ever: * here will I dwell, for I have chosen it.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Víduam ejus benedícens benedícam: * páuperes ejus saturábo pánibus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Blessing I will bless her widow: * I will satisfy her poor with bread.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Sacerdótes ejus índuam salutári: * et sancti ejus exsultatióne exsultábunt.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I will clothe her priests with salvation: * and her saints shall rejoice with exceeding great joy.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Illuc prodúcam cornu David, * parávi lucérnam Christo meo.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>There will I bring forth a horn to David: * I have prepared a lamp for my anointed.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Inimícos ejus índuam confusióne: * super ipsum autem efflorébit sanctificátio mea.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>His enemies I will clothe with confusion: * but upon him shall my sanctification flourish.</p>
     </td>
    </tr>
   </table>
