   <p:Head2Ps>Psalmus 59. Deus, repulísti nos</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>DEUS, repulísti nos, et destruxísti nos: * irátus es, et misértus es nobis.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>O GOD, thou hast cast us off, and hast destroyed us; * thou hast been angry, and hast had mercy on us.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Commovísti terram, et conturbásti eam: * sana contritiónes ejus, quia commóta est.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou hast moved the earth, and hast troubled it: * heal thou the breaches thereof, for it has been moved.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ostendísti pópulo tuo dura: * potásti nos vino compunctiónis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou hast shewn thy people hard things; * thou hast made us drink the wine of sorrow.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Dedísti metuéntibus te significatiónem: * ut fúgiant a fácie arcus:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou hast given a warning to them that fear thee: * that they may flee from before the bow:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ut liberéntur dilécti tui: * salvum fac déxtera tua, et exáudi me.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>That thy beloved may be delivered. * Save me with thy right hand, and hear me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Deus locútus est in sancto suo: * lætábor, et partíbor Síchimam: et convállem tabernaculórum metíbor.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>God hath spoken in his holy place: * I will rejoice, and I will divide Sichem; and will mete out the vale of tabernacles.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Meus est Gálaad, et meus est Manásses: * et Éphraim fortitúdo cápitis mei.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Galaad is mine, and Manasses is mine: * and Ephraim is the strength of my head.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Juda rex meus: * Moab olla spei meæ.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Juda is my king: * Moab is the pot of my hope.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>In Idumæam exténdam calceaméntum meum: * mihi alienígenæ súbditi sunt.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Into Edom will I stretch out my shoe: * to me the foreigners are made subject.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quis dedúcet me in civitátem munítam? * quis dedúcet me usque in Idumæam?</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Who will bring me into the strong city? * who will lead me into Edom?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Nonne tu, Deus, qui repulísti nos? * et non egrediéris, Deus, in virtútibus nostris?</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Wilt not thou, O God, who hast cast us off? * and wilt not thou, O God, go out with our armies?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Da nobis auxílium de tribulatióne: * quia vana salus hóminis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Give us help from trouble: * for vain is the salvation of man.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>In Deo faciémus virtútem: * et ipse ad níhilum dedúcet tribulántes nos.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Through God we shall do mightily: * and he shall bring to nothing them that afflict us.</p>
     </td>
    </tr>
