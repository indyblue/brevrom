   <p:Head2Ps>Psalmus 38. Dixi, Custódiam</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>DIXI: Custódiam vias meas: * ut non delínquam in lingua mea.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>I SAID: I will take heed to my ways: * that I sin not with my tongue.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Pósui ori meo custódiam, * cum consísteret peccátor advérsum me.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I have set guard to my mouth, * when the sinner stood against me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Obmútui, et humiliátus sum, et sílui a bonis: * et dolor meus renovátus est.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I was dumb, and was humbled, and kept silence from good things: * and my sorrow was renewed.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Concáluit cor meum intra me: * et in meditatióne mea exardéscet ignis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>My heart grew hot within me: * and in my meditation a fire shall flame out.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Locútus sum in lingua mea: * Notum fac mihi, Dómine, finem meum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I spoke with my tongue: * O Lord, make me know my end.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et númerum diérum meórum quis est: * ut sciam quid desit mihi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And what is the number of my days: * that I may know what is wanting to me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ecce mensurábiles posuísti dies meos: * et substántia mea tamquam níhilum ante te.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Behold thou hast made my days measurable: * and my substance is as nothing before thee.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Verúmtamen univérsa vánitas, * omnis homo vivens.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And indeed all things are vanity: * every man living.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Verúmtamen in imágine pertránsit homo: * sed et frustra conturbátur.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Surely man passeth as an image: * yea, and he is disquieted in vain.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Thesaurízat: * et ignórat cui congregábit ea.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He storeth up: * and he knoweth not for whom he shall gather these things.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et nunc quæ est exspectátio mea? Nonne Dóminus? * Et substántia mea apud te est.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And now what is my hope? Is it not the Lord? * And my substance is with thee.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ab ómnibus iniquitátibus meis érue me: * oppróbrium insipiénti dedísti me.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Deliver thou me from all my iniquities: * thou hast made me a reproach to the fool.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Obmútui, et non apérui os meum, quóniam tu fecísti: * ámove a me plagas tuas.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I was dumb, and I opened not my mouth, because thou hast done it: * remove thy scourges from me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>A fortitúdine manus tuæ ego deféci in increpatiónibus: * propter iniquitátem corripuísti hóminem.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The strength of thy hand hath made me faint in rebukes: * thou hast corrected man for iniquity.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et tabéscere fecísti sicut aráneam ánimam ejus: * verúmtamen vane conturbátur omnis homo.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And thou hast made his soul to waste away like a spider: * surely in vain is any man disquieted.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Exáudi oratiónem meam, Dómine, et deprecatiónem meam: * áuribus pércipe lácrimas meas.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Hear my prayer, O Lord, and my supplication: * give ear to my tears.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ne síleas: quóniam ádvena ego sum apud te, et peregrínus, * sicut omnes patres mei.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Be not silent: for I am a stranger with thee, * and a sojourner as all my fathers were.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Remítte mihi, ut refrígerer priúsquam ábeam, * et ámplius non ero.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>O forgive me, that I may be refreshed, before I go hence, * and be no more.</p>
     </td>
    </tr>
