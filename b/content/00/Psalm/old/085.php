   <p:Head2Ps>Psalmus 85. Inclína, Dómine</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>INCLÍNA, Dómine, aurem tuam, et exáudi me: * quóniam inops, et pauper sum ego.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>INCLINE thy ear, O Lord, and hear me: * for I am needy and poor.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Custódi ánimam meam, quóniam sanctus sum: * salvum fac servum tuum, Deus meus, sperántem in te.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Preserve my soul, for I am holy: * save thy servant, O my God, that trusteth in thee.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Miserére mei, Dómine, quóniam ad te clamávi tota die: * lætífica ánimam servi tui, quóniam ad te, Dómine, ánimam meam levávi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Have mercy on me, O Lord, for I have cried to thee all the day. * Give joy to the soul of thy servant, for to thee, O Lord, I have lifted up my soul.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quóniam tu, Dómine, suávis, et mitis: * et multæ misericórdiæ ómnibus invocántibus te.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For thou, O Lord, art sweet and mild: * and plenteous in mercy to all that call upon thee.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Áuribus pércipe, Dómine, oratiónem meam: * et inténde voci deprecatiónis meæ.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Give ear, O Lord, to my prayer: * and attend to the voice of my petition.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>In die tribulatiónis meæ clamávi ad te: * quia exaudísti me.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I have called upon thee in the day of my trouble: * because thou hast heard me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Non est símilis tui in diis, Dómine: * et non est secúndum ópera tua.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>There is none among the gods like unto thee, O Lord: * and there is none according to thy works.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Omnes gentes quascúmque fecísti, vénient, et adorábunt coram te, Dómine: * et glorificábunt nomen tuum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>All the nations thou hast made shall come and adore before thee, O Lord: * and they shall glorify thy name.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quóniam magnus es tu, et fáciens mirabília: * tu es Deus solus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For thou art great and dost wonderful things: * thou art God alone.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Deduc me, Dómine, in via tua, et ingrédiar in veritáte tua: * lætétur cor meum ut tímeat nomen tuum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Conduct me, O Lord, in thy way, and I will walk in thy truth: * let my heart rejoice that it may fear thy name.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Confitébor tibi, Dómine, Deus meus, in toto corde meo, * et glorificábo nomen tuum in ætérnum:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I will praise thee, O Lord my God, with my whole heart, * and I will glorify thy name for ever:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quia misericórdia tua magna est super me: * et eruísti ánimam meam ex inférno inferióri.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For thy mercy is great towards me: * and thou hast delivered my soul out of the lower hell.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Deus, iníqui insurrexérunt super me, et synagóga poténtium quæsiérunt ánimam meam: * et non proposuérunt te in conspéctu suo.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>O God, the wicked are risen up against me, and the assembly of the mighty have sought my soul: * and they have not set thee before their eyes.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et tu, Dómine, Deus miserátor et miséricors, * pátiens, et multæ misericórdiæ, et verax,</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And thou, O Lord, art a God of compassion, and merciful, * patient, and of much mercy, and true.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Réspice in me, et miserére mei, * da impérium tuum púero tuo: et salvum fac fílium ancíllæ tuæ.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>O look upon me, and have mercy on me: * give thy command to thy servant, and save the son of thy handmaid.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Fac mecum signum in bonum, ut vídeant qui odérunt me, et confundántur: * quóniam tu, Dómine, adjuvísti me, et consolátus es me.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Shew me a token for good: that they who hate me may see, and be confounded, * because thou, O Lord, hast helped me and hast comforted me.</p>
     </td>
    </tr>
   </table>
