	<p:Head2Ps>Psalmus 76 i. Voce mea</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>VOCE mea ad Dóminum clamávi: * voce mea ad Deum, et inténdit mihi.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>I CRIED to the Lord with my voice; * to God with my voice, and he gave ear to me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>In die tribulatiónis meæ Deum exquisívi, mánibus meis nocte contra eum: * et non sum decéptus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>In the day of my trouble I sought God, with my hands lifted up to him in the night, * and I was not deceived.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Rénuit consolári ánima mea, * memor fui Dei, et delectátus sum, et exercitátus sum: et defécit spíritus meus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>My soul refused to be comforted: * I remembered God, and was delighted, and was exercised, and my spirit swooned away.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Anticipavérunt vigílias óculi mei: * turbátus sum, et non sum locútus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>My eyes prevented the watches: * I was troubled, and I spoke not.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Cogitávi dies antíquos: * et annos ætérnos in mente hábui.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I thought upon the days of old: * and I had in my mind the eternal years.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et meditátus sum nocte cum corde meo, * et exercitábar, et scopébam spíritum meum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And I meditated in the night with my own heart: * and I was exercised and I swept my spirit.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Numquid in ætérnum projíciet Deus: * aut non appónet ut complacítior sit adhuc?</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Will God then cast off for ever? * or will he never be more favourable again?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Aut in finem misericórdiam suam abscíndet, * a generatióne in generatiónem?</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Or will he cut off his mercy for ever, * from generation to generation?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Aut obliviscétur miseréri Deus? * aut continébit in ira sua misericórdias suas?</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Or will God forget to shew mercy? * or will he in his anger shut up his mercies?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et dixi: Nunc cœpi: * hæc mutátio déxteræ Excélsi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And I said, Now have I begun: * this is the change of the right hand of the most High.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Memor fui óperum Dómini: * quia memor ero ab inítio mirabílium tuórum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I remembered the works of the Lord: * for I will be mindful of thy wonders from the beginning.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et meditábor in ómnibus opéribus tuis: * et in adinventiónibus tuis exercébor.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And I will meditate on all thy works: * and will be employed in thy inventions.</p>
     </td>
    </tr>
