   <p:Head2Ps>Psalmus 70 ii. Confundántur</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>CONFUNDÁNTUR, et defíciant detrahéntes ánimæ meæ: * operiántur confusióne, et pudóre qui quærunt mala mihi.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>LET them be confounded and come to nothing that detract my soul; * let them be covered with confusion and shame that seek my hurt.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ego autem semper sperábo: * et adjíciam super omnem laudem tuam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>But I will always hope; * and will add to all thy praise.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Os meum annuntiábit justítiam tuam: * tota die salutáre tuum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>My mouth shall shew forth thy justice; * thy salvation all the day long.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quóniam non cognóvi litteratúram, introíbo in poténtias Dómini: * Dómine, memorábor justítiæ tuæ solíus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Because I have not known learning, I will enter into the powers of the Lord: * O Lord, I will be mindful of thy justice alone.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Deus, docuísti me a juventúte mea: * et usque nunc pronuntiábo mirabília tua.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou hast taught me, O God, from my youth: * and till now I will declare thy wonderful works.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et usque in senéctam et sénium: * Deus, ne derelínquas me,</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And unto old age and grey hairs: * O God, forsake me not,</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Donec annúntiem bráchium tuum * generatióni omni, quæ ventúra est:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Until I shew forth thy arm * to all the generation that is to come:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Poténtiam tuam, et justítiam tuam, Deus, usque in altíssima, quæ fecísti magnália: * Deus, quis símilis tibi?</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thy power, and thy justice, O God, even to the highest great things thou hast done: * O God, who is like to thee?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quantas ostendísti mihi tribulatiónes multas et malas: et convérsus vivificásti me: * et de abýssis terræ íterum reduxísti me:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>How great troubles hast thou shewn me, many and grievous: and turning thou hast brought me to life, * and hast brought me back again from the depths of the earth:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Multiplicásti magnificéntiam tuam: * et convérsus consolátus es me.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou hast multiplied thy magnificence; * and turning to me thou hast comforted me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Nam et ego confitébor tibi in vasis psalmi veritátem tuam: * Deus, psallam tibi in cíthara, Sanctus Israël.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For I will also confess to thee thy truth with the instruments of psaltery: * O God, I will sing to thee with the harp, thou holy one of Israel.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Exsultábunt lábia mea cum cantávero tibi: * et ánima mea, quam redemísti.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>My lips shall greatly rejoice, when I shall sing to thee; * and my soul which thou hast redeemed.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Sed et lingua mea tota die meditábitur justítiam tuam: * cum confúsi et revériti fúerint, qui quærunt mala mihi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Yea and my tongue shall meditate on thy justice all the day; * when they shall be confounded and put to shame that seek evils to me.</p>
     </td>
    </tr>
