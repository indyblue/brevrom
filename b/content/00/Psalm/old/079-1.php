   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>QUI regis Israël, inténde: * qui dedúcis velut ovem Joseph.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>GIVE ear, O thou that rulest Israel: * thou that leadest Joseph like a sheep.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Qui sedes super Chérubim, * manifestáre coram Éphraim, Bénjamin, et Manásse.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou that sittest upon the cherubims, * shine forth before Ephraim, Benjamin, and Manasses.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Éxcita poténtiam tuam, et veni, * ut salvos fácias nos.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Stir up thy might, and come * to save us.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Deus, convérte nos: * et osténde fáciem tuam, et salvi érimus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Convert us, O God: * and shew us thy face, and we shall be saved.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Dómine, Deus virtútum, * quoúsque irascéris super oratiónem servi tui?</p>
     </td>
     <td:B1>
      <p:BodyEIndent>O Lord God of hosts, * how long wilt thou be angry against the prayer of thy servant?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Cibábis nos pane lacrimárum: * et potum dabis nobis in lácrimis in mensúra?</p>
     </td>
     <td:B1>
      <p:BodyEIndent>How long wilt thou feed us with the bread of tears: * and give us for our drink tears in measure?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Posuísti nos in contradictiónem vicínis nostris: * et inimíci nostri subsannavérunt nos.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou hast made us to be a contradiction to our neighbours: * and our enemies have scoffed at us.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Deus virtútum, convérte nos: * et osténde fáciem tuam, et salvi érimus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>O God of hosts, convert us: * and shew thy face, and we shall be saved.</p>
     </td>
    </tr>
