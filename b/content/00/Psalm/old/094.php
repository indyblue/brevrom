     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quóniam Deus magnus Dóminus, et Rex magnus super omnes deos: quóniam non repéllet Dóminus plebem suam: quia in manu ejus sunt omnes fines terræ, et altitúdines móntium ipse cónspicit.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For the Lord is a great God, and a great King above all gods: for the Lord will not cast off his people: for in his hand are all the ends of the earth, and he beholds the heights of the mountains.</p>
     </td>
    </tr>
    <tr>
