      <p:BodyLIndent>Hódie, si vocem ejus audiéritis, nolíte obduráre corda vestra, sicut in exacerbatióne, secúndum diem tentatiónis in desérto: ubi tentavérunt me patres vestri, probavérunt et vidérunt ópera mea.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>To day if you shall hear his voice, harden not your hearts, as in the provocation, according to the day of temptation in the desert: where your fathers tempted me, they proved and saw my works.</p>
