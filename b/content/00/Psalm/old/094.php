     </td>
     <td:B1>
      <p:BodyE><sr>The proper Invitatory is said, which is recited twice in its entirety before the Psalm.</s></p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLDrop>VENÍTE, exsultémus Dómino, jubilémus Deo, salutári nostro: præoccupémus fáciem ejus in confessióne, et in psalmis jubilémus ei.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>COME let us praise the Lord with joy, let us joyfully sing to God, our saviour: let us come before his presence with thanksgiving, and make a joyful noise to him with psalms.</p>
     </td>
    </tr>
    <tr>
