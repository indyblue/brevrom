     </td>
     <td:B1>
      <p:BodyE><sr>At the words </s>Come let us adore and fall down before God<sr>, all genuflect.</s></p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quóniam ipsíus est mare, et ipse fecit illud, et áridam fundavérunt manus ejus: VENÍTE, ADORÉMUS, ET PROCIDÁMUS ANTE DEUM: plorémus coram Dómino, qui fecit nos, quia ipse est Dóminus Deus noster; nos autem pópulus ejus, et oves páscuæ ejus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For the sea is his, and he made it: and his hands formed the dry land. COME, LET US ADORE, AND FALL DOWN BEFORE GOD: let us weep before the Lord that made us. For he is the Lord our God: and we are his people, and the sheep of his pasture.</p>
     </td>
