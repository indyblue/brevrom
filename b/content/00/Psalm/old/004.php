   <p:Head2Ps>Psalmus 4. Cum invocárem</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>CUM invocárem exaudívit me Deus justítiæ meæ: * in tribulatióne dilatásti mihi.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>WHEN I called upon him, the God of my justice heard me: * when I was in distress, thou hast enlarged me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Miserére mei, * et exáudi oratiónem meam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Have mercy on me: * and hear my prayer.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Fílii hóminum, úsquequo gravi corde? * ut quid dilígitis vanitátem, et quæritis mendácium?</p>
     </td>
     <td:B1>
      <p:BodyEIndent>O ye sons of men, how long will you be dull of heart? * Why do you love vanity, and seek after lying?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et scitóte quóniam mirificávit Dóminus sanctum suum: * Dóminus exáudiet me cum clamávero ad eum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Know ye also that the Lord hath made his holy one wonderful: * the Lord will hear me when I shall cry unto him.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Irascímini, et nolíte peccáre: * quæ dícitis in córdibus vestris, in cubílibus vestris compungímini.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Be ye angry, and sin not: * the things you say in your hearts, be sorry for them upon your beds.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Sacrificáte sacrifícium justítiæ, et speráte in Dómino. * Multi dicunt: Quis osténdit nobis bona?</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Offer up the sacrifice of justice, and trust in the Lord: * many say, Who sheweth us good things?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Signátum est super nos lumen vultus tui, Dómine: * dedísti lætítiam in corde meo.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The light of thy countenance, O Lord, is signed upon us: * thou hast given gladness in my heart.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>A fructu fruménti, vini, et ólei sui * multiplicáti sunt.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>By the fruit of their corn, their wine, and oil, * they are multiplied.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>In pace in idípsum * dórmiam, et requiéscam;</p>
     </td>
     <td:B1>
      <p:BodyEIndent>In peace in the selfsame * I will sleep, and I will rest:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quóniam tu, Dómine, singuláriter in spe * constituísti me.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For thou, O Lord, singularly * hast settled me in hope.</p>
     </td>
    </tr>
