   <p:Head2Ps>Psalmus 136. Super flúmina</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>SUPER flúmina Babylónis, illic sédimus et flévimus: * cum recordarémur Sion:</p>
     </td>
     <td:B1>
      <p:BodyEDrop>UPON the rivers of Babylon, there we sat and wept: * when we remembered Sion:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>In salícibus in médio ejus, * suspéndimus órgana nostra.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>On the willows in the midst thereof * we hung up our instruments.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quia illic interrogavérunt nos, qui captívos duxérunt nos, * verba cantiónum:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For there they that led us into captivity required of us * the words of songs.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et qui abduxérunt nos: * Hymnum cantáte nobis de cánticis Sion.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And they that carried us away, said: * Sing ye to us a hymn of the songs of Sion.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quómodo cantábimus cánticum Dómini * in terra aliéna?</p>
     </td>
     <td:B1>
      <p:BodyEIndent>How shall we sing the song of the Lord * in a strange land?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Si oblítus fúero tui, Jerúsalem, * oblivióni detur déxtera mea.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>If I forget thee, O Jerusalem, * let my right hand be forgotten.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Adhæreat lingua mea fáucibus meis, * si non memínero tui:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Let my tongue cleave to my jaws, * if I do not remember thee:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Si non proposúero Jerúsalem, * in princípio lætítiæ meæ.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>If I make not Jerusalem * the beginning of my joy.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Memor esto, Dómine, filiórum Edom, * in die Jerúsalem:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Remember, O Lord, the children of Edom, * in the day of Jerusalem:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Qui dicunt: Exinaníte, exinaníte * usque ad fundaméntum in ea.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Who say: Rase it, rase it, * even to the foundation thereof.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Fília Babylónis mísera: * beátus, qui retríbuet tibi retributiónem tuam, quam retribuísti nobis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>O daughter of Babylon, miserable: * blessed shall he be who shall repay thee thy payment which thou hast paid us.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Beátus, qui tenébit, * et allídet párvulos tuos ad petram.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Blessed be he that shall take * and dash thy little ones against the rock.</p>
     </td>
    </tr>
   </table>
