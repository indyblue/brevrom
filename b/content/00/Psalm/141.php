   <p:Head2Ps>Psalmus 141. Voce mea ad Dóminum</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>VOCE mea ad Dóminum clamávi: * voce mea ad Dóminum deprecátus sum:</p>
     </td>
     <td:B1>
      <p:BodyEDrop>I CRIED to the Lord with my voice: * with my voice I made supplication to the Lord.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Effúndo in conspéctu ejus oratiónem meam, * et tribulatiónem meam ante ipsum pronúntio.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>In his sight I pour out my prayer, * and before him I declare my trouble:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>In deficiéndo ex me spíritum meum, * et tu cognovísti sémitas meas.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>When my spirit failed me, * then thou knewest my paths.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>In via hac, qua ambulábam, * abscondérunt láqueum mihi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>In this way wherein I walked, * they have hidden a snare for me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Considerábam ad déxteram, et vidébam: * et non erat qui cognósceret me.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I looked on my right hand, and beheld, * and there was no one that would know me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Périit fuga a me, * et non est qui requírat ánimam meam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Flight hath failed me: * and there is no one that hath regard to my soul.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Clamávi ad te, Dómine, * dixi: Tu es spes mea, pórtio mea in terra vivéntium.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I cried to thee, O Lord: * I said: Thou art my hope, my portion in the land of the living.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Inténde ad deprecatiónem meam: * quia humiliátus sum nimis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Attend to my supplication: * for I am brought very low.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Líbera me a persequéntibus me: * quia confortáti sunt super me.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Deliver me from my persecutors; * for they are stronger than I.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Educ de custódia ánimam meam ad confiténdum nómini tuo: * me exspéctant justi, donec retríbuas mihi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Bring my soul out of prison, that I may praise thy name: * the just wait for me, until thou reward me.</p>
     </td>
    </tr>
   </table>
