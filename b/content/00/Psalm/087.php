   <p:Head2Ps>Psalmus 87. Dómine, Deus</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>DÓMINE, Deus salútis meæ: * in die clamávi, et nocte coram te.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>O LORD, the God of my salvation: * I have cried in the day, and in the night before thee.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Intret in conspéctu tuo orátio mea: * inclína aurem tuam ad precem meam:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Let my prayer come in before thee: * incline thy ear to my petition.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quia repléta est malis ánima mea: * et vita mea inférno appropinquávit.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For my soul is filled with evils: * and my life hath drawn nigh to hell.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Æstimátus sum cum descendéntibus in lacum: * factus sum sicut homo sine adjutório, inter mórtuos liber.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I am counted among them that go down to the pit: * I am become as a man without help, free among the dead.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Sicut vulneráti dormiéntes in sepúlcris, quorum non es memor ámplius: * et ipsi de manu tua repúlsi sunt.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Like the slain sleeping in the sepulchres, whom thou rememberest no more: * and they are cast off from thy hand.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Posuérunt me in lacu inferióri: * in tenebrósis, et in umbra mortis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>They have laid me in the lower pit: * in the dark places, and in the shadow of death.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Super me confirmátus est furor tuus: * et omnes fluctus tuos induxísti super me.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thy wrath is strong over me: * and all thy waves thou hast brought in upon me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Longe fecísti notos meos a me: * posuérunt me abominatiónem sibi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou hast put away my acquaintance far from me: * they have set me an abomination to themselves.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Tráditus sum, et non egrediébar: * óculi mei languérunt præ inópia.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I was delivered up, and came not forth: * my eyes languished through poverty.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Clamávi ad te, Dómine, tota die: * expándi ad te manus meas.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>All the day I cried to thee, O Lord: * I stretched out my hands to thee.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Numquid mórtuis fácies mirabília: * aut médici suscitábunt, et confitebúntur tibi?</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Wilt thou shew wonders to the dead? * or shall physicians raise to life, and give praise to thee?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Numquid narrábit áliquis in sepúlcro misericórdiam tuam, * et veritátem tuam in perditióne?</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Shall any one in the sepulchre declare thy mercy: * and thy truth in destruction?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Numquid cognoscéntur in ténebris mirabília tua, * et justítia tua in terra obliviónis?</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Shall thy wonders be known in the dark; * and thy justice in the land of forgetfulness?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et ego ad te, Dómine, clamávi: * et mane orátio mea prævéniet te.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>But I, O Lord, have cried to thee: * and in the morning my prayer shall prevent thee.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ut quid, Dómine, repéllis oratiónem meam: * avértis fáciem tuam a me?</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Lord, why castest thou off my prayer: * why turnest thou away thy face from me?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Pauper sum ego, et in labóribus a juventúte mea: * exaltátus autem, humiliátus sum et conturbátus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I am poor, and in labours from my youth: * and being exalted have been humbled and troubled.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>In me transiérunt iræ tuæ: * et terróres tui conturbavérunt me.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thy wrath hath come upon me: * and thy terrors have troubled me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Circumdedérunt me sicut aqua tota die: * circumdedérunt me simul.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>They have come round about me like water all the day: * they have compassed me about together.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Elongásti a me amícum et próximum: * et notos meos a miséria.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Friend and neighbour thou hast put far from me: * and my acquaintance, because of misery.</p>
     </td>
    </tr>
   </table>
