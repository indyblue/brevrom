     </td>
     <td:B1>
      <p:BodyEDrop>BEFORE him the Ethiopians shall fall down: * and his enemies shall lick the ground.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Reges Tharsis, et ínsulæ múnera ófferent: * reges Árabum et Saba dona addúcent.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The kings of Tharsis and the islands shall offer presents: * the kings of the Arabians and of Saba shall bring gifts:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et adorábunt eum omnes reges terræ: * omnes gentes sérvient ei:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And all kings of the earth shall adore him: * all nations shall serve him.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quia liberábit páuperem a poténte: * et páuperem, cui non erat adjútor.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For he shall deliver the poor from the mighty: * and the needy that had no helper.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Parcet páuperi et ínopi: * et ánimas páuperum salvas fáciet.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He shall spare the poor and needy: * and he shall save the souls of the poor.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ex usúris et iniquitáte rédimet ánimas eórum: * et honorábile nomen eórum coram illo.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He shall redeem their souls from usuries and iniquity: * and their names shall be honourable in his sight.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et vivet, et dábitur ei de auro Arábiæ, et adorábunt de ipso semper: * tota die benedícent ei.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And he shall live, and to him shall be given of the gold of Arabia, for him they shall always adore: * they shall bless him all the day.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et erit firmaméntum in terra in summis móntium, superextollétur super Líbanum fructus ejus: * et florébunt de civitáte sicut fœnum terræ.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And there shall be a firmament on the earth on the tops of mountains, above Libanus shall the fruit thereof be exalted: * and they of the city shall flourish like the grass of the earth.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Sit nomen ejus benedíctum in sæcula: * ante solem pérmanet nomen ejus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Let his name be blessed for evermore: * his name continueth before the sun.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et benedicéntur in ipso omnes tribus terræ: * omnes gentes magnificábunt eum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And in him shall all the tribes of the earth be blessed: * all nations shall magnify him.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Benedíctus Dóminus, Deus Israël, * qui facit mirabília solus:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Blessed be the Lord, the God of Israel, * who alone doth wonderful things.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et benedíctum nomen majestátis ejus in ætérnum: * et replébitur majestáte ejus omnis terra: fiat, fiat.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And blessed be the name of his majesty for ever: * and the whole earth shall be filled with his majesty. So be it. So be it.</p>
     </td>
    </tr>
