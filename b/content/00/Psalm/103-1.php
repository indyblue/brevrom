   <p:Head2Ps>Psalmus 103 i. Bénedic, ánima mea</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>BÉNEDIC, ánima mea, Dómino: * Dómine, Deus meus, magnificátus es veheménter.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>BLESS the Lord, O my soul: * O Lord my God, thou art exceedingly great.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Confessiónem, et decórem induísti: * amíctus lúmine sicut vestiménto:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou hast put on praise and beauty: * and art clothed with light as with a garment.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Exténdens cælum sicut pellem: * qui tegis aquis superióra ejus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Who stretchest out the heaven like a pavilion: * who coverest the higher rooms thereof with water.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Qui ponis nubem ascénsum tuum: * qui ámbulas super pennas ventórum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Who makest the clouds thy chariot: * who walkest upon the wings of the winds.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Qui facis ángelos tuos, spíritus: * et minístros tuos ignem uréntem.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Who makest thy angels spirits: * and thy ministers a burning fire.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Qui fundásti terram super stabilitátem suam: * non inclinábitur in sæculum sæculi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Who hast founded the earth upon its own bases: * it shall not be moved for ever and ever.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Abýssus, sicut vestiméntum, amíctus ejus: * super montes stabunt aquæ.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The deep like a garment is its clothing: * above the mountains shall the waters stand.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ab increpatióne tua fúgient: * a voce tonítrui tui formidábunt.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>At thy rebuke they shall flee: * at the voice of thy thunder they shall fear.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ascéndunt montes: et descéndunt campi * in locum, quem fundásti eis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The mountains ascend, and the plains descend * into the place which thou hast founded for them.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Términum posuísti, quem non transgrediéntur: * neque converténtur operíre terram.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou hast set a bound which they shall not pass over; * neither shall they return to cover the earth.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Qui emíttis fontes in convállibus: * inter médium móntium pertransíbunt aquæ.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou sendest forth springs in the vales: * between the midst of the hills the waters shall pass.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Potábunt omnes béstiæ agri: * exspectábunt ónagri in siti sua.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>All the beasts of the field shall drink: * the wild asses shall expect in their thirst.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Super ea vólucres cæli habitábunt: * de médio petrárum dabunt voces.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Over them the birds of the air shall dwell: * from the midst of the rocks they shall give forth their voices.</p>
     </td>
    </tr>
   </table>
