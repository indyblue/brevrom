     </td>
     <td:B1>
      <p:BodyEDrop>ATTEND, O my people, to my law: * incline your ears to the words of my mouth.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Apériam in parábolis os meum: * loquar propositiónes ab inítio.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I will open my mouth in parables: * I will utter propositions from the beginning.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quanta audívimus et cognóvimus ea: * et patres nostri narravérunt nobis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>How great things have we heard and known, * and our fathers have told us.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Non sunt occultáta a fíliis eórum: * in generatióne áltera.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>They have not been hidden from their children, * in another generation.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Narrántes laudes Dómini, et virtútes ejus: * et mirabília ejus, quæ fecit.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Declaring the praises of the Lord, and his powers, * and his wonders which he hath done.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et suscitávit testimónium in Jacob: * et legem pósuit in Israël.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And he set up a testimony in Jacob: * and made a law in Israel.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quanta mandávit pátribus nostris nota fácere ea fíliis suis: * ut cognóscat generátio áltera.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>How great things he commanded our fathers, that they should make the same known to their children: * that another generation might know them.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Fílii qui nascéntur, et exsúrgent, * et narrábunt fíliis suis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The children that should be born and should rise up, * and declare them to their children.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ut ponant in Deo spem suam, et non obliviscántur óperum Dei: * et mandáta ejus exquírant.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>That they may put their hope in God and may not forget the works of God: * and may seek his commandments.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ne fiant sicut patres eórum: * generátio prava et exásperans.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>That they may not become like their fathers, * a perverse end exasperating generation.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Generátio, quæ non diréxit cor suum: * et non est créditus cum Deo spíritus ejus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>A generation that set not their heart aright: * and whose spirit was not faithful to God.</p>
     </td>
    </tr>
