   <p:Head2Ps>Psalmus 71 i. Deus, judícium tuum</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>DEUS, judícium tuum regi da: * et justítiam tuam fílio regis:</p>
     </td>
     <td:B1>
      <p:BodyEDrop>GIVE to the king thy judgment, O God: * and to the king’s son thy justice:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Judicáre pópulum tuum in justítia, * et páuperes tuos in judício.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>To judge thy people with justice, * and thy poor with judgment.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Suscípiant montes pacem pópulo: * et colles justítiam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Let the mountains receive peace for the people: * and the hills justice.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Judicábit páuperes pópuli, et salvos fáciet fílios páuperum: * et humiliábit calumniatórem.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He shall judge the poor of the people, and he shall save the children of the poor: * and he shall humble the oppressor.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et permanébit cum sole, et ante lunam, * in generatióne et generatiónem.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And he shall continue with the sun, and before the moon, * throughout all generations.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Descéndet sicut plúvia in vellus: * et sicut stillicídia stillántia super terram.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He shall come down like rain upon the fleece; * and as showers falling gently upon the earth.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Oriétur in diébus ejus justítia, et abundántia pacis: * donec auferátur luna.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>In his days shall justice spring up, and abundance of peace, * till the moon be taken away.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et dominábitur a mari usque ad mare: * et a flúmine usque ad términos orbis terrárum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And he shall rule from sea to sea, * and from the river unto the ends of the earth.</p>
     </td>
    </tr>
   </table>
