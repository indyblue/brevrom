   <p:Head2Ps>Psalmus 110. Confitebor</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>CONFITÉBOR tibi, Dómine, in toto corde meo: * in consílio justórum, et congregatióne.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>I WILL praise thee, O Lord, with my whole heart; * in the council of the just, and in the congregation.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Magna ópera Dómini: * exquisíta in omnes voluntátes ejus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Great are the works of the Lord: * sought out according to all his wills.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Conféssio et magnificéntia opus ejus: * et justítia ejus manet in sæculum sæculi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>His work is praise and magnificence: * and his justice continueth for ever and ever.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Memóriam fecit mirabílium suórum, miséricors et miserátor Dóminus: * escam dedit timéntibus se.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He hath made a remembrance of his wonderful works, being a merciful and gracious Lord: * he hath given food to them that fear him.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Memor erit in sæculum testaménti sui: * virtútem óperum suórum annuntiábit pópulo suo:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He will be mindful for ever of his covenant: * he will shew forth to his people the power of his works.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ut det illis hereditátem géntium: * ópera mánuum ejus véritas, et judícium.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>That he may give them the inheritance of the Gentiles: * the works of his hands are truth and judgment.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Fidélia ómnia mandáta ejus: confirmáta in sæculum sæculi, * facta in veritáte et æquitáte.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>All his commandments are faithful: confirmed for ever and ever, * made in truth and equity.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Redemptiónem misit pópulo suo: * mandávit in ætérnum testaméntum suum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He hath sent redemption to his people: * he hath commanded his covenant for ever.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Sanctum, et terríbile nomen ejus: * inítium sapiéntiæ timor Dómini.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Holy and terrible is his name: * the fear of the Lord is the beginning of wisdom.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Intelléctus bonus ómnibus faciéntibus eum: * laudátio ejus manet in sæculum sæculi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>A good understanding to all that do it: * his praise continueth for ever and ever.</p>
     </td>
    </tr>
