     </td>
     <td:B1>
      <p:BodyEIndent>Sing to him a new canticle, * sing well unto him with a loud noise.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quia rectum est verbum Dómini, * et ómnia ópera ejus in fide.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For the word of the Lord is right, * and all his works are done with faithfulness.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Díligit misericórdiam et judícium: * misericórdia Dómini plena est terra.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He loveth mercy and judgment; * the earth is full of the mercy of the Lord.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Verbo Dómini cæli firmáti sunt: * et spíritu oris ejus omnis virtus eórum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>By the word of the Lord the heavens were established; * and all the power of them by the spirit of his mouth:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Cóngregans sicut in utre aquas maris: * ponens in thesáuris abýssos.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Gathering together the waters of the sea, as in a vessel; * laying up the depths in storehouses.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Tímeat Dóminum omnis terra: * ab eo autem commoveántur omnes inhabitántes orbem.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Let all the earth fear the Lord, * and let all the inhabitants of the world be in awe of him.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quóniam ipse dixit, et facta sunt: * ipse mandávit, et creáta sunt.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For he spoke and they were made: * he commanded and they were created.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Dóminus díssipat consília géntium: * réprobat autem cogitatiónes populórum, et réprobat consília príncipum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The Lord bringeth to nought the counsels of nations; * and he rejecteth the devices of people, and casteth away the counsels of princes.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Consílium autem Dómini in ætérnum manet: * cogitatiónes cordis ejus in generatióne et generatiónem.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>But the counsel of the Lord standeth for ever: * the thoughts of his heart to all generations.</p>
     </td>
    </tr>
