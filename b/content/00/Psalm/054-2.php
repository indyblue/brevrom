   <p:Head2Ps>Psalmus 54 ii. Ego autem ad Deum</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>EGO autem ad Deum clamávi: * et Dóminus salvábit me.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>BUT I have cried to God: * and the Lord will save me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Véspere, et mane, et merídie narrábo et annuntiábo: * et exáudiet vocem meam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Evening and morning, and at noon I will speak and declare: * and he shall hear my voice.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Rédimet in pace ánimam meam ab his, qui appropínquant mihi: * quóniam inter multos erant mecum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He shall redeem my soul in peace from them that draw near to me: * for among many they were with me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Exáudiet Deus, et humiliábit illos, * qui est ante sæcula.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>God shall hear, * and the Eternal shall humble them.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Non enim est illis commutátio, et non timuérunt Deum: * exténdit manum suam in retribuéndo.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For there is no change with them, and they have not feared God: * he hath stretched forth his hand to repay.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Contaminavérunt testaméntum ejus, divísi sunt ab ira vultus ejus: * et appropinquávit cor illíus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>They have defiled his covenant, they are divided by the wrath of his countenance, * and his heart hath drawn near.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Mollíti sunt sermónes ejus super óleum: * et ipsi sunt jácula.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>His words are smoother than oil, * and the same are darts.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Jacta super Dóminum curam tuam, et ipse te enútriet: * non dabit in ætérnum fluctuatiónem justo.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Cast thy care upon the Lord, and he shall sustain thee: * he shall not suffer the just to waver for ever.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Tu vero, Deus, dedúces eos, * in púteum intéritus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>But thou, O God, shalt bring them down * into the pit of destruction.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Viri sánguinum, et dolósi non dimidiábunt dies suos: * ego autem sperábo in te, Dómine.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Bloody and deceitful men shall not live out half their days; * but I will trust in thee, O Lord.</p>
     </td>
    </tr>
   </table>
