     </td>
     <td:B1>
      <p:BodyEDrop>HEAR, O God, my prayer, when I make supplication to thee: * deliver my soul from the fear of the enemy.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Protexísti me a convéntu malignántium: * a multitúdine operántium iniquitátem.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou hast protected me from the assembly of the malignant; * from the multitude of the workers of iniquity.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quia exacuérunt ut gládium linguas suas: * intendérunt arcum rem amáram, ut sagíttent in occúltis immaculátum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For they have whetted their tongues like a sword; * they have bent their bow a bitter thing, to shoot in secret the undefiled.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Súbito sagittábunt eum, et non timébunt: * firmavérunt sibi sermónem nequam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>They will shoot at him on a sudden, and will not fear: * they are resolute in wickedness.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Narravérunt ut abscónderent láqueos: * dixérunt: Quis vidébit eos?</p>
     </td>
     <td:B1>
      <p:BodyEIndent>They have talked of hiding snares; * they have said: Who shall see them?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Scrutáti sunt iniquitátes: * defecérunt scrutántes scrutínio.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>They have searched after iniquities: * they have failed in their search.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Accédet homo ad cor altum: * et exaltábitur Deus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Man shall come to a deep heart: * and God shall be exalted.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Sagíttæ parvulórum factæ sunt plagæ eórum: * et infirmátæ sunt contra eos linguæ eórum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The arrows of children are their wounds: * and their tongues against them are made weak.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Conturbáti sunt omnes qui vidébant eos: * et tímuit omnis homo.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>All that saw them were troubled; * and every man was afraid.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et annuntiavérunt ópera Dei, * et facta ejus intellexérunt.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And they declared the works of God: * and understood his doings.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Lætábitur justus in Dómino, et sperábit in eo, * et laudabúntur omnes recti corde.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The just shall rejoice in the Lord, and shall hope in him: * and all the upright in heart shall be praised.</p>
     </td>
    </tr>
