   <p:Head2Ps>Psalmus 118 vii. Quómodo diléxi</p>
   <p:Head4>Mem</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>QUÓMODO diléxi legem tuam, Dómine? * tota die meditátio mea est.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>O HOW have I loved thy law, O Lord! * it is my meditation all the day.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Super inimícos meos prudéntem me fecísti mandáto tuo: * quia in ætérnum mihi est.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Through thy commandment, thou hast made me wiser than my enemies: * for it is ever with me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Super omnes docéntes me intelléxi: * quia testimónia tua meditátio mea est.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I have understood more than all my teachers: * because thy testimonies are my meditation.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Super senes intelléxi: * quia mandáta tua quæsívi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I have had understanding above ancients: * because I have sought thy commandments.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ab omni via mala prohíbui pedes meos: * ut custódiam verba tua.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I have restrained my feet from every evil way: * that I may keep thy words.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>A judíciis tuis non declinávi: * quia tu legem posuísti mihi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I have not declined from thy judgments, * because thou hast set me a law.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quam dúlcia fáucibus meis elóquia tua, * super mel ori meo!</p>
     </td>
     <td:B1>
      <p:BodyEIndent>How sweet are thy words to my palate! * more than honey to my mouth.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>A mandátis tuis intelléxi: * proptérea odívi omnem viam iniquitátis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>By thy commandments I have had understanding: * therefore have I hated every way of iniquity.</p>
     </td>
    </tr>
