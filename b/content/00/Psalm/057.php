   <p:Head2Ps>Psalmus 57. Si vere útique</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>SI VERE útique justítiam loquímini: * recta judicáte, fílii hóminum.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>IF IN very deed you speak justice: * judge right things, ye sons of men.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Étenim in corde iniquitátes operámini: * in terra injustítias manus vestræ concínnant.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For in your heart you work iniquity: * your hands forge injustice in the earth.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Alienáti sunt peccatóres a vulva, erravérunt ab útero: * locúti sunt falsa.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The wicked are alienated from the womb; they have gone astray from the womb: * they have spoken false things.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Furor illis secúndum similitúdinem serpéntis: * sicut áspidis surdæ, et obturántis aures suas,</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Their madness is according to the likeness of a serpent: * like the deaf asp that stoppeth her ears:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quæ non exáudiet vocem incantántium: * et venéfici incantántis sapiénter.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Which will not hear the voice of the charmers; * nor of the wizard that charmeth wisely.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Deus cónteret dentes eórum in ore ipsórum: * molas leónum confrínget Dóminus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>God shall break in pieces their teeth in their mouth: * the Lord shall break the grinders of the lions.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ad níhilum devénient tamquam aqua decúrrens: * inténdit arcum suum donec infirméntur.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>They shall come to nothing, like water running down; * he hath bent his bow till they be weakened.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Sicut cera, quæ fluit, auferéntur: * supercécidit ignis, et non vidérunt solem.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Like wax that melteth they shall be taken away: * fire hath fallen on them, and they shall not see the sun.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Priúsquam intellígerent spinæ vestræ rhamnum: * sicut vivéntes, sic in ira absórbet eos.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Before your thorns could know the brier; he swalloweth them up, * as alive, in his wrath.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Lætábitur justus cum víderit vindíctam: * manus suas lavábit in sánguine peccatóris.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The just shall rejoice when he shall see the revenge: * he shall wash his hands in the blood of the sinner.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et dicet homo: Si útique est fructus justo: * útique est Deus júdicans eos in terra.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And man shall say: If indeed there be fruit to the just: * there is indeed a God that judgeth them on the earth.</p>
     </td>
    </tr>
   </table>
