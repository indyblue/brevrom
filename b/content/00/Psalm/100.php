   <p:Head2Ps>Psalmus 100. Misericórdiam et judícium</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>MISERICÓRDIAM, et judícium * cantábo tibi, Dómine:</p>
     </td>
     <td:B1>
      <p:BodyEDrop>MERCY and judgment * I will sing to thee, O Lord:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Psallam, et intélligam in via immaculáta, * quando vénies ad me.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I will sing, and I will understand in the unspotted way, * when thou shalt come to me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Perambulábam in innocéntia cordis mei, * in médio domus meæ.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I walked in the innocence of my heart, * in the midst of my house.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Non proponébam ante óculos meos rem injústam: * faciéntes prævaricatiónes odívi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I did not set before my eyes any unjust thing: * I hated the workers of iniquities.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Non adhæsit mihi cor pravum: * declinántem a me malígnum non cognoscébam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The perverse heart did not cleave to me: * and the malignant, that turned aside from me, I would not know.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Detrahéntem secréto próximo suo, * hunc persequébar.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The man that in private detracted his neighbour, * him did I persecute.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Supérbo óculo, et insatiábili corde, * cum hoc non edébam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>With him that had a proud eye, and an unsatiable heart, * I would not eat.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Óculi mei ad fidéles terræ ut sédeant mecum: * ámbulans in via immaculáta, hic mihi ministrábat.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>My eyes were upon the faithful of the earth, to sit with me: * the man that walked in the perfect way, he served me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Non habitábit in médio domus meæ qui facit supérbiam: * qui lóquitur iníqua, non diréxit in conspéctu oculórum meórum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He that worketh pride shall not dwell in the midst of my house: * he that speaketh unjust things did not prosper before my eyes.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>In matutíno interficiébam omnes peccatóres terræ: * ut dispérderem de civitáte Dómini omnes operántes iniquitátem.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>In the morning I put to death all the wicked of the land: * that I might cut off all the workers of iniquity from the city of the Lord.</p>
     </td>
    </tr>
   </table>
