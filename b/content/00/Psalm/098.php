   <p:Head2Ps>Psalmus 98. Dóminus regnávit</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>DÓMINUS regnávit, irascántur pópuli: * qui sedet super Chérubim, moveátur terra.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>THE Lord hath reigned, let the people be angry: * he that sitteth on the cherubims: let the earth be moved.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Dóminus in Sion magnus: * et excélsus super omnes pópulos.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The Lord is great in Sion, * and high above all people.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Confiteántur nómini tuo magno: quóniam terríbile, et sanctum est: * et honor regis judícium díligit.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Let them give praise to thy great name: for it is terrible and holy: * and the king’s honour loveth judgment.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Tu parásti directiónes: * judícium et justítiam in Jacob tu fecísti.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou hast prepared directions: * thou hast done judgment and justice in Jacob.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Exaltáte Dóminum, Deum nostrum, et adoráte scabéllum pedum ejus: * quóniam sanctum est.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Exalt ye the Lord our God, and adore his footstool, * for it is holy.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Móyses et Aaron in sacerdótibus ejus: * et Sámuel inter eos, qui ínvocant nomen ejus:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Moses and Aaron among his priests: * and Samuel among them that call upon his name.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Invocábant Dóminum, et ipse exaudiébat eos: * in colúmna nubis loquebátur ad eos.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>They called upon the Lord, and he heard them: * he spoke to them in the pillar of the cloud.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Custodiébant testimónia ejus, * et præcéptum quod dedit illis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>They kept his testimonies, * and the commandment which he gave them.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Dómine, Deus noster, tu exaudiébas eos: * Deus, tu propítius fuísti eis, et ulcíscens in omnes adinventiónes eórum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou didst hear them, O Lord our God: * thou wast a merciful God to them, and taking vengeance on all their inventions.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Exaltáte Dóminum, Deum nostrum, et adoráte in monte sancto ejus: * quóniam sanctus Dóminus, Deus noster.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Exalt ye the Lord our God, and adore at his holy mountain: * for the Lord our God is holy.</p>
     </td>
    </tr>
   </table>
