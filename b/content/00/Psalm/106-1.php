   <p:Head2Ps>Psalmus 106 i. Confitémini Dómino</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>CONFITÉMINI Dómino quóniam bonus: * quóniam in sæculum misericórdia ejus.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>GIVE glory to the Lord, for he is good: * for his mercy endureth for ever.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Dicant qui redémpti sunt a Dómino, quos redémit de manu inimíci: * et de regiónibus congregávit eos:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Let them say so that have been redeemed by the Lord, whom he hath redeemed from the hand of the enemy: * and gathered out of the countries.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>A solis ortu, et occásu: * ab aquilóne, et mari.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>From the rising and the setting of the sun, * from the north and from the sea.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Erravérunt in solitúdine in inaquóso: * viam civitátis habitáculi non invenérunt.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>They wandered in a wilderness, in a place without water: * they found not the way of a city for their habitation.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Esuriéntes, et sitiéntes: * ánima eórum in ipsis defécit.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>They were hungry and thirsty: * their soul fainted in them.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et clamavérunt ad Dóminum cum tribularéntur: * et de necessitátibus eórum erípuit eos.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And they cried to the Lord in their tribulation: * and he delivered them out of their distresses.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et dedúxit eos in viam rectam: * ut irent in civitátem habitatiónis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And he led them into the right way: * that they might go to a city of habitation.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Confiteántur Dómino misericórdiæ ejus: * et mirabília ejus fíliis hóminum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Let the mercies of the Lord give glory to him: * and his wonderful works to the children of men.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quia satiávit ánimam inánem: * et ánimam esuriéntem satiávit bonis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For he hath satisfied the empty soul, * and hath filled the hungry soul with good things.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Sedéntes in ténebris, et umbra mortis: * vinctos in mendicitáte et ferro.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Such as sat in darkness and in the shadow of death: * bound in want and in iron.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quia exacerbavérunt elóquia Dei: * et consílium Altíssimi irritavérunt.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Because they had exasperated the words of God: * and provoked the counsel of the most High:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et humiliátum est in labóribus cor eórum: * infirmáti sunt, nec fuit qui adjuváret.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And their heart was humbled with labours: * they were weakened, and their was none to help them.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et clamavérunt ad Dóminum cum tribularéntur: * et de necessitátibus eórum liberávit eos.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Then they cried to the Lord in their affliction: * and he delivered them out of their distresses.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et edúxit eos de ténebris, et umbra mortis: * et víncula eórum disrúpit.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And he brought them out of darkness, * and the shadow of death; and broke their bonds in sunder.</p>
     </td>
    </tr>
   </table>
