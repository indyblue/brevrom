     </td>
     <td:B1>
      <p:BodyEIndent>The Lord is compassionate and merciful: * longsuffering and plenteous in mercy.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Non in perpétuum irascétur: * neque in ætérnum comminábitur.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He will not always be angry: * nor will he threaten for ever.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Non secúndum peccáta nostra fecit nobis: * neque secúndum iniquitátes nostras retríbuit nobis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He hath not dealt with us according to our sins: * nor rewarded us according to our iniquities.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quóniam secúndum altitúdinem cæli a terra: * corroborávit misericórdiam suam super timéntes se.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For according to the height of the heaven above the earth: * he hath strengthened his mercy towards them that fear him.</p>
     </td>
    </tr>
    <tr>
