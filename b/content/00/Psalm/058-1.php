     </td>
     <td:B1>
      <p:BodyEDrop>DELIVER me from my enemies, O my God; * and defend me from them that rise up against me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Éripe me de operántibus iniquitátem: * et de viris sánguinum salva me.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Deliver me from them that work iniquity, * and save me from bloody men.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quia ecce cepérunt ánimam meam: * irruérunt in me fortes.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For behold they have caught my soul: * the mighty have rushed in upon me:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Neque iníquitas mea, neque peccátum meum, Dómine: * sine iniquitáte cucúrri, et diréxi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Neither is it my iniquity, nor my sin, O Lord: * without iniquity have I run, and directed my steps.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Exsúrge in occúrsum meum, et vide: * et tu, Dómine, Deus virtútum, Deus Israël,</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Rise up thou to meet me, and behold: * even thou, O Lord, the God of hosts, the God of Israel.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Inténde ad visitándas omnes gentes: * non misereáris ómnibus, qui operántur iniquitátem.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Attend to visit all the nations: * have no mercy on all them that work iniquity.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Converténtur ad vésperam: et famem patiéntur ut canes, * et circuíbunt civitátem.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>They shall return at evening, and shall suffer hunger like dogs: * and shall go round about the city.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ecce, loquéntur in ore suo, et gládius in lábiis eórum: * quóniam quis audívit?</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Behold they shall speak with their mouth, and a sword is in their lips: * for who, say they, hath heard us?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et tu, Dómine, deridébis eos: * ad níhilum dedúces omnes gentes.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>But thou, O Lord, shalt laugh at them: * thou shalt bring all the nations to nothing.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Fortitúdinem meam ad te custódiam, quia, Deus, suscéptor meus es: * Deus meus, misericórdia ejus prævéniet me.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I will keep my strength to thee: for thou art my protector: * my God, his mercy shall prevent me.</p>
     </td>
    </tr>
