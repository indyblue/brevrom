   <p:Head2Ps>Psalmus 2. Quare fremuérunt gentes</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>QUARE fremuérunt Gentes: * et pópuli meditáti sunt inánia?</p>
     </td>
     <td:B1>
      <p:BodyEDrop>WHY have the Gentiles raged, * and the people devised vain things?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Astitérunt reges terræ, et príncipes convenérunt in unum * advérsus Dóminum, et advérsus Christum ejus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The kings of the earth stood up, and the princes met together, * against the Lord and against his Christ.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent><text:s/>Dirumpámus víncula eórum: * et projiciámus a nobis jugum ipsórum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Let us break their bonds asunder: * and let us cast away their yoke from us.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent><text:s/>Qui hábitat in cælis, irridébit eos: * et Dóminus subsannábit eos.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He that dwelleth in heaven shall laugh at them: * and the Lord shall deride them.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Tunc loquétur ad eos in ira sua, * et in furóre suo conturbábit eos.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Then shall he speak to them in his anger, * and trouble them in his rage.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ego autem constitútus sum Rex ab eo super Sion montem sanctum ejus, * prædicans præcéptum ejus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>But I am appointed king by him over Sion his holy mountain, * preaching his commandment.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Dóminus dixit ad me: * Fílius meus es tu, ego hódie génui te.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The Lord hath said to me: * Thou art my son, this day have I begotten thee.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Póstula a me, et dabo tibi Gentes hereditátem tuam, * et possessiónem tuam términos terræ.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Ask of me, and I will give thee the Gentiles for thy inheritance, * and the utmost parts of the earth for thy possession.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Reges eos in virga férrea, * et tamquam vas fíguli confrínges eos.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou shalt rule them with a rod of iron, * and shalt break them in pieces like a potter&apos;s vessel.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et nunc, reges, intellígite: * erudímini, qui judicátis terram.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And now, O ye kings, understand: * receive instruction, you that judge the earth.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Servíte Dómino in timóre: * et exsultáte ei cum tremóre.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Serve ye the Lord with fear: * and rejoice unto him with trembling.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Apprehéndite disciplínam, nequándo irascátur Dóminus, * et pereátis de via justa.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Embrace discipline, lest at any time the Lord be angry, * and you perish from the just way.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Cum exárserit in brevi ira ejus: * beáti omnes qui confídunt in eo.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>When his wrath shall be kindled in a short time, * blessed are all they that trust in him.</p>
     </td>
    </tr>
