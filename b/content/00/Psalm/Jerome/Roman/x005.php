Psalmus 5
Verba mea áuribus pércipe, Dómine, * intéllege clamórem meum.
Inténde voci oratiónis meæ, * Rex meus et Deus meus.
Quóniam ad te orábo: * Dómine, mane exáudies vocem meam.
Mane astábo tibi et vidébo: * quóniam non volens Deus iniquitátem tu es.
Non habitábit juxta te malígnus: * neque permanébunt injústi ante óculos tuos.
Odísti Dómine, omnes, qui operántur iniquitátem: * perdes omnes, qui loquúntur mendácium.
Virum sánguinum et dolósum * abominábitur Dóminus. 
Ego autem in multitúdine misericórdiæ tuæ introíbo, Dómine, in domum tuam: * adorábo ad templum sanctum tuum in timóre tuo.
Deduc me, Dómine, in tua justítia: * propter inimícos meos dírige in conspéctu tuo viam meam.
Quóniam non est in ore eórum véritas: * cor eórum vanum est.
Sepúlcrum patens est guttur eórum, linguis suis dolóse agébant, * júdica illos, Deus.
Décidant a cogitatiónibus suis, secúndum multitúdinem impietátum eórum expélle eos, * quóniam exacerbavérunt te, Dómine.
Et læténtur omnes, qui sperant in te, in ætérnum exsultábunt et habitábis in eis: * et gloriabúntur in te omnes, qui díligunt nomen tuum.
Quóniam tu benedíces justum: * Dómine, ut scuto bonæ voluntátis tuæ coronásti nos.
