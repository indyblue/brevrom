Psalmus 4
Cum invocárem te exaudísti me Deus justítiæ meæ: * in tribulatióne dilatásti mihi.
Miserére mei Dómine, * et exáudi oratiónem meam.
Fílii hóminum, úsquequo graves corde? * ut quid dilígitis vanitátem, et quǽritis mendácium?
Et scitóte quóniam mirificábit Dóminus sanctum suum: * Dóminus exáudiet me dum clamávero ad eum.
Irascímini, et nolíte peccáre: * quæ dícitis in córdibus vestris, in cubílibus vestris compungímini.
Sacrificáte sacrifícium justítiæ, et speráte in Dómino. Multi dicunt: Quis osténdit nobis bona? * Signátum est super nos lumen vultus tui, Dómine.
Dedísti lætítiam in corde meo * a témpore fruménti, et vini, et ólei sui multiplicáti sunt.
In pace in idípsum obdórmiam: * et requiéscam.
Quóniam tu, Dómine, singuláriter in spe * constituísti me.
