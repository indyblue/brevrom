Psalmus 6
Dómine, ne in ira tua árguas me, * neque in furóre tuo corrípias me.
Miserére mihi, Dómine, quóniam infírmus sum, sana me, Dómine, quóniam conturbáta sunt ómnia ossa mea: * et ánima mea turbáta est valde.
Et tu, Dómine, úsquequo? Convértere, Dómine, et éripe ánimam meam: * salvum me fac propter misericórdiam tuam.
Quóniam non est in morte qui memor sit tui: * in inférno autem quis confitébitur tibi?
Laborávi in gémitu meo, lavábo per síngulas noctes lectum meum: * lácrimis stratum meum rigábo.
Turbátus est præ ira óculus meus: * inveterávi inter omnes inimícos meos.
Discédite a me, omnes, qui operámini iniquitátem: * quóniam exaudívit Dóminus vocem fletus mei.
Exaudívit Dóminus deprecatiónem meam, * Dóminus oratiónem meam assúmpsit.
Erubéscant, et contúrbent omnes inimíci mei: * avertántur retrórsum et erubéscant valde velóciter.
