Psalmus 7 i
Dómine, Deus meus, in te sperávi: * líbera me ab ómnibus persequéntibus me, et éripe me.
Nequándo rápiat ut leo ánimam meam, * dum non est qui rédimat, neque qui salvum fáciat.
Dómine, Deus meus, si feci istud, * si est iníquitas in mánibus meis:
Si réddidi retribuéntibus mihi mala, * décidam mérito ab inimícis meis inánis.
Persequátur inimícus ánimam meam, et comprehéndat eam, et concúlcet in terra vitam meam, * et glóriam meam in púlverem dedúcat.
Exsúrge, Dómine, in ira tua: * et exaltáre in fínibus inimicórum tuórum.
Exsúrge, Dómine Deus, in præcépto quod mandásti: * et synagóga populórum circúmdabit te.
Et propter hanc in altum regrédere, Dómine: * júdica pópulos.
Júdica me, Dómine, secúndum justítiam meam, * et secúndum innocéntiam mánuum meárum super me.
Consumétur nequítia peccatórum, et díriges justum: * scrutans corda et renes, Deus.
Justum adjutórium meum a Dómino, * qui salvos facit rectos corde.
Deus judex justus, fortis, et longáminis, numquid iráscitur per síngulos dies? * Nisi convertámini, gládium suum vibrábit.
Arcum suum teténdit, et parávit illum, et in ipso parávit vasa mortis: * sagíttas suas ardéntibus effécit.
Ecce, partúriit injustítiam, concépit dolórem: * et péperit iniquitátem.
Lacum apéruit, et effódit eum: * et íncidit in fóveam quam fecit.
Convertétur dolor ejus in caput ejus: * et in vérticem ejus iníquitas ejus descéndet.
Confitébor Dómino secúndum justítiam ejus: * et psallam nómini Dómini altíssimi.
