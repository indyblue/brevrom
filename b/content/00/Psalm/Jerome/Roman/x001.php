Psalmus 1
Beátus vir, qui non ábiit in consílio impiórum, et in via peccatórum non stetit, * et in cáthedra pestiléntiæ non sedit:
Sed in lege Dómini fuit volúntas ejus, * et in lege ejus meditábitur die ac nocte.
Et erit tamquam lignum: * quod plantátum est secus decúrsus aquárum. 
Quod fructum suum dabit * in témpore suo.
Et fólium ejus non décidet: * et ómnia quæcúmque fécerit, prosperabúntur.
Non sic ímpii, non sic: * sed tamquam pulvis, quem prójicit ventus a fácie terræ.
Ídeo non resúrgunt ímpii in judício: * neque peccatóres in concílio justórum.
Quóniam novit Dóminus viam justórum: * et iter impiórum períbit.
