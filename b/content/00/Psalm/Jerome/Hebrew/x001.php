Psalmus 1
Beátus vir, qui non ábiit in consílio impiórum, et in via peccatórum non stetit, * et in cáthedra derisórum non sedit:
Sed in lege Dómini volúntas ejus, * et in lege ejus meditábitur die ac nocte.
Et erit tamquam lignum, transplantátum: * juxta rívulos aquárum.
Quod fructum suum dabit in témpore suo, et fólium ejus non défluet: * et omne quod fécerit, prosperabúntur.
Non sic ímpii: * sed tamquam pulvis, quem prójicit ventus.
Proptérea non resúrgunt ímpii in judício: * neque peccatóres in congregatióne justórum.
Quóniam novit Dóminus viam justórum: * et via impiórum períbit.
