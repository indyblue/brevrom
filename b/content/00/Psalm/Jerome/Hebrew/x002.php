Psalmus 2
Quare turbabúntur gentes, * et tribus meditabúntur inánia?
Consúrgent reges terræ, et príncipes tractábunt pariter: * advérsus Dóminum, et advérsus Christum ejus.
Dirumpámus víncula eórum: * et projiciámus a nobis láqueos eórum.
Habitátor cœli ridébit eos: * Dóminus subsannábit eos.
Tunc loquétur ad eos in ira sua, * et in furóre suo conturbábit eos.
Ego autem unxi Regem meum: super Sion montem sanctum meum.
Annunciábo Dei præcéptum, Dóminus dixit ad me: * Fílius meus es tu, ego hódie génui te.
Póstula a me, et dabo tibi gentes hæreditátem tuam, * et possessiónem tuam términos terræ.
Pasces eos in virga férrea, * ut vas fíguli cónteres eos.
Nunc ergo reges, intellégite: * erudímini, júdices terræ.
Servíte Dómino in timóre: * et exsultáte in tremóre.
Adoráte pure ne forte irascátur: * et pereátis de via.
Cum exárserit post páululum furor ejus: * beáti omnes qui sperant in eo.
