Psalmus 3
Dómine quare multiplicáti sunt hostes mei? * multi consúrgunt advérsus me.
Multi dicunt ánimæ meæ: * Non est salus huic in Deo.
Tu autem, Dómine, clípeus circa me, * glória mea, et exáltans caput meum.
Voce mea ad Dóminum clamábo: * et exáudiet me de monte sancto suo.
Ego dormívi, et soporátus sum: * evigilávi, quia Dóminus sustentávit me.
Non timébo míllia pópuli qui circumdedérunt me: * surge, Dómine, salvum me fac, Deus meus.
Quóniam tu percussísti ómnium inimicórum meórum maxíllam: * dentes impiórum confregísti.
Dómini est salus: * et super pópulum tuum benedíctio tua.
