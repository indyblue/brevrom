Psalmus 5
Verba mea audi Dómine: * intéllige murmur meum.
Inténde voci clamóris mei Rex meus et Deus meus: * quóniam ad te déprecor.
Dómine, mane exáudies vocem meam: * mane præparábor ad te et contemplábor.
Quóniam non Deus volens iniquitátem tu: * nec habitábit juxta te malígnus.
Non stabunt iniqui: * in conspéctu oculórum tuórum.
Odísti omnes operántes iniquitátem: * perdes loquéntes mendácium.
Virum sánguinum et dolósum abominábitur Dóminus: * ego autem in multitúdine misericórdiæ tuæ.
Introíbo in domum tuam: * adorábo in templo sancto tuo in timóre tuo.
Dómine, deduc me in justítia tua: * propter insidiatóres meos dírige ante fáciem meam viam tuam.
Non est in ore eórum rectum: * interióra eórum insídiæ.
Sepúlcrum patens guttur eórum, linguam suam lævíficant: * condémna eos, Deus.
Décidant a consíliis suis, juxta multitúdinem scélerum eórum expélle eos: * quóniam provocavérunt te.
Et læténtur omnes, qui sperant in te, in perpétuum laudábunt et próteges eos: et lætabúntur in te qui díligunt nomen tuum.
Quia tu benedíces justo: * Dómine, ut scuto placabilitátis coronábis eum.
