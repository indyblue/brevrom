Psalmus 4
Invocántem me exaudísti me Deus justítiæ meæ: * in tribulatióne dilatásti mihi.
Miserére mei, * et exáudi oratiónem meam.
Fílii viri, úsquequo ínclyti mei? * ignominióse dilígitis vanitátem, quæréntes mendácium?
Et cognóscite quóniam mirábilem réddidit Dóminus misericórdem suum: * Dóminus exáudiet cum clamávero ad eum.
Irascímini, et nolíte peccáre: * loquímini in córdibus vestris, super cubília vestra, et tacéte.
Sacrificáte sacrifícium justítiæ, et fídite in Dómino. * Multi dicunt: Quis osténdit nobis bonum?
Leva super nos lucem vultus tui, Dómine: * dedísti lætítiam in corde meo.
In témpore fruméntum, vini, et vinum eórum: * multiplicáta sunt.
In pace simul requiéscam et dórmiam: * quia tu, Dómine, speciáliter fecísti me.
