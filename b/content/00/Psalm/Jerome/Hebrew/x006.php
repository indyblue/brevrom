Psalmus 6
Dómine, ne in furóre tuo árguas me, * neque in ira tua corrípias me.
Miserére mei, Dómine, quóniam infírmus sum ego: * sana me, Dómine, quóniam conturbáta sunt ossa mea.
Et ánima mea turbáta est valde: * et tu, Dómine, úsquequo?
Revértere, Dómine, érue ánimam meam: * salva me propter misericórdiam tuam.
Quóniam non est in morte recordátio tui: * in inférno quis confitébitur tibi?
Laborávi in gémitu meo, natáre fáciam tota nocte lectum meum: * lácrimis meis stratum meum rigábo.
Caligávit præ amaritúdine óculus meus: * consúmptus sum ab univérsis hóstibus meis.
Recédite a me, omnes, qui operámini iniquitátem: * quóniam audívit Dóminus vocem fletus mei.
Audívit Dóminus deprecatiónem meam, * Dóminus oratiónem meam suscépit.
Confundántur et conturbéntur veheménter omnes inimíci mei: * revertántur et Confundántur súbito.
