     </td>
     <td:B1>
      <p:BodyEDrop>HAVE mercy on me, O God, have mercy on me: * for my soul trusteth in thee.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et in umbra alárum tuárum sperábo, * donec tránseat iníquitas.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And in the shadow of thy wings will I hope, * until iniquity pass away.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Clamábo ad Deum altíssimum: * Deum qui benefécit mihi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I will cry to God the most High; * to God who hath done good to me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Misit de cælo, et liberávit me: * dedit in oppróbrium conculcántes me.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He hath sent from heaven and delivered me: * he hath made them a reproach that trod upon me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Misit Deus misericórdiam suam, et veritátem suam, * et erípuit ánimam meam de médio catulórum leónum: dormívi conturbátus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>God hath sent his mercy and his truth, * and he hath delivered my soul from the midst of the young lions. I slept troubled.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Fílii hóminum dentes eórum arma et sagíttæ: * et lingua eórum gládius acútus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The sons of men, whose teeth are weapons and arrows, * and their tongue a sharp sword.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Exaltáre super cælos, Deus, * et in omnem terram glória tua.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Be thou exalted, O God, above the heavens, * and thy glory above all the earth.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Láqueum paravérunt pédibus meis: * et incurvavérunt ánimam meam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>They prepared a snare for my feet; * and they bowed down my soul.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Fodérunt ante fáciem meam fóveam: * et incidérunt in eam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>They dug a pit before my face, * and they are fallen into it.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Parátum cor meum, Deus, parátum cor meum: * cantábo, et psalmum dicam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>My heart is ready, O God, my heart is ready: * I will sing, and rehearse a psalm.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Exsúrge, glória mea, exsúrge, psaltérium et cíthara: * exsúrgam dilúculo.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Arise, O my glory, arise psaltery and harp: * I will arise early.</p>
     </td>
    </tr>
