   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>AD DÓMINUM cum tribulárer clamávi: * et exaudívit me.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>IN MY trouble I cried to the Lord: * and he heard me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Dómine, líbera ánimam meam a lábiis iníquis, * et a lingua dolósa.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>O Lord, deliver my soul from wicked lips, * and a deceitful tongue.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quid detur tibi, aut quid apponátur tibi * ad linguam dolósam?</p>
     </td>
     <td:B1>
      <p:BodyEIndent>What shall be given to thee, or what shall be added to thee, * to a deceitful tongue?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Sagíttæ poténtis acútæ, * cum carbónibus desolatóriis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The sharp arrows of the mighty, * with coals that lay waste.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Heu mihi, quia incolátus meus prolongátus est: habitávi cum habitántibus Cedar: * multum íncola fuit ánima mea.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Woe is me, that my sojourning is prolonged! I have dwelt with the inhabitants of Cedar: * my soul hath been long a sojourner.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Cum his, qui odérunt pacem, eram pacíficus: * cum loquébar illis, impugnábant me gratis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>With them that hated peace I was peaceable: * when I spoke to them they fought against me without cause.</p>
     </td>
    </tr>
   </table>
