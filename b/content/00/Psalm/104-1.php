     </td>
     <td:B1>
      <p:BodyEIndent>He hath remembered his covenant for ever: * the word which he commanded to a thousand generations.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quod dispósuit ad Ábraham: * et juraménti sui ad Isaac:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Which he made to Abraham; * and his oath to Isaac:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et státuit illud Jacob in præcéptum: * et Israël in testaméntum ætérnum:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And he appointed the same to Jacob for a law, * and to Israel for an everlasting testament:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Dicens: Tibi dabo terram Chánaan, * funículum hereditátis vestræ.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Saying: To thee will I give the land of Chanaan, * the lot of your inheritance.</p>
     </td>
    </tr>
    <tr>
