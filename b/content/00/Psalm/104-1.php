     </td>
     <td:B1>
      <p:BodyEIndent>Sing to him, yea sing praises to him: * relate all his wondrous works.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Laudámini in nómine sancto ejus: * lætétur cor quæréntium Dóminum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Glory ye in his holy name: * let the heart of them rejoice that seek the Lord.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quærite Dóminum, et confirmámini: * quærite fáciem ejus semper.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Seek ye the Lord, and be strengthened: * seek his face evermore.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Mementóte mirabílium ejus, quæ fecit: * prodígia ejus, et judícia oris ejus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Remember his marvellous works which he hath done; * his wonders, and the judgments of his mouth.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Semen Ábraham, servi ejus: * fílii Jacob, elécti ejus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>O ye seed of Abraham his servant; * ye sons of Jacob his chosen.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ipse Dóminus Deus noster: * in univérsa terra judícia ejus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He is the Lord our God: * his judgments are in all the earth.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
