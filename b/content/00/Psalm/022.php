   <p:Head2Ps>Psalmus 22. Dóminus regit me</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>DÓMINUS regit me, et nihil mihi déerit: * in loco páscuæ ibi me collocávit.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>THE Lord ruleth me: and I shall want nothing. * He hath set me in a place of pasture.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Super aquam refectiónis educávit me: * ánimam meam convértit.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He hath brought me up, on the water of refreshment: * he hath converted my soul.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Dedúxit me super sémitas justítiæ, * propter nomen suum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He hath led me on the paths of justice, * for his own name’s sake.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Nam, et si ambulávero in médio umbræ mortis, non timébo mala: * quóniam tu mecum es.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For though I should walk in the midst of the shadow of death, I will fear no evils, * for thou art with me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Virga tua, et báculus tuus: * ipsa me consoláta sunt.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thy rod and thy staff, * they have comforted me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Parásti in conspéctu meo mensam, * advérsus eos, qui tríbulant me.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou hast prepared a table before me * against them that afflict me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Impinguásti in óleo caput meum: * et calix meus inébrians quam præclárus est!</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou hast anointed my head with oil; * and my chalice which inebriateth me, how goodly is it!</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et misericórdia tua subsequétur me * ómnibus diébus vitæ meæ:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And thy mercy will follow me * all the days of my life.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et ut inhábitem in domo Dómini, * in longitúdinem diérum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And that I may dwell in the house of the Lord * unto length of days.</p>
     </td>
    </tr>
   </table>
