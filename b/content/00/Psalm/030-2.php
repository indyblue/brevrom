     </td>
     <td:B1>
      <p:BodyEIndent>My strength is weakened through poverty * and my bones are disturbed.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Super omnes inimícos meos factus sum oppróbrium et vicínis meis valde: * et timor notis meis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I am become a reproach among all my enemies, and very much to my neighbours; * and a fear to my acquaintance.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Qui vidébant me, foras fugérunt a me: * oblivióni datus sum, tamquam mórtuus a corde.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>They that saw me without fled from me. * I am forgotten as one dead from the heart.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
