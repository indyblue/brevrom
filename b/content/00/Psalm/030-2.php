     </td>
     <td:B1>
      <p:BodyEIndent>But I have put my trust in thee, O Lord: * I said: Thou art my God. My lots are in thy hands.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Éripe me de manu inimicórum meórum, * et a persequéntibus me.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Deliver me out of the hands of my enemies; * and from them that persecute me.</p>
     </td>
    </tr>
    <tr>
