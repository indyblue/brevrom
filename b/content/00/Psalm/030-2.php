     </td>
     <td:B1>
      <p:BodyEIndent>I am become as a vessel that is destroyed. * For I have heard the blame of many that dwell round about.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>In eo dum convenírent simul advérsum me, * accípere ánimam meam consiliáti sunt.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>While they assembled together against me, * they consulted to take away my life.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
