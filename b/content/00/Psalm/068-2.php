   <p:Head2Ps>Psalmus 68 ii. In multitúdine</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>IN MULTITÚDINE misericórdiæ tuæ exáudi me, * in veritáte salútis tuæ:</p>
     </td>
     <td:B1>
      <p:BodyEDrop>IN THE multitude of thy mercy hear me, * in the truth of thy salvation.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Éripe me de luto, ut non infígar: * líbera me ab iis, qui odérunt me, et de profúndis aquárum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Draw me out of the mire, that I may not stick fast: * deliver me from them that hate me, and out of the deep waters.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Non me demérgat tempéstas aquæ, neque absórbeat me profúndum: * neque úrgeat super me púteus os suum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Let not the tempest of water drown me, nor the deep swallow me up: * and let not the pit shut her mouth upon me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Exáudi me, Dómine, quóniam benígna est misericórdia tua: * secúndum multitúdinem miseratiónum tuárum réspice in me.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Hear me, O Lord, for thy mercy is kind; * look upon me according to the multitude of thy tender mercies.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et ne avértas fáciem tuam a púero tuo: * quóniam tríbulor, velóciter exáudi me.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And turn not away thy face from thy servant: * for I am in trouble, hear me speedily.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Inténde ánimæ meæ, et líbera eam: * propter inimícos meos éripe me.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Attend to my soul, and deliver it: * save me because of my enemies.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Tu scis impropérium meum, et confusiónem meam, * et reveréntiam meam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou knowest my reproach, and my confusion, * and my shame.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>In conspéctu tuo sunt omnes qui tríbulant me: * impropérium exspectávit cor meum, et misériam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>In thy sight are all they that afflict me; * my heart hath expected reproach and misery.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et sustínui qui simul contristarétur, et non fuit: * et qui consolarétur, et non invéni.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And I looked for one that would grieve together with me, * but there was none: and for one that would comfort me, and I found none.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et dedérunt in escam meam fel: * et in siti mea potavérunt me acéto.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And they gave me gall for my food, * and in my thirst they gave me vinegar to drink.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Fiat mensa eórum coram ipsis in láqueum, * et in retributiónes, et in scándalum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Let their table become as a snare before them, * and a recompense, and a stumblingblock.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Obscuréntur óculi eórum ne vídeant: * et dorsum eórum semper incúrva.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Let their eyes be darkened that they see not; * and their back bend thou down always.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Effúnde super eos iram tuam: * et furor iræ tuæ comprehéndat eos.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Pour out thy indignation upon them: * and let thy wrathful anger take hold of them.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Fiat habitátio eórum desérta: * et in tabernáculis eórum non sit qui inhábitet.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Let their habitation be made desolate: * and let there be none to dwell in their tabernacles.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quóniam quem tu percussísti, persecúti sunt: * et super dolórem vúlnerum meórum addidérunt.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Because they have persecuted him whom thou hast smitten; * and they have added to the grief of my wounds.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Appóne iniquitátem super iniquitátem eórum: * et non intrent in justítiam tuam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Add thou iniquity upon their iniquity: * and let them not come into thy justice.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Deleántur de libro vivéntium: * et cum justis non scribántur.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Let them be blotted out of the book of the living; * and with the just let them not be written.</p>
     </td>
    </tr>
   </table>
