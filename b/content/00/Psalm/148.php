   <p:Head2Ps>Psalmus 148. Laudáte Dóminum</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>LAUDÁTE Dóminum de cælis: * laudáte eum in excélsis.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>PRAISE ye the Lord from the heavens: * praise ye him in the high places.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Laudáte eum, omnes Ángeli ejus: * laudáte eum, omnes virtútes ejus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Praise ye him, all his angels: * praise ye him, all his hosts.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Laudáte eum, sol et luna: * laudáte eum, omnes stellæ et lumen.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Praise ye him, O sun and moon: * praise him, all ye stars and light.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Laudáte eum, cæli cælórum: * et aquæ omnes, quæ super cælos sunt, laudent nomen Dómini.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Praise him, ye heavens of heavens: * and let all the waters that are above the heavens, praise the name of the Lord.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quia ipse dixit, et facta sunt: * ipse mandávit, et creáta sunt.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For he spoke, and they were made: * he commanded, and they were created.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Státuit ea in ætérnum, et in sæculum sæculi: * præcéptum pósuit, et non præteríbit.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>He hath established them for ever, and for ages of ages: * he hath made a decree, and it shall not pass away.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Laudáte Dóminum de terra, * dracónes, et omnes abýssi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Praise the Lord from the earth, * ye dragons, and all ye deeps:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ignis, grando, nix, glácies, spíritus procellárum: * quæ fáciunt verbum ejus:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Fire, hail, snow, ice, stormy winds * which fulfill his word:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Montes, et omnes colles: * ligna fructífera, et omnes cedri.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Mountains and all hills, * fruitful trees and all cedars:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Béstiæ, et univérsa pécora: * serpéntes, et vólucres pennátæ:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Beasts and all cattle: * serpents and feathered fowls:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Reges terræ, et omnes pópuli: * príncipes, et omnes júdices terræ.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Kings of the earth and all people: * princes and all judges of the earth:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Júvenes, et vírgines: senes cum junióribus laudent nomen Dómini: * quia exaltátum est nomen ejus solíus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Young men and maidens: let the old with the younger, praise the name of the Lord: * For his name alone is exalted.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Conféssio ejus super cælum et terram: * et exaltávit cornu pópuli sui.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The praise of him is above heaven and earth: * and he hath exalted the horn of his people.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Hymnus ómnibus sanctis ejus: * fíliis Israël, pópulo appropinquánti sibi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>A hymn to all his saints: * to the children of Israel, a people approaching to him.</p>
     </td>
    </tr>
   </table>
