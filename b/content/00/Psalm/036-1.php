   <p:Head2Ps>Psalmus 36 i. Noli æmulári</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>NOLI æmulári in malignántibus: * neque zeláveris faciéntes iniquitátem.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>BE NOT emulous of evildoers; * nor envy them that work iniquity.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quóniam tamquam fœnum velóciter aréscent: * et quemádmodum ólera herbárum cito décident.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For they shall shortly wither away as grass, * and as the green herbs shall quickly fall.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Spera in Dómino, et fac bonitátem: * et inhábita terram, et pascéris in divítiis ejus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Trust in the Lord, and do good, * and dwell in the land, and thou shalt be fed with its riches.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Delectáre in Dómino: * et dabit tibi petitiónes cordis tui.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Delight in the Lord, * and he will give thee the requests of thy heart.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Revéla Dómino viam tuam, et spera in eo: * et ipse fáciet.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Commit thy way to the Lord, and trust in him, * and he will do it.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et edúcet quasi lumen justítiam tuam: et judícium tuum tamquam merídiem: * súbditus esto Dómino, et ora eum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And he will bring forth thy justice as the light, and thy judgment as the noonday: * be subject to the Lord and pray to him.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Noli æmulári in eo, qui prosperátur in via sua: * in hómine faciénte injustítias.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Envy not the man who prospereth in his way; * the man who doth unjust things.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Désine ab ira, et derelínque furórem: * noli æmulári ut malignéris.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Cease from anger, and leave rage; * have no emulation to do evil.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quóniam qui malignántur, exterminabúntur: * sustinéntes autem Dóminum, ipsi hereditábunt terram.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For the evildoers shall be cut off: * but they that wait upon the Lord shall inherit the land.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et adhuc pusíllum, et non erit peccátor: * et quæres locum ejus et non invénies.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For yet a little while, and the wicked shall not be: * and thou shalt seek his place, and shalt not find it.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Mansuéti autem hereditábunt terram: * et delectabúntur in multitúdine pacis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>But the meek shall inherit the land, * and shall delight in abundance of peace.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Observábit peccátor justum: * et stridébit super eum déntibus suis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The sinner shall watch the just man: * and shall gnash upon him with his teeth.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Dóminus autem irridébit eum: * quóniam próspicit quod véniet dies ejus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>But the Lord shall laugh at him: * for he foreseeth that his day shall come.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Gládium evaginavérunt peccatóres: * intendérunt arcum suum,</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The wicked have drawn out the sword: * they have bent their bow.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ut dejíciant páuperem et ínopem: * ut trucídent rectos corde.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>To cast down the poor and needy, * to kill the upright of heart.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Gládius eórum intret in corda ipsórum: * et arcus eórum confringátur.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Let their sword enter into their own hearts, * and let their bow be broken.</p>
     </td>
    </tr>
   </table>
