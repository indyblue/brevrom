   <p:Head2Ps>Psalmus 1. Beátus vir</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>BEÁTUS vir, qui non ábiit in consílio impiórum, et in via peccatórum non stetit, * et in cáthedra pestiléntiæ non sedit:</p>
     </td>
     <td:B1>
      <p:BodyEDrop>BLESSED is the man who hath not walked in the counsel of the ungodly, nor stood in the way of sinners, * nor sat in the chair of pestilence.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Sed in lege Dómini volúntas ejus, * et in lege ejus meditábitur die ac nocte.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>But his will is in the law of the Lord, * and on his law he shall meditate day and night.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et erit tamquam lignum, quod plantátum est secus decúrsus aquárum, * quod fructum suum dabit in témpore suo:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And he shall be like a tree which is planted near the running waters, * which shall bring forth its fruit, in due season.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et fólium ejus non défluet: * et ómnia quæcúmque fáciet, prosperabúntur.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And his leaf shall not fall off: * and all whosoever he shall do shall prosper.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Non sic ímpii, non sic: * sed tamquam pulvis, quem prójicit ventus a fácie terræ.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Not so the wicked, not so: * but like the dust, which the wind driveth from the face of the earth.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ideo non resúrgent ímpii in judício: * neque peccatóres in concílio justórum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Therefore the wicked shall not rise again in judgment: * nor sinners in the council of the just.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quóniam novit Dóminus viam justórum: * et iter impiórum períbit.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For the Lord knoweth the way of the just: * and the way of the wicked shall perish.</p>
     </td>
    </tr>
   </table>
