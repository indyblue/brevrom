   <p:Head2Ps>Psalmus 120. Levávi óculos</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>LEVÁVI óculos meos in montes, * unde véniet auxílium mihi.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>I HAVE lifted up my eyes to the mountains, * from whence help shall come to me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Auxílium meum a Dómino, * qui fecit cælum et terram.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>My help is from the Lord, * who made heaven and earth.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Non det in commotiónem pedem tuum: * neque dormítet qui custódit te.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>May he not suffer thy foot to be moved: * neither let him slumber that keepeth thee.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ecce, non dormitábit neque dórmiet, * qui custódit Israël.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Behold he shall neither slumber nor sleep, * that keepeth Israel.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Dóminus custódit te, Dóminus protéctio tua, * super manum déxteram tuam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The Lord is thy keeper, the Lord is thy protection * upon thy right hand.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Per diem sol non uret te: * neque luna per noctem.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The sun shall not burn thee by day: * nor the moon by night.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Dóminus custódit te ab omni malo: * custódiat ánimam tuam Dóminus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The Lord keepeth thee from all evil: * may the Lord keep thy soul.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Dóminus custódiat intróitum tuum, et éxitum tuum: * ex hoc nunc, et usque in sæculum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>May the Lord keep thy coming in and thy going out; * from henceforth now and for ever.</p>
     </td>
    </tr>
   </table>
