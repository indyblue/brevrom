     </td>
     <td:B1>
      <p:BodyEIndent>Let all the earth adore thee, * and sing to thee: let it sing a psalm to thy name.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Veníte, et vidéte ópera Dei: * terríbilis in consíliis super fílios hóminum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Come and see the works of God; * who is terrible in his counsels over the sons of men.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
