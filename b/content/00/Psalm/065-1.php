     </td>
     <td:B1>
      <p:BodyEIndent>Who turneth the sea into dry land, in the river they shall pass on foot: * there shall we rejoice in him.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Qui dominátur in virtúte sua in ætérnum, óculi ejus super Gentes respíciunt: * qui exásperant non exalténtur in semetípsis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Who by his power ruleth for ever: his eyes behold the nations; * let not them that provoke him he exalted in themselves.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Benedícite, Gentes, Deum nostrum: * et audítam fácite vocem laudis ejus,</p>
     </td>
     <td:B1>
      <p:BodyEIndent>O bless our God, ye Gentiles: * and make the voice of his praise to be heard.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Qui pósuit ánimam meam ad vitam: * et non dedit in commotiónem pedes meos.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Who hath set my soul to live: * and hath not suffered my feet to be moved:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quóniam probásti nos, Deus: * igne nos examinásti, sicut examinátur argéntum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>For thou, O God, hast proved us: * thou hast tried us by fire, as silver is tried.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Induxísti nos in láqueum, posuísti tribulatiónes in dorso nostro: * imposuísti hómines super cápita nostra.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou hast brought us into a net, thou hast laid afflictions on our back: * thou hast set men over our heads.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Transívimus per ignem et aquam: * et eduxísti nos in refrigérium.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>We have passed through fire and water, * and thou hast brought us out into a refreshment.</p>
     </td>
    </tr>
