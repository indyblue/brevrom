   <p:Head2Ps>Psalmus 27. Ad te, Dómine</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>AD TE, Dómine, clamábo, Deus meus, ne síleas a me: * nequándo táceas a me, et assimilábor descendéntibus in lacum.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>UNTO thee will I cry, O Lord: O my God, be not thou silent to me: * lest if thou be silent to me, I become like them that go down into the pit.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Exáudi, Dómine, vocem deprecatiónis meæ dum oro ad te: * dum extóllo manus meas ad templum sanctum tuum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Hear, O Lord, the voice of my supplication, when I pray to thee; * when I lift up my hands to thy holy temple.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ne simul trahas me cum peccatóribus: * et cum operántibus iniquitátem ne perdas me.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Draw me not away together with the wicked; * and with the workers of iniquity destroy me not:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Qui loquúntur pacem cum próximo suo, * mala autem in córdibus eórum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Who speak peace with their neighbour, * but evils are in their hearts.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Da illis secúndum ópera eórum, * et secúndum nequítiam adinventiónum ipsórum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Give them according to their works, * and according to the wickedness of their inventions.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Secúndum ópera mánuum eórum tríbue illis: * redde retributiónem eórum ipsis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>According to the works of their hands give thou to them: * render to them their reward.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Quóniam non intellexérunt ópera Dómini, et in ópera mánuum ejus * déstrues illos, et non ædificábis eos.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Because they have not understood the works of the Lord, and the operations of his hands: * thou shalt destroy them, and shalt not build them up.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Benedíctus Dóminus: * quóniam exaudívit vocem deprecatiónis meæ.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Blessed be the Lord, * for he hath heard the voice of my supplication.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Dóminus adjútor meus, et protéctor meus: * in ipso sperávit cor meum, et adjútus sum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The Lord is my helper and my protector: * in him hath my heart confided, and I have been helped.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et reflóruit caro mea: * et ex voluntáte mea confitébor ei.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And my flesh hath flourished again, * and with my will I will give praise to him.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Dóminus fortitúdo plebis suæ: * et protéctor salvatiónum Christi sui est.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The Lord is the strength of his people, * and the protector of the salvation of his anointed.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Salvum fac pópulum tuum, Dómine, et bénedic hereditáti tuæ: * et rege eos, et extólle illos usque in ætérnum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Save, O Lord, thy people, and bless thy inheritance: * and rule them and exalt them for ever.</p>
     </td>
    </tr>
   </table>
