Ps 94. Veníte, exsultémus Dómino
<sr>Dicitur conveniens Invitatorium, quod ante Psalmum bis integrum recitatur.</s>
Veníte, exsultémus Dómino, jubilémus Deo, salutári nostro: præoccupémus fáciem ejus in confessióne, et in psalmis jubilémus ei.
<sr>Repetitur integrum Invitatorium.</s>
Quóniam Deus magnus Dóminus, et Rex magnus super omnes deos: quóniam non repéllet Dóminus plebem suam: quia in manu ejus sunt omnes fines terræ, et altitúdines móntium ipse cónspicit.
<sr>Repetitur altera pars Invitatorii.</s>
<sr>Ad verba </s>veníte, adorémus, et procidámus ante Deum<sr>, genuflectitur.</s>
Quóniam ipsíus est mare, et ipse fecit illud, et áridam fundavérunt manus ejus: VENÍTE, ADORÉMUS, ET PROCIDÁMUS ANTE DEUM: plorémus coram Dómino, qui fecit nos, quia ipse est Dóminus Deus noster; nos autem pópulus ejus, et oves páscuæ ejus.
<sr>Repetitur integrum Invitatorium.</s>
Hódie, si vocem ejus audiéritis, nolíte obduráre corda vestra, sicut in exacerbatióne, secúndum diem tentatiónis in desérto: ubi tentavérunt me patres vestri, probavérunt et vidérunt ópera mea.
<sr>Repetitur altera pars Invitatorii.</s>
Quadragínta annis próximus fui generatióni huic, et dixi: Semper hi errant corde; ipsi vero non cognovérunt vias meas: quibus jurávi in ira mea: Si introíbunt in réquiem meam.
<sr>Repetitur integrum Invitatorium.</s>
Réquiem ætérnam dona eis, Dómine. Et lux perpétua lúceat eis.
<sr>Repetitur altera pars Invitatorii, deinque repetitur integrum Invitatorium.</s>
