   <p:Head2Ps>Psalmus 108 ii. In memóriam</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>IN MEMÓRIAM rédeat iníquitas patrum ejus in conspéctu Dómini: * et peccátum matris ejus non deleátur.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>MAY THE iniquity of his fathers be remembered in the sight of the Lord: * and let not the sin of his mother be blotted out.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Fiant contra Dóminum semper, et dispéreat de terra memória eórum: * pro eo quod non est recordátus fácere misericórdiam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>May they be before the Lord continually, and let the memory of them perish from the earth: * because he remembered not to shew mercy,</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et persecútus est hóminem ínopem, et mendícum, * et compúnctum corde mortificáre.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>But persecuted the poor man and the beggar; * and the broken in heart, to put him to death.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et diléxit maledictiónem, et véniet ei: * et nóluit benedictiónem, et elongábitur ab eo.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And he loved cursing, and it shall come unto him: * and he would not have blessing, and it shall be far from him.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et índuit maledictiónem sicut vestiméntum, * et intrávit sicut aqua in interióra ejus, et sicut óleum in óssibus ejus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And he put on cursing, like a garment: * and it went in like water into his entrails, and like oil in his bones.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Fiat ei sicut vestiméntum, quo operítur: * et sicut zona, qua semper præcíngitur.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>May it be unto him like a garment which covereth him; * and like a girdle with which he is girded continually.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Hoc opus eórum, qui détrahunt mihi apud Dóminum: * et qui loquúntur mala advérsus ánimam meam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>This is the work of them who detract me before the Lord; * and who speak evils against my soul.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Et tu, Dómine, Dómine, fac mecum propter nomen tuum: * quia suávis est misericórdia tua.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>But thou, O Lord, do with me for thy name’s sake: * because thy mercy is sweet.</p>
     </td>
    </tr>
   </table>
