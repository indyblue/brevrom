   <p:Head2Ps>Psalmus 51. Quid gloriáris</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>QUID gloriáris in malítia, * qui potens es in iniquitáte?</p>
     </td>
     <td:B1>
      <p:BodyEDrop>WHY dost thou glory in malice, * thou that art mighty in iniquity?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Tota die injustítiam cogitávit lingua tua: * sicut novácula acúta fecísti dolum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>All the day long thy tongue hath devised injustice: * as a sharp razor, thou hast wrought deceit.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Dilexísti malítiam super benignitátem: * iniquitátem magis quam loqui æquitátem.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou hast loved malice more than goodness: * and iniquity rather than to speak righteousness.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Dilexísti ómnia verba præcipitatiónis, * lingua dolósa.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou hast loved all the words of ruin, * O deceitful tongue.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Proptérea Deus déstruet te in finem, * evéllet te, et emigrábit te de tabernáculo tuo: et radícem tuam de terra vivéntium.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Therefore will God destroy thee for ever: * he will pluck thee out, and remove thee from thy dwelling place: and thy root out of the land of the living.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Vidébunt justi, et timébunt, et super eum ridébunt, et dicent: * Ecce homo, qui non pósuit Deum adjutórem suum:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The just shall see and fear, and shall laugh at him, and say: * Behold the man that made not God his helper:</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Sed sperávit in multitúdine divitiárum suárum: * et præváluit in vanitáte sua.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>But trusted in the abundance of his riches: * and prevailed in his vanity.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ego autem, sicut olíva fructífera in domo Dei, * sperávi in misericórdia Dei in ætérnum: et in sæculum sæculi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>But I, as a fruitful olive tree in the house of God, * have hoped in the mercy of God for ever, yea for ever and ever.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Confitébor tibi in sæculum, quia fecísti: * et exspectábo nomen tuum, quóniam bonum est in conspéctu sanctórum tuórum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I will praise thee for ever, because thou hast done it: * and I will wait on thy name, for it is good in the sight of thy saints.</p>
     </td>
    </tr>
   </table>
