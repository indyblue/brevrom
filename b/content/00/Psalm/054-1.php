     </td>
     <td:B1>
      <p:BodyEIndent>And I said: Who will give me wings like a dove, * and I will fly and be at rest?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ecce, elongávi fúgiens: * et mansi in solitúdine.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Lo, I have gone far off flying away; * and I abode in the wilderness.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Exspectábam eum, qui salvum me fecit * a pusillanimitáte spíritus et tempestáte.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I waited for him that hath saved me * from pusillanimity of spirit, and a storm.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Præcípita, Dómine, dívide linguas eórum: * quóniam vidi iniquitátem, et contradictiónem in civitáte.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Cast down, O Lord, and divide their tongues; * for I have seen iniquity and contradiction in the city.</p>
     </td>
    </tr>
    <tr>
