      <p:BodyLIndent>Et non defécit de platéis ejus * usúra, et dolus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>And usury and deceit * have not departed from its streets.</p>
     </td>
    </tr>
    <tr>
