     </td>
     <td:B1>
      <p:BodyEIndent>My heart is troubled within me: * and the fear of death is fallen upon me.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Timor et tremor venérunt super me: * et contexérunt me ténebræ.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Fear and trembling are come upon me: * and darkness hath covered me.</p>
     </td>
    </tr>
    <tr>
