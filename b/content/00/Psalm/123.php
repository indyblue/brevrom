   <p:Head2Ps>Psalmus 123. Nisi quia Dóminus</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>NISI quia Dóminus erat in nobis, dicat nunc Israël: * nisi quia Dóminus erat in nobis,</p>
     </td>
     <td:B1>
      <p:BodyEDrop>IF IT had not been that the Lord was with us, let Israel now say: * If it had not been that the Lord was with us,</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Cum exsúrgerent hómines in nos, * forte vivos deglutíssent nos:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>When men rose up against us, * perhaps they had swallowed us up alive.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Cum irascerétur furor eórum in nos, * fórsitan aqua absorbuísset nos.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>When their fury was enkindled against us, * perhaps the waters had swallowed us up.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Torréntem pertransívit ánima nostra: * fórsitan pertransísset ánima nostra aquam intolerábilem.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Our soul hath passed through a torrent: * perhaps our soul had passed through a water insupportable.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Benedíctus Dóminus * qui non dedit nos in captiónem déntibus eórum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Blessed be the Lord, * who hath not given us to be a prey to their teeth.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ánima nostra sicut passer erépta est * de láqueo venántium:</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Our soul hath been delivered as a sparrow * out of the snare of the fowlers.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Láqueus contrítus est, * et nos liberáti sumus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The snare is broken, * and we are delivered.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Adjutórium nostrum in nómine Dómini, * qui fecit cælum et terram.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Our help is in the name of the Lord, * who made heaven and earth.</p>
     </td>
    </tr>
   </table>
