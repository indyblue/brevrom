   <p:Head2Ps>Psalmus 126. Nisi Dóminus</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>NISI Dóminus ædificáverit domum, * in vanum laboravérunt qui ædíficant eam.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>UNLESS the Lord build the house, * they labour in vain that build it.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Nisi Dóminus custodíerit civitátem, * frustra vígilat qui custódit eam.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Unless the Lord keep the city, * he watcheth in vain that keepeth it.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Vanum est vobis ante lucem súrgere: * súrgite postquam sedéritis, qui manducátis panem dolóris.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>It is vain for you to rise before light, * rise ye after you have sitten, you that eat the bread of sorrow.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Cum déderit diléctis suis somnum: * ecce heréditas Dómini fílii: merces, fructus ventris.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>When he shall give sleep to his beloved, * behold the inheritance of the Lord are children: the reward, the fruit of the womb.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Sicut sagíttæ in manu poténtis: * ita fílii excussórum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>As arrows in the hand of the mighty, * so the children of them that have been shaken.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Beátus vir, qui implévit desidérium suum ex ipsis: * non confundétur cum loquétur inimícis suis in porta.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Blessed is the man that hath filled the desire with them; * he shall not be confounded when he shall speak to his enemies in the gate.</p>
     </td>
    </tr>
   </table>
