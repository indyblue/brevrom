   <p:Head2Ps>Psalmus 73 ii. Úsquequo, Deus</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>USQUEQUO, Deus, improperábit inimícus: * irrítat adversárius nomen tuum in finem?</p>
     </td>
     <td:B1>
      <p:BodyEDrop>HOW long, O God, shall the enemy reproach: * is the adversary to provoke thy name for ever?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Ut quid avértis manum tuam, et déxteram tuam, * de médio sinu tuo in finem?</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Why dost thou turn away thy hand: and thy right hand * out of the midst of thy bosom for ever?</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Deus autem Rex noster ante sæcula: * operátus est salútem in médio terræ.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>But God is our king before ages: * he hath wrought salvation in the midst of the earth.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Tu confirmásti in virtúte tua mare: * contribulásti cápita dracónum in aquis.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou by thy strength didst make the sea firm: * thou didst crush the heads of the dragons in the waters.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Tu confregísti cápita dracónis: * dedísti eum escam pópulis Æthíopum.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou hast broken the heads of the dragon: * thou hast given him to be meat for the people of the Ethiopians.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Tu dirupísti fontes, et torréntes: * tu siccásti flúvios Ethan.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou hast broken up the fountains and the torrents: * thou hast dried up the Ethan rivers.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Tuus est dies, et tua est nox: * tu fabricátus es auróram et solem.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thine is the day, and thine is the night: * thou hast made the morning light and the sun.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Tu fecísti omnes términos terræ: * æstátem et ver tu plasmásti ea.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou hast made all the borders of the earth: * the summer and the spring were formed by thee.</p>
     </td>
    </tr>
   </table>
