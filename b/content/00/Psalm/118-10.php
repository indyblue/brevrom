   <p:Head2Ps>Psalmus 118 x. Clamávi in toto corde meo</p>
   <p:Head4>Coph</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLDrop>CLAMÁVI in toto corde meo, exáudi me, Dómine: * justificatiónes tuas requíram.</p>
     </td>
     <td:B1>
      <p:BodyEDrop>I CRIED with my whole heart, hear me, O Lord: * I will seek thy justifications.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Clamávi ad te, salvum me fac: * ut custódiam mandáta tua.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I cried unto thee, save me: * that I may keep thy commandments.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Prævéni in maturitáte, et clamávi: * quia in verba tua supersperávi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I prevented the dawning of the day, and cried: * because in thy words I very much hoped.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Prævenérunt óculi mei ad te dilúculo: * ut meditárer elóquia tua.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>My eyes to thee have prevented the morning: * that I might meditate on thy words.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Vocem meam audi secúndum misericórdiam tuam, Dómine: * et secúndum judícium tuum vivífica me.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Hear thou my voice, O Lord, according to thy mercy: * and quicken me according to thy judgment.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Appropinquavérunt persequéntes me iniquitáti: * a lege autem tua longe facti sunt.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>They that persecute me have drawn nigh to iniquity; * but they are gone far off from thy law.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Prope es tu, Dómine: * et omnes viæ tuæ véritas.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Thou art near, O Lord: * and all thy ways are truth.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Inítio cognóvi de testimóniis tuis: * quia in ætérnum fundásti ea.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I have known from the beginning concerning thy testimonies: * that thou hast founded them for ever.</p>
     </td>
    </tr>
   </table>
   <p:Head4>Res</p>
   <table>
    <tr>
     <td:A1>
      <p:BodyLIndent>Vide humilitátem meam, et éripe me: * quia legem tuam non sum oblítus.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>See my humiliation and deliver me: * for I have not forgotten thy law.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Júdica judícium meum, et rédime me: * propter elóquium tuum vivífica me.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Judge my judgment and redeem me: * quicken thou me for thy word’s sake.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Longe a peccatóribus salus: * quia justificatiónes tuas non exquisiérunt.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Salvation is far from sinners; * because they have not sought thy justifications.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Misericórdiæ tuæ multæ, Dómine: * secúndum judícium tuum vivífica me.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Many, O Lord, are thy mercies: * quicken me according to thy judgment.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Multi qui persequúntur me, et tríbulant me: * a testimóniis tuis non declinávi.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Many are they that persecute me, and afflict me; * but I have not declined from thy testimonies.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Vidi prævaricántes, et tabescébam: * quia elóquia tua non custodiérunt.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>I beheld the transgressors, and I pined away; * because they kept not thy word.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Vide quóniam mandáta tua diléxi, Dómine: * in misericórdia tua vivífica me.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>Behold I have loved thy commandments, O Lord; * quicken me thou in thy mercy.</p>
     </td>
    </tr>
    <tr>
     <td:A1>
      <p:BodyLIndent>Princípium verbórum tuórum, véritas: * in ætérnum ómnia judícia justítiæ tuæ.</p>
     </td>
     <td:B1>
      <p:BodyEIndent>The beginning of thy words is truth: * all the judgments of thy justice are for ever.</p>
     </td>
    </tr>
   </table>
