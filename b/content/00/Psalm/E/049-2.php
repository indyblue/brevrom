But to the sinner God hath said: * Why dost thou declare my justices, and take my covenant in thy mouth?
Seeing thou hast hated discipline: * and hast cast my words behind thee.
If thou didst see a thief thou didst run with him: * and with adulterers thou hast been a partaker.
Thy mouth hath abounded with evil, * and thy tongue framed deceits.
Sitting thou didst speak against thy brother, and didst lay a scandal against thy mother&apos;s son: * these things hast thou done, and I was silent.
Thou thoughtest unjustly that I should be like to thee: * but I will reprove thee, and set before thy face.
Understand these things, you that forget God; * lest he snatch you away, and there be none to deliver you.
The sacrifice of praise shall glorify me: * and there is the way by which I will shew him the salvation of God.
