Psalm 94
Come let us praise the Lord with joy, let us joyfully sing to God our Saviour: let us come before his presence with thanksgiving, and make a joyful noise to him with psalms.
The King, to whom all things live: * Come, let us adore him.
For the Lord is a great God, and a great King above all gods: for the Lord will not cast off his people: for in his hand are all the ends of the earth, and he beholds the heights of the mountains.
Come, let us adore him.
<sr>At the words </s>Come let us adore and fall down before God<sr>, all genuflect.</s>
For the sea is his, and he made it: and his hands formed the dry land. COME, LET US ADORE, AND FALL DOWN BEFORE GOD: let us weep before the Lord that made us. For he is the Lord our God: and we are his people, and the sheep of his pasture.
The King, to whom all things live: * Come, let us adore him.
Today if you shall hear his voice, harden not your hearts, as in the provocation, according to the day of temptation in the wilderness: where your fathers tempted me, they proved and saw my works.
Come, let us adore him.
Forty years long was I offended with that generation, and I said: These always err in heart; and these men have not known my ways: so I swore in my wrath that they shall not enter into my rest.
The King, to whom all things live: * Come, let us adore him.
Eternal rest grant unto them, O Lord. And perpetual light shine upon them.
Come, let us adore him.
