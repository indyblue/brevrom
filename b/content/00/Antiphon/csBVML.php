Dum esset Rex * in accúbitu suo, nardus mea dedit odórem suavitátis.
While the king * was at his repose, my spikenard sent forth the odour of sweetness.
Læva ejus * sub cápite meo, et déxtera illíus amplexábitur me.
His left hand * is under my head, and his right hand shall embrace me.
Nigra sum, * sed formósa, fíliæ Jerúsalem; ídeo diléxit me Rex, et introdúxit me in cubículum suum.
I am black * but beautiful, O ye daughters of Jerusalem; therefore the King loved me, and brought me into his chamber.
Jam hiems tránsiit, * imber ábiit et recéssit: surge, amíca mea, et veni.
For winter is now past, * the rain is over and gone; arise, my beloved, and come.
Speciósa * facta es et suávis in delíciis tuis, sancta Dei Génitrix.
Thou art beautiful * and sweet in thy delights, O holy Mother of God.
