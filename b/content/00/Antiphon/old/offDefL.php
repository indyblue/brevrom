Exsultábunt Dómino * ossa humiliáta.
The humbled bones * shall rejoice in the Lord.
Exáudi, Dómine, * oratiónem meam: ad te omnis caro véniet.
Hear, O Lord, * my prayer: all flesh shall come to thee.
Me suscépit * déxtera tua, Dómine.
Thy right hand * hath received me, O Lord.
A porta ínferi * erue, Dómine, ánimam meam.
From the gate of hell * deliver my soul, O Lord.
Omnis spíritus * laudet Dóminum.
Let every spirit * praise the Lord.
