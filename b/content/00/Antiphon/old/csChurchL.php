Domum tuam, Dómine, * decet sanctitúdo in longitúdinem diérum.
Holiness becometh thy house, O Lord, * unto length of days.
Domus mea, * domus oratiónis vocábitur.
My house * shall be called the house of prayer.
Hæc est domus Dómini * fírmiter ædificáta, bene fundáta est supra firmam petram.
This is the house of the Lord, * strongly built, it is firmly founded upon the solid rock.
Bene fundáta est * domus Dómini supra firmam petram.
The house of the Lord * is well founded upon the solid rock.
Lápides pretiósi * omnes muri tui, et turres Jerúsalem gemmis ædificabúntur.
All thy walls * are precious stones, and the towers of Jerusalem shall be built up with jewels.
