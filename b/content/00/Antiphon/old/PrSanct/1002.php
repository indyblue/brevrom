Angelis suis * Deus mandávit de te, ut custódiant te in ómnibus viis tuis.
God hath given * his Angels charge over thee, to keep thee in all thy ways.
Laudémus Dóminum, * quem laudant Angeli, quem Chérubim et Séraphim, sanctus, sanctus, sanctus proclámant.
Let us bless the Lord * whom the Angels praise, unto whom the Cherubim and Seraphim cry: Holy, Holy, Holy.
Angeli eórum * semper vident fáciem Patris mei, qui est in cælis.
Their Angels * always behold the face of my Father who is in heaven.
Benedíctus Deus, * qui misit Angelum suum, et éruit servos suos qui credidérunt in eum.
Blessed be God, * who sent his Angel, and delivered his servants who trusted in him.
Laudáte Deum, * omnes Angeli ejus: laudáte eum, omnes Virtútes ejus.
Praise ye God, * all his Angels; praise ye him, all his hosts!
