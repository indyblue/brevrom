Dedísti, Dómine, * habitáculum Mártyri tuo Cleménti in mari, in modum templi marmórei angélicis mánibus præparátum: Iter præbens pópulo terræ, ut enárrent mirabília tua.
O Lord, Thou hast given * thy Martyr Clement a tabernacle in the sea, after the fashion of a temple of marble, built by the hands of Angels. And thou givest a way thither unto the people on the land, that they may tell of thy marvellous works.

>Bute, Matins, First Nocturn, Third Responsory
