Vidi turbam magnam, * quam dinumeráre nemo póterat, ex ómnibus géntibus stantes ante thronum.
I saw a great multitude, * which no man could number, of all nations, standing before the throne.
Et omnes Angeli * stabant in circúitu throni, et cecidérunt in conspéctu throni in fácies suas et adoravérunt Deum.
And all the Angels * stood round about the throne, and they fell down before the throne upon their faces, and adored God.
Redemísti nos, * Dómine Deus, in sánguine tuo ex omni tribu et lingua et pópulo et natióne, et fecísti nos Deo nostro regnum.
O Lord God, * thou hast redeemed us in thy blood, out of every tribe, and tongue, and people, and nation, and hast made us a kingdom to our God.
Benedícite Dóminum, * omnes elécti ejus: ágite dies lætítiæ, et confitémini illi.
Bless ye the Lord, * all his elect, keep his days of joy, and give glory to him.
Hymnus * ómnibus Sanctis ejus: fíliis Israël, pópulo approprinquánti sibi: glória hæc est ómnibus Sanctis ejus.
A hymn * to all his Saints; to the children of Israel, a people approaching to him: this glory is to all his Saints.
