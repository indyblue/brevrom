Virgo gloriósa semper Evangélium Christi gerébat in péctore suo, et non diébus neque nóctibus a collóquiis divínis et oratióne cessábat.
The glorious Virgin carried the Gospel of Christ always in her breast, and day and night did not cease talking with God and praying.

>Modified from Bute, Matins I Nocturn, Third Responsory
