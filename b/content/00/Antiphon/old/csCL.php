Dómine, quinque talénta * tradidísti mihi, ecce ália quinque superlucrátus sum.
Lord, thou gavest me five talents; * behold I have gained another five over and above.
Euge serve bone, * in módico fidélis, intra in gáudium Dómini tui.
Well done, good servant, * faithful in little things, enter thou into the joy of thy Lord.
Fidélis servus * et prudens, quem constítuit Dóminus super famíliam suam.
A faithful servant * and wise, whom the Lord hath set over his household.
Beátus ille servus, * quem, cum vénerit Dóminus ejus et pulsáverit jánuam, invénerit vigilántem.
Blessed is that servant, * whom, when the Lord shall come and knock at the gate, he shall find watching.
Serve bone * et fidélis, intra in gáudium Dómini tui.
Good servant * and faithful, enter thou into the joy of thy Lord.
