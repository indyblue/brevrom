Dómine, quinque talénta * tradidísti mihi, ecce ália quinque superlucrátus sum.
Euge serve bone, * in módico fidélis, intra in gáudium Dómini tui.
Fidélis servus * et prudens, quem constítuit Dóminus super famíliam suam.
Beátus ille servus, * quem, cum vénerit Dóminus ejus et pulsáverit jánuam, invénerit vigilántem.
