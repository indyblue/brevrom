Placébo Dómino * in regióne vivórum.
I will please the Lord * in the land of the living.
Hei mihi, Dómine, * quia incolátus meus prolongátus est.
Woe is me, O Lord, * that my abode is prolonged.
Dóminus custódit te * ab omni malo: custódiat ánimam tuam Dóminus.
The Lord preserve thee * from all evil: may the Lord keep thy soul.
Si iniquitátes * observáveris, Dómine: Dómine, quis sustinébit?
If thou wilt observe * iniquities, O Lord; Lord, who shall bear it?
Ópera * mánuum tuárum, Dómine, ne despícias.
The works * of thy hands despise not, O Lord.
