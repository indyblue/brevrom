Oránte * sancto Cleménte, appáruit ei Agnus Dei.
While St. Clement prayed, * there appeared unto him the Lamb of God.
Non meis méritis * ad vos me misit Dóminus, vestris corónis partícipem fíeri.
Not for my own merits * hath the Lord sent me unto you, that I may share your crowns.
Vidi supra montem * Agnum stantem, de sub cujus pede fons vivus emánat.
I saw on the mountain * the Lamb standing, from beneath whose foot flowed a fountain of living waters.
De sub cujus pede * fons vivus emánat: flúminis ímpetus lætíficat civitátem Dei.
From beneath whose foot * a fountain of living waters flowed forth: the stream of the river that maketh glad the city of God.
Omnes gentes * per gyrum credidérunt Christo Dómino.
All the nations * round about believed in Christ the Lord.
