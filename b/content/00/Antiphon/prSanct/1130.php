Salve, crux pretiósa, * súscipe discípulum ejus, qui pepéndit in te magíster meus Christus.
Hail, precious Cross! * receive a disciple of him who hung on thee, my master, Christ.
Beátus Andreas * orábat, dicens: Dómine, Rex ætérnæ glóriæ, súscipe me pendéntem in patíbulo.
Blessed Andrew * prayed, saying: O Lord, King of eternal glory, receive me hanging upon the cross.
Andreas Christi fámulus, * dignus Dei Apóstolus, germánus Petri, et in passióne socius.
Andrew, the servant of Christ, * a worthy Apostle of God, brother of Peter, and his companion in suffering.
Maximílla Christo amábilis * tulit corpus Apóstoli, óptimo loco cum aromátibus sepelívit.
Maximilla, beloved of Christ, * carried away the body of the Apostle, and buried it with sweet spices in an honourable place.
Qui persequebántur justum, * demersísti eos, Dómine, in inférno, et in ligno crucis dux justi fuisti.
Thou, O Lord, * hast plunged into hell those who persecuted the just man; on the wood of the cross thou wast his leader.
