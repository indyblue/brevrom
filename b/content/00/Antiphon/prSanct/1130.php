Salve, crux pretiósa, * súscipe discípulum ejus, qui pepéndit in te magíster meus Christus.
Beátus Andréas * orábat, dicens: Dómine, Rex ætérnæ glóriæ, súscipe me pendéntem in patíbulo.
Andréas Christi fámulus, * dignus Dei Apóstolus, germánus Petri, et in passióne sócius.
Maximílla Christo amábilis * tulit corpus Apóstoli, óptimo loco cum aromátibus sepelívit.
Qui persequebántur justum, * demersísti eos, Dómine, in inférno, et in ligno crucis dux justi fuísti.
