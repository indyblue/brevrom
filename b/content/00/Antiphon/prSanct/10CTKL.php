Suscitábit * Deus cæli regnum quod commínuet et consúmet univérsa regna, et ipsum stabit in ætérnum.
The God of heaven * will set up a kingdom that shall break in pieces and shall consume all these kingdoms, and itself shall stand for ever.
Dedit ei Dóminus * potestátem et honórem et regnum; et omnes pópuli, tribus et linguæ ipsi sérvient.
The Lord gave him * power and glory, and a kingdom; and all peoples, tribes and tongues shall serve him.
Exíbunt aquæ vivæ * de Jersúsalem; et erit Dóminus Rex super omnem terram.
Living waters shall go out * from Jerusalem; and the Lord shall be king over all the earth.
Magnificábitur * usque ad términos terræ, et erit iste pax.
Now shall he be magnified * even to the ends of the earth, and this man shall be our peace.
Gens et regnum * quod non servíerit tibi, períbit: et Gentes solitúdine vastabúntur.
The nation and the kingdom * that will not serve thee, shall perish: and the Gentiles shall be wasted with desolation.

