Suscitábit * Deus cæli regnum quod commínuet et consúmet univérsa regna, et ipsum stabit in ætérnum.
Dedit ei Dóminus * potestátem et honórem et regnum; et omnes pópuli, tribus et linguæ ipsi sérvient.
Exíbunt aquæ vivæ * de Jersúsalem; et erit Dóminus Rex super omnem terram.
Magnificábitur * usque ad términos terræ, et erit iste pax.
Gens et regnum * quod non servíerit tibi, períbit: et Gentes solitúdine vastabúntur.
