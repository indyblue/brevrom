Cantántibus óganis, * Cæcília Dómino decantábat dicens: Fiat cor meum immaculátum, ut non confúndar.
While the musical instruments were playing, * Cecilia sang unto the Lord, saying: Let my heart be undefiled, that I be not confounded.
Valeriánus * in cubículo Cæcíliam cum Angelo orántem invénit.
Valerian * found Cecilia praying in her chamber, and with her an angel.
Cæcília * fámula tua, Dómine, quasi apis tibi argumentósa desérvit.
Cecilia * thy handmaid serves thee, O Lord, as a busy bee.
Benedíco te, * Pater Dómini mei Jesu Christi, quia per Fílium tuum ignis exstínctus est a látere meo.
I bless thee, * O Father of my Lord Jesus Christ, for through thy Son the fire was quenched at my side.
Triduánas * a Dómino popósci indúcias, ut domum meam ecclésiam consecrárem.
I asked * of the Lord three days, that I might consecrate my house as a church.
