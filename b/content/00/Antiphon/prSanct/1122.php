Cantántibus óganis, * Cæcília Dómino decantábat dicens: Fiat cor meum immaculátum, ut non confúndar.
Valeriánus * in cubículo Cæcíliam cum Ángelo orántem invénit.
Cæcília * fámula tua, Dómine, quasi apis tibi argumentósa desérvit.
Benedíco te, * Pater Dómini mei Jesu Christi, quia per Fílium tuum ignis exstínctus est a látere meo.
Triduánas * a Dómino popósci indúcias, ut domum meam ecclésiam consecrárem.
