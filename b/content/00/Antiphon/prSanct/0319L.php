Ibant paréntes Jesu * per omnes annos in Jerúsalem, in die solémni Paschæ.
Cum redírent, * remánsit puer Jesus in Jerúsalem, et non cognovérunt paréntes ejus.
Non inveniéntes * Jesum, regréssi sunt in Jerúsalem requiréntes eum; et post tríduum invenérunt illum in templo sedéntem in médio doctórum, audiéntem et interrogántem eos.
Dixit Mater ejus * ad illum: Fili, quid fecísti nobis sic? Ecce pater tuus et ego doléntes quærebámus te.
Descéndit Jesus * cum eis, et venit Názareth, et erat súbditus illis.
