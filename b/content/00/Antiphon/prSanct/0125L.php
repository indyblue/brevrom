Ego plantávi, * Apóllo rigávit, Deus autem increméntum dedit allelúja.
Libénter gloriábor * in infirmitátibus meis, ut inhábitet in me virtus Christi.
Grátia Dei * in me vácua non fuit, sed grátia ejus semper in me manet.
Damásci, præpósitus * gentis Arétæ regis vóluit me comprehéndere: a frátribus per murum demíssus sum in sporta, et sic evási manus ejus in nómine Dómini.
Ter virgis cæsus sum, * semel lapidátus sum, ter naufrágium pértuli pro Christi nómine.
