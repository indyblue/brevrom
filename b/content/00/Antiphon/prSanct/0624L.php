Elísabeth Zacharíæ * magnum virum génuit, Joánnem Baptístam, præcursórem Dómini.
Innuébant patri ejus * quem vellet vocári eum: et scripsit dicens: Joánnes est nomen ejus.
Joánnes vocábitur * nomen ejus: et in nativitáte ejus multi gaudébunt.
Inter natos mulíerum * non surréxit major Joánne Baptísta.
Tu, puer, * Prophéta Altíssimi vocáberis; præíbis ante Dóminum paráre vias ejus.
