Deus, mundi ópifex, * pósuit hóminem ut operarétur et custodíret terram, allelúja.
Christus, Dei Fílius, * mánibus suis operári dignátus est, allelúja.
Artem fabri * fidéliter exércens sanctus Joseph, labóris mirábile refúlget exémplar, allelúja.
Fidélis servus * et prudens, quem constítuit Dóminus super famíliam suam, allelúja.
Joseph, ópifex sancte, * ópera nostra tuére, allelúja.
