She is the brightness * of eternal light, and the unspotted mirror.
A woman * clothed with the sun, and the moon under her feet, and on her head a crown of twelve stars.
Thou art the glory of Jerusalem, * thou art the joy of Israel, thou art the honour of our people.
Blessed art thou, * O Virgin Mary, by the Lord, the most high God, above all women upon the earth.
This day the Lord * hath so magnified thy name that thy praise shall not depart out of the mouth of men.
