I planted, * Apollo watered, but God gave the increase, alleluia.
Gladly will I glory * in my infirmities, that the power of Christ may dwell in me.
The grace of God * in me was not in vain; but his grace abideth ever in me.
At Damascus the governor * of the nation under Aretas the king would have apprehended me: but I was let down the wall in a basket by the brethren, and so escaped out of his hands in the name of the Lord.
Thrice was I beaten with rods, * once was I stoned, thrice I suffered shipwreck, for the name of Christ.
