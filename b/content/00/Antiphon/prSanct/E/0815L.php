Mary is taken up into heaven: * the Angels rejoice, and praising bless the Lord.
The Virgin Mary is taken up * into the heavenly dwelling, where the King of kings sits on his starry throne.
We run after thee * to the odour of thy ointments: young maidens have loved thee exceedingly.
Daughter, * thou art blessed of the Lord: for by thee we have partaken of the fruit of life.
Thou art beautiful * and comely, O daughter of Jerusalem, terrible as an army set in array.
