Mary rising up, * went into the hill country with haste, into a city of Juda.
Mary entered * into the house of Zachary, and saluted Elizabeth.
When Elizabeth heard * the salutation of Mary, the infant leaped in her womb; and she was filled with the Holy Ghost, alleluia.
Blessed * art thou among women, and blessed is the fruit of thy womb.
For behold, * as soon as the voice of thy salutation sounded in my ears, the infant in my womb leaped for joy, alleluia.
