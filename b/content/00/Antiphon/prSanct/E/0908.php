This is the Nativity * of the glorious Virgin Mary, of the seed of Abraham, born of the tribe of Juda, of the noble race of David.
Today is the Birthday * of the holy Virgin Mary, whose glorious life sheddeth lustre on all the Churches.
Sprung from a royal race, * Mary shines forth to the world; the help of whose prayers we devoutly implore with heart and mind.
With heart and mind * let us sing glory to Christ, on this sacred solemnity of Mary, the peerless Mother of God.
Let us celebrate * with joy the Birth of blessed Mary; that she may intercede for us with our Lord Jesus Christ.
