Laurence * has entered the lists as a martyr, and has confessed the name of our Lord Jesus Christ.
Laurence * has wrought a good work, who by the sign of the Cross gave sight to the blind.
My soul * hath cleaved to thee, for my flesh has been burned with fire for thy sake, O my God.
The Lord sent * his Angel and delivered me from the midst of the fire, and I have not been consumed.
Blessed Laurence * prayed, saying: I give thee thanks, O Lord, that I have been found worthy to enter thy gates.
