An Angel stood * at the altar of the temple, having a golden censer in his hand.
While Michael the Archangel * fought with the dragon, there was heard the voice of them that said: Salvation to our God, alleluia.
O Archangel Michael, * I have appointed thee prince over all souls about to be received into eternity.
O ye Angels of the Lord, * bless ye the Lord for ever.
Angels, Archangels, * Thrones and Dominations, Principalities and Powers, Virtues of heaven, praise ye the Lord from the heavens, alleluia.
