The parents of Jesus went * every year to Jerusalem at the solemn Feast of the Passover.
When they returned, * the child Jesus tarried behind in Jerusalem, and his parents knew not of it.
Not finding * Jesus, they returned to Jerusalem seeking him, and after three days they found him in the temple, sitting in the midst of the doctors, hearing them and asking them questions.
His Mother said * unto him: Son, why hast thou done thus to us? Behold thy father and I have sought thee sorrowing.
Jesus went down * with them, and came to Nazareth, and was subject unto them.
