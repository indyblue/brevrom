Rejoice, * O Virgin Mother, Christ has risen from the tomb.
God hath ascended * with jubilee, and the Lord with the sound of the trumpet.
The Spirit of the Lord * hath filled the whole world.
Mary is assumed * into heaven; the Angels rejoice; praising, they bless the Lord, alleluia.
The Virgin Mary * has been exalted above the choirs of Angels, and on her head is a crown of twelve stars.
