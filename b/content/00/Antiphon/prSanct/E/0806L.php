Jesus taketh unto him * Peter, and James, and John his brother, and bringeth them up into a high mountain apart: and he was transfigured before them.
His face did shine * as the sun, and his garments became white as snow, alleluia.
And behold * there appeared to them Moses and Elias talking with Jesus.
And Peter answering * said to Jesus: Lord, it is good for us to be here.
As he was yet speaking, * behold a bright cloud overshadowed them.
