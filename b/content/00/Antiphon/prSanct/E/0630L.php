I have planted, * Apollo watered, but God gave the increase, alleluia.
Gladly I will glory * in my infirmities, that the power of Christ may dwell in me.
The grace of God * in me hath not been void, but his grace abideth ever in me.
At Damascus, * the governor of the nation under Aretas the king was desirous to apprehend me; by the brethren I was let down in a basket by the wall, and so escaped his hands, in the name of the Lord.
Thrice was I beaten with rods, * once was I stoned, thrice I suffered shipwreck for the name of Christ.
