The Angel Raphael * was sent to Tobias and Sarah to cure them.
The Angel entering * into the house of Tobias, saluted him, and said: Joy be unto thee for ever.
Be of good courage, * Tobias, for the time is at hand when God shall heal thee.
Bless ye the God of heaven, * and give thanks unto him before all the living, for he hath dealt with you according to his mercy.
Peace be unto you, * fear not: bless God, and sing unto him.

