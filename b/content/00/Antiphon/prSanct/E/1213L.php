While St. Lucy prayed, * there appeared unto her the blessed Agatha, and she comforted the handmaid of Christ.
O Virgin Lucy, * why askest thou of me what thou thyself canst at once give unto thy mother?
Through thee, O Virgin Lucy, * the city of Syracuse shall be adorned by the Lord Jesus Christ.
I bless thee, * the Father of my Lord Jesus Christ, because by thy Son the fire that surrounded me was quenched.
