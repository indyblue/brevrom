Agnes having entered * the place of infamy, found an Angel of the Lord ready.
For I have with me * an Angel of the Lord as guardian of my body.
My Lord Jesus Christ * hath espoused me with his ring, and as a bride hath he adorned me with a crown.
I bles thee, * O Father of my Lord Jesus Christ, because through thy Son the fire that surrounded me was quenched.
Rejoice with me * and be glad, because among all these I have received a throne of glory.
