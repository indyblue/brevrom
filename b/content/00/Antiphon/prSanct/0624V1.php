Ipse præíbit * ante illum in spíritu et virtúte Elíæ paráre Dómino plebem perféctam.
Joánnes * est nomen ejus: vinum et síceram non bibet, et multi in nativitáte ejus gaudébunt.
Ex útero senectútis * et stérili Joánnes natus est, præcúrsor Dómini.
Iste puer * magnus coram Dómino: nam et manus ejus cum ipso est.
Nazarǽus * vocábitur puer iste: vinum et síceram non bibet, et omne immúndum non manducábit ex útero matris suæ.
