Missus est * Ángelus Ráphaël ad Tobíam et Saram, ut curáret eos.
Ingréssus Ángelus * ad Tobíam, salutávit eum, et dixit: Gáudium sit tibi semper.
Forti ánimo * esto, Tobía: in próximo enim est ut a Deo curéris.
Benedícite Deum cæli, * et coram ómnibus vivéntibus confitémini illi, quia fecit vobíscum misericórdiam suam.
Pax vobis, * nolíte timére: Deum benedícite, et cantáte illi.
