Ingréssa Agnes * turpitúdinis locum, Ángelum Dómini præparátum invénit.
Mecum enim hábeo * custódem córporis mei, Ángelum Dómini.
Ánulo suo * subarrhávit me Dóminus meus Jesus Christus, et tamquam sponsam decorávit me coróna.
Benedíco te, * Pater Dómini mei Jesu Christi, quia per Fílium tuum ignis exstínctus est a látere meo.
Congaudéte mecum * et congratulámini, quia cum his ómnibus lúcidas sedes accépi.
