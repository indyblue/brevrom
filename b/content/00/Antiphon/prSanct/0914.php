O magnum pietátis opus: * mors mortua tunc est, in ligno quando mortua Vita fuit.
Salva nos, * Christe Salvátor, per virtútem Crucis: qui salvásti Petrum in mari, miserére nobis.
Ecce Crucem Dómini, * fúgite, partes advérsæ, vicit leo de tribu Juda, radix David, allelúja.
Nos autem gloriári * opórtet in Cruce Dómini nostri Jesu Christi.
Per signum Crucis * de inimícis nostris líbera nos, Deus noster.
