O magnum pietátis opus: * mors mortua tunc est, in ligno quando mortua Vita fuit.
O great work of love! * death then died, when life died on the tree.
Salva nos, * Christe Salvátor, per virtútem Crucis: qui salvásti Petrum in mari, miserére nobis.
Save us, * O Saviour Christ, by the power of the Cross; thou who didst save Peter in the sea, have mercy on us.
Ecce Crucem Dómini, * fúgite, partes advérsæ, vicit leo de tribu Juda, radix David, allelúja.
Behold the Cross of the Lord! * Flee, O ye his enemies, for the lion of the tribe of Juda, the root of David, hath conquered, alleluia.
Nos autem gloriári * opórtet in Cruce Dómini nostri Jesu Christi.
But us it behoveth to glory * in the Cross of our Lord Jesus Christ.
Per signum Crucis * de inimícis nostris líbera nos, Deus noster.
By the sign of the Cross * deliver us from our enemies, O thou our God.
