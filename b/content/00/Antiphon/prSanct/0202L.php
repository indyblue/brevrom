Símeon justus * et timorátus exspectábat redemptiónem Israël, et Spíritus Sanctus erat in eo.
Respónsum accépit Símeon * a Spíritu Sancto, non visúrum se mortem, nisi vidéret Dóminum.
Accípiens Símeon * Púerum in mánibus, grátias agens benedíxit Dóminum.
Lumen ad revelatiónem Géntium, * et glóriam plebis tuæ Israël.
Obtulérunt * pro eo Dómino par túrturum aut duos pullos columbárum.
