Oránte sancta Lúcia, * appáruit ei beáta Ágatha, et consolabántur ancíllam Christi.
Lúcia virgo, * quid a me petis quod ipsa póteris præstáre contínuo matri tuæ?
Per te, Lúcia virgo, * cívitas Syracusána decorábitur a Dómino Jesu Christo.
Benedíco te, * Pater Dómini mei Jesu Christi, quia per Fílium tuum ignis exstínctus est a látere meo.
Soror mea, Lúcia, * virgo Deo devóta, quid a me petis, quod ipsa póteris præstáre contínuo matri tuæ?
