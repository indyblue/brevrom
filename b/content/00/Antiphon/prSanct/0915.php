Quo ábiit * diléctus tuus, o pulchérrima mulíerum? quo declinávit diléctus tuus, et quærémus eum tecum?
Whither is * thy beloved gone, o thou most beautiful among women? Whither is thy beloved turned aside, and we will seek him with thee?
Recédite a me, * amáre flebo, nolíte incúmbere ut consolémini me.
Depart from me, * I will weep bitterly; labour not to comfort me.
Non est ei * spécies neque decor, et vídimus eum, et non erat aspéctus.
There is no beauty * in him, nor comeliness: and we have seen him, and there was no sightliness.
A planta pedis * usque ad vérticem cápitis non est in eo sánitas.
From the sole of the foot * unto the top of the head, there is no soundness in him.
Fulcíte me flóribus, * stipáte me malis, quia amóre lángueo.
Stay me up with flowers, * compass me about with apples, because I languish with love.
