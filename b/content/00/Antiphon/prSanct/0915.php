Quo ábiit * diléctus tuus, o pulchérrima mulíerum? quo declinávit diléctus tuus, et quærémus eum tecum?
Recédite a me, * amáre flebo, nolíte incúmbere ut consolémini me.
Non est ei * spécies neque decor, et vídimus eum, et non erat aspéctus.
A planta pedis * usque ad vérticem cápitis non est in eo sánitas.
Fulcíte me flóribus, * stipáte me malis, quia amóre lángueo.
