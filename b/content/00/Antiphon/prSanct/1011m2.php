Matérnitas tua, * Dei Génitrix Virgo, gáudium annuntiávit univérso mundo: ex te enim ortus est sol justítiæ, Christus Deus noster.
Thy maternity, * O Virgin Mother of God, announced joy to the whole world; for out of thee arose the sun of justice, Christ our God.

> Taken from Stanbrook, Sept. 8, modified by Editor.
