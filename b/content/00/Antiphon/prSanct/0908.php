Natívitas gloriósæ * Vírginis Maríæ ex sémine Ábrahæ, ortæ de tribu Juda, clara ex stirpe David.
Natívitas est hódie * sanctæ Maríæ Vírginis, cujus vita ínclyta cunctas illústrat ecclésias.
Regáli ex progénie * María exórta refúlget; cujus précibus nos adjuvári, mente et spíritu devotíssime póscimus.
Corde et ánimo * Christo canámus glóriam in hac sacra solemnitáte præcélsæ Genitrícis Dei Maríæ.
Cum jucunditáte * Nativitátem beátæ Maríæ celebrémus, ut ipsa pro nobis intercédat ad Dóminum Jesum Christum.
