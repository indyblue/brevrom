Petrus et Joánnes * ascendébant in templum ad horam oratiónis nonam.
Argéntum et aurum * non est mihi: quod autem hábeo, hoc tibi do.
Dixit Ángelus ad Petrum: * Circúmda tibi vestiméntum tuum, et séquere me.
Misit Dóminus * Ángelum suum, et liberávit me de manu Heródis, allelúja.
Tu es Petrus, * et super hanc petram ædificábo Ecclésiam meam.
