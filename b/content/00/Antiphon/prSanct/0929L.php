Stetit Ángelus * juxta aram templi, habens thuríbulum áureum in manu sua.
Dum præliarétur * Míchaël Archángelus cum dracóne, audíta est vox dicéntium: Salus Deo nostro, allelúja.
Archángele Míchaël, * constítui te príncipem super omnes ánimas suscipiéndas.
Ángeli Dómini, * Dóminum benedícite in ætérnum.
Ángeli, Archángeli, * Throni et Dominatiónes, Principátus et Potestátes, Virtútes cælórum, laudáte Dóminum de cælis, allelúja.
