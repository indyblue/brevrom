Lauréntius * ingréssus est Martyr, et conféssus est nomen Dómini Jesu Christi.
Lauréntius * bonum opus operátus est, qui per signum crucis cæcos illuminávit.
Adhǽsit * ánima mea post te, quia caro mea igne cremáta est pro te, Deus meus.
Misit Dóminus * Ángelum suum, et liberávit me de médio ignis, et non sum æstuátus.
Beátus Lauréntius * orábat dicens: Grátias tibi ago, Dómine, quia jánuas tuas íngredi mérui.
