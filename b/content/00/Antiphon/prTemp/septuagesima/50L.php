According to the multitude * of thy tender mercies, O Lord, blot out my iniquity.
Thou art my God, * and I will praise thee: thou art my God, and I will exalt thee.
Unto thee do I watch * at break of day, O God, that I may behold thy power.
Sing a hymn, * and exalt him above all for ever.
All ye his Angels, * praise ye the Lord from the heavens.
