Viri Galilǽi, * quid aspícitis in cælum? Hic Jesus, qui assúmptus est a vobis in cælum, sic véniet, allelúja.
Cumque intueréntur * in cælum eúntem illum, dixérunt, allelúja.
Elevátis mánibus, * benedíxit eis, et ferebátur in cælum, allelúja.
Exaltáte Regem regum, * et hymnum dícite Deo, allelúja.
Vidéntibus illis, * elevátus est, et nubes suscépit eum in cælo, allelúja.
