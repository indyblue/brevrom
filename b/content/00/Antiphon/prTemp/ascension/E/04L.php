Ye men of Galilee, * why look ye up to heaven? This Jesus, who is taken from you into heaven, shall so come, alleluia.
And while they beheld him * going up to heaven, they said, alleluia.
Lifting up his hands, * he blessed them, and was carried up to heaven, alleluia.
Praise ye the King of kings, * and sing a hymn to God, alleluia.
As they looked on, * he was raised up, and a cloud received him into heaven, alleluia.
