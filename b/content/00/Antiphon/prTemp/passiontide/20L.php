Dóminus Deus * auxiliátor meus: et ídeo non sum confúsus.
Circumdántes * circumdedérunt me: et in nómine Dómini vindicábor in eis.
Júdica causam meam: * defénde, quia potens es, Dómine.
Cum Ángelis * et púeris fidéles inveniámur, triumphatóri mortis clamántes: Hosánna in excélsis.
Confundántur * qui me persequúntur, et non confúndar ego, Dómine, Deus meus.
