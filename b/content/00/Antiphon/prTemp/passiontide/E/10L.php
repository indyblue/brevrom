Behold, O Lord, * my affliction, for the enemy hath magnified himself.
In my trouble * I called upon the Lord, and he heard me and set me at large.
Thou hast pleaded, O Lord, * the cause of my soul, thou defender of my life, O Lord, my God.
My people, * what have I done to thee, or wherein have I grieved thee? Answer thou me.
Shall evil be rendered * for good, for they have dug a pit for my soul?
