The Lord God * is my helper, and therefore am I not confounded.
Surrounding me, * they compassed me about: and in the name of the Lord I will be revenged on them.
Judge my cause, * defend me, for thou art mighty, O Lord.
With the Angels * and with the children may we be found faithful, singing to him who triumphed over death: Hosanna in the highest.
Let them be confounded * that persecute me, and let not me be confounded, O Lord, my God.
