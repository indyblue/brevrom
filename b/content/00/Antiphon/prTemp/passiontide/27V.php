Hódie afflíctus sum valde, sed cras solvam víncula mea.
Cum his, * qui odérunt pacem, eram pacíficus: dum loquébar illis impugnábant me gratis.
Ab homínibus * iníquis líbera me, Dómine.
Custódi me * a láqueo, quem statuérunt mihi, et a scándalis operántium iniquitátem.
Considerábam * ad déxteram, et vidébam, et non erat qui cognósceret me.
