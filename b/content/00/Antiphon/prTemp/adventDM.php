Véniet ecce Rex * excélsus cum potestáte magna ad salvándas Gentes, allelúja.
Confortáte * manus dissolútas: confortámini, dícite: Ecce Deus noster véniet, et salvábit nos, allelúja.
Gaudéte omnes, * et lætámini: ecce quia véniet Dóminus ultiónis, addúcet retributiónem: ipse véniet, et salvábit nos.
Gaude et lætáre, * fília Jerúsalem: ecce Rex tuus véniet tibi: Sion, noli timére, quia cito véniet salus tua.
Rex noster * advéniet Christus, quem Joánnes prædicávit Agnum esse ventúrum.
Ecce vénio * cito, et merces mea mecum est, dicit Dóminus: dare unicuíque secúndum ópera sua.
Gábriel Ángelus * locútus est Maríæ, dicens: Ave, grátia plena; Dóminus tecum: benedícta tu inter mulíeres.
María dixit: * Putas qualis est ista salutátio, quia conturbáta est ánima mea, et quia paritúra sum Regem, qui claustrum virginitátis meæ non violábit?
In advéntu * summi Regis mundéntur corda hóminum, ut digne ambulémus in occúrsum illíus: quia ecce véniet, et non tardábit.
