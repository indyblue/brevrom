Suávi jugo tuo * domináre, Dómine, in médio inimicórum tuórum.
Miséricors * et miserátor Dóminus: escam dedit timéntibus se.
Exórtum est * in ténebris lumen rectis; miséricors et miserátor Dóminus.
Quid retríbuam * Dómino pro ómnibus quæ retríbuit mihi?
Apud Dóminum * propitiátio est et copiósa apud eum redémptio.
