Unus mílitum * láncea latus ejus apéruit et contínuo exívit sanguis et aqua.
Stans Jesus * clamábat dicens: Si quis sitit, véniat ad me et bibat.
In caritáte perpétua * diléxit nos Deus, ídeo, exaltátus a terra, attráxit nos ad Cor suum, míserans.
Veníte ad me * omnes qui laborátis et oneráti estis et ego refíciam vos.
Fili, * præbe mihi cor tuum et óculi tui custódiant vias meas.
