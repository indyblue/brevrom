Wisdom * hath built herself a house, mingled her wine, and set forth her table, alleluia.
Thou hast nourished thy people * with the bread of angels, and hast granted them bread from heaven, alleluia.
The bread of Christ is fat, * and it shall yield dainties to kings, alleluia.
Holy priests * offer incense and bread unto God, alleluia.
To him that overcometh, I will give * hidden manna, and a new name, alleluia.
