Glory be to thee, O equal Trinity * one Deity, both before all ages, and now, and for ever.
Praise and perpetual glory * be to God, Father and Son, together with the holy Paraclete, for ever and ever.
Let the glory of praise * sound in every mouth, to the Father, and to the begotten of him; to the Holy Ghost also let perpetual praise be given.
Praise be to God the Father, * and to his co-equal Son, and to thee, O Holy Ghost: with perpetual delight may it resound from our lips for ever.
From whom are all things, * by whom are all things, in whom are all things: to him be glory for ever.

