One of the soldiers * with a spear opened his side, and immediately there came out blood and water.
Standing, Jesus * cried, saying: If any man thirst, let him come to me, and drink.
With everlasting love, * God hath loved us; therefore, having been lifted up from the earth, he hath drawn us to his Heart, taking pity on us.
Come to me, * all you that labour, and are burdened, and I will refresh you.
Son, * give me thy heart and let thy eyes keep my ways.

