With thy sweet yoke, * rule thou, O Lord, in the midst of thy enemies.
Being a merciful * and gracious Lord: he hath given food to them that fear him.
To the righteous * a light is risen up in darkness; the Lord is merciful and compassionate.
What shall I render * to the Lord, for all the things that he hath rendered unto me?
With the Lord * there is merciful forgiveness: and with him plentiful redemption.

