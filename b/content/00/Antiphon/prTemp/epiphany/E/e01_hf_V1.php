And Jacob * begot Joseph the husband of Mary, of whom was born Jesus, who is called Christ.
The Angel of the Lord * appeared to Joseph in his sleep, saying: Joseph, son of David, fear not to take unto thee Mary thy wife, for that which is conceived in her, is of the Holy Ghost.
The shepherds came * with haste; and they found Mary and Joseph, and the infant lying in the manger.
The Magi, entering into the house, * found the child with Mary his mother.
His father * and mother were wondering at those things which were spoken concerning him.
