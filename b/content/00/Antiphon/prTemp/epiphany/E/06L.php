The Lord our Saviour, * begotten before the day-star and before all ages, is this day made manifest to the world.
Thy light is come, * O Jerusalem, and the glory of the Lord is risen upon thee; and the Gentiles shall walk in thy light, alleluia.
Opening their treasures, * the Magi offered to the Lord gold, frankincense, and myrrh, alleluia.
Ye seas and rivers, * bless the Lord: ye fountains, sing a hymn to the Lord, alleluia.
This star * shineth as a flame, and pointeth out God, the King of kings: the Magi saw it, and offered gifts unto the great King.
