Jacob autem * génuit Joseph virum Maríæ; de qua natus est Jesus, qui vocátur Christus.
Ángelus Dómini * appáruit in somnis Joseph, dicens: Joseph, fili David, noli timére accípere Maríam cónjugem tuam; quod enim in ea natum est, de Spíritu Sancto est.
Pastóres venérunt * festinántes, et invenérunt Maríam, et Joseph, et Infántem pósitum in præsépio.
Magi, intrántes domum, * invenérunt Púerum cum María, matre ejus.
Erat Pater ejus * et Mater mirántes super his quæ dicebántur de illo.
