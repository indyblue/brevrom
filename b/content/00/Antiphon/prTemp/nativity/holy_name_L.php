Sitívit * ánima mea ad nomen sanctum tuum, Dómine.
Benedíctum * nomen glóriæ tuæ sanctum, et laudábile, et superexaltátum in sǽcula.
Júvenes et vírgines, * senes cum junióribus, laudáte nomen Dómini: quia exaltátum est nomen ejus solíus.
