With thee is the principality * in the day of thy strength, in the brightness of thy Saints: from the womb before the day-star I begot thee.
He hath sent * redemption to his people: he hath commanded his covenant for ever.
To the upright * of heart a light has risen up in darkness: the merciful, and compassionate, and just Lord.
With the Lord * there is merciful forgiveness: and with him is plentiful redemption.
Of the fruit * of thy womb I will set upon thy throne.
