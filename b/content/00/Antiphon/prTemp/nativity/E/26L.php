They stoned Stephen, * and he called upon the Lord, saying: Lay not this sin to their charge.
The stones of the torrent * were sweet unto him: all just souls follow him.
My soul cleaveth * unto thee, for my flesh hath been stoned for thee, O my God.
Stephen saw * the heavens opened: he saw and entered in: blessed is the man to whom the heavens were opened.
Behold, I see * the heavens opened, and Jesus standing at the right hand of the power of God.
