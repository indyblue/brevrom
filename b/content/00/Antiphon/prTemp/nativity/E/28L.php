Herod being wroth, * slew many children in Bethlehem of Juda, the city of David.
Many children * from two years old and under, Herod slew because of the Lord.
Their Angels * do always behold the face of my Father.
A voice * was heard in Rama, lamentation and bitter weeping, Rachel weeping for her children.
Beneath the throne of God * all the Saints cry: Avenge our blood, O our God.
