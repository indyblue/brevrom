Thy name * is as oil poured out, therefore do the young maidens love thee.
Know ye * that the Lord he is God, and his name endureth for ever.
My soul thirsteth * for thy Holy Name, O Lord.
Blessed * is the name of thy glory, and praiseworthy, and exalted above all for ever.
Young men and maidens, * old men and children, praise the name of the Lord: for his name alone is exalted above all.
