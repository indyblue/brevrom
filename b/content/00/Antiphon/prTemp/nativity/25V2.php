Tecum princípium * in die virtútis tuæ, in splendóribus sanctórum, ex útero ante lucíferum génui te.
Redemptiónem * misit Dóminus pópulo suo: mandávit in ætérnum testaméntum suum.
Exórtum est * in ténebris lumen rectis corde: miséricors, et miserátor, et justus Dóminus.
Apud Dóminum * misericórdia, et copiósa apud eum redémptio.
De fructu * ventris tui ponam super sedem tuam.
