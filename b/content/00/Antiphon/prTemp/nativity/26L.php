Lapidavérunt Stéphanum, * et ipse invocábat Dóminum, dicens: Ne státuas illis hoc peccátum.
Lápides torréntis * illi dulces fuérunt: ipsum sequúntur omnes ánimæ justæ.
Adhǽsit ánima mea * post te, quia caro mea lapidáta est pro te, Deus meus.
Stéphanus vidit * cælos apértos, vidit, et introívit: beátus homo, cui cæli patébant.
Ecce vídeo * cælos apértos, et Jesum stantem a dextris virtútis Dei.
