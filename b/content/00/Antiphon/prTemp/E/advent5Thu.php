The Lord almighty * shall come from Sion, that he may save his people.
Turn again, O Lord, * at the last, and delay not to come unto thy servants.
Out of Sion * the Lord shall come that shall reign, Emmanuel is his great name.
Behold, he is my God, * and I will honour him: the God of my father, and I will exalt him.
The Lord * is our Lawgiver, the Lord is our King, he shall come, and shall save us.
