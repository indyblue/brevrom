Be ye steadfast, * ye shall see the help of the Lord upon you.
Unto thee, O Lord, * have I lifted up my soul: come and deliver me, O Lord, to thee have I fled.
Come, O Lord, * and delay not: forgive the sins of thy people Israel.
God shall come from Libanus, * and his brightness shall be as the light.
Yet will I * look to the Lord, I will await God my Saviour.

>Ant 2, part from Ps. 142:9
