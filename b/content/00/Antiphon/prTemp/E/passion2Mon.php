I turned not away * my face from them that rebuked me and spat upon me.
Let the sword go forth * against those that scatter my flock.
They took * for my price thirty pieces of silver, the price that I was prized at by them.
Waters flowed * over my head; I said: I perish, I will call on thy name, O Lord, my God.
Behold, O Lord, * the lips of those who rise up against me, and see their thoughts.
