Drop down dew, ye heavens, from above, * and let the clouds rain the Just One: let the earth open and bud forth the Saviour.
Send forth, O Lord, the Lamb, * the Ruler of the earth, from the Rock of the desert, to the mountain of the daughter of Sion.
That we may know thy way * upon earth, O Lord, thy salvation in all Nations.
Reward them, O Lord, * that patiently wait for thee, that thy prophets may be found faithful.
The law was given through Moses, * grace and truth came through Jesus Christ.
