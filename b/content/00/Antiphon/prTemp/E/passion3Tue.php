Behold, O Lord, * and see, for I am in trouble: hear me speedily.
Distinguish my cause, * O Lord: from the iniquitous and deceitful man deliver me.
When I was in trouble * I cried unto the Lord out of the belly of hell, and he heard me.
O Lord, I suffer violence, * answer for me: for I know not what I shall say to mine enemies.
The wicked said: * Let us oppress the righteous man, because he is contrary to our doings.
