O death! * I will be thy death. O hell! I will be thy ruin.
They shall mourn * for him as for an only son, because the innocent Lord is slain.
Attend, * all ye people, and see my sorrow.
From the gate of hell, * deliver, O Lord, my soul.
O all ye * that pass by the way, attend and see if there be any sorrow like unto my sorrow.
