Deliver me * from blood, O God, my God: and my tongue shall extol thy justice.
Reproaches * and terrors have I suffered at their hands: and the Lord is with me as a mighty warrior.
But thou, O Lord, * knowest all their counsel against me unto death.
All mine enemies * have heard of my affliction: they have rejoiced, O Lord, that thou hast done it.
Execute judgment, O Lord, * for them that suffer wrong: and destroy the ways of sinners.
