Judǽa et Jerúsalem, * nolíte timére: cras egrediémini, et Dóminus erit vobíscum, allelúja.
Hódie sciétis * quia véniet Dóminus: et mane vidébitis glóriam ejus.
Crástina die * delébitur iníquitas terræ: et regnábit super nos Salvátor mundi.
Dóminus véniet, * occúrrite illi, dicéntes: Magnum princípium, et regni ejus non erit finis: Deus, Fortis, Dominátor, Princeps pacis, allelúja.
Crástina erit * vobis salus, dicit Dóminus Deus exercítuum.
