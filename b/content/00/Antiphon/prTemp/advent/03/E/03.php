The Lord will come, * and he will not delay, and he will reveal things hidden in darkness, and will manifest himself to all nations, alleluia.
Rejoice, O Jerusalem, * with great joy, for thy Saviour will come to thee, alleluia.
I will settle * salvation in Sion, and my glory in Jerusalem, alleluia.
Mountains and hills * shall be brought low: the crooked paths shall be made straight, and the rough ways smooth: come, O Lord, and delay not, alleluia.
Let us live justly and piously, * expecting the blessed hope, and coming of the Lord.
