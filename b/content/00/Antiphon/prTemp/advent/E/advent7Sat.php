Behold, * how glorious is he, who cometh to save the people.
His dominion * shall be increased, and there shall be no end of peace.
I the Lord * have brought my justice nigh, it shall not be put off, and my salvation shall not delay.
Let the word * of the Lord be expected as rain: and let our God descend upon us as the dew.
Be ready, * O Israel, to meet the Lord, for he is coming.
