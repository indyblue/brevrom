O Judah and Jerusalem, * fear not: tomorrow ye shall go forth, and the Lord will be with you, alleluia.
Today ye shall know * that the Lord will come: and on the morrow ye shall see His glory.
Tomorrow * the wickedness of the earth shall be blotted out: and the Saviour of the world shall reign over us.
The Lord cometh, * go ye out to meet him, saying: The great Prince, and of his kingdom there shall be no end: God, the Mighty, the Ruler, the Prince of Peace, alleluia.
On the morrow * ye shall have salvation, saith the Lord God of hosts.
