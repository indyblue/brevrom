Sound the trumpet * in Sion, for the day of the Lord is nigh: behold, he will come to save us, alleluia, alleluia.
Lo! the desired of all Nations * will come: and the house of the Lord shall be filled with glory, alleluia.
The crooked ways * shall be made straight, and the rough smooth: come O Lord, and delay not, alleluia.
The Lord will come, * go ye forth to meet him, and say: Great is his empire, and his reign shall have no end: he is God, the Mighty, the Ruler, the Prince of peace, alleluia, alleluia.
The almighty Word, * O Lord, shall come from thy royal throne, alleluia.
