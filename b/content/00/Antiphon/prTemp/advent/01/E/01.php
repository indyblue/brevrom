On that day * the mountains shall drop sweetness, and the hills shall flow with milk and honey, alleluia.
Be glad, O daughter of Sion, and rejoice exceedingly, O daughter of Jerusalem, alleluia.
Behold the Lord will come, * and all his Saints with him: and there shall be a great light on that day, alleluia.
O all ye that thirst, * come to the waters: seek the Lord while he may be found, alleluia.
Behold the great Prophet * will come, and himself will renew Jerusalem, alleluia.
