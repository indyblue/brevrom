Deal favourably, * O Lord, in thy good will with Sion, that the walls of Jerusalem may be built up.
The Lord * is my helper, I will not fear what man can do unto me.
My soul hath cleaved * unto thee, my God.
The fire forgot the power * of its own strength, that thy children might be delivered unhurt.
O sun and moon, * praise ye God: for his name alone is exalted.
