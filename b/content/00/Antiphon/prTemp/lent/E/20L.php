O Lord, * thou shalt open my lips, and my mouth shall declare thy praise.
The right hand of the Lord * hath done mightily: the right hand of the Lord hath exalted me.
My God * hath become my helper.
Let us sing * the hymn of the Three Children, which they sang in the fiery furnace, blessing the Lord.
He hath established them * for ever, and for ages of ages: he hath made a decree, and it shall not pass away.
