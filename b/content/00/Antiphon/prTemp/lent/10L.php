Create in me * a clean heart, O God, and renew a right spirit within me.
O Lord, * save me: O Lord, give good success.
Thus will I bless thee * all my life long, O Lord, and in thy name I will lift up my hands.
In a humble spirit * and a contrite heart, let us be accepted of thee, O Lord: and let our sacrifice be so offered that it may be accepted by thee today, and be well-pleasing unto thee, O Lord God.
Praise God, * O ye heavens of heavens, and all ye waters.
