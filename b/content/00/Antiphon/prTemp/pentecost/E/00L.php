They were all filled * with the Holy Ghost, and they began to speak, alleluia, alleluia.
O ye fountains, and all things * that move in the waters, sing a hymn to God, alleluia.
The Apostles spoke * with divers tongues, the wondrous works of God, alleluia, alleluia, alleluia.
