Qui me conféssus fúerit * coram homínibus, confitébor et ego eum coram Patre meo.
He that shall have confessed me * before men, him will I also confess before my Father.
Qui séquitur me, * non ámbulat in ténebris, sed habébit lumen vitæ, dicit Dóminus.
He that followeth me * walketh not in darkness, but shall have the light of life, saith the Lord.
Qui mihi minístrat, * me sequátur: et ubi ego sum, illic sit et miníster meus.
If any man minister unto me, * let him follow me; and where I am, there shall my servant be also.
Si quis mihi ministráverit, * honorificábit eum Pater meus, qui est in cælis, dicit Dóminus.
Whosoever shall have ministered unto me, * my Father who is in heaven will honour him, saith the Lord.
Volo, Pater, * ut ubi ego sum, illic sit et miníster meus.
Father, I will * that where I am, there also my servant may be.
