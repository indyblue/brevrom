Tóllite portas, * príncipes vestras, et elevámini, portæ æternáles.
Erit mihi Dóminus * in Deum, et lapis iste vocábitur domus Dei.
Ædificávit * Móyses altáre Dómino Deo.
Non est hic áliud, * nisi domus Dei et porta cæli.
Vidit Jacob scalam, * súmmitas ejus, cælos tangébat, et descendéntes Ángelos, et dixit: Vere locus iste sanctus est.
Eréxit Jacob * lápidem in títulum, fundens óleum désuper.
Qui hábitat * in adjutório Altíssimi, in protectióne Dei cæli commorábitur.†
Templum Dómini * sanctum est, Dei structúra est, Dei ædificátio est.
Benedícta * glória Dómini, de loco sancto suo, allelúja.
