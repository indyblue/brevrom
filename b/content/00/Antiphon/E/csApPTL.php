Thy Saints, * O Lord, shall flourish as the lily, alleluia: and as the odour of balsam shall they be in thy sight, alleluia.
In the heavenly kingdoms * is the dwelling of the Saints, alleluia: and their rest for evermore, alleluia.
Thy Saints, * O Lord, cried out within the veil, alleluia, alleluia, alleluia.
O ye spirits and souls * of the just, sing ye a hymn to our God, alleluia, alleluia.
The just shall shine * as the sun in the sight of God, alleluia.
