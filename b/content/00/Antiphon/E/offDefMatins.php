Direct, * O Lord my God, my way in thy sight.
Turn, * O Lord, and deliver my soul: for there is none in death who will be mindful of thee.
Lest at any time * the enemy snatch my soul as a lion, whilst there is none to redeem, nor to save it.
In a place of pasture * he hath set me.
The sins * of my youth, and my ignorances, remember not, O Lord.
I believe I shall see * the good things of the Lord in the land of the living.
May it please * thee, O Lord, to deliver me: Lord, have regard to help me.
Heal, O Lord, * my soul, for I have sinned against thee.
My soul hath thirsted * after the living God; when shall I come and appear before the face of the Lord?
