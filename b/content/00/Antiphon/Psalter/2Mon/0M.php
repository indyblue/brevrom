Dóminus de cælo * prospéxit super fílios hóminum.
Qui operátur justítiam, * requiéscet in monte sancto tuo, Dómine.
Inclína, Dómine, * aurem tuam mihi, et exáudi verba mea.
Díligam te, * Dómine, virtus mea.
Retríbuet mihi Dóminus, * secúndum justítiam meam.
Vivit Dóminus, * et benedíctus Deus salútis meæ.
Exáudiat te * Dóminus in die tribulatiónis.‡
Dómine, * in virtúte tua lætábitur rex.‡
Exaltábo te, * Dómine, quóniam suscepísti me.‡
