Memor fuit in sǽculum * testaménti sui Dóminus Deus noster.
Auxit Dóminus * pópulum suum: et firmávit eum super inimícos ejus.
Edúxit Deus * pópulum suum in exsultatióne, et eléctos suos in lætítia.
Salvávit eos Dóminus * propter nomen suum.
Oblíti sunt Deum, * qui salvávit eos.
Cum tribularéntur, * vidit Dóminus: et audívit oratiónem eórum.
Clamavérunt ad Dóminum: * et de necessitátibus eórum liberávit eos.
Ipsi vidérunt * ópera Dei, et mirabília ejus.
Vidébunt recti, * et lætabúntur, et intélligent misericórdias Dómini.
