The Lord set up * a testimony in Jacob: and made a law in Israel.
Wonderful things did God do * in the sight of their fathers.
Thou had opened the doors of heaven, * O Lord, and had rained down manna upon them to eat.
God was * their helper: and the Most High was their redeemer.
Thou redeemed them, * O Lord, from the hand of him that afflicted them.
God built * his sanctuary in the land.
Help us, * O God, our saviour: and forgive us our sins.
For I am the Lord * thy God, O Israel, who brought thee out of the land of Egypt.
Hold not thy peace, O God, * for lo, thy enemies have lifted up the head.
