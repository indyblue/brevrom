Suscitávit Dóminus * testimónium in Jacob: et legem pósuit in Israël. 
Coram pátribus eórum * fecit Deus mirabília. 
Jánuas cæli apéruit * Dóminus, et pluit illis manna ad manducándum. 
Deus adjútor * est eórum: et Excélsus redémptor eórum est. 
Redémit eos * Dóminus de manu tribulántis.
Ædificávit * Deus sanctifícium suum in terra. 
Ádjuva nos, * Deus, salutáris noster: et propítius esto peccátis nostris. 
Ego sum Dóminus * Deus tuus, Israël, qui edúxi te de terra Ægýpti. 
Ne táceas, Deus, * quóniam inimíci tui extulérunt caput. 
