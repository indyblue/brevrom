Blessed is the man, * who meditates on the law of the Lord.
Serve ye the Lord * with fear: and rejoice unto him with trembling.
Arise, * O Lord; save me, O my God.
How admirable * is thy name, O Lord, in the whole earth!
Thou hast sat on the throne, * who judgest justice.
Arise, O Lord, * let not man prevail.
Why, O Lord, * hast thou retired afar off?‡
Arise, * O Lord God, let thy hand be exalted.‡
The Lord is just, * and hath loved justice.
