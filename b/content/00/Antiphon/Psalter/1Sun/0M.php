Beátus vir, * qui in lege Dómini meditátur.
Servíte Dómino * in timóre: et exsultáte ei cum tremóre.
Exsúrge, * Dómine, salvum me fac, Deus meus.
Quam admirábile * est nomen tuum, Dómine, in univérsa terra!
Sedísti super thronum, * qui júdicas justítiam.
Exsúrge, Dómine, * non præváleat homo.
Ut quid, Dómine, * recessísti longe?‡
Exsúrge, * Dómine Deus, exaltétur manus tua.‡
Justus Dóminus, * et justítiam diléxit.
