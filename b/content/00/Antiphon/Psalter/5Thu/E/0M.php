In God is my salvation * and my glory: and my hope is in God.
Behold ye the works of the Lord, * and make the voice of his praise to be heard.
Hear, all ye * that fear God, what great things he hath done for my soul.
Let God arise, * and let his enemies be scattered.‡
Our God * is the God of salvation: and of the Lord are the issues from death.
In the churches * bless ye God the Lord.
Save me, * O God: for the waters are come in even unto my soul.‡
Save me * because of my enemies, O Lord.
Seek ye the Lord, * and your soul shall live.
