Overthrow, O Lord, * them that fight against me.
Rescue thou my soul * from their malice, O Lord.
Arise, O Lord, * and be attentive to my judgment.
Envy not * the man who prospereth in his way, and doth unjust things.
The arms of the wicked * shall be broken in pieces; but the Lord strengtheneth the just.
Keep innocence, * and behold justice.
Chastise me not * in thy wrath, O Lord.
Attend unto my help, * O Lord, the strength of my salvation.
Remove, O Lord, * thy scourges from me.
