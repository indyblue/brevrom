Fr. Z

Te sæculórum Príncipem,
Te, Christe, Regem Géntium,
Te méntium te córdium
Unum fatémur árbitrum.

we profess that you prince of all ages
you, o christ are the king of the nations
you are the sole judge 
of all hearts and minds

Scelésta turba clámitat:
Regnáre Christum nólumus:
Te nos ovántes ómnium
Regem suprémum dícimus.

the wicked mob screams out
we don't want christ to reign as king
we, exalting, say 
you are the king of all

O Christe, Princeps Pácifer,
Mentes rebélles súbice:
Tuóque amóre dévios,
Ovíle in unum cóngrega.

oh christ, peacebringing prince
subjugate rebellious minds
and in your love, gather into one flock
those going astray

Ad hoc cruénta ab árbore
Pendes apértis bráchiis,
Diráque fossum cúspide
Cor igne flagrans éxhibes.

for this you hang,
arms outstretched on the bloody tree
you show a heart burning with fire
pierced with the sharp speartip

Ad hoc in aris ábderis
Vini dapísque imágine,
Fundens salútem fíliis
Transverberáto péctore.

for this you are hidden on altars
in the image of bread and wine 
pouring out salvation on your children
from your pierced breast

Te natiónum Præsides
Honóre tollant público,
Colant magístri, júdices,
Leges et artes éxprimant.

may the rulers of the nations
extol you with public honor
may teachers, judges, worship you
may laws and arts have you as their model

Submíssa regum fúlgeant
Tibi dicáta insígnia:
Mitíque sceptro pátriam
Domósque subde cívium.

may the standards of kings submitted
shine forth dedicated to you
bring under your gentle scepter our homeland
and the homes of your citizens

Jesu tibi sit glória,
Qui sceptra mundi témperas,
Cum Patre, et almo Spíritu,
In sempitérna sæcula. Amen.

glory to you o Jesus
who rules the scepters of the world
glory with the father and the loving spirit
for endless ages amen.

All glory be, Jesu, to thee,
Who holds the scepter of humanity;
All glory, as is ever meet,
To Father and to Paraclete. Amen.
