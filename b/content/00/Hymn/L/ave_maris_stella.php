Unk. 6-9th c.
>Prima stropha sequentis hymni dicitur flexis genibus:

Ave, maris stella,
Dei Mater alma,
Atque semper Virgo,
Felix cæli porta.

Sumens illud Ave
Gabriélis ore,
Funda nos in pace,
Mutans Hevæ nomen.

Solve vincla reis,
Profer lumen cæcis,
Mala nostra pelle,
Bona cuncta posce.

Monstra te esse matrem,
Sumat per te preces,
Qui pro nobis natus
Tulit esse tuus.

Virgo singuláris,
Inter omnes mitis,
Nos, culpis solútos,
Mites fac et castos.

Vitam præsta puram,
Iter para tutum,
Ut, vidéntes Jesum,
Semper collætémur.

Sit laus Deo Patri,
Summo Christo decus,
Spirítui Sancto,
Tribus honor unus. Amen.
