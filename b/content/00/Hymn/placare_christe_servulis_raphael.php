Nobis adésto, Archángele,
Dei medélam dénotans:
Morbos repélle córporum,
Affer salútem méntibus.

O Archangel, come to us
you denote/(are the messenger of) the healing of God 
fend off vice/sickness of our flesh
announce/convey health of mind

O blest Archangel to us now sent,
God’s healing thou dost represent;
Defend our flesh from every ill;
With thoughts divine our minds do fill.
