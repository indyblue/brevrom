Solis, o Virgo, rádiis amícta,
Bis caput senis redimíta stellis,
Luna cui præbet pédibus scabéllum
Inclita fulges.

O Virgin, clothed by the sun's rays
twice thy distinguished head is crowned by the stars
the moon which is made thy footstool
O Illustrious one, shine forth!

Mortis, inférni domitríxque noxæ,
Assides Christo studiósa nostri,
Teque regínam célebrat poténtem
Terra polúsque.

O woman who subdues to hell our punishment of death,
who eagerly watches over us with Christ
and who the poles of the earth honor/glorify 
as mighty queen

Damna sed perstat sóboli minári
Créditæ quondam tibi dirus anguis;
Mater, huc clemens ades, et malígni
Cóntere collum.

but the damned persist in threatening the race/people/offspring
formerly belonging to the terrible serpent, now having been given to you
Merciful Mother, come to our aid, and break the necks
of the malignant.

Asseclas diæ fídei tuére,
Tránsfugas adduc ad ovíle sacrum,
Quas diu gentes tegit umbra mortis
Undique coge.

protect the followers of the divinely inspired faith
may the sacraments lead those who go astray back to the one sheepfold
which still protects the nations from the shadow of death
gathering them from everywhere.

Sóntibus mitis véniam precáre,
Adjuva flentes, ínopes et ægros,
Spes mica cunctis per acúta vitæ
Certa salútis.

gentle pardon/mercy we beg for the criminal/guilty
always helping/aiding the weak/helpless and sick/injured
help all who tremble/quiver by spurring us on 
to life, to certain salvation

Laus sit excélsæ Tríadi perénnis,
Quæ tibi, Virgo, tríbuit corónam,
Atque regínam statuítque nostram
Próvida matrem. Amen.

continual praise be to the preeminent trinity
who to thee, o virgin, bestow the crown
thus establishing thee as our queen
our provident mother. amen.
