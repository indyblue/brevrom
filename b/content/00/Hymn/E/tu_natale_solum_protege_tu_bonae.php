Fr. E.Caswall


Protect thy native City, Spirit blest!
And give to Christendom sweet days of peace;
Bid the shrill trumpet, and the shock of war,
Within her realms to cease.

And gathering her kings beneath the Cross,
Regain Jerusalem from our proud foe;
Avenge the innocent blood and the proud strength
Of Islam overthrow.

