Cento, The Hymner (1905)


This is the Confessor of the Lord, whose triumph
Now all the faithful celebrate with gladness
Erst on this feast-day merited to enter
Into his glory.

Saintly and prudent, modest in behaviour,
Peaceful and sober, chase was he, and lowly,
While that life’s vigour, coursing through his members,
Quicken’d his being.

Sick ones of old time to his tomb resorting,
Sorely by ailments manifold afflicted,
Oft-times have welcomed health &amp; strength returning,
At his petition.

Whence we in chorus gladly do him honour,
Chanting his praises with devout affection,
That in his merits we may have a portion
Now and for ever.

His be the glory, power, and salvation,
Who over all things reigneth in the highest,
Earth’s mighty fabric ruling and directing,
Onely and Trinal. Amen.
